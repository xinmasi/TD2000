      <p> 　　<BR> 
　　本合同是由以下双方于＿＿＿＿＿＿年＿＿＿＿＿＿月＿＿＿＿＿＿日签订的：中国＿＿＿＿＿＿是根据中华人民共和国法律正式成立并注册的企业法人（简称“买方”）；＿＿＿＿＿＿国＿＿＿＿＿＿公司（简称“卖方”）。<BR> 
　　鉴于卖方多年来从事＿＿＿＿＿＿系统的设计、生产、销售业务并为该系统提供服务，并在进一步开发；<BR> 
　　鉴于买方愿意建立一个＿＿＿＿＿＿系统；<BR> 
　　鉴于引进＿＿＿＿＿＿系统将会提高科学技术水平，改进＿＿＿＿＿＿的质量和类型，在先进技术产品的使用及服务方面提供培训机会，并且通过创造一种平等、积极的工作环境促进工人的权利和尊严，从而对中国人民做出贡献；<BR> 
　　鉴于按照平等互利的原则，经友好协商，买方已决定它愿成为＿＿＿＿＿＿系统的使用人，而卖方愿意提供该等系统供买方使用。<BR> 
　　因此，考虑到本合同中所含的相互条款和协议，现双方特协议如下：<BR> 
　　１．双方间的协议（简称“合同”）由本合同所规定的条款和条件以及以下所提及的附件构成：<BR> 
　　附件一　　系统组件<BR> 
　　附件二　　交货和安装时间表<BR> 
　　附件三　　价格和支付条件<BR> 
　　附件四　　产品说明和规格<BR> 
　　附件五　　卖方软件许可合同<BR> 
　　附件六　　软件分许可合同<BR> 
　　附件七　　租赁合同<BR> 
　　２．定义<BR> 
　　下列词语在本合同中应有如下含义：<BR> 
　　（ａ）商用＿＿＿＿＿＿系统：（略）<BR> 
　　（ｂ）用户＿＿＿＿＿＿系统：（略）<BR> 
　　（ｃ）控制用计算机：指由买方用于＿＿＿＿＿＿系统及其所控制的一切＿＿＿＿＿＿系统组成部分。<BR> 
　　（ｄ）＿＿＿＿＿＿（注册商标）：指卖方的一个注册商标，卖方用其表示生产和销售的＿＿＿＿＿＿系统的一个较早版本。<BR> 
　　（ｅ）＿＿＿＿＿＿（注册商标）Ⅱ：指＿＿＿＿＿＿系统的一个较晚版本，卖方用该词指作为本合同标的的系统。<BR> 
　　（ｆ）系统：指卖方的＿＿＿＿＿＿系统。（略）<BR> 
　　（ｇ）标准转换器：（略）<BR> 
　　３．系统的提供<BR> 
　　（ａ）卖方根据作为本合同一部分的产品说明和规格及交货和安装时间表提供系统，买方根据作为本合同一部分的价格和支付条件就系统付款。<BR> 
　　（ｂ）在买方订购的＿＿＿＿＿＿系统可以提供使用之前，卖方应根据本合同附件七租赁合同所规定的条款将此系统租赁给买方。买方承认并同意，卖方可以自行决定，通过改进租赁给买方的＿＿＿＿＿＿系统、提供一个不同的系统或者将前述两个系统结合起来，提供买方所订购的＿＿＿＿＿＿系统。<BR> 
　　４．交货和安装<BR> 
　　（ａ）系统按附件二规定的交货时间表交付。运费将加在卖方发票上，由买方支付。买方有权指定承运人，并以书面形式将其所选择的承运人通知卖方。如果买方未将其所选择的承运人通知卖方，卖方将挑选承运人。但是，卖方不应因此承担有关运输的任何责任，并且也不应将承运人视为卖方的代理人。除非买方要求，卖方没有义务为买方取得保险。<BR> 
　　（ｂ）卖方或其指定的服务供应商应在买方指定的中华人民共和国境内设施上安装＿＿＿＿＿＿系统。安装费应加在卖方发票上，由买方支付。买方应根据本合同的安装时间表，按照预先交给买方的场地准备指南所规定的规格，负责按时完成任何必要的现场准备及买方设施的修改。必要时，买方应按照场地准备指南提供其他测试设备及物资（包括但不限于占地面积、电源插座、中断电缆、卫星设施等）。买方应负责一切该等设施、准备、设备、物资以及为此所需的许可和批准，并支付其费用。<BR> 
　　５．遵守卖方所在国出口管制法律<BR> 
　　（ａ）卖方同意为买方从卖方购买的产品和技术，申请＿＿＿＿＿＿向中国出口产品和技术所必需的一切＿＿＿＿＿＿政府出口许可、同意和批准。如果卖方虽尽其最大努力仍不能取得该等产品和技术从＿＿＿＿＿＿合法出口中国所需的任何及一切许可、同意和批准，则本合同立即终止，卖方免除履行，并且买方使卖方不受损害。<BR> 
　　（ｂ）买方特承诺遵守＿＿＿＿＿＿出口法律及法规，并且同意，在未取得必要的＿＿＿＿＿＿政府批准许可的情况下，买方不会故意：<BR> 
　　（ｉ）直接或间接地向在出口时＿＿＿＿＿＿政府或其任何机构要求出口许可或其他政府批准的任何国家出口从卖方获得的、源于＿＿＿＿＿＿的技术资料、软件或该技术资料的任何直接产品；<BR> 
　　（ｉｉ）向＿＿＿＿＿＿政府或其任何机构要求出口许可或其他政府批准的任何国家国民透露从卖方获得的、源于＿＿＿＿＿＿的任何技术资料或软件。<BR> 
　　６．所有权和损失风险<BR> 
　　本合同项下购买的一切物品的所有权及损失风险，应在卖方设施所在地，于该等物品交付承运人后转移给卖方。在购买价款全部支付前，买方授予卖方对该系统的担保权益，作为买方按照本合同付款的保证。在交付承运人后，系统或其任何部分的损失或损坏应由买方负责。在损失风险转给买方后，卖方应有权得到所损失或损坏的任何物品的全部购买价款。<BR> 
　　７．验收测试和验收<BR> 
　　（ａ）系统的验收测试应依照卖方的验收测试程序进行。卖方和其指定的服务供应商应根据前述测试程序进行验收测试。如果任何该等测试没有成功完成，卖方或其指定的服务供应商应对测试进行评估，并且对系统进行任何调整或校正，使系统能按规格运行。一切该等测试的开始，应给予买方合理的事先通知，并且给予买方观察一切该等测试的合理机会。<BR> 
　　（ｂ）“成功完成”一词，在本合同中用于任何测试方面时，指在特定的测试程序中规定的该等测试的成功完成，并且一切对测试的提及指的都是卖方验收测试程序中的测试。<BR> 
　　（ｃ）为附件三之目的，在系统的验收测试程序中规定的一切测试已在买方设施地点成功完成时，系统的验收（简称“验收”）应被视为已经进行。<BR> 
　　（ｄ）卖方声明并保证，卖方将向买方交付完整、准确、有效的系统。该系统能够达到在产品说明和规格中规定的技术指标，并由系统验收测试的成功完成予以证明。成功完成应被视为系统完整、准确、有效并能达到规格所述技术指标的决定性证据。<BR> 
　　８．装运到场和验收合格<BR> 
　　（ａ）如果系统或其任何部分已按附件二规定的日期准备装运或安装，但根据买方请求或者由于买方不能提供验收或安装系统所必需的设施、测试设备或物资而使该等装运或安装延迟超过＿＿＿＿＿＿个日历日，则卖方可以根据其选择通知买方，系统或其任何部分作为已事实上装运、交付并安装（简称“装运到场”）对待。此外，对于该等延迟所导致的一切储存费或其他费用，买方应补偿卖方。<BR> 
　　（ｂ）通行装运到场后＿＿＿＿＿＿天，卖方有权按本合同附件三规定的交付条件就下列款项向买方开出发票：<BR> 
　　（ｉ）装运到场的商用＿＿＿＿＿＿系统百分之百（１００％）的购买价款：及<BR> 
　　（ｉｉ）就＿＿＿＿＿＿系统而言，为该系统已实际装运情况下的到期金额，余额在验收开出发票。<BR> 
　　（ｃ）在附件二规定的装运或安装日期前后的任何时候，有以下任一情形的，买方即对系统或其任何部分进行了合格验收（简称“合格验收”）：<BR> 
　　（ｉ）买方决定并书面通知卖方，系统适宜开始买方拟用系统进行的操作；<BR> 
　　（ｉｉ）系统已经开始买方拟用系统进行的操作。<BR> 
　　（ｄ）合格验收应具有与上述第８（ｂ）条相同的结果，但＿＿＿＿＿＿系统的到期金额应为迄今已付金额与百分之九十（９０％）的验收后到期金额的差额，余额在验收后开发票。<BR> 
　　（ｅ）装运到场和合格验收都不免除卖方在本合同项下的任何责任，包括验收测试的成功完成以及按本合同条款和条件对瑕疵或缺陷进行纠正。<BR> 
　　９．税收<BR> 
　　销售设备和在中国提供修理及安装服务的价款不包括一切派款、关税、销售税、使用税、国内消费税、增值税及类似税收（含买方政府从源扣缴的金额），买方应承担并支付上述税款。任何要求卖方就销售、交付或使用系统所收、缴的税款（卖方的所得税除外）应由买方支付，并且该等税款应在系统交付后到期应付。买方同意就因买方过失的行为与不行为或者因买方违反或不履行本第九条而导致的一切请求补偿卖方，使卖方不受损害并为其辩护。<BR> 
　　１０．付款<BR> 
　　（ａ）作为卖方按本合同提供本合同系统及一切有关物品的全部对价，买方应支付卖方在附件三中规定的系统购买价款。<BR> 
　　（ｂ）付款应按附件三进行。<BR> 
　　（ｃ）如果在发票金额或其任何部分到期时买方没有付款，买方特同意就一切该等金额按年利率百分之十八（１８％）或法律允许的最高利率向卖方支付从到期应付日期至付款日期的利息。<BR> 
　　１１．操作手册和其他资料／培训<BR> 
　　（ａ）卖方应在提供每个商用＿＿＿＿＿＿系统时随附一份用户指南。<BR> 
　　（ｂ）卖方应向买方提供３份＿＿＿＿＿＿系统所有操作手册和安装指南。<BR> 
　　（ｃ）系统安装以后，卖方或其指定的服务供应商应在系统使用和操作期间给买方雇员提供为期＿＿＿＿＿＿日的培训课程。<BR> 
　　１２．系统保证<BR> 
　　（ａ）卖方声明并保证，卖方是本合同中向买方许可、租赁或销售的一切知识产权的所有人或受益被许可人，并且卖方有权向买方许可、租赁或销售前述知识产权。卖方保证本合同项下提供的系统和一切设备及有关软件（除控制用计算机及控制用计算机操作系统软件）自验收日期后的一年内没有品质及工艺方面的瑕疵，但商用及用户＿＿＿＿＿＿系统除外，两者的保证期应自装运日期后为期一年。卖方在该期间内应免费在买方场地修理、更换并重新安装＿＿＿＿＿＿系统或其任何有瑕疵的部分。卖方保证不包括控制用计算机或系统中包含的控制用计算机操作系统软件，但卖方应（在可转让的范围内）向买方转让任何著名生产厂商对控制用计算机或系统中包含的控制用计算机操作系统软件的保证。如果不存在验收日期后为期一年的该等转让保证，卖方应为买方利益并作为购买价款的一部分，为该等控制用计算机及操作系统软件购买硬件和软件维修续期合同，以取代保证，该维修续期合同应自验收日期后为期一年。如果由于保证范围外的瑕疵或正常使用和常规安装外的原因导致修理和更换，买方应对卖方为纠正该等瑕疵所提供的一切劳务和物资（包括差旅费）向卖方付款。<BR> 
　　（ｂ）本合同规定的保证仅适用于常规安装、正常使用并在保证期内发现瑕疵的物品。该等保证不应适用于未经卖方书面同意而修改、改动或者被滥用、发生事故、过失或不当使用的物品。<BR> 
　　（ｃ）本合同规定的保证取代对系统性能一切其他明示或默示保证，包括但不限于关于适销性或适合某一特殊用途的任何默示保证。买方就卖方违反本合同规定的任何保证享有的唯一救济应是卖方为履行该等保证而进行的修理和（或）更换。卖方在任何时候都不对视听或数据信号的任何损失、业务中断或者任何种类或性质的特殊、间接或结果性损害负责。卖方不对买方过失或过错所导致的履行本条项下义务的延迟负责。<BR> 
　　１３．非保修性修理及备件支持<BR> 
　　（ａ）在（ｉ）系统验收满＿＿＿＿＿＿年之日，或（ｉｉ）买方停止系统操作之日（依较早者）前，卖方应提供系统的修理服务和／或备件（简称“支持期”）。双方理解，对不在保证范围内的备件及修理，卖方应按其当时的原料价或备件价格收费，并在工作完成后尽快开出发票。卖方在本合同或其他合同项下，没有义务向未就任何该等备件或修理及时付款的任何人进一步提供备件或修理服务。<BR> 
　　（ｂ）在支持期之后，卖方提前６个月书面通知买方其要中止提供系统的备件或修理服务的，可以中止提供。但是，卖方可以选择以下其中一项。<BR> 
　　（ｉ）授予买方为自用而非出售之目的而制造任何该系统组件（不包括控制用计算机或其操作系统软件）的非排他性许可，并向买方提供一切必需的文件、规格、图纸及其他资料；<BR> 
　　（ｉｉ）允许买方有机会购买其认为是系统维修和支持所需的足量备件。<BR> 
　　（ｃ）为本合同之目的，如果在＿＿＿＿＿＿系统装运后的任何＿＿＿＿＿＿个月内，买方没有在正常使用的仪器上使用卖方提供的系统，作为其正常使用的一部分，编译或拆译信号码，共计至少为＿＿＿＿＿＿小时，或者在该等装运后连续＿＿＿＿＿＿个月内，买方在每＿＿＿＿＿＿个月内没有在正常使用的仪器上使用卖方提供的系统，作为其正常使用的一部分，编译或拆译信号码，共计至少为＿＿＿＿＿＿小时，则买方应视为“停止系统操作”。<BR> 
　　１４．维修续期<BR> 
　　在本合同规定的＿＿＿＿＿＿系统的保证期届满前，卖方和／或其指定的服务供应商应给买方提供机会，签订一份＿＿＿＿＿＿系统（包括有关控制用计算机的软件，但不包括控制用计算机的硬件平台）的维修续期合同，并可连续续展一年。<BR> 
　　１５．系统许可<BR> 
　　（ａ）控制用计算机的操作系统软件按本合同附件五规定的操作系统许可人进行分许可的条件和条款向买方提供。<BR> 
　　（ｂ）系统操作所需的一切卖方软件按本合同附件四规定的许可向买方提供。<BR> 
　　（ｃ）本合同未在涉及卖方所有的任何产品、系统或者卖方拥有的或由卖分许可的任何所有权方面给予技术转让或转移。在本合同项下，也未给予授予分许可的权利，亦不能从中推断出或暗示有分许可权。<BR> 
