　　第一条　本合同双方当事人<BR> 
　　出让方：中华人民共和国＿＿省（自治区、直辖市）＿＿市（县）土地管理局（以下简称甲方）：<BR> 
　　法定地址＿＿＿＿；邮政编码＿＿；法定代表人：姓名＿＿；职务＿＿＿。<BR> 
　　受让方：＿＿＿＿＿＿（以下简称乙方）；法定地址＿＿＿＿＿＿；邮政编码＿＿；法定代表人：<BR> 
　　姓名＿＿；职务＿＿＿＿。<BR>
        　　根据《中华人民共和国城镇国有土地使用权出让和转让暂行办法》、《＿＿＿省国有土地使用权出让转让实施办法》和国家有关规定，双方本着平等、自愿、有偿的原则，订立本合同。<BR>
        　　第二条　甲方依据本合同出让土地的使用权，土地所有权属中华人民共和国，地下资源、埋藏物和市政公用设施均不在土地使用权出让范围。<BR>
        　　第三条　乙方根据本合同受让的土地使用权在使用年限内，依据有关规定可以转让、出租、抵押或用于其他经济活动。<BR>
        　　乙方在受让土地使用权范围内所进行的开发、利用、经营土地的活动，应遵守中华人民共和国法律、法规及＿＿＿＿省（自治区、直辖市）的有关规定，并不得损害社会公共利益，其合法权益受法律保护。　<BR>
        　　第四条　甲方出让给乙方的地块位于＿＿＿＿，面积为＿＿＿＿平方米。其位置与四至范围如本合同附图所示。附图已经甲、乙双方签安确认。<BR>
        　　第五条　本合同项下的土地使用权出让年限为＿＿年，自颁发该地块的《中华人民共和国国有土地使用证》之日起算。<BR>
        　　第六条　本合同项下的出让地块，按照批准的总体规划是建设＿＿＿＿＿项目。（注：根据具体情况定）。<BR>
        　　在出让期限内，如需改变本合同规定的土地用途，应当取得甲方和城市规划行政主管部门批准，依照有关规定重新土地使用权出让合同，调整土地使用权出让金，并办理土地使用权登记手续。<BR>
        　　第七条　本合同附件《土地使用条件》是本合同的组成部分，与本合同具有同等法律效力。乙方同意按《土地使用条件》使用土地。<BR>
        　　第八条　乙方同意按合同规定向甲方支付土地使用权出让金、土地使用费以及乙方向第三方转让时的土地增值费（税）。<BR>
        　　第九条　该地块的土地使用权出让金为每平方米＿＿元人民币（美元或港元等），总额为＿＿＿元人民币（美元或港元等）。<BR>
        　　第十条　本合同经双方签字后＿＿＿日内，乙方须以现金支票或现金向甲方缴付土地使用权出让金总额的＿＿％共计＿＿元人民币（美元或港元等）作为履行合同的定金。<BR>
        　　乙方应在签订本合同后６０日内，支付完全部土地使用权出让金，逾期＿＿日仍未全部支付的，甲方有权解除合同，并可请求乙方赔偿。<BR>
        　　第十一条　乙方在向甲方支付全部土地使用权出让金后＿＿＿日内，依照规定办理土地使用权登记手续，领取《中华人民共和国国有土地使用证》，取得土地使用权。<BR>
        　　第十二条　乙方同意从＿＿年开始，按政府规定逐年缴纳土地使用费，缴纳时间为当年＿＿月＿＿日。土地使用费用每年每平方米为＿＿＿元人民币（美元或港元等）。<BR>
        　　第十三条　乙方同意以美元（港元等）向甲方支付土地使用权出让金及其他费用。（注；根据具体情况定）<BR>
        　　美元（港元等）与人民币的比价，以合同签定当天中国国家外汇管理局公布的买入价和卖出价的中间值计算。<BR>
        　　第十四条　除合同另有规定外，乙方应在本合同规定的付款日或付款日之前，将合同要求支付的费用汇入甲方的银行帐号内。银行名称：＿＿＿银行＿＿＿分行，帐户号＿＿＿。<BR>
        　　甲方银行帐户如有变更，应在变更后＿＿日内，以书面形式通知乙方。由于甲方未及时通知此类变更而造成误期付款所引起的任何延迟收费，乙方均不承担违约责任。<BR>
        　　第十五条　本合同规定的出让年限届满，甲方有权无偿收回出让地块的使用权，该地块上建筑物及其他附着物所有权也由甲方无偿取得。土地使用者应当交还土地使用证，并依照规定办理土使用权济销登记手续。<BR>
        　　乙方如需继续使用该地块，须在期满前＿＿＿天内向甲方提交续期申请书，并在确定了新的土地使用权出让年限和出让金及其他条件后，与甲方签订续期合同，并重新办理土地使用权登记手续。<BR> 
　　第十六条　任何一方对于因发生不可抗力且自身无过错造成延误或不能履行合同义务不不负责任。<BR> 
　　但必须采取一切必要的补救措施以减少造成的损失。<BR>
        　　遇有不可抗力的一方，应在＿＿＿小时内将事件的情况以信件或电报（电传或传真）的书面形式通知另一方，并且事件发生后＿＿日内，向另一方提交合同不能履行或部分不能履行以及需要延期履行理由的报告。<BR>
        　　第十七条　如果乙方不能按时支付任何应付款项，从滞纳之日起，每日按应缴纳费用的＿＿＿％缴纳滞纳金。<BR> 
　　第十八条　乙方未按合同规定或连续两年不投资建设的，甲方有权无偿收回土地使用权。<BR>
        　　第十九条如果由于甲方的过失致使乙方延期占用土地使用权,则本合同项下的土地使用权出让期限应相应推延.同时甲方应承担乙方由此而造成的一切经济损失.<BR> 
　　第二十条　本合同订立、效力、解释、履行及争议的解决均受中华人民共和国法律的保护和管辖。<BR>
