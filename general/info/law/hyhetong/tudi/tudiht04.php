　　土地使用条件<BR> 
　　（宗地项目）<BR> 
　　<BR> 
　　一、界桩定点<BR>
        　　１．１《国有土地使用权出让合同》（以下简称本合同）正式签订后＿＿日内，＿＿市（县）土地管理局，会同用地者依图验明红线所标示座标的各拐点界桩。面积核定无误后，双方在用地红线图上签字认定。界桩由用地者妥善保护，不得私自改动，界桩遭受破坏或移动时，应及时报告＿＿＿＿市（县）土地管理局，请求重新埋设。<BR> 
　　二、土地利用要求<BR> 
　　２．１　用地者在用地红线图兴建建筑物应符合下列要求：<BR> 
　　（１）主体建筑物的性质规定为＿＿＿；<BR> 
　　（２）附属建筑物＿＿＿＿＿；<BR> 
　　（３）建筑容积率（建筑面积密度）＿＿＿；<BR> 
　　（４）建筑覆盖率（建筑密度）＿＿＿＿＿；<BR> 
　　（５）总建筑面积不超过＿＿＿＿＿＿＿＿＿平方米；<BR> 
　　（６）建筑层数，最高／平均＿＿＿＿＿层；<BR> 
　　（７）绿化比率＿＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　（８）室外地面标高＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　（９）所有建筑物的设计均应符合国家现行建筑设计标准、规程的规定。<BR> 
　　（注：根据具体情况定）<BR> 
　　三、公益工程<BR> 
　　３．１　用地者表示同意在用地红线范围内一并建造下列公益工程，并同意免费提供使用。<BR> 
　　（１）＿＿＿＿蹲位公厕<BR> 
　　（２）小区公用停车场<BR> 
　　（３）自行车棚<BR> 
　　（４）配电室（开关站）<BR> 
　　（注：根据具体情况定）<BR>
        　　３．２用地者表示同意政府的下列工程可在其红线范围内的规划位置建造或通过，而无需作任何补偿。<BR> 
　　（１）＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　（２）＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　四、设计、施工、竣工<BR>
        　　４．１　红线范围内的建筑设计、建筑用途等必须符合土地利用要求，涉及交通、管线、消防、环保、人防、航道等问题，还须报经有关主管部门审批后建设，并由地方政府派出机构管理，由此所发生的一切费用均由用地者负担。<BR>
        　　４．２　用地者自签订本合同之日起＿＿＿＿日内应按批准的规划设计图纸和施工设计图纸动土施工。并于＿＿年＿＿月＿＿日前所完成的建筑物面积不少于＿＿＿＿平方米。<BR>
        　　４．３规模大的、特殊的复杂工程，其动土施工时间按上述要求进行有困难的，用地者至迟应在离建设期限届满之日前＿＿日内，向市（县）土地管理局提出足够理由延建申请，且延续期不得超过＿＿日。<BR> 
　　４．４　用地者应在＿＿＿＿年＿＿月＿＿日以前竣工（受不可抗力影响者除外），延期竣工<BR>
        　　＿＿＿＿日的，市（县）土地管理部门有权收回土地使用权，注销其《国有土地使用证》，其地上建筑物无偿归国家所有。<BR> 
　　五、建筑维修活动<BR> 
　　５．１　用地者用地红线范围内进行建设及维修活动时，对周围环境及设施应承担的责任包括：<BR>
        　　（１）所属建筑物品或废弃物（即泥土、碎石、建筑垃圾等）不得侵占或破坏红线图以外的土地及设施。<BR> 
　　如需临时占用市政道路，应报请市公安部门批准。<BR>
        　　如需临时使用红线以外土地，应与该用地者协商；若属政府未批准土地，应报市（县）土地管理部门批准，并按规定交纳土地费用。<BR> 
　　（２）未获有关部门批准，不得在公共用地上倾倒、储存任何材料或进行任何工程活动。<BR>
        　　（３）用地者必须确保土地使用范围内的污水、污物、恶臭物或响环境的排泄物均应有可靠的排除方法，不得损害周围的环境。<BR>
        　　（４）在土地使用期限内，用地者对该地段内的所有城市市政设施，均应妥善保护，避免损坏，否则，应承担修复工程的一切费用。<BR> 
　　５．２　用地者不得开辟、铲除或挖掘毗邻地段的土地。<BR>
        　　５．３　在兴建建筑或维修工程之前，用地者必须摸清地段或相邻地段公有的明渠，水道（包括水龙喉管）、电缆、电线以及其他设施的位置，并向有关部门呈报处理上述设施的计划；用地者在未获批准之前，不得动工。基本需要改道、重新铺设或装设的费用，均由用地者负责。<BR> 
　　六、供水、供电<BR> 
　　６．１　用地者所需的用水，应与市自来水公司签订供水合同。<BR> 
　　６．２　用地者所需的用电，应与市供电局签订供电合同。<BR> 
　　６．３　用地者接水、接电及开设路口，所需费用均自行负责。<BR> 
　　七、监督检查<BR>
        　　７．１　在土地使用期间，市（县）土地管理部门有权对用地者红线范围内的土地使用情况进行检查监督，用地者不得拒绝和阻挠。<BR>
        　　７．２　在地者不得以任何理由占用红线范围以外的土地（包括堆放物品、器材等），否则，按违法占地处理。<BR> 
　　７．３　用地者在用地范围内，应按规定的土地用途和工程设计图纸的要求进行建设。<BR>
        　　７．４　用地者对用地范围内的建筑物，未经规划部门批准，不得任意拆除或改建、重建。否则，有关部门有权责令其恢复原状或拆除，拒不执行的，可强制执行，所需费用由用地者支付。<BR> 
