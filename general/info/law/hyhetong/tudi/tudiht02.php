        　　第十二条　土地使用权转让（出租；抵押）时，应当签订转让合同（租赁合同；抵押合同）或在房产买卖合同中设立土地使用权转让（出租；抵押）章节，但不得违背国家法律法规的本合同规定。并依照规定办理转让（出租；抵押）登记。<BR>
        　　第十三条　土地出让年限届满，土地使用者可以申请续期，重新与甲方签订出让合同，支付土地使用权出让金，并办理登记。土地使用者不再申请续期的，本合同项下的土地使用权、地上建筑物和其他附着物所有权由国家无偿收回，土地使用者应交还土地使用证并依照规定办理注销登记。<BR>
        　　第十四条　如果一方未能履行合同项下的义务，应被视为违反本合同，违约方从另一方收到具体说明违约情况的通知后，应在＿＿＿＿日内纠正该违约。如＿＿＿＿日内没有纠正，违约方应向另一方赔偿违约引起的一切直接和可预见的损失。<BR>
