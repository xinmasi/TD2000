        　　第六条　乙方按本合同规定向甲方支付土地使用权出让金、土地使用费以及向第三方转让时的土地增值费（税）。<BR>
        　　第七条　本合同项下的土地使用权出让金为每平方米＿＿＿＿元人民币，总额为＿＿＿＿＿＿元人民币。<BR> 
