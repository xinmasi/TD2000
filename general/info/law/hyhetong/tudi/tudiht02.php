　　第四条　土地使用权出让的年限为＿＿＿年，自＿＿＿＿＿＿＿＿＿起算。<BR>
        　　第五条　＿＿＿＿＿房产使用范围内的土地，按原批准文件为＿＿＿＿＿＿＿用地，乙方须按国家确定的用途、城市规划和建设要求使用土地。<BR>
