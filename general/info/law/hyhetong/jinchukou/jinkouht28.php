        　　卖方接到买方索赔证书后，应立即无偿换货、补发短缺部分或降低货价，并负担由此产生的到安装现场的换货费用、风险以及买方的检验费用。如卖方对索赔有异议时，应在接到买方索赔证书后２个星期内提出异议，双方另行协商；逾期，索赔即作成立。<BR> 
　　卖方换货和／或补交货物的时间，不迟于卖方收到买方索赔证书后＿＿＿个月。<BR>
        　　７．１０　在开箱检验中，由于买方的原因，发现“设备”有损坏，通知卖方后，卖方应尽快补发、更换，其费用由买方负担。<BR> 
　　７．１１　上述检验并不能解除卖方对第八章、第九章所承担的责任。<BR>
        　　７．１２　在检验中，如发现卖方提供的检验所需的标准仍不完整或提供的不及时，经与卖方协商，买方有权按照买方国家现行标准进行检验。<BR> 
