        　　８．６　投料试生产期为首次投料生产开始日起的＿＿＿个月。在此期间，当合同工厂主要设备达到良好稳定运行后由双方代表商定首次考核日期。考核应按本合同附件＿＿＿的规定在卖方技术人员指导下进行。<BR> 
　　每次考核的结果应作出记录，在每次考核完成后３天内双方在性能考核报告上签字确认。<BR>
        　　８．７　如按本合同附件＿＿＿规定的考核期内实现了本合同附件＿＿＿规定的全部保证数值时，双方代表应在５天内签署合同工厂验收证书一式４份，双方各执２份，此即视作合同工厂为买方所验收。<BR>
        　　如因卖方原因致任何一次考核未能成功时，卖方应尽快在双方同意的期限内对“设备”进行必要的修理、更换和／或修改。修改后应重新按照本合同附件＿＿＿＿的规定尽快再次考核，买方应大力协助。<BR>
        　　如上述修理、更换和／或修改在现场进行，所需费用（如工时费、材料费等）应在合同工厂交接验收前，由双方授权代表根据修理、更换和修改记录最后一次结算并由卖方负担。<BR> 
　　上述记录在每次发生修理、更换和／或修改后由双方代表会签。<BR>
        　　如因卖方原因，需将任何设备运出中国以外进行修理或更换时，全部运费、修理或更换费用应由卖方负担。更换或修理的“设备”应在合同工厂现场交货。<BR>
        　　８．８　在本合同８．６条规定的投料试生产期间，本合同附件＿＿＿中规定的保证数值如有任何一项或多项未能达到时，双方应会同研究，找出原因，澄清责任，并按以下规定处理：<BR>
        　　８．８．１　如由于卖方原因未能达到保证数值时，买方同意延长投料试生产期３个月，以便卖方对合同工厂进行改进并再次进行考核。如在延长的３个月中由于卖方原因仍未能达到保证数值时，买方同意再次延长投料试生产期３个月。如在再次延长的３个月期满时，由于卖方原因，考核仍失败，未能达到保证数值时，应按本合同第九章第９．８条的规定办理。在两次延长的两个３个月内改进合同工厂所需全部费用和卖方技术人员的全部费用均由卖方负担。<BR>
        　　８．８．２　如由于买方原因未能达到保证数值时，投料试生产期应延长３个月。在此期间，买方将按本合同附件＿＿＿的规定继续支付卖方技术人员的费用。如由于买方原因，改进合同工厂所需费用由买方负担。所需卖方技术人员的人数由双方讨论决定，如在此延长的３个月期满时，仍由于买方原因未能达到保证数值时，合同工厂应由买方所验收，双方应在７天内签署验收证书。<BR>
        　　然而卖方应协助买方采取一切必要措施，使合同工厂达到正常生产所需的指标。由此而产生的费用由买方负担。<BR>
