        　　投料试生产和考核所需的仪表校准、记录项目、取样方法和分析方法等详细程序应由卖方在安装及试车完成日前提出，并经双方代表讨论决定。<BR>
        　　在投料试生产前，买方应准备充足的维修工具、实验室及检验设施和熟练的操作、维修及测试人员，其中包括本合同附件＿＿＿＿第＿＿＿＿条所列的人员，并准备好按本合同附件＿＿＿＿所列必要数量和质量的全部原料、材料和设施。卖方技术人员可出入试验室和检验设施，以便取样分析。<BR> 
　　投料试生产和性能试验期间的采样、化验将在双方代表在场的情况下进行。<BR> 
　　除已同意的程序外，卖方技术人员认为必要的采样和化验，在经与买方代表协商后由买方进行。<BR>
