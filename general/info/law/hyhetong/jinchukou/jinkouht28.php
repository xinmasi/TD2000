　　第八条　安装、试车和验收（方案一）<BR> 
　　<BR>
        　　８．１　“安装”系指合同工厂全部设备、材料的装配、就位和联接等安装工作。“试车”系指机器和／或设备的单独或联动的试运转。<BR> 
　　“投料试生产”系指合同工厂投入原料和公用工程以试生产。<BR> 
　　“考核”系指检查本合同附件＿＿＿所规定的各项保证数值而进行的试验。<BR>
        　　“验收”系指如果考核结果表明，本合同附件＿＿＿所规定的各项保证指标能够全部达到，则合同工厂即为买方所验收。<BR>
        　　８．２　合同工厂的安装将在买方负责组织下和在卖方负责技术指导下进行，卖方仅对其进行详细设计有关的所有“设备”的安装以及界区接点的安装进行技术指导。<BR>
        　　合同工厂的试车、投料试生产和考核应在买方组织安排下和卖方技术指导下进行。在安装工作开始前２个月，双方各自授权一名代表处理合同工厂从安装到验收期间有关合同工厂的全部技术工作。具体工作应由双方代表友好协商安排。双方代表应充分合作，使合同工厂在本合同生效日后＿＿＿个月内建设完毕。<BR> 
　　如果双方之间有任何问题和分歧时，双方将分析原因，澄清责任并在现场通过友好协商解决。<BR>
        　　８．３　在安装工作开始前，卖方技术人员应详细介绍安装方法和要求。在安装期间卖方技术人员应对安装工作进行技术指导并参加所有设备安装质量的检验和试验。卖方技术人员的重要技术指导应以书面提出。<BR>
        　　８．４　安装完毕后，如双方代表认为安装工作完全符合设计要求时，双方代表应按技术文件和图纸一起进行检验和试车。双方代表将签订同类设备安装证书及单机试车和机械与设备的系统联运证书。<BR> 
