　　……市　　　　１９９＿＿年＿＿月＿＿日<BR> 
　　俄罗斯莫斯科市＿＿＿＿外贸公司（下称外贸公司）与中国哈尔滨市＿＿＿＿公司（下称公司）签定本合同如下：<BR> 
　　一　合同的标的；价格和总价<BR> 
　　外贸公司在俄中国界车上交货条件下按本合同附件１向公司提供商品。商品以美元计价，系俄中国界车上交货价，包括包皮、包装和标记的费用。<BR> 
　　根据附件１由俄罗斯向中国供货的总值为＿＿＿＿美元。<BR> 
　　公司相应地在中俄国界车上交货条件下按本合同附件２向外贸公司提供商品。商品以美元计价，系中俄国界车上交货价，包括包皮、包装和标记的费用。<BR> 
　　根据附件２由中国向俄罗斯供货的总值为＿＿＿＿美元。<BR> 
　　二　供货期<BR> 
　　售方应在本合同附件规定的期限内交货。售方有权按双方商定的数量和金额提前供货。购方有义务按合同规定接收货物。<BR> 
　　三　结算<BR> 
　　本合同所供货物之价款，在易货基础上以美元计价，不通过银行记帐。货物交接后，由售方商务代表到购方结算，或将结算凭证寄给购方进行结算，并凭下列单据办理：<BR> 
　　１．发货帐单２份；<BR> 
　　２．盖有发货站戳记的铁路运单副本１份<BR> 
　　３．明细单２份<BR> 
　　４．品质证明书１份<BR> 
　　购方接到上述单据核对无误后给售方出以等值易货贸易结算凭证予以确认。<BR> 
　　四　包装<BR> 
　　卖方应在包装货物时采取所有预防措施以保证货物在储存、海运、陆运、吊装时完好无损。<BR> 
　　五　商品的品质和保证<BR> 
　　所供商品的品质应由品质证明书加以确认，该证书确认商品品质符合生产国的技术条件和国家标准。<BR> 
　　所供商品的品质性能应与标准样品相一致，标准样品在签定合同时交给买方，在保证期内留存买方并在对供货品质发生争议的情况下供双方使用。<BR> 
　　保证期为供货后九个月。<BR> 
　　六　索赔<BR> 
　　购方可按１９９０年３月１３日“由中国向苏联和苏联向中国交货的共同条件”所规定的期限和程序在下列方面提出索赔。<BR> 
　　１．货物的数量<BR> 
　　如货物数量与明细单注明的数量不符，在包装完整和没有外部损伤（内部短缺）的情况下，购方有权凭检验证书提出索赔。<BR> 
　　如果货物的发运系按发货人确定的重量发出，而国境交接站双方铁路交接中发现不足，并从实际情况看来，不是铁路方面的过失。可根据双方铁路方面编制的商务记录提出索赔。<BR> 
　　２．货物的质量<BR> 
　　如货物品质与合同规定不符时，可根据商品检证或无利害关系的权威机关的代表参与制成的记录提出异议。<BR> 
　　如售方所供货物的品质，不符合合同规定的技术条件或与双方确认的样品不符时，购方有权要求售方或者削价、或者更换货物。如果售方自收到异议之日起６０天内不作最后决定，或不同意检验证书中确定的削价百分比时，则购方有权将品质不合格的货物按售方提供的地址退给售方。售方应在本合同规定的异议审理期限内将返回地址通知购方。<BR> 
　　如果在每批货物中发现残次品占２０％以上，收货人则将退回全部货物。由于质量原因退回货物时所产生的全部费用由售方承担。<BR> 
　　在这种情况下，售方没有免除向购方补发数量相同并符合合同规定的货物的责任。<BR> 
　　在终点站检验货物的数量和／或质量或需要的一切费用由购方承担。<BR> 
　　七　不可抗力条款<BR> 
　　由于发生不可抗力情况（如：火灾、水灾、地震等），而直接影响本合同的履行时，售方对本合同的部分义务或全部义务无法履行的责任不予承担。经双方协商，履行本合同义务的期限也可相应推迟。发生不可抗力情况一方应自灾情结束之日起，十日内将有关发生不可抗力的性质、毁坏程度及影响合同履行的情况书面通知对方。如对方有异议，发生不可抗力情况一方凭其国家有关机关的认证书豁免责任，中方为中国贸促会，俄方为俄罗斯工商会。<BR> 
　　八、仲裁<BR> 
　　由本合同所产生或与本合同有关的一切纠纷，应尽可能通过双方谈判解决。如双方不能达成协议，可通过被告国对外贸易仲裁机关审理。<BR> 
　　九　其它条件<BR> 
