　　日签订.<BR> 
　　1,反购金额:<BR> 
　　第____号合同/电传项下由，买方购置的________货物的金额为由买方出售给卖方的货物的金额,即__________美元.<BR> 
　　2,时间:<BR> 
　　买方的反购义务务必于________之前履行.<BR> 
　　3,货物:<BR> 
　　卖方保证,在反购义务履约时限内,向买方提供下列货物:________________________________并予以交货.<BR> 
　　4,出售货物的企业:<BR> 
　　兹同意,买方可向下列出售货物的企业采购,以履行其反购义务:________________________________________________________________________________.5,交货:<BR> 
　　(1)交货时间应准确无误.<BR> 
　　(2)货物可交至任一目的地,但____________________________________除外.6,支付条款:<BR> 
　　由买方购货付款的条件是:____________________________________________.7,违约条款:<BR> 
　　买方未履行本合同项下采购货物义务时,买方向卖方仅支付未履行反购义务部份的金额,以求补偿.买方不承担上述未履约而引起直接的或间接的,可预见的或不可预见的或带有后果性的损失或赔偿金.<BR> 
　　<BR> 
　　一.反购条件(2)<BR> 
　　<BR> 
　　1,概述:<BR> 
　　(1)买方承担的反购义务包括下列条件,除买方另有特别的书面变更,适用变更的条件外,卖方的报价或报价所陈述的出售条件,或在卖方销售合同中所陈述的条件或卖方规定的其他条件不予以修改,若废除反购条件需经买方书面同意.<BR> 
　　(2)货物系指买方反购义务的全部或部分货物.<BR> 
　　2,让与第三方:<BR> 
　　买方可将其义务和权利转让给任何人,商号或公司.<BR> 
　　3,反诉:<BR> 
　　不论卖方对买方有何索赔要求,凡根据已签合同中所规定的买方售给卖方的货款应予以支付.<BR> 
　　4,价格:<BR> 
　　依据反购义务条款规定,买方所支付的价格应为市场价格且应考虑到交货条件.5,所有权的转移:<BR> 
　　货物的所有权,或应在交货时转移,或应在付款时转移.<BR> 
　　6,货物的特殊性质:<BR> 
　　若货物具有其特殊性质,卖方应立即用书面通知买方,向买方提供必要的应提供的信息,此信息系作为货物说明书的组成部份.<BR> 
　　7,合同和仲裁的法律:<BR> 
　　(1)凡适用所述条件的合同均应受_________________法律的管辖.<BR> 
