      <p> 　　<BR> 
　　反购贸易或者称互购亦称反向购买(Counterpurchase).贸易的双方因受某些条件的限制和约束,不能直接广泛地采用不限额的自由外汇进行贸易,故采用互购方式进行贸易.<BR> 
　　西方资本主义国家与苏联,东欧国家采用互购贸易,其特点是贸易双方的任一方所出口货物的总金额相当于,略高于或略低于进口货物的总金额,以求进出口贸易额基本平衡.<BR> 
　　为此,进出口商品的交换期限较短,进出口货单是按年限,批量进行,也就是说,进口一批货物并随之出口一批货物而且在指定的年限,限定的货物,双方各自进口的货物都以现金支付给另一方,而且买方或卖方可将货物转让给第三方.<BR> 
　　贸易双方交换货物的企业,厂家可由一家或由几家来承担,故反购贸易合同的条款较复杂,其来往关系必须用条款予以明文规定.<BR> 
　　兹介绍互购标准合同实例, 其中包括五个部分, 但非每份订单的互购贸易都如此典型,应视贸易程序和实际情况来书就合同条款.<BR> 
　　<BR> 
　　反购协议书<BR> 
　　<BR> 
