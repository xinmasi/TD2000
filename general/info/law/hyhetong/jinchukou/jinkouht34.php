　　<BR> 
　　一,反购条件(3)<BR> 
　　1,所有权的转移:<BR> 
　　货物的所有权,或应在交货时转移,或应在付款时转移.<BR> 
　　2,担保:<BR> 
　　(1)货物在交付给买方后30天内,若发现该货物有任何缺陷,买方可用书面通知卖方,且买方有权按以下第5条规定取消其安排.<BR> 
　　(2)卖方担保货物的规格与卖方的说明书的规格相一致.<BR> 
　　3,不可抗力:<BR> 
　　由于不可抗力或买方无法控制的任何原因,买方未接收,或未就货物作出支付,买方没有责任.<BR> 
　　4,赔偿损失:<BR> 
　　由于卖方未能向买方出售货物而导致未能履行其义务,卖方将保证买方免于受损失(不论是例外的,还是其他的损失).<BR> 
　　5,取消:<BR> 
　　如果卖方未遵守或履行(不论是否由于不可抗力)其向买方出售货物而承担的义务(在不影响上述一般情况下,卖方没有按时交货或没有按同意的质量交货),<BR> 
　　或卖方不能(不论是否由于不可抗力)遵守和履行上述的义务时,不论出于何种原因,买方应有下列权利:<BR> 
　　(1)向卖方退还已交付给买方的全部或任何一部份货物(不论所有权是否已转给买方),风险由卖方承担,和/或全部或部分取消给卖方的定单(不论取消时货<BR> 
　　物是否已交付给买方,不论货物的所有权是否已转移给买方). 在上述任何一种情况下,卖方为应买方的请求,立即将买方已支付给卖方的金额退还给买方,买方对卖方不应承担任何义务.<BR> 
　　(2)对第5条不作任何限制的情况下,买方的反购义务将被取消,买方应按其<BR> 
　　退还货物的金额解除其反购义务,和/或按全部或部份取消其货物定单的金额解除反购义务.<BR> 
　　(3)第5条项下准予买方行使的任何权利不应损及或影响今后买方应采取的或将采取的行动或赔偿.<BR> 
　　6,侵犯专利权:<BR> 
　　由于使用或出售货物而侵犯或被诉侵犯专利证书,注册设计,商标或贸易名称权利而引起的向买方提出的任何法律行动,索赔,及所需费用,以及买方对这类侵权采取任何法律行动可能发生的全部费用和损失,卖方应向买方赔偿损失.<BR> 
