　　二,预购定单.<BR> 
　　三,购货定单.<BR> 
　　四,向第三方发出的出售反购货物的标准电传报价.<BR> 
　　五.转让合同.<BR> 
　　涉及的三方有:<BR> 
　　X方:卖方/反购方(称X方或买方)<BR> 
　　Y方:买方/反售方(称Y方或卖方)<BR> 
　　Z方:X 方向Y方承担的反购义务之受让方.<BR> 
　　<BR> 
　　一,反购的特殊条件(1)<BR> 
