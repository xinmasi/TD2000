　　在“确认日期”前一天从办公完毕时间至２４：００时抵装港的油轮，在办完入港手续后，如有条件提前装油者，以开装时间起算，如没有条件者，则以确认日下午二时起算．在“确认日期”以后抵装港的油轮，以开装时间起算．<BR> 
