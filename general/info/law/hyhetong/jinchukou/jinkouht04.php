　　②　由买方所派油轮，当在装港遇事故而发生油类和油类混合物的污染或有发生这类污染的可能性时，船东或船长应迅速采取清除措施，防止油污而造成的损失，或为避免油污损失进一步扩大所采取合理而有效的措施．但船东或船长不采取上述措施时，卖方在通知船东或船长之后，可以采取合理的必要的措施进行清除工作和减轻油污可能造成的损失．卖方应随时将所要采取的措施和后果告诉船东或船长，如时间允许，应在采取措施前，将拟采取的措施告诉船东或船长．卖方采取的上述措施可以认为是按船东或船长委托进行的．在这种场合，对于被委托者采取的措施，委托者有权按实际情况向对方提出异议或者通知其停止工作．委托人提出通知后，被委托者则应无权再继续进行工作．<BR> 
