      <p> 　　<BR> 
　　订立合同双方：<BR> 
　　<BR> 
　　抵押人：＿＿＿＿＿，以下简称甲方：<BR> 
　　抵押权人：＿＿＿＿，以下简称乙方。<BR> 
　　<BR> 
　　鉴于甲方欠乙方货款（或贷款）＿＿＿＿元暂时不能偿还，甲方为担保还款，经与乙方协商一致，特订立本抵押合同。<BR> 
　　第一条　抵押物的名称、数量和价值<BR> 
　　１．名称：＿＿＿＿＿。<BR> 
　　２．数量：＿＿＿＿＿。<BR> 
　　３．价值：＿＿＿＿＿。<BR> 
　　第二条　抵押期限<BR> 
　　抵押期限为＿＿＿＿年，自＿＿＿＿年＿＿月＿＿日起，至＿＿＿＿年＿＿月＿＿日止。<BR> 
　　第三条　抵押物的清点、暂管和保险<BR> 
　　１．清点：本合同生效后五天内，甲、乙双方共同清点检查抵押物的数量、质量，并列出清单，经核实无误后双方在清单上签名，加盖公章，以示认可。<BR> 
　　２．暂管：抵押物仍由甲方负责暂管完整无损，一切仓储及其它管理费用均由甲方承担。<BR> 
　　３．保险：在合同生效后五天内，甲方应向保险公司投保仓库财产保险，并将保险后的财产过户给乙方。投保的抵押物由于不可抗力遭受损失，乙方则从保险公司直接取得全部赔偿金作为归还所欠款的一部分。<BR> 
　　第四条　抵押物在抵押期限内的销售和监督<BR> 
　　１．抵押物的销售，仍由甲方负责，甲方应组织人员积极推销，并将所销售的货款直接交入乙方指定的帐户，作为偿还欠款本息的资金来源之一。<BR> 
　　２．甲方在与需方签订供销合同时，应在合同中写明款项汇入＿＿＿银行＿＿＿分行＿＿＿帐户，即乙方指定的帐户。甲方在发货之前，应提前三个工作日将销售合同提交乙方审，在外地签订的销售合同，应及时将合同副本或影印件提交甲方审定后方可发货。<BR> 
　　３．甲方每月应向乙方提供财务计划、物资库存、财务会计报表及有关经济资料，乙方认为必要时，有权进行检查抵押物的库存，销售情况以及与抵押物有关的帐目资料，甲方应给予协助。<BR> 
　　第五条　甲方的义务及违约责任<BR> 
　　１．甲方应保证是该抵押的合法所有权人，今后如因该抵押物的所有权归属问题发生纠纷，并因此而引起乙方的损失时，甲方应负责赔偿。<BR> 
　　２．在本合同签订之后，甲方应将与抵押物有关的一切原始单证、票据交给乙方。<BR> 
　　３．甲方应妥善保管抵押物，不得遗失、毁损，甲方如因故意或过失造成抵押物毁损，应在十五天内向乙方提供新的抵押物。<BR> 
　　４．甲方未经乙方同意，不得将抵押物转让、出售、再抵押或以其它方式处分。<BR> 
　　甲方如违反前款规定，乙方有权立即暂管抵押物，并书面通知甲方。甲方应在接到通知书后三天内，将抵押物交给乙方，逾期不交者，乙方可依法向＿＿＿人民法院申请强制执行。乙方因此所受的经济损失由甲方赔偿。<BR> 
　　５．甲方如违反本合同第四条第二项之规定，处分抵押物的行为无效，乙方因此所受的经济损失由甲方赔偿。<BR> 
　　第六条　抵押物的处分、处分方式和还款项的使用顺序<BR> 
　　１．本合同期满，甲方尚不能还清欠款本息者，乙方有权向＿＿＿人民法院申请处分抵押物。<BR> 
　　２．抵押物处分方式和程序由＿＿＿＿人民法院裁定。<BR> 
　　３．抵押物的价格由＿＿＿市物价局作价。<BR> 
　　４．处分抵押物所得的款项，接下列顺序使用。<BR> 
　　第一：支付处分抵押物的费用。<BR> 
　　第二：缴纳抵押物的税金。<BR> 
　　第三：偿还欠乙方贷款的税金。<BR> 
　　如扣除以上款项，尚有余额，应全部交还甲方。如处分抵押物所得的款项仍不足以抵还欠款本息者。乙方仍可根据原借款合同向债务人追索欠款。<BR> 
　　第七条　其它<BR> 
　　１．本合同经甲、乙双方法定代表人签字加盖公章并经＿＿＿市公证处公证后生效，公证费用由甲方承担。<BR> 
　　２．当甲方抵押期限到期，仍因实际困难无法如期偿清贷款本息、要求延长抵押期限者，经甲方提出书面申请，乙方审查同意并签订补充协议和作为本合同书的附件，可以延长抵押期限。<BR> 
　　３．本合同系经＿＿＿市公证处依法赋予强制执行力的债权文书。任何一方当事人如不履行合同，对方当事人可根据《民事诉讼法》第一百八十六条之规定，直接向＿＿＿人民法院申请强制执行。<BR> 
　　４．本合同未尽事宜，甲、乙双方可另行协商，签订补充协议。补充协议与本合同具有同等效力。<BR> 
　　本合同正本一式三份，甲乙双方各执一份，公证处存档一份。副本一式＿＿份，送＿＿＿等有关单位各留存一份。<BR> 
