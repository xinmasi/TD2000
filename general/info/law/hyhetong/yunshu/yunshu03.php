　　赔要求，但船东应负责由于本租船合同船只交付时存在的船舶、船舶机器或装置的潜在缺陷所引起的修<BR> 
　　理和换新，但这些缺陷必须是在交船后１８个月内出现，除非对此在第３２栏另有订明。<BR> 
　　２．交船时间<BR> 
　　交船时间不得早于第１６栏栏中列明的日期，除非经租船人同意者外。<BR> 
　　对于船舶预计何时可准备好交付，船东要给租船人不少于３０个连续天的通知，除非第１９栏另有<BR> 
　　协议者外，对于船舶情况的变化，船东要紧密地和租船人通消息。<BR> 
　　３．取消合同<BR> 
　　如果船舶最迟不能在第１７栏栏中订明的租船合同取消日期交付的话，租船人有取消本租船合同的<BR> 
　　选择权。<BR> 
　　如果看来船舶将延滞到租船合同取消日期之后才可交付的话，船东应在能够合理肯定船只将予何日<BR> 
　　备妥时尽快给租船人发通知，问租船人是否将行使他们可取消租船合同的选择权，而租船人必须在收到<BR> 
　　该通知后的１６８小时内宣布其选择决定，如果租船人不行使其取消合同的选择权，那条船东通知中的<BR> 
　　船舶备妥日期之后的第７天将认为是本条款中的一个新的租船合同取消日期。<BR> 
　　４．航行区域范围<BR> 
　　船舶将用于进行合法的贸易，在第２１栏栏内列明的航行区域范围内载运合适的合法货物。<BR> 
　　不管本租船合同中有其他条款规定，双方协议本租船合同中准予装载或运输的货物中明确排除核燃<BR> 
　　料或放射性产品或原料。但对于使用于或拟使用于工业、商业、农业、医药或科学方面的放射性同位素<BR> 
　　不在排除之列，但要事前取得船东对装运的批准。<BR> 
　　５．检验<BR> 
　　交船和还船检验，船东和租船人将各指定验船师测定并书面证明船舶在交付时和还船时的状况。起<BR> 
　　租检验的费用和如有时间损失由船东负担，退租检验的费用和如有时间损失则由租船人负担，如有时间<BR> 
　　损失按每天的租金率或按比例计算，如需要进坞，进出坞费用亦包括在以上费用内。<BR> 
　　６．检查<BR> 
　　船东有权在任何时候对船舶进行检查和检验或指示一位正式授权的验船师代表其进行检验以查明船<BR> 
　　舶的状况和确信船舶获得正常的修理和保养，在干船坞内的检查或检验只有在租方安排船舶进干船坞时<BR> 
　　进行，可是如果租船人未在正常的船级检查间隔安排船舶进坞，则船东有权要求安排船舶进干坞检查，<BR> 
　　如查明船舶处于本租船合同第八条规定的状况时，则检查和检验费用由船东负担，只有在查得船舶须经<BR> 
　　处理或保养以达到所规定的状况时，检查和检验费用才由租船人负担，所有检查、检验和修理的时间作<BR> 
　　为租期内的计算并作为租期的一部分。<BR> 
　　租船人每当船东提出要求时应容许船东检查船舶的航海日志和每当船东需要时向他们提供伤亡事故<BR> 
　　或其他事故或船只遭受损坏的全部情况。如有需要，租船人应不时将打算安排的船舶任务通知船东。<BR> 
　　７．财产目录和消耗的油和供应品<BR> 
　　在接船和以后还船时租船人应会同船东对船上的整个设备，全套装备装置和船上的所有消耗供应品<BR> 
　　编制一套完整的财产目录。租船人和船东将分别在交船和还船时接收船上的所有燃料，润滑油，水和未<BR> 
　　启封的供应品，并分别按交船港和还船港的当时市价支付费用。<BR> 
