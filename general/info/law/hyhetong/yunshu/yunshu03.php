　　船东应在交船前和交船时做到克尽职责使船适航，并在船壳、机器和设备的各个方面准备好投入下<BR> 
　　述的服务。船舶在交付时应具备各项有效使用的证书。船交给租船人和租船人接了船就作为船东完全履<BR> 
