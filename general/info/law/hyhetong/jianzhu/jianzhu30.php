　　停、窝工损失的，应由发包方承担。<BR> 
　　六、本合同造价结算方式：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　七、承包方在单项工程竣工验收后＿＿天内，将竣工结算文件送交发包方和经<BR> 
　　办银行审查，发包方在接到结算文件＿＿天内审查完毕，如到期未提出书<BR> 
　　面异议，承包方可请求经办银行审定后拨款。<BR> 
　　第七条　施工与设计变更<BR> 
　　一、发包方交付的设计图纸、说明和有关技术资料，作为施工的有效依据，开<BR> 
　　工前由发包方组织设计交底和三方会审作出会审纪要，作为施工的补充依<BR> 
　　据，承、发包双方均不得擅自修改。<BR> 
　　二、施工中如发现设计有错误或严重不合理的地方，承包方及时以书面形式通<BR> 
　　知发包方，由发包方及时会同设计等有关单位研究确定修改意见或变更设<BR> 
　　计文件，承包方按修改或变更的设计文件进行施工。若发生增加费用（包<BR> 
　　括返工损失、停工、窝工、人员和机械设备调迁、材料构配件积压的实际<BR> 
　　损失）由发包方负责，并调整合同造价。<BR> 
　　三、承包方在保证工程质量和不降低设计标准的前提下，提出修改设计、修改<BR> 
　　工艺的合理化建议，经发包方、设计单位或有关技术部门同意后采取实施<BR> 
　　，其节约的价值按国家规定分配。<BR> 
　　四、发包方如需设计变更，必须由原设计单位作出正式修改通知书和修改图纸<BR> 
　　，承包方才予实施。重大修改或增加造价时，必须另行协商，在取得投资<BR> 
　　落实证明，技术资料设计图纸齐全时，承包方才予实施。<BR> 
　　第八条　工程验收<BR> 
　　一、竣工工程验收，以国家颁发的《关于基本建设项目竣工验收暂行规定》、<BR> 
　　《工程施工及验收规范》、《建筑安装工程质量检验评定标准》和国务院<BR> 
　　有关部门制订的竣工验收规定及施工图纸及说明书、施工技术文件为依据。<BR> 
　　二、工程施工中地下工程、结构工程必须具有隐蔽验收签证、试压、试水、抗<BR> 
　　渗等记录。工程竣工质量经当地质量监督部门检验合格后，发包方须及时<BR> 
　　办理验收签证手续。<BR> 
　　三、工程竣工验收后，方包方方可使用。<BR> 
