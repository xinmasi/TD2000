　　每次连续４小时以上），影响正常施工；<BR> 
　　５．非承包方原因而监理签证不及时而影响下一道工序施工；<BR> 
　　６．未按合同规定拨付预付款、工程进度款或代购材料差价款而影响施工；<BR> 
　　７．人力不可抗拒的因素而延误工期。<BR> 
　　第四条　工程质量<BR> 
　　一、本工程质量经双方研究要求达到：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　二、承包方必须严格按照施工图纸、说明文件和国家颁发的建筑工程规范、规<BR> 
　　程和标准进行施工，并接受发包方派驻代表的监督。<BR> 
　　三、承包方在施工过程中必须遵守下列规定：<BR> 
　　１．由承包方提供的主要原材料、设备、构配件、半成品必须按有关规定提供<BR> 
　　质量合格证，或进行检验合格后方可用于工程；<BR> 
　　２．由发包方提供的主要原材料、设备、构配件、半成品也必须有质量合格证<BR> 
　　方可用于工程。对材料改变或代用必须经原设计单位同意并发正式书面通<BR> 
　　知和发包方派驻代表签证后，方可用于工程；<BR> 
　　３．隐蔽工程必须经发包方派驻代表检查、验收签章后，方可进行下一道工序；<BR> 
　　４．承包方应按质量验评标准对工程进行分项、分部和单位工程质量进行评定<BR> 
　　，并及时将单位工程质量评定结果送发包方和质量监督站。单位工程结构<BR> 
　　完工时，应会同发包方、质量监督站进行结构中间验收；<BR> 
　　５．承包方在施工中发生质量事故，应及时报告发包方派驻代表和当地建筑工<BR> 
　　程质量监督站。一般质量事故的处理结果应送发包方和质量监督站备案；<BR> 
　　重大质量事故的处理方案，应经设计单位、质量监督站、发包方等单位共<BR> 
　　同研究，并经设计建设单位签证后实施；<BR> 
　　６．工程竣工后，承包方按规定对工程实行保修，保修时间自通过竣工验收之<BR> 
　　日算起。<BR> 
