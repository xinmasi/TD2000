　　保修。其保修条件、范围和期限按城乡建设环境保护部（８４）城建字第<BR> 
　　７９号通知印发的《建筑工程保修办法（试行）》执行。<BR> 
　　第九条　违约责任<BR> 
　　承包方的责任：<BR> 
　　一、工程质量不符合合同规定的，负责无偿修理或返工。由于修理或返工造成<BR> 
　　逾期交付的，偿付逾期违约金。<BR> 
　　二、工程不能按合同规定的工期交付使用的，按合同中第九条关于建设工期提<BR> 
　　前或拖后的奖罚规定偿付逾期罚款。<BR> 
　　发包方的责任：<BR> 
　　一、未能按照合同的规定履行自己应负的责任，除竣工日期得以顺延外，还应<BR> 
　　赔偿承包方由此造成的实际损失。<BR> 
　　二、工程中途停建、缓建或由于设计变更以及设计错误造成的返工，应采取措<BR> 
　　施弥补或减少损失。同时，赔偿承包方由此造成的停工、窝工、返工、倒<BR> 
　　运、人员和机械设备调迁、材料和构件积压的实际损失。<BR> 
　　三、工程未经验收，发包方提前使用或擅自动用，由此而发生的质量或其他问<BR> 
　　题，由发包方承担责任。<BR> 
　　四、承包方验收通知书送达＿＿＿＿＿＿＿＿日后不进行验收的，按规定偿付<BR> 
　　逾期违约金。<BR> 
　　五、不按合同规定拨付工程款，按银行有关逾期付款办法的规定延付金额每日<BR> 
　　万分之三偿付承包方赔偿金。<BR> 
