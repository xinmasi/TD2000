　　工图＿份，施工技术资料（包括地质及水准点座标控制点）＿＿＿份。<BR> 
　　５．组织承、发包双方和设计单位及有关部门参加施工图交底会审，并做好三<BR> 
　　方签署的交底会审纪要，在＿天内分送有关单位，＿天内提供会审纪要和<BR> 
　　修改施工图＿＿份。<BR> 
