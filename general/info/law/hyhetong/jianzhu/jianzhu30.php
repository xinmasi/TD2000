　　％备料款，计人民币＿＿万元；临时设施费，按土建工程合同总造价的<BR> 
　　＿＿％计人民币＿＿万元，安装工程按人工费的＿＿％计人民币＿＿万元<BR> 
　　；材料设备差价＿＿万元，分＿＿次支付，每次支付时间、金额＿＿＿＿<BR> 
　　＿＿＿<BR> 
　　二、发包方收到承包方的工程进度月报后必须在＿＿日内按核实的工程进度支<BR> 
　　付进度款，工程进度款支付达到合同总价的＿＿％时，按规定比例逐步开<BR> 
　　始扣回备料款。<BR> 
　　三、工程价款支付达到合同总价款的９５％时，不再按进度付款，办完交工验<BR> 
　　收后，待保修期满连本息（财政拨款不计息）一次支付给承包方。<BR> 
　　四、如发包方拖欠工程进度款或尾款，应向承包方支付拖欠金额日万分之＿＿<BR> 
　　的违约金。<BR> 
