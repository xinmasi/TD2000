　　成品、半成品等进场计划（包括月计划），用水、用电计划，送发包方。<BR> 
　　第三条　工程期限<BR> 
　　一、根据国家工期定额和使用需要，商定工程工期为＿＿＿天（日历天），自<BR> 
　　＿＿年＿月＿日开工至＿＿年＿月＿日竣工验收（附各单位工程开竣工日<BR> 
　　期，见附表一）。<BR> 
　　二、开工前＿＿天，承包方向发包方发出开工通知书。<BR> 
　　三、如遇下列情况，经发包方现场代表签证后，工期相应顺延：<BR> 
　　１．按施工准备规定，不能提供施工场地、水、电源道路未能接通，障碍物未<BR> 
　　能清除，影响进场施工；<BR> 
　　２．凡发包方负责供应的材料、设备、成品或半成品未能保证施工需要或因交<BR> 
　　验时发现缺陷需要修、配、代、换而影响进度；<BR> 
　　３．不属包干系数范围内的重大设计变更，提供的工程地质资料不准，致使设<BR> 
　　计方案改变或由于施工无法进行的原因而影响进度；<BR> 
