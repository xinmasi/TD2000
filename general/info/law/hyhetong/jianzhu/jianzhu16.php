      <p> 　　<BR> 
　　雇主：＿＿＿＿＿＿＿＿<BR> 
　　承包人：＿＿＿＿＿＿＿<BR> 
　　<BR> 
　　工程项目及相关文件：＿＿＿＿＿＿＿<BR> 
　　上述双方经友好协商，达成如下合同条款：<BR> 
　　<BR> 
　　第一部分　共同条件<BR> 
　　<BR> 
　　定义与解释<BR> 
　　定义<BR> 
　　第一条　本合同（按下文规定的定义）中的下列词和用语，除文中另有要求者外，应具有此处为其所规定的含义：<BR> 
　　（１）“雇主”是指合同条件第二部分中指明的已就工程的建筑、安装或交付进行招标并将雇用承包人的一方以及雇主名下的合法继续人，但不包括（经承包人同意者除外）雇主的任何受让人。<BR> 
　　（２）“承包人”是指其投标已被雇主接受的个人或多人、商号或公司，并包括承包人的私人代表、继承人和经许可的受让人。<BR> 
　　（３）“工程师”是指第二部分中指定的工程师，或由雇主随时指派并书面通知承包人要为本合同的目的替代上述指定的工程师以工程师身份行事的工程师。<BR> 
　　（４）“工程师的代表”是指驻工地工程师或工程师的助理，或任何由雇主或工程师随时派来履行本文件第二条所规定的职责的工程建筑管理员，其职权应由工程师书面通知承包人。<BR> 
　　（５）“工程”是指依照本合同应进行的工程。<BR> 
　　（６）“合同”是指合同条件、说明书、图纸、经标价的建筑工程清单、费率与价格表（如果有的话）、投标书和合同协议。<BR> 
　　（７）“合同价格”是指投标书所列明的，但经根据下文所载规定予以增减的金额。<BR> 
　　（８）“施工设施”是指为本工程或临时工程（按本条下述定义）的进行、建成或维护方面所需的一切设备器械或诸如此类性质的各物，但不包括打算用以构成或正在构成永久性工程组成部分的材料或其它各物。<BR> 
　　（９）“临时工程”是指为本工程的进行、建成或维护方面所需的所有各种临时工程。<BR> 
　　（１０）“图纸”是指说明书中所提到的图纸和任何经工程师书面认可的对这类图纸所作的修改，以及工程师随时提供或书面认可的其它图纸。<BR> 
　　（１１）“现场”是指要在其上、其下或其中进行本工程的土地和其它场所，以及任何由雇主为本合同的目的而连同本合同中特别指定为现场组成部分的其它场所一起提供的其他土地或场所。<BR> 
　　（１２）“经认可”是指业经书面认可，包括对先前的口头认可所作的随后的书面确认；而“认可”是指书面认可，包括上述情况。<BR> 
　　单数与复数<BR> 
　　视文中需要，仅表明单数的词也包括复数在内，反之亦然。<BR> 
　　页旁小标题或注释<BR> 
　　这些一般条件中的页旁小标题或注释均不得被认为是本合同条件的组成部分，也不得作为解释本合同条件或本合同时的考虑因素。<BR> 
　　工程师的代表<BR> 
　　工程师的代表的责任和权力<BR> 
　　第二条　工程师的代表的责任是视察和监督本工程，并试验与检查有关本工程所要使用的材料与做工。他无权免除承包人根据本合同所承担的任何责任或义务，除本文件下面或本合同其它地方另有明文规定者外，他无权命令进行任何会引起迟延的工程或指示雇主作任何额外支付，也不能对本工程作任何变更。<BR> 
　　工程师可以随时以书面方式把授予工程师的任何权力和授权委托给工程师的代表，并应将所有这种权力和授权的书面委托的副本提交给承包人。工程师的代表在这种委托范围之内（但不是委托范围之外）给予承包人的任何书面指示或认可，应如同工程师所作出的指示或认可一样，对承包人与雇主具有约束力。但是必须以下述为条件：<BR> 
　　１．工程师的代表对任何工作或材料未予不认可，并不损害工程师日后不认可该工作或材料并下令予以推倒、清除或拆毁的权力。<BR> 
　　２．如果承包人对于工程师的代表所作的任何决定感到不满意，他应有权把此问题提交工程师，工程师应随即对该决定予以确认、撤销或更改。<BR> 
　　转让与转包<BR> 
　　１．转让<BR> 
　　第三条　承包人未经雇主事先书面同意，不得转让本合同或本合同的任何组成部分或其中的任何好处或利益（付给承包人银行的任何根据本合同到期应付的款项不在此列）。<BR> 
　　２．转包<BR> 
　　第四条　承包人不得把整个工程转包出去。除本合同另有规定者外，承包人未经工程师事先书面同意（不得无理不予同意）不得把本工程的任何组成部分转包出去，如予同意，不得免除承包人根据本合同所承担的任何责任或义务，承包人对任何分包人或分包人的代理人、雇员或工人的行动、违约和疏忽，就如同对承包人或承包人的代理人、雇员或工人的行动、违约或疏忽一样，应承担全部责任。但是，按计件方式提供劳动力，不应认为是本条所说的转包。<BR> 
　　合同的范围<BR> 
　　第五条　本合同包括本工程的施工、建成和维护，除本合同另有规定者外，还包括提供经本合同指定或从本合同中合理地推知需要提供的为本工程的施工、建成和维护所需的一切劳力、材料、施工设备、临时工程和不论临时性或永久性的各物。<BR> 
　　合同文件<BR> 
　　６．语言<BR> 
　　（１）制订本合同文件所用的语言应在第二部分中予以说明，如果本合同文件用不止一种语言写成，则还应在第二部分中指定解释本合同所据以为准的该种语言，即其中指定的“主要语言”。<BR> 
　　文件互相解释<BR> 
　　（２）除本合同另有规定者外，共同条件和专用条件中的各项条款规定，应成为任何构成本合同组成部分的其它文件中各项条款规定的主导。在上述条件下，构成本合同的若干文件应视为是彼此相互解释的，如有意义不明确或不一致之处，应由工程师对之作出解释和调处，工程师应即为此向承包人发出指示，指导应按何种方式完成该项工作。但是，如果工程师认为，遵照任何这种指示执行而为承包人带来的任何费用，由于文件的这种意义不明确或不一致，并不在而且有理由不在承包人所预计之列，则工程师应予证明，而雇主应按可以合理负担此项费用的金额支付这笔额外款项。<BR> 
　　７．图纸的保管<BR> 
　　（１）图纸应由工程师单独保管，但应向承包人免费提供图纸副本两份。承包人如还需要更多的副本，应由承包人自费提供和制作。在本合同履行完毕时，承包人应把所有根据本合同提供的图纸退还给工程师。<BR> 
　　承包人应就执行根据本合同规定的工程或其它事项所进一步需要的任何图纸或说明书，给工程师或工程师的代表以适当的书面通知。<BR> 
　　图纸副本应有一份保存在现场<BR> 
　　（２）上述提供给承包人图纸副本中一份，应由承包人保存在现场，该分图纸应供工程师、工程师的代表和经工程师书面授权的任何其它人员在一切适当时间检查和使用。<BR> 
　　８．进一步提供的图纸和指示<BR> 
　　工程师应完全有权在本工程进行过程中，随时向承包人提供为了本工程的正确和适当的进行和维护的目的而必须进一步提供的图纸和指示，承包人应予执行并受其约束。<BR> 
　　一般义务<BR> 
　　９．合同协议<BR> 
　　承包人应邀立约时，应按附件所列并视情况经过必要修改的格式签订合同协议（由雇主承担费用制订）。<BR> 
　　１０．履约保证书<BR> 
　　如果投标书中载明，承包人承诺在需要时取得一家保险公司或银行的担保或是提供两名合适而殷实的担保人，同承包人一道负有连带责任地向雇主承担义务，按不超过投标金额１０％的金额根据保证书的条件担保照章履行本合同者，则该保险公司或银行或担保人以及该保证书，均应是须经雇主认可的，而取得这种担保或提供这种担保人以及所要订立的这种保证书的费用，应统由承包人承担，但本合同另有规定者除外。<BR> 
　　１１．现场检查<BR> 
　　投标书应认为是根据雇主在其为投标的目的提交给承包人的文件中所应提供的有关水文、气候和自然条件的数据资料所制订的。但是承包人应检查和考察现场及其周围环境，并应在提交投标书以前（尽可能）确实弄清现场的形状与性质、守成本工程所必需的工作和材料的数量与性质进入现场的交通工具以及他可能需要的居住设备，总之，他应亲自取得一切有关风险、意外事件和其它可能影响其投标的各种情况的必要资料（按上所述）。<BR> 
　　投标的充分条件<BR> 
　　不利的自然条件和人为的障碍<BR> 
　　１２．承包人应被认为在投标以前，已确实弄清他对于本工程的投标以及在标价的建筑工程清单和费率与价格表（如有的话）中所列的费率与价格都是正确和充分的。此费率与价格除本合同另有规定者外，应包括承包人根据本合同所承担的一切义务和为本工程的正确建成与维护所必需的一切事物。但是，如果在本工程进行中，承包人遇到自然条件或人为的障碍，而此种条件或障碍并非一个有经验的承包人所应能合理预见的，则承包人应立即将此书面通知工程师的代表。如果工程师认为，此种条件或人为障碍并非一个有经验的承包人所应能合理预见的，则工程师应予证明，而雇主则应支付由于此种条件而加给承包人的额外费用，包括由于遇到此种条件或障碍而引起的以下适当而合理的费用：<BR> 
　　①遵照工程师在与此有关情况下向承包人所发出的任何指示执行需引起的适当而合理的费用，以及<BR> 
　　②承包人在未得工程师具体指示情况下，可采取的任何经工程师认可的正当合理措施所引起的适当而合理的费用。<BR> 
　　１３．工作必须使工程师满意<BR> 
　　除了法律上不可能或实际上不可能以外，承包人必须严格按照本合同要求进行，建成和维护本工程，做到使工程师满意，并应执行和严格遵循工程师对于涉及或有关本工程的任何事项（不论本合同中是否提到）所作的指示和指导。承包人只领受工程师或（在按照本文件第２条所提到的限制条件下）工程师的代表所作的指示和指导。<BR> 
　　１４．应提供的方案<BR> 
　　在承包人的投标被接受以后，承包人如经要求应当尽快向工程师提交一份表明他所建议本工程进行的步骤次序和方法的方案，请工程师认可，并凡经工程师或工程师的代表提出要求时，应向他们书面提供关于承包人对进行本工程以及承包人打算提供、使用或建造的（视情况而定）施工设备和临时工程所作安排的详细资料。向工程师或工程师的代表提交这种方案并经他们认可，或者向其提供这种详细资料，不应免除承包人根据本合同所承担的任何义务或责任。<BR> 
　　１５．承包人的监督权<BR> 
　　承包人应本工程进行中，并在其后工程师认为是正当履行承包人根据本合同承担的义务所必需的期间，给予或提供一切必要的监督。承包人或经工程师书面认可的（该认可随时可予撤销）一名有法定资格的指定的代表人或代表应当常驻本工程，并应把他的全部时间专用于本工程的监督。如果工程师撤销此项认可，承包人应在接到撤销此项认可的书面通知以后，尽快（在考虑到下文所述需要替换他的情况下）将该处理人撤离现场，而且日后不得再雇用他担任现场任何工作，并应由经工程师认可的其它代理人来替换他。这种指定的代表人或代表应代表承包人领受工程师或（在按本文件第２条规定的限制条件下）工程师的代表所作的指导和指示。<BR> 
　　１６．承包人的雇员<BR> 
　　（１）承包人有关本工程的进行和维护应在现场提供和雇用：<BR> 
　　仅属具有各该行业的熟练技能与经验的技术能手，和能对要求他们监督的工作进行适当监督的助副代理人、领班和工长，以及为本工程的正确而及时的进行和维护所必需的熟练、半熟练和非熟练工人。<BR> 
　　（２）承包人为了或关于本工程的进行或维护而雇用的任何人员，如果工程师认为其行为不当或不称职或玩忽职守，或者工程师另外认为雇用该人是不合需要的，工程师有权反对并要求承包人立即将该人撤离本工程，而且非经工程师书面允许，不得再雇用该人从事本工程。任何因此而撤离本工程的人员应尽快地由经工程师认可的称职的替换人员接替。<BR> 
　　１７．工程定位<BR> 
　　承包人应负责按工程师书面规定的原始基准点、线和高程对本工程进行准确和正确的定位，负责本工程所有各个部分的位置高程、尺寸和定线正确无误（按上所述），并负责提供与此有关的一切必需的仪表器具和劳动力。如果在本工程进展中的任何时候，本工程的任何部分在位置高程，尺寸或定线上出现或发生任何错误，承包人经工程师或工程师的代表要求，应自行承担费用纠正此种错误，使工程师或工程师的代表满意，除非此种错误是由于工程师或工程师的代表书面提供的不正确的数据资料所造成的，在这种情况下，纠正此种错误的费用应当由雇主承担。工程师或工程师的代表对任何定位或任何线或高程所作的检查决不应免除承包人对此项定位的准确性所承担的责任，因此，承包人应当仔细保护和保持本工程定位中所用的一切水准基点、龙门板、标桩和其它各物。<BR> 
　　１８．钻孔和勘探挖掘<BR> 
　　如果在本工程进行中的任何时候，工程师要求承包人进行钻孔或进行勘探挖掘，应以书面命令提出此项要求，而且除非建筑工程清单中已列有关于这种预期的工作的临时金额，否则应被认为是根据本文件第五十一条的规定所命令的一项追加的要求。<BR> 
　　１９．守卫与照明<BR> 
　　承包人在必要的时候和地方，或者经工程师或工程师的代表或者经合法当局提出要求时，应自行承担费用为有关工程提供和保持全部照明、保卫、围栏和看守，以保护本工程或保障公众或其它人员的安全和方便。<BR> 
　　２０．对工程的照管<BR> 
　　（１）从本工程开始到完成，承包人应承担全部责任照管本工程及一切临时工程，如果由于任何原因（本条第（２）款所规定的例外风险除外）使本工程或其中的任何部分或任何临时工程遭受任何损坏、灭失或伤害，承包人应自行承担费用予以修理和弥补，使得本工程在竣工时处于良好的状态，并且在一切方面都符合本合同的要求和工程师的指示。如果任何这种损坏、灭失或伤害，是由于任何例外风险引起的，承包人在经工程师要求并且符合本文件第六十五条规定的情况下，应予以修理和弥补，如上所述，其费用则由雇主承担。承包人还应对他在为了履行本文件第四十九条规定的义务而进行的任何作业过程中所引起的对本工程的任何损害负责。<BR> 
　　例外风险<BR> 
　　“例外风险”有：战争、战争行动（不论是否已经宣战）、入侵、外国敌人的行动、判乱、革命、暴动或者军事政变或篡权、内战或（非承包人自己的雇员之间的）动乱、骚动或骚扰，或者是雇主使用或占有已经对之出具竣工证书的本工程的任何部分，或者是完全由于工程师对本工程的设计所造成的起因，或者是由于并非承包人方面的合理预见性和能力所能预见或合理预防的任何自然力的作用所造成的（所有这些，在此都统称为“例外风险”）。<BR> 
　　２１．工程的保险等<BR> 
