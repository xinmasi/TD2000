　　工程师和他所授权的任何人应能随时进入本工程和现场，以及进入正在为本工程进行工作或提供材料、制成品或机械的一切车间和地方，承包人应为他们进入这些地方或获得进入的权利提供一切便利和协助。<BR> 
　　３８．在覆盖以前对工程的检查<BR> 
　　（１）任何工作未经工程师或工程师的代表认可不得予以覆盖或置于视线之外，承包人应提供充分机会由工程师或工程师的代表检查和测量任何将要覆盖或置于视线之外的工作，并在永久性工程就位以前检查其基础。承包人应在任何这种工程或基础准备就绪或即将准备就绪以供检查时，及时通知工程师的代表，而工程师的代表除非认为此项检查没有必要并相应通知承包人，否则应为检查和测量该工程或检查该基础的目的而出席，不得无理拖延。<BR> 
　　移动覆盖物和打开<BR> 
　　（２）承包人应按工程师不时作出的指示，将本工程任何部分移去覆盖物或从中打开，并应使各该部分恢复原状和修补好，使工程师满意。如果任何各该部分是在依照本条第（１）款的要求执行后予以覆盖或置于视线之外的，并经认为是按照本合同进行的，则将其移去覆盖物、从中打开、恢复原状和修补等各项费用均应由雇主负担，但在任何其它情况下，所有上述各项费用均应由承包人负担，并应由雇主向承包人索还或可由雇主从任何到期应付或将到期应付给承包人的款项中予以扣除。<BR> 
　　３９．清除不适当的工作和材料<BR> 
　　（１）工程师在本工程进行过程中应有权随时发出书面命令：<BR> 
　　①在该命令规定的时间内从现场清除工程师认为不符合本合同要求的任何材料。<BR> 
　　②代之以适当的和合适的材料，并<BR> 
　　③拆除工程师认为其材料或做工不符合本合同要求的任何工程，并按要求重建（不管其以前的任何试验或中期付款）。<BR> 
　　承包人违约<BR> 
　　（２）如果承包人拖欠不执行上述命令，雇主有权雇用其他人员进行此项工作并付给工资，由此产生的或伴随而来的一切费用开支均应由承包人负担，并应由雇主向承包人索还或可由雇主从任何到期应付或将到期应付给承包人的款项中予以扣除。<BR> 
　　４０．工程暂停<BR> 
　　（１）承包人应在接到工程师的书面命令时，按工程师认为必要的时间和方式，暂停进行本工程或其中的任何部分，并应在暂停时间，在工程师认为必要的情况下适当地保护本工程并保证其安全。承包人在根据本条规定执行工程师的指示方面所招致的额外费用，包括所有现场应支付的日常工资、薪水、设备的折旧和保养、现场的杂费和本合同的一般管理费，均应由雇主负担和支付，除非这种暂停是：　　<BR> 
　　①本合同另有规定者，或<BR> 
　　②为适当进行本工程所必需者，或由于天气条件影响本工程的安全和质量，或由于承包人方面的违约，或<BR> 
　　③为本工程或其中任何部分的安全所必需者。<BR> 
　　但是，除非承包人在接到工程师的命令后的２８天内将其索赔意向以书面通知工程师，否则承包人将无权索还任何此种额外费用。工程师应就其认为是公平合理的“索赔要求”，解决和决定应付给承包人的这种额外款项。<BR> 
　　暂停持续超过９０天<BR> 
　　（２）如果根据工程师书面命令（本款称之为“暂停命令”）本工程或其中任何部分的进展暂停一段时期或连续几段时间总计达９０天，或者如果工程师在先前已发出为期不到９０天的暂停命令以后，又在这一暂停期限届满时起不到９０天以内，再次发出不论是关于整个工程的或是（如果先前的暂停命令仅影响本工程的一个部分）影响或包括本工程的该一部分的暂停命令，则在上述任何情况下，承包人可以送交工程师一份书面通知，要求工程师在接到此通知后的２８天内许可继续进行已暂停进行的本工程或其中的该一部分。如果未在上述时间内给予这种许可，承包人通过再送一份书面通知，可以（但并非必须）选择如下：如果此项暂停仅影响本程的一部分，则按根据本文件第５１条的规定删除该部分对待；如果此项暂停涉及整个工程，则按雇主放弃本合同对待。<BR> 
　　开工时间和迟延<BR> 
　　４１．工程的开始<BR> 
　　承包人在接到工程师有关开工的书面命令后，应在投标书指定的期限内，在现场开始本工程，并应以应有的速度进行，不得迟延，但如有工程师明文批准或命令者或完全属于非承包人所能控制的原因者除外。<BR> 
　　４２．现场的占用<BR> 
　　（１）除本合同可规定应让承包人随时占有的现场各部分的范围和各该部分应向承包人提供的顺序以外，在遵照本合同关于本工程进行顺序的任何要求的条件下，雇主将随着工程师发出开始本工程的书面命令，让承包人占有为使承包人能按照文件第１４条所提到的方案（如果有的话）或者按照承包人应以书面通知向工程师提出的合理建议，开始和继续本工程施工所需要的现场各部分，并将随着工程的进展，随时让承包人占有为使承包人能按照上述方案或建议（视情况而定）以应有的速度进行本工程施工所进一步需要的现场各部分。如果由于雇方方面没有按照本条规定让承包人占有现场各部分而使承包人招致迟延或费用开支，工程师应准许延长工程完成时间，并证明他认为是公平合理的对所招致开支的偿付金额，该金额应由雇主支付。<BR> 
　　道路通行权等<BR> 
　　（２）承包人应为取得他所需要的有关进入现场的特别或临时的道路通行权负担一切开支和费用。承包人还应自费提供他为本工程的目的所需要的在现场以外的任何附加设施。<BR> 
　　４３．竣工的时间<BR> 
　　在遵照说明书中有关本工程的任何部分应在整个工程竣工前完工的任何要求的条件下，本工程应在投标书所指定的时间内全部竣工，从投标中所指定的开工期限的最后一天起算，或根据本文件第４４条允许的延长时间计算。<BR> 
　　４４．竣工期限的延长<BR> 
　　如果任何额外的或追加的工作量，或可能发生的不论何种其它特殊情况，使承包人有正当权利延长竣工时间，工程师应确定这种延长时日。但是，工程师并无义务要考虑任何额外的或追加的工作或其它特殊情况，除非承包人在这种工作开始后或这种情况出现后的２８天内，或其后尽快地向工程师的代表递交有关他认为自己有权延长时间的任何权利主张的充分而详细的材料，以便当时能对该权利主张作出调查。<BR> 
　　４５．夜间或星期日不工作<BR> 
　　在遵照本合同所载的相反规定的条件下，任何长久性工作，除下文规定者外，非经工程师的代表书面许可不得在夜间或星期日（如果当地公认休息日）或当地公认的休息日进行，为拯救人命或财产或为本工程的安全而不可避免的或者是绝对必需的工作除外，在这种情况下，承包人应立即通知工程师的代表。但是，本条规定不适用于任何通常采用轮班制或两班制进行的工作。<BR> 
　　４６．进度<BR> 
　　承包人根据文件第五条所提供的全部资料、设备和工人，以及本工程进行和维护的方式、方法与速度，应是同一种类的，并应按使工程师满意的方式进行。如果任何时候工程师认为本工程或其任何部分的进度太慢，不能保证本工程在规定的或延长的竣工时间内完成，工程师应将此以书面通知承包人，承包人在接到通知后应采取他认为必要的措施，而工程师则可同意加快进度，以便使本工程能在规定的或延长的竣工时间内完成。如果此项工作未在夜间进行，承包人应请求许可在夜间和白天一样工作。如果得到工程师这种许可，承包人应无权因这样而获得任何额外支付，但是，如果这种请求遭到拒绝，而又没有相当的、切实可行的加快工程进度的方法，则竣工的时间应延长仅由于这种拒绝所致的一段时间。进行夜间作业不得有不合理的噪音或干扰。承包人应就由于在进行作业时所产生的噪音或其它干扰而引起的损害赔偿责任以及与此赔偿责任有关的一切权利主张、要求、诉讼、各项费用和开支，给雇主以补偿。<BR> 
　　４７．对延期规定的违约偿金<BR> 
　　（１）如果承包人未能在本文件第四十三条规定的时间或延长的时间内完成本工程，承包人应向雇主支付投标中规定的金额，作为对这种违约规定的违约偿金，而不作为对本工程竣工时期按本文件第４３条规定的时间或延长的时间（视情况而定）所超过的每一天或不足一天的罚款。雇主在不妨碍其它补偿方法的情况下可以从他手头到期应付或将到期应付给承包人的任何款项中扣除该项违约偿金的金额，支付或扣除该违约偿金均不能免除承包人完成本工程的义务，或免除其根据本合同所承担的任何其它义务与责任。<BR> 
　　规定的违约偿金的削减<BR> 
　　（２）如果在本工程全部竣工以前，本工程中的任何一部分业经工程师按本文件第４８条出具竣工证书，并已由雇主占有或使用，则对延期规定的违约偿金，有关出具该竣工证书以后的任何延迟时间应按业经证明已竣工部分的价值占整个工程的价值的比例予以削减。<BR> 
　　对提前竣工的奖金<BR> 
　　（３）如果希望在本合同中规定对提前完成本工程或本工程的任何一部分支付奖金，应在第二部分第四十七（３）条中予以规定。<BR> 
　　４８．竣工证书<BR> 
　　一俟工程师认为本工程已基本竣工并已令人满意地通过本合同规定的最后试验时，工程师应在接到承包人关于将在维护期完成任何未完成的工作的书面保证后，对本工程出具竣工证书，本工程的维护期应从出具此项证书之日开始。但是，工程师可以在本工程全部竣工以前，对本工程的任何一部分出具这种证书，并应根据承包人的书面申请对业已竣工使工程师感到满意而且已经为雇主占有或使用的本工程的任何主要部分，出具这种证书。当给予本工程的一部分以这种证书时，应当认为该部分已经竣工，而且该部分的维护期应从出具这种证书之日开始。但是，按上述规定对上述被占有或使用的本工程的任何部分所出具的竣工证书，不得认为是证明任何需要恢复原状的地面或表面工程的竣工，除非这种证书明确如此说明。<BR> 
　　维护和缺陷<BR> 
　　４９．“维护期”的定义<BR> 
　　（１）本条件中，“维护期”一词应指投标中指定的维护期限，从工程师按照本文件第４８条证明本工程竣工之日起计算，或者如果工程师根据第四十八条出具不止一份证书时，则从各该证书出具之日起分别计算，而与维护期有关的“本工程”一词则应作相应的解释。<BR> 
　　进行修理工作等<BR> 
　　（２）为了使本工程在维护期满之时或期满之后尽快交付给雇主时，处于维护期开始时的同样完好状态（正常的磨损除外）使工程师感到满意，承包人应按照工程师在维护期间或在维护期满后１４天内根据工程师或以工程师外义在维护期满之前所进行检验的结果可能以书面向承包人提出的要求，对缺陷、不完善之处、收缩或其它缺点进行修理、改正、重建、矫正和弥补等一切工作。<BR> 
　　进行修理工作等的费用<BR> 
　　（３）如果工程师认为之所以需要进行这种修理工作是由于使用的材料或做工不符合本合同的规定，或者由于承包人方面忽视或没有遵照履行其根据本合同所承担的任何明示或默示的义务所致，则承包人应自行承担费用进行所有这种修理工作。如果工程师认为之所以必需进行修理是由于任何其它原因，则应作为追加工作确定这种修理工作的价值并予以付款。<BR> 
　　对承包人不进行要求他做的工作的补救办法<BR> 
　　（４）如果承包人不进行上述工程师要求他做的任何工作，雇主有权用他自己的工人或请其它承包人进行此项工作，如果此项工作是属于承包人应自行承担费用进行的工作，雇主应有权向承包人索回此项费用，或可从任何到期应付或将到期应付给承包人的款项中扣除此项费用。<BR> 
　　５０．承包人进行调查<BR> 
　　承包人如经工程师书面要求，应根据工程师的指示，调查造成任何缺陷、不完善之处或缺点的原因。如果此种缺陷、不完善之处或缺点不是属于承包人根据本合同应予负责之列者，承包人在上述调查中所进行的工作的费用应由雇主承担。但是，如果此种缺陷、不完善之处或缺点是属于上述承包人应予负责之列者，进行上述调查工作的费用应由承包人承担，在这种情况下，承包人应按照本文件第四十九条的规定自行承担费用对此种缺陷、不完善之处或缺点进行修理、矫正和补救。<BR> 
　　修改、增补与删除<BR> 
　　５１．变动<BR> 
　　（１）工程师应对于本工程或其中任何部分的形状、质量或数量作出他认为可能是必要的变动，并且为此目的或由于任何其它原因，他如认为可取，应有权命令承包人去做下列任何一项，而承包人应照办：　　<BR> 
　　①增加或减少本合同所包括的任何工作的数量，<BR> 
　　②删除任何此类工作，<BR> 
　　③改变任何此类工作的性质或质量或种类，<BR> 
　　④改变本工程任何部分的高程、线、方位和大小，<BR> 
　　⑤进行为完成本工程所必需的任何种类的追加工作，所有这种变动决不应使本合同无效，但是所有这些变动的价值（如果有的话）在确定本合同价格的金额时，应予以考虑。<BR> 
　　有关各项变动的命令应是书面的<BR> 
　　（２）承包人没有工程师的书面命令，不得作任何变动。但是，如果当任何工作数量的增加或减少并非由于根据本条发出命令的结果，而是由于其数量超过或不足建筑工程清单所规定的数量，则对这种工作数量的增加或减少不应要求有任何书面命令。但是，又如果由于任何原因工程师认为任何这种命令以口头方式发出是可取的，承包人应遵照执行这种命令，而工程师不论在该命令执行之前或之后对这种口头命令所作的任何书面确认，均应认为是属于本条所指的书面命令。但是，又如果承包人就工程师的任何口头命令，向工程师作出书面确认，而此项确认未遭工程师的书面否认，则此项确认即应认为是工程师的书面命令。<BR> 
　　５２．变动的估价<BR> 
　　（１）工程师应就其命令进行的任何额外的或增加的工作或删除的工作确定他认为应对投标中指定的金额予以增加或减去的金额（如果有的话）。所有这些工作均应按工程师如果认为适用的本合同所规定的费率估价。如果本合同没有载明任何适用于额外的或增加的工作的费率，则应由工程师与承包人商定合适的价格。如果不能取得一致意见，应由工程师确定他认为合理而适当的价格。<BR> 
　　工程师确定费率的权力<BR> 
　　（２）但是，如果任何删除或增加部分的性质或工作量都同整个合同工程或其中的任何一部分的性质或工作量有关，工程师认为由于这种删除或增加，使本合同对本工程任何项目所规定的费率或价格变得不合理或不适用，则应由工程师和承包人商定一个合适的费率或价格。如果不能取得一致意见，应由工程师在考虑到各种情况之后另外确定他认为合理适当的费率或价格。<BR> 
　　但是还必须规定，除非在该命令发出之日以后（而在额外的或增加的工作的情况下，则在此项工作开始之前或在其后）已尽快地发出书面通知：<BR> 
　　①由承包人通知工程师关于他要求额外付款或变动费率的意图，或<BR> 
　　②由工程师通知承包人关于他准备视情况变动费率或价格的意见，否则就不得按本条第（１）款增加本合同的价格，或按本条第（２）款变动费率或价格。<BR> 
　　变动超过１５％<BR> 
　　（３）如果在本工程全部竣工时，发现各项变动（由于任何有关改变材料和（或）劳动力价格的条款所引起者除外）的最后结果，较投标所指定的金额增加或减少１５％以上，则本合同价格的金额应按工程师和承包人双方商定的金额进行修正。如果不能取得一致意见，应由工程师在考虑到一切实质性的和有关的因素（包括承包人的杂费和管理费）之后，确定在他认为是合理而适当的金额。<BR> 
　　计日工作<BR> 
　　（４）工程师如果认为有必要或合乎需要时，可以书面命令任何增加的或替代的工作均应按计日工作进行。这样，对这种工作应根据建筑工程清单内的计日工作表所规定的条件和承包人在其投标中确定的费率与价格支付承包人。<BR> 
　　承包人应向工程师提供必要的收据或其它凭证以证明所付金额，并应在定购物料以前先把关于该物料的报价单提请工程师认可。<BR> 
　　对于所有按计日工作进行的工作，承包人应在此种工作持续进行期间，每天向工程师的代表提交此种工作所雇用的全部工人的姓名、工种和时间的准确清单一式两份，以及列明此种工作所用的全部物料与设备（已按照本文件上面提到的表计入百分比增加的设备除外）的品名及数量的报表也一式两份。每份清单和报表如果或经认为正确无误，其中一份将由工程师的代表签字后退还承包人。每个月终，承包人应向工程师的代表提交一份关于所使用的劳动力、材料和设备（上述例外除外）的价格报表，这种清单和报表如果没有全部和准时提交，承包人就无权得到任何支付。但是如果工程师认为由于任何原因，要承包人按上述规定送交这种清单或报表并非切实可行的，则工程师应有权批准对此种工作按计日工作（在对此种工作所用的时间和设备及材料感到满意时）或是按他认为是公平合理的价格付款。<BR> 
　　权利主张<BR> 
　　（５）承包人应每月一次向工程师的代表递交一份账目单，详细列举（尽可能详尽）承包人认为自己有权提出的对任何增加的费用的各项权利主张，以及承包人根据工程师的命令已在当月进行的一切额外的或增加的工作。凡没有列入此项详细账目单内的对任何这类工作索取偿付的要求，将一概不予考虑。但是尽管承包人没有遵守此项条件，如果他已在尽早的时机，通知工程师关于他打算就这类工作提出权利主张，则工程师仍有权批准对任何这类工作付款。<BR> 
　　设备、临时工程和材料<BR> 
　　５３．本工程专用的设备等<BR> 
　　（１）承包人所提供的一切施工设备、临时工程和材料一经携入现场就应当认为是专供本工程的施工和建成之用未经工程师的书面同意（不得无理拒绝同意），承包人不得将其或其中的任何一部分移走（为了将其从现场的某部分移至另一部分者除外）。<BR> 
　　设备的撤离等<BR> 
　　（２）在本工程竣工时，承包人应把他所提供的留在现场的所有上述施工设备和临时工程以及任何未使用的材料撤离现场。<BR> 
　　雇主对设备等损坏不承担责任<BR> 
　　（３）除本文件第２０条和第６５条有规定者外，雇主在任何时候对上述任何施工设备、临时工程或材料所受的损失或伤害概不负责。<BR> 
　　设备的再输出<BR> 
　　（４）对于承包人为本工程的目的而输入的任何施工设备，在按上述规定撤离现场后，雇主将在有需要的情况下协助承包人就承包人再输出这种施工设备获得必要的政府的批准同意。<BR> 
　　海关放行<BR> 
　　（５）雇主在需要时将协助承包人就本工程所必需的施工设备、材料和其它各物取得海关的放行。<BR> 
　　（６）任何涉及设备、临时工程和材料的其它条件，包括有关支付或减免关税或其它进口税、港口费、码头费、卸货费、引水费和任何其它费用或应付款的各项条件，视需要在第二部分第５３条第（６）、（７）等项中予以规定。<BR> 
　　５４．对材料等的认可不得是默示的<BR> 
　　本文件第５３条的实施，不得被认为是默示工程师对其中所提到的材料或其它事项的任何认可，也不得妨碍工程师在任何时候拒绝任何这种材料。<BR> 
　　测量<BR> 
　　５５．数量<BR> 
　　建筑工程清单中所列出的数量是对工作的估计数量，不能作为承包人为履行其根据本合同所承担的义务时应进行的本工程的实际的和准确的数量。<BR> 
　　５６．应予测量的工程<BR> 
　　除另有规定者外，工程师应按照本合同通过测量查明和确定按本合同完成的工作的价值。当工程师要求对本工程的任何部分进行测量时，他应通知承包人的指定代理人或代表，承包人应立即参加或派一名职称的代理人协助工程师或工程师的代表进行此项测量，并应提供工程师或工程师的代表所要求的一切详细资料。如果承包人不参加或者忽略或遗漏派代理人参加，则由工程师所作的或认可的测量应被认为是对工作的正确的测量。为了测量要用记录和图纸进行测量的长久性工作的目的，工程师的代表应对这种工作逐月作出记录和图纸，承包人在得到书面邀请参加检查时，应在１４天内参加与工程师的代表一道检查并同意这些记录和图纸，并应在业经同意的记录和图纸上签字，如果承包人不参加检查和同意任何这种记录和图纸，则这些记录和图纸就应被认为是正确的。如果承包人在检查这些记录和图纸以后，同意这些记录和图纸或同意后不在其上签字，除非承包人在进行此项检查后的１４天内向工程师的代表递交书面通知，提出他声称这些记录和图纸是不正确的各个方面，请工程师决定，否则这些记录和图纸仍应被认为是正确的。<BR> 
　　５７．测量方法<BR> 
　　除本合同另有特别说明或规定者外，不论任何一般的或当地的习惯，本工程仍应测量净值。<BR> 
　　临时金额和直接成本金额<BR> 
　　５８．各项临时金额<BR> 
　　（１）在建筑工程清单中（不论是在签订本合同时未作详细规定而应由承包人进行的工作，还是应由按下文规定的指定的分包人进行的工作）列出的每一项临时金额（非根据本条第（２）款规定的直接成本价格）连同承包人已经计入此项金额的费用和利润（如果有的话）均应从本合同价格中予以扣除，并应代之以下列各项计入本合同价格；①与该临时金额有关的工作系经工程师命令并由承包人进行的情况下，对这样进行的工作按本文件第５２条所估定的价值。②与该临时金额有关的工作系经工程师命令并由指定的分包人（定义见下文）进行的情况下，承包人按工程师的指示所实际支付给该分包人的金额（但是必须遵照本条第（５）款的规定），以及（如果承包人已把关于费用和利润的任何金额计入与该工作有关的临时金额）一项金额其所占实际支付金额的比例与上述费用和利润占上述临时金额的比例相同者。<BR> 
　　直接成本项目<BR> 
　　（２）建筑工程清单中的每一项金额其中包括（占该金额的全部或部分）应为本工程提供的或用于本工程的货物或材料的直接成本价格者，应由以承包人根据工程师的指示为该货物或材料所支付的实际价格（但是必须遵照本条第（２）款的规定）代替直接成本价格而变动，同时本合同价格则应按照建筑工程清单中的金额由于此种代替所增加或减少的金额而予以增减（视情况而定）。对于列入直接成本价格的关于劳动力的任何金额，不得由于上述实际价格高于或低于直接成本价格而变动。但是，对于所有其它费用和利润，则应按照根据建筑工程清单中所规定的有关直接成本的该一具体项目的百分比或者（如果无此种百分比时）根据承包人列入投标书作为调整直接成本金额的百分比而得出的金额，予以增加或减少（视情况而定）。<BR> 
　　临时和应急项目的使用<BR> 
　　（３）在建筑工程清单中所列的各项经规定为临时或应急的款项，只能凭工程师的指示和决定动用。如果该款项全部或部分未经动用，则应从本合同价格中减去未动用的金额。<BR> 
　　提供证明等<BR> 
　　（４）承包人如经工程师要求应出示全部有关临时项目或直接成本项目开支的报价单、发票、证件以及帐目或收据。<BR> 
　　现金折扣<BR> 
　　（５）承包人根据本条第（１）款（１）项或第（２）款的规定，按工程师的指示付给指定的分包人（按下文定义）的任何金额，只要是在承包人收到雇主该项付款之前支付的，为了根据本条第（１）或（２）款（视情况而定）调整本合同价格的目的，应在上述承包人实际支付的金额上增加这一实际付款额的＿＿＿％，而与此有关的任何现金折扣的好处，均应转给雇主。<BR> 
　　分包人义务的转让<BR> 
　　（６）如果指定的分包人（按下文定义）已就他所进行的工程或他所供应的货物或材料和承包人承担任何超过本合同规定的维护期一段期间的持续义务，承包人应在维护期满后的任何时候应雇主的请求并由雇主承担费用把此项义务尚未届满期间的好处转让给雇主。<BR> 
　　５９．指定的分包人<BR> 
　　（１）可能经雇主或工程师指定或选定或认可的一切专家、商人和其它人员，其所进行的任何工作或供应的任何货物的临时或直接成本金额已列入建筑工程清单者以及根据建筑工程清单或说明书的规定要求承包人把任何工作转包给他并正在进行此种工作和供应此种货物的一切人员，均应被认为是承包人所雇用的分包人，并在下文称之为“指定的分包人”，但是，雇主或工程师不得要求承包人、或认为承包人有义务雇用任何不愿意同承包人签订含有如下条款的分包合同的指定的分包人：<BR> 
　　①指定的分包人将就作为分包合同主题的工作或货物向承包人承担如同承包人根据本合同条款向雇主所承担的同样的义务和责任，并将就此并就凡是由此引起的或与此有关的或是由于未履行这种义务或责任所引起的或与之有关的一切权利主张、要求、诉讼、损害赔偿费、各项费用和开支等，保障承包人免受损害并给承包人以补偿。<BR> 
　　②指定的分包人将就其本人、其代理人、工人及雇员的任何疏忽，就其本人或其人员对承包人为本合同的目的所提供的施工设备或临时工程的任何滥用，并就上述一切权利主张，保障承包人免受损害并给承包人以补偿。<BR> 
　　对指定的分包人的支付<BR> 
　　（２）工程师在根据本文件第六十条出具其中包括对任何指定的分包人所完成的工作或所提供的货物付款的任何证书以前，应有权要求承包人提出适当证据证明，先前证书中所包括的对该指定的分包人的工作或货物的一切付款（减去保留额），已由承包人如数支付或清偿；如未支付或清偿，除非承包人。<BR> 
　　①以书面通知工程师，说明他有正当理由扣留或拒绝此项支付，并且<BR> 
　　②向工程师出示适当证据，证明他已将此以书面通知该指定的分包人，否则雇主应有权根据工程师的证书把承包人未付给指定的分包人的一切款项（减去保留额）直接付给该指定的分包人，并从雇主到期应付或将到期应付给承包人的任何金额中用抵销雇主为此所付的金额的方式予以扣除。<BR> 
　　但是，如果按上所述工程师已经出具证书而雇主已直接付款，则工程师在再出具任何一份以承包人为受款人的证书时应从其金额中扣除上述由雇主直接支付的金额，但是不得扣留或迟延发给根据本合同的条款应当出具的证书本身。<BR> 
　　证书和支付<BR> 
　　６０．证书和支付<BR> 
　　（１）除另有规定者外，应按照第二部分第六十条所提出的条件，每月支付。<BR> 
　　对施工设备和材料的预付款<BR> 
　　（２）如果雇主应就施工设备和材料向承包人预付款，其支付和偿还的条件将如第二部分第六十条所列。<BR> 
　　用外币支付<BR> 
　　（３）如果进行本工程需要从本工程所在国以外的国家进口材料、设备或装备，或者如果本工程或其中的任何部分须由从任何其它国家进口的劳动力来进行，或者由于任何其它情况使其有必要或有需要，则根据本合同所应支付的款项的一部分应以适当的外币支付，各该外币支付部分和其适用的汇率以及支付条件均应在第二部分第六十条中予以规定。<BR> 
　　６１．仅由维护证书认可<BR> 
　　本文件第六十二条所提到的维护证书以外的任何证书都不得被认为是对任何工作或为之出具证书的其它事宜的认可，也不得作为对正当履行本合同或其中任何部分的承认或对承包人所提出的任何权利主张或要求的准确性或工程师所命令增加或变动的工作的准确性的承认，也无任何其它证书能终止或妨碍工程师的任何权力。<BR> 
　　６２．维护证书<BR> 
　　（１）本合同应在工程师签署并向雇主递交维护证书，说明本工程业已竣工并使他满意以后，才能被认为已经履行完毕。维护证书应由工程师在维护期满（如果本工程的不同部分适用不同的维护期限，则应在最后的维护期届满）后２８天，或在其后一俟维护期间按照本文件第四十九条和五十条所命令进行的任何工程业已完成并使工程师满意之时发出，即使雇主在此以前已进入本工程或已占有、运用或使用本工程或其中的任何部分，本条仍具有充分效力。但是，不得以出具维护证书作为按照第二部分第六十条中所规定的条件向承包人支付第二部分保留金的前提条件。<BR> 
　　雇主责任的终止<BR> 
　　（２）雇主对由于本合同或进行本工程所引起的或与此有关的任何事宜。均不再向承包人承担责任，除非承包人在根据本条规定的维护证书发出以前已经书面提出与之有关的权利主张。<BR> 
　　未履行的义务<BR> 
　　（３）即使发出维护证书，承包人和（遵照本条第（２）款的条件下）雇主对于在维护证书发出以前根据本合同条款产生的而在此项证书发出时尚未履行的任何义务的履行依然承担责任，并且为了确定任何这类义务的性质和范围，应认为本合同在合同缔约双方之间仍然有效。<BR> 
　　补救方法和权力<BR> 
　　６３．没收<BR> 
　　（１）如果承包人破产或已就其发出法院委派破产者产业管理人的委任书，或者承包人已提出申请宣布破产的请求书，或者已经同其债权人作出安排或已向其债权人进行转让，或者同意在其债权人检查委员会监督下执行本合同，或者（作为法人）停业清理（并非为合并或改组而自动停业清理），或者承包人未经首先取得雇主的书面同意而转让本合同，或者承包人的货物已被依法扣押，或者工程师用书面向雇主证明他认为承包人：<BR> 
　　①已放弃本合同，或<BR> 
　　②在接到工程师关于继续进行工程的书面通知后，无正当理由而不开始进行本工程或中止进行本工程达２８天之久，或<BR> 
　　③在接到工程师关于材料或工作已由工程师根据本条件宣告不适用并予以拒收的书面通知后达２８天，仍未从现场撤走该材料或拆除和更换该工作，或<BR> 
　　④未按本合同进行本工程，或持续地或公然地忽视履行其根据本合同所承担的义务，或<BR> 
　　⑤已将本合同的任何一部分转包出去，有损于良好的做工或无视工程师的相反指示，则雇主可在向承包人发出书面通知１４天后进入现场和本工程，并把承包人驱逐出现场和本工程，而并不由此废止本合同或免除承包人根据本合同所承担的任何义务或责任或影响本合同所授予雇主或工程师的权利与权力，雇主可以自行完成本工程，或雇用任何其它承包人完成本工程，而雇主或该其它承包人可以为完成本工程而使用根据本合同规定认为是专供本工程施工和建成之用的施工设备、临时工程和材料中他们认为合适的部分，雇主可以在任何时候出售上述施工设备、临时工程和未经使用的材料，并把出售所得用作抵偿承包人根据合同应付给他的任何款项。<BR> 
　　没收之日的估价<BR> 
　　（２）工程师应在任何上述雇主进入现场并驱逐承包人之后尽快单方面地或通过向各方了解情况以后，或在进行他认为适宜进行的调查或询问以后，确定并证明在雇主进入现场并驱逐承包人之时承包人对于其根据本合同已于当时实际完成的工作所已合理得到的或将合理归于其的金额（如果有的话），以及任何上述未经使用或经部分使用的材料、任何施工设备和任何临时工程的价值。<BR> 
　　没收的支付<BR> 
