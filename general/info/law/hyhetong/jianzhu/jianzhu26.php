　　业主／咨询工程师标准服务协议书条件<BR> 
　　<BR> 
　　第一部分　标准条件<BR> 
　　<BR> 
　　定义及解释<BR> 
　　１．定义<BR> 
　　除上下文另有要求外，以下各词和用语，应具有如下的涵义：<BR> 
　　（１）“项目”是指第二部分中指定的并为之建造的工程项目。<BR> 
　　（２）“服务”是指按照协议书咨询工程师履行的服务，包括正常的服务、附加的服务和额外的服务。<BR> 
　　（３）“工程”是指完成项目而实施的永久工程（包括提供给业主的物品和设备）。<BR> 
　　（４）“业主”是指本协议书中所指的雇用咨询工程师的一方及业主的合法继承人和允许的代理人。<BR> 
　　（５）“咨询工程师”是指本协议书中所指的，由业主雇用的作为一个独立的专业公司去履行服务的一方及咨询工程师的合法继承人和允许的代理人。<BR> 
　　（６）“一方”和“各方”是指业务和咨询工程师。“第三方”是指上下文要求的任何其他当事人或实体。<BR> 
　　（７）“协议书”是指包括业主－－咨询工程师标准服务协议书的第一部分和第二部分条件以及附件Ａ（服务范围），附件Ｂ（业主提供的职员、设备、设施和其他人员的服务），附件Ｃ（报酬和支付），中标通知书和正式协议书（若已签订），或在第二部分中的其他规定。<BR> 
　　（８）“日”是指任何一个午夜至下一个午夜的时间段。<BR> 
　　（９）“月”是指按公历从一个月份中任何一天开始的一个月的时间段。<BR> 
　　（１０）“当地货币”（ＬＣ）是指项目所在国的货币，“外币”（ＦＣ）是指任何其他的货币。<BR> 
　　（１１）“商定的补偿”是指根据协议书支付在第二部分中所规定的款项。<BR> 
　　２．解释<BR> 
　　（１）本协议书中的标题不应在其解释中使用。<BR> 
　　（２）视上下文需要，本文中词的单数包含复数的含义，阳性包含阴性的含义，反之亦然。<BR> 
　　（３）如果协议书条款中有相互矛盾之处，则按时间顺序以最后编写的为准。<BR> 
　　咨询工程师的义务<BR> 
　　３．服务范围<BR> 
　　咨询工程师应履行与项目有关的服务。在附件Ａ中已规定了服务的范围。<BR> 
　　４．正常的、附加的和额外的服务<BR> 
　　（１）在附件Ａ中所述的那类服务称为正常服务。<BR> 
　　（２）在附件Ａ中所述的那类服务或通过双方的书面协议另外附加于正常服务的那类服务称为附加服务。<BR> 
　　（３）那些既不是正常的也不是附加的，但按照第２８条款咨询工程师需履行的服务称之为额外服务。<BR> 
　　５．认真地尽职和职权的行使<BR> 
　　（１）咨询工程师应运用合理的技能，认真和勤奋地履行本协议书规定的义务。<BR> 
　　（２）当服务包括行使权力或履行授权的或业主和任何第三方签订的合同条款要求的职责时，咨询工程师应：<BR> 
　　①根据合同进行工作，如果该权力和职责的详细规定未在附件Ａ中加以说明，则这些详细规定应是他可以接受的。<BR> 
　　②如果授权的话，应在业主和第三方之间公正地开证明，决定或行使自己的处理权，但不是作为仲裁人而是作为一名独立的专业人员根据自己的职能和判断进行工作。<BR> 
　　③如果授权后的话，可变更任何第三方的义务。但对费用或质量或时间可能有重大影响的任何变更，则须事先征得业主的同意（除非发生任何紧急情况，此时咨询工程师应尽快地通知业主）。<BR> 
　　６．业主的财产<BR> 
　　由业主提供或支付的供咨询工程师使用的任何物品均属于业主的财产，在实际可行时应加以标明。当服务完成或终止时，咨询工程师应将履行服务中未使用的物品库存清单提交给业主，并按业主的指示移交此类物品。此类移交应视为附加的服务。<BR> 
　　业主的义务<BR> 
　　７．资料<BR> 
　　业主应在一个合理的时间内免费向咨询工程师提供他能够得到的与服务有关的所有资料以不耽误服务。<BR> 
　　８．决定<BR> 
　　业主应在一个合理的时间内就咨询工程师以书面形式提交给他的一切事情作出书面决定便不耽误服务。<BR> 
　　９．协助<BR> 
　　在项目所在国，对咨询工程师和他的职员及下属，业主应尽一切努力按照具体情况提供以下协助：<BR> 
　　（１）入境、居留、工作和出境所需的文件条款；<BR> 
　　（２）在服务所需要的任何地方提供畅通无阻的通道；<BR> 
　　（３）个人财产和服务所需物品的进口、出口以及海关结关；<BR> 
　　（４）发生意外事件时的遣返；<BR> 
　　（５）允许咨询工程师因服务目的和他的职员因个人使用将外币带入该国以及允许将履行服务中所赚外币带出该国的权力的条款；<BR> 
　　（６）为了方便咨询工程师收集他要获取的信息，应提供与其他组织相联系的渠道。<BR> 
　　１０．设备和设施<BR> 
　　为了服务的目的，业主应免费向咨询工程师提供附件Ｂ中所规定的设备和设施。<BR> 
　　１１．业主的职员<BR> 
　　在与咨询工程师协商后，业主应根据附件Ｂ的规定，自费从其雇员中为咨询工程师挑选和提供职员。此类职员在涉及到服务时只应从咨询工程师处接受指示。<BR> 
　　１２．其他人员的服务<BR> 
　　业主应按附件Ｂ的说明，自费安排其他人员的服务供给。咨询工程师应与此类服务的提供者合作，但不对此类人员或他们的行为负责。<BR> 
　　职员<BR> 
　　１３．职员的提供<BR> 
　　由咨询工程师派往项目所在国工作的职员应接受体格检查并应能适应他们的工作，同时他们的资格应得到业主的认可。<BR> 
　　根据第１１条款，由业主提供的职员应得咨询工程师的认可。<BR> 
　　如果业主未能提供他应负责提供的业主的职员或其他人员的服务，而双方都认为有必要提供此类服务以便于满意地履行服务时，则咨询工程师可安排此类服务的提供，并作为附加的服务。<BR> 
　　１４．代表<BR> 
　　每一方应指定一位职员或个人作为其代表以便于本协议书的管理。<BR> 
　　如果业主要求的话，咨询工程师应指定一人与项目所在国的业主代表建立联络关系。<BR> 
　　１５．职员的更换<BR> 
　　如果有必要更换任何人员，则负责任命的一方应立即安排一位具有同等能力的人员来替换。<BR> 
　　除非此类更换由另一方提出的，否则，这类更换的费用应由负责任命的一方承担。<BR> 
　　（１）这要求应以书面形式提出并申述更换理由，并且<BR> 
　　（２）如果不能把渎职或不能圆满地执行任务作为理由成立的话，则提出要求的一方应承担更换费用。<BR> 
　　责任和保险<BR> 
　　１６．双方之间的责任<BR> 
　　（１）咨询工程师的责任<BR> 
　　如果确认咨询工程师违背了第５条款第（ｉ）子款，则他应仅对由本协议书引起的或与此有关的事情负责向业主赔偿。<BR> 
　　（２）业主的责任<BR> 
　　如果确认业主违反了他对咨询工程师的责任，则业主应负责向咨询工程师赔偿。<BR> 
　　（３）赔偿<BR> 
　　如果认为任何一方对另一方负有责任时，则仅对下列条件进行支付赔偿：<BR> 
　　①这类赔偿应限于由此违约造成的可合理预见到的损失或遭受的损害的数额，而对其他则不予赔偿；<BR> 
　　②在任何情况下，这些赔偿数额应限于第１８．１款规定的数额；<BR> 
　　③如果认为任一方与第三方共同对另一方负有责任时，负有责任的任一方所支付的赔偿比例应限于由其违约所应负责的那部分比例。<BR> 
　　１７．责任的期限<BR> 
　　无论是业主还是咨询工程师都不应对由任何事件引起的任何损失或损害负责，除非第二部分规定的相应时段终止之前或法律可能规定的更早日期之前，正式向业主或咨询工程师提出索赔。<BR> 
　　１８．赔偿的限额和保障<BR> 
　　（１）赔偿的限额<BR> 
　　根据第１６条款有关责任方面的款项，任何一方向另一方支付赔偿的最大数额应限于第二部分中规定的数额。此限额不影响按第３１条款第（ｉｉ）子款规定的或本协议书另外规定的任何商定的补偿。<BR> 
　　在可能另外支付的赔偿总计超过应支付的最大数额的情况下，则每一方均应同意放弃对另一方的所有索赔要求。<BR> 
　　如果任何一方向另一方提出索赔要求而该要求不能确立的话，则提出索赔者应对由于该索赔所引起对方的各种费用完全补偿。<BR> 
　　（２）保障<BR> 
　　如果适用的法律允许，则业主应保障咨询工程师免受由索赔造成的不利影响，包括由本协议书引起的或与之有关的第三方提出的这类索赔：<BR> 
　　①除非这类索赔已包括在按第１９条款规定办理的保险范围内：<BR> 
　　②在第１７条款提及的责任期终止后提出的这类索赔。<BR> 
　　（３）例外<BR> 
　　第（１）和（２）款不适用于由下列情况引起的索赔：<BR> 
　　（１）故意违约或粗心引起的渎职；<BR> 
　　（２）与本协议书规定义务的履行无关的情况。<BR> 
　　１９．责任的保险与保障<BR> 
　　业主可以书面的形式要求咨询工程师：<BR> 
　　（１）对第１６．１条款规定的咨询工程师的责任进行保险；<BR> 
　　（２）在业主首次邀请咨询工程师为服务提交建议书之日，对按１６．１条款规定的咨询工程师的责任进行保险的基础上，对其追加保险额；<BR> 
　　（３）对公共的或第三方进行责任保险；<BR> 
　　（４）在业主首次邀请咨询工程师为服务提交建议书之日，对公共的或第三方责任进行保险的基础上，对其追加保险额；<BR> 
　　（５）对其他各项进行保险。<BR> 
　　如果这样要求的话，在业主可接受的条件下，咨询工程师应做出一切合理努力，让此类保险或追加保险额由承保人来办理。<BR> 
　　业主应负担此类保险的费用或追加保险额的费用。<BR> 
　　２０．业主财产的保险<BR> 
　　除非业主有另外的书面要求，咨询工程师应按业主可接受的条件尽一切合理的努力，进行下列各项保险：<BR> 
　　（１）按第６条款提供或支付的业主财产的损失或损害；<BR> 
　　（２）因使用该财产而产生的责任。<BR> 
　　业主应负担此类保险的费用。<BR> 
　　协议书的开始、完成、变更与终止<BR> 
　　２１．协议书生效<BR> 
　　协议书从咨询工程师收到业主对其建议发出中标通知书之日或完成正式协议书所需的最后签字之日（如有时）的较晚的那个日期起生效。<BR> 
　　２２．开始和完成<BR> 
　　除根据协议书可延期外，服务必须在第二部分所规定的时间或期限内开始和完成。<BR> 
　　２３．更改<BR> 
　　当任何一方提出申请并经双方书面同意后，可对本协议书进行更改。<BR> 
　　２４．进一步的建议<BR> 
　　如果业主以书面的形式提出要求的话，则咨询工程师应提交变更服务的建议。这类建议的准备和提交应视为附加的服务。<BR> 
　　２５．延误<BR> 
　　如果业主或其承包商使服务受到阻碍或延误，导致增加服务工作量或服务时间，则：<BR> 
　　（１）咨询工程师应将此情况及此可能产生的影响通知业主；<BR> 
　　（２）此增加部分应作为附加的服务；<BR> 
　　（３）服务的完成时间应相应在予以延长。<BR> 
　　２６．情况的改变<BR> 
　　如果出现根据本协议书咨询工程师不应负责的情况，以及该情况使咨询工程师不负责或不能履行全部或部分服务时，他应立即通知业主。<BR> 
　　在这种情况下，如果某些服务不得不暂停时，则此类服务的完成期限应予以延长，直到这种情况不再持续。为了恢复服务还应加上一个不超过４２天的合理期限。<BR> 
　　如果某些服务履行的速度不得不减慢，则完成该类服务的期限因此情况而须给予延长。<BR> 
　　２７．撤销、暂停或终止<BR> 
　　（１）业主的通知<BR> 
　　①至少在５６天前业主可通知咨询工程师暂停全部或部分服务或终止本协议书，咨询工程师应立即对停止服务且将支出减到最小的事宜作出安排。<BR> 
　　②如果业主认为咨询工程师没有正当理由而未履行其义务时，他可通知咨询工程师并说明发出该通知的原因。如果有２１天内业主未收到满意的答复，则他可发出进一步的通知终止本协议书，但该进一步的通知应在业主第一个通知发出后３５天内发出。<BR> 
　　（２）咨询工程师的通知<BR> 
　　在下列①、②情况下，当咨询工程师向业主发出通知至少１４天后，咨询工程师可发出进一步的通知，在进一步通知发出至少４２天后，他才能终止本协议书，或在不损害其终止权利的情况下，可自行暂停或继续暂停履行全部或部分的服务。<BR> 
　　①当支付单据应予支付的日期后３０天，他仍未收到届时未提出书面异议的那一部分款项时，或<BR> 
　　②按第２６条或第２７．１款当服务已暂停且暂停期限已超过１８２天时。<BR> 
　　２８．额外的服务<BR> 
　　当发生第２６条款所述情况时，或撤销或暂停或恢复服务时，或并不按第２７．１第（ｉｉ）子款终止本协议书时，咨询工程师需做的任何工作或支出的费用除正常的或附加的服务之外应视为额外的服务。<BR> 
　　咨询工程师有权获得履行额外的服务所需的额外的时间和费用。<BR> 
　　２９．各方的权利和责任<BR> 
　　本协议书的终止不应损害或影响各方应有的权利或索赔及责任。<BR> 
　　支付<BR> 
　　３０．对咨询工程师的支付<BR> 
　　（１）根据合同条件和附件Ｃ中规定的细则业主应向咨询工程师支付正常的服务报酬，并按附件Ｃ的规定费率和价格或基于此费率和价格支付附加的服务报酬，只要此费率和价格适用，否则根据第２３条款商定的费率和价格支付。<BR> 
　　（２）业主应就有关额外的服务向咨询工程师支付下列款项，除非另有书面协定。<BR> 
　　①在履行服务当中，咨询工程师的职员所花费额外的时间用于附加服务的报酬；<BR> 
　　②由咨询工程师花费的所有其他额外开支的净成本。<BR> 
　　３１．支付的时间<BR> 
　　（１）应迅速支付给咨询工程师的到期款项。<BR> 
　　（２）咨询工程师在第二部分规定的时间内未收到付款时，则应根据第二部分规定的利率向其支付商定的补偿，每月将该补偿加到过期未支付的金额中，此补偿以过期未付金额的货币从发票注明的应支付之日起计算。<BR> 
　　该商定的补偿不应对第２７款第（２）点所规定的咨询工程师的权利产生影响。<BR> 
　　３２．支付的货币<BR> 
　　（１）适用于本协议书的货币是第二部分中所规定的货币。<BR> 
　　如果使用其他货币支付，则应按第二部分规定的汇率计算并支付未加扣除的净额。业主应保证咨询工程师能将其在业主所在国内收到的与履行服务有关的那部分当地货币或外币迅速汇往国外，除非在附件Ｃ中另有规定。<BR> 
　　（２）如果在签订本协议书之日或服务履行期间，业主所在国内的情况与协议书中规定的情况可能相反时，如：<BR> 
　　①阻止或延误咨询工程师将业主国内收到的当地货币或外币汇往国外，或<BR> 
　　②在业主所在国内限制外币的有效或使用，或<BR> 
　　③当咨询工程师因用当地货币开支而从国外向业主所在国汇入外币，以及随后将总额相等的当地货币再汇往国外时，对其征税或规定不同汇率，从而阻止咨询工程师的服务的履行或导致他财务上的损失。<BR> 
　　这时，若在财务上未作出其他令咨询工程师满意的安排，业主应保证此情况是适用于按第２６款所规定的情况。<BR> 
　　３３．有关第三方对咨询工程师的收费<BR> 
　　除在第二部分或附件Ｃ中规定以外<BR> 
　　（１）对咨询工程师及其通常不居住在项目所在国内的职员因本协议书引起的，为该国政府或授权的第三方所要求的支付款，业主在任何可能时都应为他们办理豁免，包括：<BR> 
　　①他们的报酬；<BR> 
　　②他们进口的物品，除食品和饮料以外；<BR> 
　　③用于服务的进口物品；<BR> 
　　④文件。<BR> 
　　（２）如果业主未能成功地办理上述豁免，则他应偿付合理支付的此类款项给咨询工程师。<BR> 
　　（３）当不再需要上述物品用于服务且上述物品不是业主的财产时，规定：<BR> 
　　①未经业主批准，不得在项目所在国内将上述物品处理掉；<BR> 
　　②未向业主支付从政府或授权的第三方处加收并收到的退款或退税时，不得将上述物品出口。<BR> 
　　３４．有争议的发票<BR> 
　　如果业主对咨询工程师提交的发票中的任何项目或某项目的一部分提出异议，则业主应立即发出通知说明理由，但他不得延误支付发票中的其他项目。第３１条款第（２）子款应适用于最终支付给咨询工程师的一切有关争议的金额。<BR> 
　　３５．独立的审计<BR> 
　　咨询工程师应保存能清楚证明有关时间和费用的最新的记录。<BR> 
　　除协议书规定固定总价支付外，在完成或终止服务后１２个月内，业主可在发出通知不少于７天要求由他指定一家有声誉的会计事务所对咨询工程师申报的任何金额进行审计，并应在正常工作时间保存记录的办公室内进行该审计工作。<BR> 
　　一般规定<BR> 
　　３６．语言和法律<BR> 
　　协议书的一种或几种语言、主导语言及协议书所遵循的法律在第二部分中作了规定。<BR> 
　　３７．立法的变动<BR> 
　　除第二部分指明的咨询工程师的业务总部所在地外，若在订立本协议书以后，因履行服务所在的任何国家的法规发生变动或增加从而引起服务费用或服务期的改变，则商定的报酬和完成时间应作相应的调整。<BR> 
　　３８．转让和分包合同<BR> 
　　（１）未经业主书面同意，除支付款的转让外，咨询工程师不得将本协议书涉及到的利益转让出去。<BR> 
　　（２）未经对方书面同意，无论业主或咨询工程师均不得将本协议书规定的义务转让出去。<BR> 
　　（３）未经业主书面同意，咨询工程师不得开始实施、更改或终止履行全部或部分服务的任何分包合同。<BR> 
　　３９．版权<BR> 
　　咨询工程师拥有由他编制的所有文件的版权。业主仅有权为工程和预定的目的使用或复制此类文件，为此目的使用而复制这类文件时不需经咨询工程师的许可。<BR> 
　　４０．利益的冲突<BR> 
　　咨询工程师及其职员不应有也不应接受协议书规定以外的与项目有关的利益和报酬，除非业主另外书面同意。<BR> 
　　咨询工程师不得参与可能与协议书中规定的业主的利益相冲突的任何活动。<BR> 
　　４１．通知<BR> 
　　本协议书的有关通知应用书面的形式，并从在第二部分写明的地点收到时生效。通知可由人员递送，或传真通讯，但要有书面回执确认；或通过挂号信，或电传，但随后要用信函确认。<BR> 
　　４２．出版<BR> 
　　咨询工程师可单独或与他人联合出版与工程和服务有关的材料，除非在第二部分中另有规定。但若在服务完成或终止后两年内出版有关材料时，则须经业主批准。<BR> 
　　争端的解决<BR> 
　　４３．对损失或损害的索赔<BR> 
　　因违反或终止协议书而引起的对损失或损害的任何赔偿，按第１７条款的规定，应在业主与咨询工程师之间达成一致意见。如未达成一致，则应按第４４条的规定，提交仲裁。<BR> 
　　４４．仲裁<BR> 
　　由协议书引起的或与之有关的任何争议或索赔，或违约、终止协议书或使之无效，均应按第二部分所订的，在协议书生效日期的规则，通过仲裁解决。<BR> 
　　双方同意遵守裁决的结果，并放弃他们的任何形式的上诉权，只要这种弃权实际有效。<BR> 
