      <p> 　　<BR> 
　　业主／咨询工程师标准服务协议书的条款由国际咨询工程师联合会（ＦＩＤＩＣ）编制。在国际范围内邀请提交对此类服务的建议书时，这些条款被推荐通用于投资前研究、可行性研究、设计与施工管理、项目管理。此文本代替了以前发行的三种文件，即：ＩＧＲＡ１９７９Ｄ＆Ｓ，ＩＧＡＲ１９７９ＰＩ，ＩＧＲＡ１９８０ＰＭ。这些条款同样适用于国内的协议书。<BR> 
　　在编制过程中认识到，虽然有许多条款是普遍适用的，但有些条款则须考虑要履行服务的环境和地区而作必要的变更。它们将一起编入构成协议书的文件中，被称为第一部分－－标准条件的通用条款已被编排在本文件中。<BR> 
　　由条款的相应顺序编号把标准条件与被称为第二部分的特殊应用条件相联系起来，这样第一部分和第二部分共同构成确定各方权利和义务的条件。<BR> 
　　第二部分的内容必须专门拟定，以适应每一单独的协议书和服务类型。应将必须完成的第二部分的内容刊印在活页纸上，以便在增编附加条款时可将其撤换。<BR> 
　　ＦＩＤＩＣ预计不久将出版“咨询协议书文件注释”，该书将包括对标准服务协议书条款的解释以及对编制附件Ａ及Ｃ（“服务范围”及“报酬和支付”）的注释。<BR> 
　　使用者参阅ＦＩＤＩＣ的其他出版物也可能是有益的，如：<BR> 
　　－－为工程服务的独立咨询工程师使用指南<BR> 
