　　本协议书于＿＿＿＿年＿＿月＿＿日由＿＿＿＿＿＿（下简称“业主”）为一方与＿＿＿＿＿（以下简称“咨询工程师”）为另一方签订。<BR> 
　　鉴于业主欲请咨询工程师履行某些服务，即＿＿＿＿＿并已接受咨询工程师为履行该类服务所提出的建议书。<BR> 
　　兹就以下事项达成本协议：<BR> 
　　１．本协议书中的措词和用语应与下文提及的“业主／咨询工程师标准服务协议书条件”中分别赋予它们的含义相同。<BR> 
　　２．下列条件应被认为是组成本协议书的一部分，并应被作为其一部分进行阅读和理解，即：<BR> 
　　（１）中标通知书；<BR> 
　　（２）业主／咨询工程师标准服务协议书条件（第一部分－－标准条件和第二部分－－特殊应用条件）；<BR> 
