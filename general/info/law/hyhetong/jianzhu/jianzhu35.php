      <p> 　　<BR> 
　　（一）工程总承包合同<BR> 
　　<BR> 
　　Ａ国的一家业主和Ｂ国的一家国际建筑工程承包公司之间，就在Ａ国的一座学校的施工建设，签订了工程承包合同。现将合同全文介绍如下：<BR> 
　　<BR> 
　　合　同<BR> 
　　<BR> 
　　日期：１９８３年３月３０日<BR> 
　　地点：＿＿＿＿＿市<BR> 
　　本合同经下述双方签署：<BR> 
　　１．＿＿＿＿＿＿＿市卫生局局长＿＿＿＿＿＿先生（以下简称甲方）<BR> 
　　２．＿＿＿＿＿＿＿建筑工程承包公司海外工程部经理＿＿＿＿＿＿＿先生（１９８３年３月２０日由＿＿＿＿＿＿建筑工程承包公司授权）（以下简称乙方）<BR> 
　　出生地：＿＿＿＿＿＿＿（国家）<BR> 
　　国籍：＿＿＿＿＿＿＿（国家）<BR> 
　　总部：＿＿＿＿＿＿＿（国）＿＿＿＿＿＿＿市<BR> 
　　在Ａ国的通讯地址：＿＿＿＿＿＿＿＿＿＿＿信箱<BR> 
　　鉴于甲方要兴建的＿＿＿＿＿＿学校１００００m2的教学楼，乙方提交报价已于１９８３年２月１０日被＿＿＿＿＿市卫生局招标委员会接受并已中标。根据Ａ<BR> 
　　￣国＿＿＿部１９８３年２月５日第３１８号决议，双方缔约于下：<BR> 
　　（一）合同内容<BR> 
　　乙方要完全、准确地按照该合同条款、技术规范、设计图纸、工程量表、价格表及与合同条款有关的书面协议，在＿＿＿＿市对＿＿＿＿＿学校１００００m2的教学楼（该工程为楼房，包括全套家俱、设备、空调）进行施工。<BR> 
　　乙方承认自己对合同的正文和附件，已有正确的理解，在此基础上同意缔约，按合同施工。<BR> 
　　所有上述文件、附件，均是本合同不可分割的组成部分。<BR> 
　　（二）合同金额<BR> 
　　本合同总金额为＿＿＿＿＿＿＿第纳尔。兑换美元的比例为７０％。<BR> 
　　这个金额是用单价乘实际工程量的方法计算出来的，这是指“概算工程”而言。至于“分段工程”则根据临时报表进行支付。临时报表的书写方法应是双方在合同中一致同意的。对乙方的支付要根据合同及其附件的规定。<BR> 
　　合同价格是固定的，它包括乙方为施工所承担的所有费用、开支、义务、各种税款，包括因施工不良而支出的工程维修费及在合同期内规定的保修、保养费。乙方无权以任何理由要求增加合同价格，如市场物价上涨，货币价格浮动，生活费用提高，工资的基限提高，调整税法、关税及税务，在Ａ国国内或国外新增加赋税。<BR> 
　　￣<BR> 
　　（三）工期<BR> 
　　乙方必须根据本合同第＿＿＿号附件的说明，在自移交场地之日起的３８０天之内完成全部合同工程的施工。正式的节假日包括在工期之内。如果乙方由于某些不可抗拒的原因而认为不可能按期完工的话，可书面通知甲方，说明原因，并提出要求增加期限。如果乙方证实这些原因存在，合同期限可以适当延长。<BR> 
　　（四）移交场地<BR> 
　　根据甲方向乙方发出的通知，进行工地交接，交接时须签订纪要，双方各持一份。如果乙方或其接收场地的代表在通知中规定的期限内缺席，甲方可照常起草纪要，并将纪要的复印件寄给乙方。起草纪要之日等于乙方接收场地之时。<BR> 
　　（五）查看工地<BR> 
　　乙方承认在签订合同之前已查看了土地及周围的环境，掌握了所有与工程施工有关或对施工有影响的情况，如地质土壤情况、水源、当地气候情况、道路、交通流量、劳动力的提供范围等。由这些因素产生的后果均由乙方负责。<BR> 
　　（六）履约保证金<BR> 
　　乙方应自被通知中标的第２天起的３０天内，向甲方寄存履约保证金，金额为该合同总额的２０％，或在指定期限将以前寄存的投标保证金补充到上述比例。该合同只有在寄存保证金之后才对甲方产生义务。<BR> 
　　用现金、信用支票，或按合同第十条规定的条件提供保函作保均可。按第五十六条规定，保函须在合同整个执行期中都有效，直到最后交付工程为止。<BR> 
　　该保证金始终都由甲方保存，作为乙方完美施工和履行甲方权利的保证。乙方应在甲方发出挂号函件后，最多在一个月内，按可能业经被扣除的金额的相同额度补足保证金。否则，甲方有权没收保证金的余额，或者从乙方手中收回工程，由甲方自行施工而由乙方承担费用。甲方有权索赔，只要用挂号函件通知乙方即可，无须诉诸法院或采取其它措施。<BR> 
　　甲方有权从乙方在本合同的任何收益中索取其拖欠的债务。<BR> 
　　（七）图纸和规范完整无缺<BR> 
　　甲方提交的任何技术说明、设计、绘图中的所有错误或疏忽，随时可以更正。乙方应亲自审查规范、设计、图纸的有效程度，并在适当的时候把自己的意见告诉甲方。在甲方接受这些意见的情况下，甲方就要对这些意见负责，就如同是其自己提出来的一样。<BR> 
　　（八）施工进度计划表<BR> 
　　乙方自接收工地起，１５天内向甲方递交一份施工计划，阐明为完成本合同工程所采用的方法、施工进度、施工安排、运进施工现场的设备，以及建立临时设施等。乙方应根据甲方意见和施工进展情况，对自己的施工计划进行调整。<BR> 
　　（九）合同内容的变更<BR> 
　　甲方有权调整合同内容，但增加或减少的工程量不得超过合同金额的１５％。实际工程量的增加或减少在合同金额的１５％之内，乙方无权要求任何补偿。<BR> 
　　上述调整金额，要用业已议定的单价计算。如果没有这种单价，则在责成乙方进行调整时的当地市场价格或世界市场价格的基础上进行计算。<BR> 
　　根据本条款的规定，如果乙方认为提出的调整会导致工程不能按期完工或交工，乙方可以自通知进行调整之日起最多两个星期内向甲方提出延长工期的要求。否则，等于放弃权利。这样，乙方就要在原工期内完成包括调整部分在内的工程，并交付使用。<BR> 
　　甲方在收到乙方呈交的延期要求时，可将工期延长到其认为是适当的时候。<BR> 
　　（十）支付条件<BR> 
　　１．在乙方接收工地、提交履约保函并办完合同注册登记付税手续之后，甲方根据乙方的要求，向乙方支付合同金额＿＿＿％的预付款。支付预付款应在乙方提交与预付款等值的保函之后，该保函应该在施工期间始终有效，且应开自一家Ａ国的银行，或是经过这家银行认证，同时不许附加任何条件，也不得被作废。上述银行须承认有一笔与保函等值的款项由甲方支配，并承认可全部支付甲方，无须提醒，无须诉诸司法部门，也无须办理其他手续，同时也不管乙方或第三者提出的异议。预付款应从支付给乙方的月工程进度款中按预付款在合同总价中所占的比例扣除，直到扣完为止。保函额度也可以按预付款收回的金额递减。<BR> 
　　２．乙方运到工地而又是工程实际需要并被甲方接受的材料，其费用中的＿＿％应付给乙方。条件是乙方提供的材料应是优质、适用、符合规定，为甲方工程师所接受，并须妥善储存在仓库里。上述规定同样适用于乙方运到工地的机器设备，这些机器设备是工程的组成部分，在安装时应是完好无损的。但属乙方所有的机械则不包括在内，因为这些机械并非是工程的组成部分，只不过是为了施工而引进到现场。<BR> 
　　上述预付款要首先从乙方用库存材料所完成的工程的进度款中扣除，直到全部收回为止。<BR> 
　　３．对符合条件与规范而且是实际上完成了的工程，甲方在双方确定的单价基础上，向乙方支付其造价的＿＿％。这些支付款项的应根据本合同第二十五条的规定和每月完成的工程量报表而定。这些款项要在扣除本条第一款所述的预付款比例和先前业已支付的入库材料费后才能支付，每月支付金额不少于＿＿第纳尔。甲方支付本条规定的月工程进度款，要在工程报表得到甲方认可的４５日之内进行。条件是完成的工程应全部符合本合同所规定的条件。<BR> 
　　支付要在审查报表、支付申请单、并证实无误后进行。支付迟于４５天时，乙方可按＿＿＿＿银行规定的利息率索取利息。由于乙方对工程量的统计欠及时，或者在制作工程量表时乙方（或其代表）外出，或甲方认定工程的进度差、乙方（或其代表，或其分包人）的表现差，甲方对其逾期支付不负责支付利息。同样，由于报表出现缺陷、疏忽和错误－－不管是被甲方发现还是在支付前被Ａ国任何稽核、监督单位发现－－甲方对其逾期支付也不承担支付利息的责任。在这种情况下，可将支付（月进度）款的时限适当推迟，以便对报表进行校正。本文规定的因迟付而享受利息的规定不适用予预付款、合同决算款、过期退回保证金，以及材料款。一言以蔽之，这个规定只适于月工程进度款。<BR> 
　　４．乙方应向Ａ国有关方面交的款项，可从其与甲方签订的合同的收益中扣除。<BR> 
　　５．甲方从每月的月工程进度款中扣除５％的金额，作为保证施工完好的保留金。在工程竣工和按第五十四条规定进行初步验收后，遂将这个比例金额付还乙方。也可以依据一份符合本条规定且与上述比例等值的保函，把这个比例的金额付给乙方，该保函自初步验收之日起３０日内开出有效。<BR> 
　　６．在遵守货币管理法及有关规定的情况下，乙方可以把用于工程而从国外购来的进口材料款、机械设备款汇往国外。也可根据现行规定和状况，把本合同的盈余款额汇往国外。<BR> 
　　（十一）信用证<BR> 
　　为本合同施工而需要从国外进口的材料费、设备费，乙方可以用一家Ａ国的银行所开的信用证兑换外汇。<BR> 
　　这个金额的确定要根据乙方开信用证前向甲方提供的材料、设备单子总价而定。<BR> 
　　信用证可以分着开。根据乙方向甲方提供的须进口的每一批施工材料、设备的时间表而确定支付金额和日期。同时根据该表确定信用证的有效期。<BR> 
　　在确定受益人之后，开给乙方的信用证，非经甲方同意，不得转让给任何其他受益人。上述信用证的每次支付通过下列方式进行：<BR> 
　　１．每次装运支付７５％金额时，要提供下列凭证：<BR> 
　　（１）五份认证的原始帐单。<BR> 
　　（２）一套货运单据，证实已按正常的海运价付过运费。<BR> 
　　（３）由Ａ国一家保险公司为甲方开的为之承担一切风险的保险文件，从制造厂到Ａ国项目现场均在保险范围。<BR> 
　　（４）装运清单。<BR> 
　　（５）根据本合同的规定，由甲方承认和批准的检查证明。<BR> 
　　（６）制造厂家的证明和Ａ国驻国外使馆或领事馆在原件上认证。<BR> 
　　２．当这些材料和设备根据工艺要求和技术规范安装完毕后，甲方通知银行并支付１５％比例的信用证金额。<BR> 
　　３．信用证金额的１０％保存到全部合同工程初步验收结束时为止。上述支付比例，由甲方通知银行支付。<BR> 
　　（十二）甲方有权检查材料及其种类，只有甲方开了证明以后才允许启运。<BR> 
　　海关税收以及所有储存费，运往现场的开支，装卸费，只要没有得到有关方面豁免，都由乙方根据法律交付。<BR> 
　　为办理信用证及付款手续上的需要，乙方负责向其在Ａ国国内外的开户银行提交一份合同副本。<BR> 
　　（十三）完美施工<BR> 
　　乙方应根据工程图纸、技术规范和施工计划及专业技术规定的要求认真地进行施工。在施工中要使用优质材料、设备、机械，在投入使用之前，要给甲方工程师提供所有样品，并给予充分时间进行认可。<BR> 
　　因材料有瑕疵，或制造粗糙，或施工错误而致使甲方拒收的任何工程部分，需对之进行修理或拆除时，一切费用均由乙方负责。<BR> 
　　（十四）第三方保险<BR> 
　　乙方应在一家保险公司对因施工而使第三者及其财产蒙受损失的责任进行保险。不论什么情况，保险金额均为＿＿＿＿第纳尔。为避免可能发生的工人和其他人员伤亡，避免给社会和个人造成损失，乙方须采取必要的措施，其中包括按交通规定设置白天与夜晚的路标（用标记和指示灯）。由于施工，由于乙方或其代理人，或其承包人，或其职工的不慎而可能造成的死亡、事故、损伤，乙方要负直接责任，并独自承担由此而引起的索赔和补偿。<BR> 
　　（十五）工程保险<BR> 
　　乙方应在一家保险公司交纳保险金，对现场已完成的工程、材料和器具遭到破坏、失火或被窃进行保险。保险文件要为承包商承担一切风险。根据本合同第五十四条，到工程初步接收时此项保险才可结束。<BR> 
　　（十六）乙方未尽保险义务<BR> 
　　乙方在开工时，要为其前两条所规定的保险义务提出已履行的证据。如对保险未尽义务，或没有支付这笔款项，甲方可以代为保险，承担这笔费用，可将这些费用从乙方应得的款项中扣除。在所有情况下，甲方都应停止向乙方支付任何款项直至乙方确实已经承担此项保险义务并支付这笔款项。<BR> 
　　（十七）分包合同<BR> 
　　不允许乙方将合同中规定的工程分包出去，但经甲方的书面同意后，允许乙方和那些有能力和经验的专业施工公司，就合同中的部分工程的施工签订分包合同。在所在情况下，把部分工程分包出去的乙方，始终都要和他的分包人一起对执行本合同共同负责。<BR> 
　　（十八）转让合同<BR> 
　　不允许乙方向他人转让整个或部分合同，如乙方违反这一条规定，合同就被废除，履约保险金就被没收。这无须经过任保法律手续，也不影响甲方要求赔偿损失的权利，如有必要，甲方还有权向乙方索取因物价上涨所增加的费用。但允许签约人将其应得款的全部或局部转让给Ａ国的一家银行，条件如下：<BR> 
　　１．针对甲方的转让未经甲方书面同意不能成立。<BR> 
　　２．这种转让不影响甲方对乙方的权利。<BR> 
　　３．一旦乙方向某银行转让，就不能反悔，除非得到银行的同意。<BR> 
　　（十九）施工监督<BR> 
　　对签约工程的施工监督和对乙方施工的检查，由甲方工程师或管理机构授权的职员承担。如甲方工程师认为工程进展缓慢，可以向乙方提出书面警告，要求增加工人，增加机器设备，加班以及采取其他以保证工程顺利进行和按时竣工的措施。所有这些费用均由乙方承担。<BR> 
　　甲方工程师和他的职员有权在任何时候进入现场，对施工的工程进行检查、管理，乃至发布旨在使施工顺利进行的命令和指示；这些指令应书面通知乙方并记入现场记录；乙方和他的工人、分包者应执行这些指令，而且不使用那些甲方工程师认为不符合合同条件或附件条件的材料。为了保证按技术规范和合同条件进行施工，在甲方工程师认为有必要时，他们有权书面通知停工，或者不接受不符合议定的材料和工程；同时允许在不影响工程造价、不违反合同宗旨的情况下，根据技术规范要求进行细小的调整和修改。对于那些需要增加费用的工程应经甲方批准。甲方工程师有权要求乙方做材料试验、材料分析、检查材料强度和提交各工期的照片，这些费用均由乙方承担。只有甲方要求再次对这些材料进行检查和试验，而且两次试验结果都符合技术规范要求时，甲方才承担这笔费用。<BR> 
　　为继续施工起见，乙方也可以要求甲方工程师进行任何一项必要的检查。这要由乙方向甲方工程师或其代表提交一份书面报告，由后者在报告的复印件上作收讫签字。在这种情况下，甲方工程师应作必要的检查，并尽快提出意见，把检查结果记入工地记录。<BR> 
　　（二十）甲方工程师办公室<BR> 
　　乙方应在现场为甲方工程师提供办公室。该办公室应完全符合条件，包括全套设备。建立办公室、供电、安装设备的费用全部由乙方承担。<BR> 
　　（二十一）乙方的现场代表<BR> 
　　乙方应指定现场代表，负责接收并执行甲方工程师的指令，同时应为他设立一个具有同等施工经验、有高度技术水平的技术机构，该机构成员在整个工作期间均应在现场。<BR> 
　　对于现场代表和技术机构成员的选配和更换，应事先经甲方同意。必要时甲方有权要求更换人选，这不影响乙方完美施工的责任。<BR> 
　　（二十二）为管理工作和其他承包人提供方便<BR> 
　　乙方和他所属的工地负责人、代表、职工，要为甲方工程师提供必要的方便，包括到现场进行检查、测量、试验及其他工作，同时还应照顾到其他承包人在现场及其附近地方的施工，并为他们的施工创造有利条件。<BR> 
　　（二十三）维护工地秩序<BR> 
　　乙方负责维护现场秩序，对那些玩忽职守，拒绝执行工程师指示，或者技术能力差，或有关当局要求解雇的职工，乙方应在接到甲方书面命令后的２４小时内把他们送出现场；对任何扰乱工地治安的行为应马上报警。<BR> 
　　（二十四）合同文件应保存在工地<BR> 
　　乙方现场至少应保存两套合同文本、技术规范、工程图纸及其他与本工程有关的文件，以便在甲方工程师或他的代表需要时参阅。<BR> 
　　（二十五）已完工程的计算<BR> 
　　每月底在甲方代表同意的情况下，乙方出资对完工部分进行计算，同时应由甲方代表参加。工程计算必须由双方代表签字才有效。如果乙方拖延工程计算，他就要服从甲方的算法并承担计算费用。工程量表中的工程量只是一种估算，用上述计算方式计算出来的实际完成的工程，才是实际工程量。<BR> 
　　乙方应保存专门的记录，把由双方签字的月统计表中反映出来的工程量记录在案。<BR> 
　　（二十六）设备处理<BR> 
　　未经甲方同意不允许乙方将他运入现场的材料、工具、机械、设备、临时设施运走和处理，直至工程初步交工时为止。上述材料设备丢失、损坏、被窃等，甲方概不负责。<BR> 
　　（二十七）保险公共设施<BR> 
　　如果在现场挖掘土方工作的过程中，发现电线、水管管道，或者其他东西，乙方应立即停止挖掘工作，并及时报告甲方或专门机构。乙方根据有关指示，负责修理那些被破坏的公共设施，并恢复原状，费用由乙方承担。<BR> 
　　（二十八）文物和有价值的物品<BR> 
　　在施工过程中发现文物和有价值的物品时，乙方在采取防止损坏、打碎的措施的同时，应立即告诉甲方、文物局和警察局，如果这些文物不易搬动，应就地停工，并通知甲方代表和专门机构或警察局。<BR> 
　　（二十九）停工<BR> 
　　只有根据甲方的书面命令，才允许乙方停工。在停工期间，乙方负责保护工程。<BR> 
　　如果停工的原因不是属于乙方，那么甲方应向乙方补足相当于停工时间的工期，如果停工时间超过６个月而原因又归结于甲方，那么乙方有权要求终止合同。<BR> 
　　在这样的情况下，乙方有权要求索取他已经施工的工程费用和停工期间的实际损失，但不许要求其他方面的赔偿。<BR> 
　　（三十）误期罚款<BR> 
　　如果乙方不能按照合同中规定的期限，或在其他业已议定的期限内完成施工，他应承担误期罚款。比例如下：<BR> 
　　如果甲方认为可以从已经完成的工程中获益的话，每误期一天的罚款占延期工程总价的１％，如果甲方认为不能从已经完成的工程中获得收益的话，每误期一天的罚款占工程总价的０．１％。<BR> 
　　在所有的情况下，罚款均不应超过合同总价的５％。即使未因延误工期而造成损害，误期也要罚款，无须提醒，不用警告，不用提交仲裁或采取任何其他措施，也不影响甲方因误期使自己蒙受的损失要求乙方赔偿的权利。<BR> 
　　如果甲方证实乙方误期是由于意外原因所致，那么误期罚款可以全部或部分免除。罚款可以从保证金或者乙方在甲方处的应得款项或者从其在任何其他的公共机构的应得款项中扣除。任何罚款的扣除并不意味着免除乙方承担施工以及完成议定工程的责任。<BR> 
　　在乙方于甲方处还有足够款项偿还延期罚款的情况下，甲方根据自己的全面估计，可以延期索回罚款。但要有乙方提出申请，并阐明延期交纳罚款的详细理由和延迟期限。<BR> 
　　（三十一）废除合同<BR> 
　　在不影响甲方取得误期罚金和赔偿权利的条件下，在遇到下列情况时，甲方有权废除合同，没收保证金。<BR> 
　　１．乙方延期开工，或施工很慢，虽然给予书面提醒，甲方仍认为他不可能按期完成合同工程。<BR> 
　　２．如果乙方停工１５天以上，停工理由不被甲方接受。<BR> 
　　３．如果乙方撤离工地，或放弃工程，或证明他没有施工，或者他被证实施工混乱，而又指望不着他完美施工。<BR> 
　　４．如果乙方没有按合同规定的期限施工，甲方认为没有必要给他一个宽限期，或者在给他的宽限期之内未能完成施工。<BR> 
　　５．在执行合同或同甲方在施工交往中，如甲方自己或通过他人直接或间接地采用欺骗或营私舞弊的手法。<BR> 
　　６．如果乙方被证实通过自己或他人直接或间接地贿赂甲方的官员或工人，或监工代表，或伙同上述人员相勾结搞有损于甲方和有损于公益的事。<BR> 
　　７．如果乙方破产，或宣布破产，或陷于与债权人调停状态。<BR> 
　　８．如果乙方对合同的执行严重敷衍了事，或者忽视履行合同中实质性义务，而且从书面要求其改正之日起十五日之内仍无动于衷。<BR> 
　　对取消合同和没收保证金，甲方用备忘录形式通知乙方即可，无须诉诸司法和采取其他措施。<BR> 
　　（三十二）收回工程由乙方出资施工<BR> 
　　在甲方可以废除合同的情况下，他可不废除合同，只要用挂号函件通知乙方，就可以收回工程，这无须诉诸法院，也无须办理什么手续。甲方可亲自动手施工，也可通过投标议价、缔约方式让任何缔约者对未完工程的全部或局部进行施工。其费用、责任由乙方承担。甲方可以从乙方存在甲方的保险金或乙方在甲方或其他任何部门的应得款项中扣除合同金额外他所花费的一切开支和损失。因工程造价比原议定的价格下降而出现的盈余，乙方无权要求分享。甲方可以查封工地的临时设施，机具设备和材料，用以保证自己的权利。上述机具设备、材料，甲方可以无偿用于施工。对其损坏、短缺，甲方对乙方不承担责任。甲方还可以变卖这些物品，用以补偿自己的权益，由于变卖上述机具、设备，而给乙方造成的损失，甲方不承担责任。<BR> 
　　除了合同金额外，甲方有权索取误期罚款，有权要求乙方赔偿其因收回工程自己所蒙受的损失。<BR> 
　　（三十三）终止合同<BR> 
　　如果出于公益需要，甲方可在乙方没有任何过错的情况下结束合同，条件是在必要时可向乙方赔偿。<BR> 
　　（三十四）工程清点<BR> 
　　特别是在第三十一、三十二、三十三条中所指的那些情况下，由甲方确定检查现场的时间，清点已经完成的工程，清点乙方运到现场的机械、材料、设备。甲方至少在７天之前用挂号函件将已确定的时间告诉乙方，让其参加清点并签署纪要。如果乙方或他的代表在规定的时间缺席，那么甲方就在乙方缺席的情况下进行清点，并记入清点纪要，甲方代表在纪要上签字，将该纪要用挂号函件寄一份给乙方，让乙方对纪要发表意见。如果乙方在纪要送去７天当中没有发表意见，就说明他承认纪要中的内容正确无误，并接受了清点。<BR> 
　　本条规定的工程量计算和清点费用由乙方负担。<BR> 
　　在因执行法令而结束合同的情况下，本条款有效。<BR> 
　　（三十五）丧失能力<BR> 
　　１．如果乙方丧失能力，那么甲方有权在终止合同和允许乙方的继承人继续施工二者之间作出选择，如果乙方这样要求，甲方也证实乙方的继承人有继续施工的财务和技术能力的话。<BR> 
　　２．如果乙方多于一人，当其中的一人丧失能力，甲方可以在终止合同和委托乙方的其他人继续施工二者之间进行选择。<BR> 
　　３．如果乙方只是拥有人员，或只有资金的公司而且已经散伙，而散伙的目的不是为了重新组合，甲方就结束合同。<BR> 
　　４．在所有可以结束合同的情况下，用挂号函件将甲方的意见通知乙方即可，无须采取任何其他措施或通知诉讼。<BR> 
　　（三十六）乙方遇到不可抗拒的情况<BR> 
　　如果遇到不可抗拒和无法预料的情况，使施工任务繁重，乙方受到重大损失的威胁，而施工并非不可能进行，乙方可以用合理提高合同金额的方法向甲方索取赔偿。如果这种情况继续存在，而又不能消除，那就终止合同。如果出现使施工无法进行的不可抗拒的力量，乙方履行施工的义务就被免除。<BR> 
　　（三十七）支付印花税<BR> 
　　乙方应在Ａ国税务局办理合同登记手续，要在签订合同１５天内交印花税，对误期交纳此税要承担各种费用和罚款。乙方还应交纳为执行合同应交的其他印花税，并向甲方递交他已交完税收的正式文件。<BR> 
　　（三十八）所得税<BR> 
　　根据Ａ国所得税法和依此法公布的其他条例，乙方应交所得税和本合同利润附加税。<BR> 
　　（三十九）海关税<BR> 
　　根据海关法及有关制度和规定，乙方负责交纳进口货物和与执行合同有关的理应交纳的海关税和其他税收。<BR> 
　　（四十）享受临时免税制度<BR> 
　　根据Ａ国海关法及海关制度和规定，对于为了完成合同工程而进口到Ａ国的施工机械、设备和器具，在提交竣工后将上述机械、设备、器具重新外运的保证书提交后，乙方可享受海关临时免税制度。<BR> 
　　（四十一）当地材料<BR> 
　　只要在Ａ国市场上有建筑材料、设备、机械、器具等国内产品，乙方必须尽可能使用。<BR> 
　　（四十二）组织施工和使用当地工人<BR> 
　　乙方负责技术工人和非技术工人、设备、机械、器具及其他与施工、完美施工有关事宜的组织工作，甲方向Ａ国的有关当局提交文件，以便在该合同工程需要雇工时，为之提供方便。<BR> 
　　（四十三）工人花名册<BR> 
　　在开工时，人员增加或变动时，乙方应向甲方提供花名册，用以反映工人、职员、技术人员的姓名和工地管理人员的不同职务。乙方还要向甲方提供有关乙方运入现场的机械、设备、仪器的说明和资料。<BR> 
　　（四十四）安排工人居住，提供现场公共设施<BR> 
　　乙方负责向其工人、职工提供住房，并负担现场居住费用；还要负责提供这些住房的照明、饮水十四）安排工人居住，提供现场公共设施<BR> 
　　乙方负责向其工人、职工提供住房，并负担现场居住费用；还要负责提供这些住房的照明、饮水及必要的公共设施。如果乙方为其工人建临时住房时，应得到甲方的同意；在这种情况下，乙方负责所有建房费用、维修费，并负责提供必要的服务。工程竣工之后，乙方还要负责拆除临时住房，并恢复原状；如甲方需要，可按双方协商的价格进行转让。甲方提供必要的电源、水源，但乙方要承担内部的临时管线安装费、照明费；用于工程施工的发电机、机械等所用水、电和燃料消耗费，由乙方承担；乙方还应负责处理废水和废渣。<BR> 
　　（四十五）违禁物品<BR> 
　　不允许乙方以任何方式进口、出售、从事交换任何酒精饮料和麻醉药品，以及其他法律所禁止的物品。乙方也不得允许他的分包人、代表、职工和工人饮用、进口、出售及从事交换上述违禁物品的任何活动。也不允许交换外币（在货币管理法和Ａ国中央银行许可的情况下　在Ａ国银行进行兑换者例外）。除非经专门的机构批准，不允许乙方和任何人交换、使用任何一种武器、弹药、炸药。同时未经甲方工程师批准，不许乙方在施工中使用炸药或爆炸物。<BR> 
　　（四十六）禁止政治活动<BR> 
　　乙方及其合作者、分包人、代理人、外国雇工以及他们的家属在Ａ国境内居住期间，不能以任何形式参与任何政治活动。<BR> 
　　（四十七）周假和法定假<BR> 
　　乙方应让他的外国工人和职工在星期天和Ａ国法定假日休息。根据现行法律、条例的规定，因工作需要工人们在假日工作时应给予补偿。<BR> 
　　（四十八）工人的工资和奖金<BR> 
　　乙方负担工人的工资、奖金和补偿。按劳动法、社会保险法、工资最低基限法及现有和将来要制定的其他法律和制度，均由乙方独自承担业主（对工人）应尽的所有义务。<BR> 
　　（四十九）合同机密<BR> 
　　未经甲方事先书面同意，不允许乙方披露合同的细节，也不允许乙方在商业印刷品、专业印刷品或其他场合中披露这些细节。<BR> 
　　在施工期间直至合同结束和维修期终止后，乙方均应保密，未经甲方的书面同意，不允许传播表报中的全部或局部内容。<BR> 
　　（五十）遵守法律条款和条例<BR> 
　　乙方在执行合同过程中，应遵守Ａ国的有效法律，特别是有关工程、工资、社会保障、工程保险、物资保险、运输工具保险的法律，和有关文物、海关、税收及其它诸如此类的法律。对于为施工而取得的任何一种许可所花费用，均应由乙方承担。<BR> 
　　在遵守合同条款的同时，在执行本合同中所产生的各种赋税，其中包括海关税和海关附加税均由乙方负担。除了法定的和合同条件中规定可豁免的以外，任何一项税收均不能免除。<BR> 
　　（五十一）专利权和发明权<BR> 
　　乙方应尊重他人的专利权、发明权和垄断权－－对于因乙方不尊重他人的这种权利，其中包括Ａ国境内外项目的技术说明和图纸－－他人向甲方提出的任何要求而因之造成的任何损失，乙方应给予赔偿。<BR> 
　　（五十二）Ａ国的司法权限<BR> 
　　有关合同的所有解释和执行必须服从Ａ国的现行法律和法规，Ａ国的司法部门负责审理由合同引起的纠纷。<BR> 
　　任何一方诉诸法律都不能中止合同的执行，都不影响双方的义务和权利，直到对争执的问题有了必须执行的判决为止。<BR> 
　　（五十三）初步验收<BR> 
