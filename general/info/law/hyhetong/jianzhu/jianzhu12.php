      <p> 　　<BR> 
　　《建设工程施工合同条件》（以下简称《合同条件》）和《建设工程施工合同协议条款》（以下简称《协议条款》）组成了《建设工程施工合同示范文本》（以下简称《示范文本》）。<BR> 
　　《合同条件》是根据《中华人民共和国经济合同法》、《建筑安装工程承包合同条例》对承发包双方权利义务作出的规定，除双方协商一致对其中的某些条款作出修改、补充或取消外，都必须严格履行。<BR> 
　　由于合同标的－－建设工程的内容各不相同，工期造价也就随之变动，承发包双方的自身条件、能力、施工现场的环境和条件也都各异，双方的权利、义务也就各有特性。因此，《合同条件》也就不可能完全适用于每个具体工程，需要进行必要的修改、补充，即配之以《协议条款》。《协议条款》是按《合同条件》的顺序拟定的，主要是为《合同条件》的修改补充提供一个协议的格式，承发包双方针对工程的实际情况，把对《合同条件》的修改补充和不予采用的一致意见按《协议条款》的格式形成协议，《合同条件》和《协议条款》就是双方统一意愿的体现，成为合同文件的组成部分。<BR> 
　　为了使承发包双方在签订合同时能更好地使用《合同条件》，对施工中可能出现的问题尽量事先达成协议或确立解决的原则，并使协议尽可能准确、严密，以减少扯皮，保证施工的顺利进行。我们编制了这个《使用说明》，供承发包双方在签订合同时参考。<BR> 
　　采用招标发包的工程，《合同条件》应是招标书的组成部分，发包方对其修改、补充或不予采用的意见，要在招标书中说明。承包方对招标书的说明是否同意及本身对《合同条件》的修改、补充和不予采用的意见，也要在投标书中一一列出。中标后，双方将协商一致的意见写入《协议条款》。不采用招标发包的工程，在要约和承诺时，都要把对《合同条件》的修改、补充和不予采用的意见一一提出，将取得一致的意见写入《协议条款》。<BR> 
　　下面对《协议条款》的使用加以说明：<BR> 
　　发包方（以下简称甲方）：可以是具备法人资格的国家机关、事业单位、国营企业、集体企业、私营企业、经济联合体和社会团体，也可以是依法登记的个人合伙、个体经营户或个人，即一切以协议、法院判决或其它合法完备手续取得甲方的资格，承认全部合同文件，能够而且愿意，履行合同规定义务的单位和人员。上列单位的名称和个人的姓名，应准确地写在《协议条款》甲方位置内，不得简称。与甲方合并的单位、兼并甲方的单位，购买甲方合同和接受甲方出让的单位和人员，均可成为甲方，履行合同规定的义务，享有合同规定的权利。<BR> 
　　承包方（以下简称乙方）：应是具备与工程相应资质和法人资格的国营企业、集体企业、私营企业，上述单位的名称应准确地写在《协议条款》承包方位置内，不得简称。但乙方不能将工程转包或出让，如按《合同条件》第３６条进行分包，应在合同签订前提出并征得甲方同意。<BR> 
　　第１条<BR> 
　　１．１　本款内应准确写出工程的名称；详细的地址；工程内容，主要包括建筑规模和结构特征；承包的范围，主要指是否包括土建、设备安装、装饰装修等工作。<BR> 
　　１．２　本款内应写明双方商定的开工日期，也可以将此规定为甲方代表发布开工令的日期；双方商定的乙方提交竣工报告的日期，包括星期日和法定假日在内的总日历工期天数。<BR> 
　　１．３　写明双方商定的工程应达到的质量等级（即合格还是优良）。<BR> 
　　１．４　应写明工程价款的金额和承包方式。<BR> 
　　工程为群体或小区工程时，以上各项应按附表格式将单位工程列表说明，作为《协议条款》的附件。<BR> 
