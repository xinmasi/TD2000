　　５．１　双方同意在新公司股东准备投资时，但在工程竣工前建立新公司，为此＿＿＿国政府应推荐并指定一个＿＿＿国实体成为新公司的股东。Ｃ占股份＿＿＿％，应是主要公司，应任命新公司董事长。其余股份可由＿＿＿国实体占有。<BR> 
