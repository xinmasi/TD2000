　　１）可行性研究、设计和工程技术服务及其他咨询服务的费用；<BR> 
　　２）建造和安装费用；<BR> 
　　３）购买设备和材料的费用；<BR> 
　　４）管理费用（含国外支出）；<BR> 
　　５）其他费用；<BR> 
　　６）不可预见费；<BR> 
　　７）建造期利息＿＿＿％／年加银行手续费；<BR> 
　　８）工程建设和出口信用保险的保险费。<BR> 
