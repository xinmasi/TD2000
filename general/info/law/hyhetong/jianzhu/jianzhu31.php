　　２０．争议解决<BR> 
　　２０．１　在本协议有效期限内，双方代表应至少半年与会一次讨论工程的进展和基础设施的运行以便保证双方的安排在互相满意的基础上继续进行。<BR> 
　　２０．２　双方同意如在执行本协议或解释有关规定时产生争议或歧义，双方应协商努力解决这种争议，如不能解决，双方代表应开会解决争议或歧义，双方的联合决定对各方均有约束力，如根据本款不能解决争议或歧义，适用于第２３款。<BR> 
　　２０．３　如在基础设施建造或运行的技术问题双方有争议，应提供设计和生产标准，提交双方同意的专家决定。这种决定应是最终的，但可以仲裁。如该专家未能在３０天内做出决定，或有一方不满意这种决定，该方可以在收到这种决定通知后或在争议提交３０天后的３０天内将争议提交仲裁，详见第２３款。<BR> 
