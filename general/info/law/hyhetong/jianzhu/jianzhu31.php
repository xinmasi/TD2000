　　５）Ｂ允许本工程所需的施工机械、材料和设备等可由海运经＿＿＿进入＿＿＿国和／或陆运经＿＿＿边境以及经空运进入＿＿＿国境内，＿＿＿国边境检查机关和海关应予以方便和及时的放行。<BR> 
　　４．５　Ｂ保证其不改变或不允许他人改变现场环境以致影响本工程的建造和运行。<BR> 
　　４．６　Ｂ工程建设费用的＿＿％由中方提供买方信贷，其支付给Ｃ的时间分别为：<BR> 
　　１）合同签订＿＿日支付贷款额的＿＿％；<BR> 
　　２）＿＿＿年＿＿月＿＿日支付贷款额的＿＿％；<BR> 
