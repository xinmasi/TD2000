　　３）工程进展必需的国家其它批准和地方批准。<BR> 
　　４）由＿＿＿国有关当局对下述项目免税的批准：<BR> 
　　－－在项目移交前对进口设备、材料和工程所需的施工设备和物品的关税和进口的其它税收全免；<BR> 
　　－－自第＿＿个盈利年起＿＿＿年内利润税、附加税、地方税和营业税＿＿＿％免除，接下来利润税免除＿＿＿％。<BR> 
　　５）投资的自由寄出，包括但不仅限于这种投资的利润和／或收到的付款或销售盈利，不扣留税、汇款税或根据＿＿＿国法律征收的其他税款。<BR> 
　　６）＿＿＿国政府有关机构对同本工程有关的所有设备材料等直接进口的批准。<BR> 
　　７）Ｃ将在＿＿＿国注册分公司或子公司。Ｂ签发的有关本工程的注册证书，确认Ｃ（包括分、子公司）是一个根据＿＿＿国外国投资法注册的企业。<BR> 
　　８）可以将酬金（美元）自由寄出＿＿＿＿国，免收个人所得税、汇款税或其他税款。<BR> 
　　<BR> 
　　１０．基础设施的运行<BR> 
　　１０．１　新公司应在运行期，即基础设施开始商业运行后的＿＿＿年内负责其管理、运行、维护、修理和大修，应尽力确保状况良好。如在３０年运行期内内部回收率未达到＿＿＿％，上述运行期的延长应经双方商定。<BR> 
　　１０．２　为了从事必要的大修、维护、检查和修理，新公司自竣工日起每年应停机检修一次。<BR> 
　　１０．３　根据第１０．１款的义务，新公司应有权利：<BR> 
　　１）签订提供材料和服务包括购买替换设备的合同，参与基础设施商业运行协议的谈判和签约；<BR> 
　　２）任命、组织和领导职员，管理和监督电站；<BR> 
　　３）建立和保持定期检查、维修和大修程序；<BR> 
　　４）从事基础设施运行所需要的一切其他工作。<BR> 
　　１０．４　Ｂ和新公司应经常地共同讨论并就基础设施运行的安全规则达成共识。<BR> 
　　１０．５　新公司应按照签署本协议时现行的所有＿＿国国家和地方法律及规则经营基础设施。<BR> 
　　如果任何变化或新的立法<BR> 
　　１）使基础设施不能在通常的条件下运行；<BR> 
　　２）使新公司的利益在很大程度上受到减少、受到歧视或其他不利影响；那末双方应协商，努力就修改本协议达成共识。<BR> 
　　１０．６　Ｂ应有权任命和分配其专家机构监督新公司对基础设施的经营，费用由Ｂ承担。这种监督不应妨碍新公司对电站的正常经营。<BR> 
　　<BR> 
　　１１．基础设施的服务<BR> 
　　１１．１　新公司应按照合同的规定向其他国家提供基础设施的服务，但如服务对象不是合同所规定的国家，则双方另行签订协议。<BR> 
　　１１．２　如果Ｂ打算将基础设施的服务分配到需要的当地市场，Ｂ应将其作为第１２．４款规定的资源补偿费，如Ｂ的需要多于资源补偿费，应经新公司同意，但是应按照第１２款的规定以美元向新公司支付费用。<BR> 
　　<BR> 
　　１２．基础设施的服务费用和收入分配<BR> 
　　１２．１　为了达到第＿＿＿、＿＿＿、＿＿＿和＿＿＿款的目的，Ｂ应负责同＿＿＿＿国政府谈判，签署基础设施服务协议。Ｂ应提供对谈判有用和必要的信息和资料，并指定一人会同Ｃ或新公司和＿＿＿国保险公司代表负责谈判。<BR> 
　　负责谈判人员应按照下述程序行使职责：<BR> 
　　１）自签署本协议之日＿＿天内提交工程的基本思路和整体规划；<BR> 
　　２）自签署本协议之日＿＿天内开始有关基础设施服务费的具体的技术谈判和讨论；<BR> 
　　３）自签署本协议之日＿＿个月内结束有关事宜，签署基础设施服务协议。<BR> 
　　Ｃ或新公司应全面协助和支持Ｂ，提供谈判所需要的一切技术资料、信息和材料。如双方派遣代表团同要求基础设施服务者谈判，Ｃ或新公司应任命经验丰富、技术过硬的人员参加代表团。<BR> 
　　１２．２　Ｂ和Ｃ或新公司同意作为谈判的一个要点：基础设施服务出口付款应按月收取，直接转到新公司指定的＿＿＿国的新公司帐号，以美元结算，不得扣留税费。<BR> 
　　１２．３　基础设施服务收入应由新公司的主要股东－－Ｃ按照下述顺序分配：<BR> 
　　１）按第１２．４款的规定向Ｂ支付资源补偿费；<BR> 
　　２）运行和维护费用；<BR> 
　　３）建造基础设施的贷款本息；<BR> 
　　４）给新公司股东的股息。<BR> 
　　双方同意使新公司不作任何折旧扣除或设立任何贮备金，在支付１）、２）两项后，应首先保证偿还基础设施建设的本息。＿＿＿国保险公司作为出口信用险的保险人，在所担保的贷款偿还期内，作为新公司的财务总监，以便确保贷款本息的偿还。<BR> 
　　１２．４　应使用附件一作为参考计算分配给Ｂ和新公司股东的收入。作为资源补偿费支付给Ｂ的比例如下所述，并根据基础设施服务协议和工程造价予以调整：<BR> 
　　－－在运行期的第一个＿＿年内＿＿％；<BR> 
　　－－在运行期的其他时期＿＿％。<BR> 
　　上述支付给Ｂ的资源补偿费的补偿范围包括但不仅限于Ｂ提供给Ｃ或新公司自由使用现场、技术援助、行政安排和支持以及本协议规定的其他协助和支持。<BR> 
　　１２．５　如主要设备需要替换，新公司股东应按其股份比例增加投资额，用以购买和安装替换设备。替换设备和部件的使用年限由国际上接受的会计原则确定，如使用年限超过转让期，替换费应由Ｂ和新公司按照超过转让期的年份比例和在转让期的年份比例分担。<BR> 
　　<BR> 
　　１３．转让所有权<BR> 
　　１３．１　在新公司成立以前，Ｃ和Ｂ在建造期和／或运行期应是基础设施或其任何部分的业主，应拥有和行使各种权利、特权、对基础设施的权利和利益。新公司成立时，这种所有权应由新公司各股东按其股份比例分享直至转让期。<BR> 
　　Ｂ承认这种所有权，并保证这种所有权应根据＿＿＿国法律予以尊重和保护，Ｂ应做出决定、发布命令或采取必要的措施以保护这种所有权。Ｂ保证在任何情况下，无论是任何理由，在建造期或竣工期之后，基础设施的所有权和Ｃ或新公司的其他资产不得予以征用、充公、收归国有或受到限制。但Ｃ或新公司取消本工程则例外。<BR> 
　　本款中的取消指：ｉ）Ｃ或新公司通知Ｂ不建基础设施的最终意愿；ｉｉ）Ｃ或新公司无故不建基础设施超过＿＿个月。<BR> 
　　在建造期和／或运行期，Ｃ或新公司应自行对基础设施和构成本工程的其他设施行使置留权，Ｂ不应对此予以反对。<BR> 
　　１３．２　在转让期，新公司应将对基础设施的权利转让给Ｂ，新公司不应置留，也不能要求补偿，除非协议或补充协议中另有规定。<BR> 
　　１３．３　转让期１年前，Ｂ和新公司应讨论基础设施转让的必要程序，在转让期６个月前，Ｂ和新公司应开会商讨有关的清单和转让的机制。<BR> 
　　１３．４　根据第１３款转让的基础设施和所有其他设备应照原样转让，在转让期之后，新公司对Ｂ经营基础设施或Ｂ指定的人员经营基础设施以及基础设施发生的责任等均不负任何责任。<BR> 
　　１３．５　Ｂ应负责同第１３款所述的与转让有关的所有费用和支出（含律师费、税费或关税，以及获得政府的批准和其他批准、执照、注册和归档的费用），并采取必要的措施，以补偿新公司因转让产生的所有费用和支出。<BR> 
　　<BR> 
　　１４．赔偿责任<BR> 
　　１４．１　Ｃ或新公司对任何损失均不负赔偿责任，但由于严重违反本协议规定的义务而造成的直接损失除外。<BR> 
　　１４．２　在第１４．１款所述情况下，Ｃ或新公司有权将Ｃ或新公司对建造中或已竣工的基础设施的拥有权和所有权作为违约罚金全部或部分转让给Ｂ，以替代对实际损失的计算和赔偿。这种转让应是Ｃ自愿的或仲裁第２３款裁定的。在贷款本息偿还期中，任何转让须经＿＿＿国保险公司和贷款银行确认并批准。在上述情况下，Ｃ或新公司对Ｂ的赔偿责任仅限于将其对基础设施的拥有权和所有权转让给Ｂ，Ｂ向Ｃ或新公司的索赔一律无效。<BR> 
　　<BR> 
　　１５．文件和专利<BR> 
　　１５．１　本协议中的任何规定不应被解释为对本协议所述设备的专利或版权进行转让，所有这些权利均属于其真正的合法的主人，Ｃ或新公司应保留对有关规范、图纸和其他文件的所有权利，Ｂ保证在未经Ｃ或新公司事先书面同意的条件下，将有关内容泄露给第三国。<BR> 
　　１５．２　对有关该项目的所有商务文件、技术文件、协议、议案均应保密，其保密权归Ｃ或新公司所有。Ｂ保证在未经Ｃ或新公司事先书面同意的条件下，将有关内容泄露给第三国或第三方。<BR> 
　　<BR> 
　　１６．不可抗力<BR> 
　　１６．１　如未履行本协议的有关条款、规定或条件是由于天灾或Ｃ或新公司力所不及的类似情况所造成的，则这种情况不应被视作违反本协议。<BR> 
　　不可抗力包括但不仅限于下述内容：<BR> 
　　ａ．战争、敌意行为（无论是否宣战）、侵略、国外敌对势力的行为；<BR> 
　　ｂ．叛乱、革命、暴乱、兵变、篡权或内战；<BR> 
　　ｃ．核燃料的辐射造成的离子辐射或污染，核燃料、辐射性有毒爆炸物、爆炸性核聚集的其他危险性能或核成份的燃烧产生的核废物造成的离子辐射或污染；<BR> 
　　ｄ．以音速或超音速行驶的飞行器或其他空中设施造成的压力波；<BR> 
　　ｅ．骚乱、骚动、混乱、罢工、破坏、封锁或雇员的其他工业行为影响到Ｃ或新公司或其分包商；<BR> 
　　ｆ．任何政府当局的进出口限制、关闭港口、码头、运河；<BR> 
　　ｇ．火灾、异常的水灾、地震、泥石流、塌方、滑坡、暴风雨、闪电或其他异常恶劣的天气；<BR> 
　　ｈ．航行事故、船舶故障或损坏；<BR> 
　　ｉ．流行病、防疫隔离；<BR> 
　　ｊ．原材料短缺、不可预见的主要货源关闭；<BR> 
　　ｋ．船卸货和报关时多于３０天的不合理延误；<BR> 
　　ｌ．现场表面下遇到的隐蔽状况。<BR> 
　　１６．２　尽管有１６．１款的规定，Ｂ不应自行宣布发生不可抗力。<BR> 
　　１６．３　援引不可抗力条款的一方应：<BR> 
　　ａ．尽快书面通知其他不可抗力的性质和迫使该方暂停履行本协议规定的该方义务的程度；<BR> 
　　ｂ．在不可抗力状况一经结束，尽快恢复履行义务。<BR> 
　　１６．４　如在竣工期之前遇到不可抗力，双方应开会讨论修改工程竣工的时间表。<BR> 
　　１６．５　如在运行期遇到不可抗力，运行期的延长期应等同于不可抗力的适用期。<BR> 
　　１６．６　尽管有第１６．４和１６．５款的规定，如不可抗力超过９０天，双方应开会讨论本协议继续执行的基础和条件，如双方认为本协议不能继续执行，双方应讨论减少损失的办法。<BR> 
　　１６．７　双方应互相协商，采取一切合理的步骤将各方因不可抗力造成的损失减少到最低程度。<BR> 
　　１６．８　如不可抗力的发生损坏了工程或基础设施，Ｃ或新公司没有义务重新恢复之，或完成其修建，但双方应这种恢复或完成的条件达成一致意见则例外。　　<BR> 
　　１７．保险<BR> 
　　Ｃ或新公司应负责基础设施建造或运行所要求的保险。<BR> 
　　<BR> 
　　１８．情况的变化<BR> 
　　如由于＿＿＿国法律或规则、政府控制下任何机构或组织的法律或法规，基础设施所在区域地区当局的法律或法规在协议签字之日后生效，如由于现行的这种法律或法规（含Ｃ签署本协议时适用的任何官方解释）在协议签字日后予以修改、撤销，那么由此产生的对Ｃ在现场、工程或基础设施的利益和／或Ｃ对其投资的经济回收受到很大程度的影响、歧视或不利影响（包括但不仅限于对把美元资金汇出＿＿＿国的限制），双方应开会努力就修改本协议达成一致意见。<BR> 
