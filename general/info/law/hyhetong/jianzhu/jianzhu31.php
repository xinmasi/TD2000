　　６．基础设施的建造<BR> 
　　６．１　根据第４．１款关于基础设施建造的义务，Ｃ完全有权利同Ｂ协商从事下述工作：<BR> 
　　１）进行详细的设计，其设计和施工应符合＿＿＿＿国现行国家标准；<BR> 
　　２）任命顾问和专业顾问；<BR> 
　　３）购买设备和材料包括建筑设备，设备和材料以及安装、调试等应符合＿＿国现行国家标准；<BR> 
