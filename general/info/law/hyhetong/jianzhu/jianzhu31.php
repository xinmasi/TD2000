　　２３．仲裁<BR> 
　　在执行本协议中产生的或关于违约的所有分歧，应经过友好协商予以解决，如经三次以上协商解决无效，应按照《中华人民共和国政府和＿＿＿＿＿＿＿＿＿国政府关于相互鼓励和保护投资协定》进行。如《投资协定》失效，则应根据国际商会调解仲裁规则，由此法指定三个仲裁员最终仲裁解决，仲裁应在＿＿＿＿进行，仲裁员的仲裁应是最终的，对双方均有约束力。<BR> 
