　　９．２　Ｃ应通过Ｂ的安排从有关当局获得下述特权的批准文件，在新公司建立时，应将该特权转让于新公司或与新公司同时共享。<BR> 
　　１）＿＿＿国中央银行和财政部对下述的批准：<BR> 
