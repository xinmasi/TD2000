　　２）建造基础设施的所有费用及所有必要的融资安排；<BR> 
　　３）基础设施的运行；<BR> 
　　４）在转让日期将基础设施转让给Ｂ。<BR> 
　　４．２　Ｂ应负责：<BR> 
　　１）安排<BR> 
　　－－向Ｃ或新公司提供一切必要的条件，包括开发批准、许可、同意、现场转让、使用及其他权利，解决公司注册、进口许可证、关税免除等必需的要求；<BR> 
　　－－为Ｃ获得贷款提供一切必要的、必需的帮助；<BR> 
　　－－获得实施工程必要的一切管理、法律等方面的同意、批准、授权、税款减免、投资鼓励；<BR> 
　　－－为Ｃ或新公司将资金和盈利（美元）从＿＿＿国带出从＿＿＿有关当局获得一切必要的许可和批准；<BR> 
　　－－为Ｃ或新公司的职员以及工程设计、制造、安装、施工人员以及家属从有关当局获得工作期的居住签证、工作许可和其他必要的批准；<BR> 
　　－－如果Ｃ或新公司愿意，在扩大基础设施建设方面提供一切必要的批准和许可；<BR> 
　　－－Ｂ应在转让日期之前为本工程提供必要的安全保卫，确保工程建设人员、运行人员和设施的安全；<BR> 
　　－－为工程建设和运行提供通讯和交通方便。<BR> 
　　２）协助基础设施运作。<BR> 
　　３）向Ｃ和／或新公司免费提供现场包括但不限于施工场地、交通道路用地、输变电线路用地和某些范围内的开发和利用等，不予妨碍。<BR> 
　　４）同Ｃ或新公司、＿＿＿＿保险公司的代表一起与其他国家政府谈判并签署基础设施服务协议，应保证Ｃ或新公司、＿＿＿＿保险公司等代表出席谈判，并征得其同意。<BR> 
　　５）在现场和新公司派驻代表，协助Ｃ或新公司，并在Ｃ或新公司和Ｂ之间作必要的协调。<BR> 
　　４．３　Ｂ保证本工程运行期不少于＿＿＿年。但是如果内部回收率未达到＿＿＿％，双方将商谈适当延长运行期直至年内部回收率达到＿＿＿％。<BR> 
　　４．４　先决条件<BR> 
　　Ｂ应负责提供下述文件，使Ｃ在签署本协议之日起一个月内获得本协议的各个部分，这些文件对新公司应是有效的：<BR> 
　　１）由Ｃ接受的＿＿＿国议会签发主权担保书，该担保书应在转让日期交回Ｂ。<BR> 
　　２）Ｂ承担工程建设费用的＿＿＿％由Ｂ提出，由＿＿＿国中央银行作为借款人，由＿＿＿国财政部作为担保人，签订和开出＿＿＿国银行和＿＿＿国保险公司可以接受的贷款协议和担保手续以及按照＿＿＿国银行的要求，开出信用证或保函。贷款期为＿＿＿年（包括工程建设期＿＿＿年），贷款利息（年息）＿＿＿％。Ｂ的贷款可以提前偿还。<BR> 
