　　２１．放弃主权豁免权<BR> 
　　Ｂ声明并保证：本协议是商业协议而不是公共或政府法令，因而Ｂ放弃就其本身或其任何资产由于主权或根据任何法律或在任何管辖范围内要求免予诉讼的权力。<BR> 
