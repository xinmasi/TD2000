      <p> 　　<BR> 
　　１．格式　建筑施工物资租赁合同<BR> 
　　<BR> 
　　出租方：　　　　　　　合同编号：<BR> 
　　承租方：　　　　　　　签订地点：<BR> 
　　签订时间：　　年　　月　　日<BR> 
　　根据《经济合同法》及有关规定，按照平等互利的原则，为明确出租方与承租方的权利义务，经双方协商一致，签订本合同。<BR> 
　　第一条　租赁物资的品名、规格、数量、质量。（详见合同附件）<BR> 
　　第二条　租赁期限<BR> 
　　自＿＿＿＿年＿＿＿＿月＿＿＿＿日至＿＿＿＿年＿＿＿＿月＿＿＿＿日。共计＿＿＿＿天。承租方因工程需要延长租期，应在合同届满前＿＿＿＿日内，重新签订合同。<BR> 
　　第三条　租金<BR> 
　　租金收取：（双方应约定租金计算方式及其支付方式）<BR> 
　　第四条　押金（保证金）<BR> 
　　经双方协商，出租方收取承租方押金＿＿＿＿元。承租方交纳后办理提货手续。租赁期间不得以押金抵作租金；租赁期满，扣除应付租赁物资缺损赔偿金后，押金余额退还承租方。<BR> 
　　第五条　租赁物资的维修保养<BR> 
　　租赁期间，承租方对租用物资要妥善保管，并负担维修保养费用。租赁物资退还时，双方检查验收，如有损坏、缺少、保养不善等，要按双方协商议定的《租赁物资缺损赔偿及维修收费办法》，由承租方向出租方偿付赔偿金、维修及保养费。<BR> 
　　第六条　租赁合同变更<BR> 
　　一、在租赁期间，出租方如将租赁物资所有权转移给第三方，应正式通知承租方，租赁物资新的所有权方即成为本合同的当然出租方。<BR> 
　　二、在租赁期间，承租方不得将租赁物资转让、转租给第三方使用，也不得变卖或作抵押品。<BR> 
　　第七条　违约责任<BR> 
　　一、出租方违约责任：<BR> 
　　１．未按时间提供租赁物资，应向承租方偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　２．未按质量提供租赁物资，应向承租方偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　３．未按数量提供租赁物资，致使承租方不能如期正常使用的，除按规定如数补齐外，还应偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　４．其它违约行为。<BR> 
　　二、承租方违约责任：<BR> 
　　１．不按时交纳租金，应向出租方偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　２．逾期不还租赁物资，应向出租方偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　３．如有转让、转租或将租赁物资变卖、抵押等行为，除出租方有权解除合同，限期如数收回租赁物资外，承租方还应向出租方偿付违约期租金＿＿＿＿％的违约金。<BR> 
　　４．其它违约行为。<BR> 
