　　２．说明<BR> 
　　<BR> 
　　建筑施工物资租赁合同是指当事人双方就租用建筑工程物资所签订的明确双方权利义务的协议。提供物资的一方为出租方，接受物资的一方为承租方。由于建筑施工工程所需的特殊物资比较多，工程承包方往往不能一一备齐，而通过租赁方式既可以节约资金，提高效益，又使出租方的物资充分发挥作用。<BR> 
　　签订建筑施工物资租赁合同应当注意的问题有：<BR> 
　　（１）必须明确租赁物资的名称、质量标准和租赁期限。租赁物资的名称应当按照国家物资目录的标准称谓写清楚，而不应用习惯用语代替。双方对租赁物资的质量要进行交接验收，不得马虎从事，以免发生纠纷。<BR> 
　　（２）要明确租赁物资在租赁期限内的维修保养责任。通常应由承租方维保养，但有些特殊机械承租方无力维修保养的，也可以约定由出租方维修保养。对于由承租方维修保养的，租赁费用应低一些；由出租方维修的，租赁费用应高一些。<BR> 
　　（３）要明确租金的数额、支付方式。双方可以协商出租方收取一定的保证金或者叫做押金。在租赁期满后，折抵租金。<BR> 
　　（４）对于违约责任，应当写明出租方违约如何处理、承租方违约如何处理的内容。通常出租方承担的责任以租金为标准计算违约金；承租方违约按租金或者按损失数额计算赔偿额。<BR> 
　　本格式是国家工商行政管理局主持制定的示范文本。<BR> 
