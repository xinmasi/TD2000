　　第２４条　乙方采购材料设备的几种特殊情况应在本条作出说明：<BR> 
　　１．由甲方提供三材指标，乙方进行采购的，应写明指标名称、内容、价格、数量、提供的时间和<BR> 
　　甲方不能按规定提供应承担的违约责任；<BR> 
　　２．甲方不提供指标，由乙方采购三材的，应写明材料名称、数量、规格、质量等级和价格。<BR> 
　　３．甲方指定厂家由乙方采购材料设备的，应写明如产品价格高于乙方预算价格时价差由谁承担，<BR> 
　　以及由于制造厂家的原因造成产品的质量等级、规格型号和交货时间达不到合同要求，造成损失的责任<BR> 
　　和产生的费用由谁承担。<BR> 
　　第２６条　本条应写明变更发生后，乙方提交变更价款的时间及金额。<BR> 
　　第２７条　本条应写明乙方提交竣工图的时间、份数和要求；甲方收到竣工报告后应在多长时间内<BR> 
　　组织验收，参加验收的部门和人员。<BR> 
　　第２９条　如双方不再签订保修合同，本条应写明保修的项目、内容、范围、期限、保修金额和保<BR> 
　　修金的预留与支付方法，以及保修金的利率。另签保修合同的应将以上内容写入保修合同。<BR> 
　　第３０条　双方协商后，对争议的解决方法和程序作出以下说明：<BR> 
　　如：双方发生争议，首先请工程所在地建设主管部门调解，在一方提出调解要求后１０天内，双方<BR> 
　　应保证合同的继续执行。建设主管部门１０天内不能作出调解，任何一方可在１０天内不能作出调解，<BR> 
　　任何一方可在１０天后提请工程所在地工商行政管理机关进行仲裁。<BR> 
　　双方接受调解结果，应在调解作出后７天内执行<BR> 
　　对调解的结果不能接受，或由于一方不执行调解结果等原因使调解结果无法施行，任何一方可在调<BR> 
　　解作出７天后提请工程所在地工商行政管理机关进行仲裁。在实际工程中双方可以协议进行一次或几次<BR> 
　　调解，这些调解都要按上述例子一一写明；也可以协议直接申请仲裁或要求法院判决，仲裁和法院判决<BR> 
　　的程序、时间按有关规定执行。<BR> 
　　第３１条　甲方违约应负的违约责任应按以下各项分别作出说明：<BR> 
　　１．承担因违约发生的费用，应写明费用的种类，如工程的损坏及因此发生的拆除、修复等费用支<BR> 
　　出，乙方因此发生的人工、材料、机械和管理等费用支出；<BR> 
　　２．支付违约金，要写明违约金的数额或计算方法、支付的时间；<BR> 
　　３．赔偿损失、违约金的数额不足以赔偿乙方的损失时应将不足部分支付给乙方，作为赔偿。并写<BR> 
　　明损失的范围和计算方法，如损失的性质是直接损失还是间接损失，损失的内容是否包括乙方窝工的人<BR> 
　　工费、机械费和管理费，是否包括窝工期间乙方本应获得的利润。<BR> 
　　第３６条　乙方对工程的分包应详细写入本条。主要包括分包工程的名称、内容、分包单位的名称<BR> 
　　和承包范围。<BR> 
　　如甲方要求乙方将部分工程分包给其指定的分包人，除以上各项外，还应写明分包工程的价款，开<BR> 
　　工竣工的时间，应达到的质量等级。写明分包方不能按上述规定完成工程或不能按照乙方派驻的监督管<BR> 
　　理人员的指令工作，造成工期延误和其它损失时，责任和费用由谁承担。<BR> 
　　第３７条　本条应根据当地的地理气候情况和工程的要求，对造成工期延误和工程破坏的不可抗力<BR> 
　　自然灾害作出规定，可采用以下形式：<BR> 
　　１．Ｘ级以上的地震；<BR> 
　　２．Ｘ级以上持续Ｘ天的大风；<BR> 
　　３．Ｘｍｍ以上持续Ｘ天的大雨；<BR> 
　　４．Ｘ年以上未发生过，持续Ｘ天的高温天气；<BR> 
　　５．Ｘ年以上未发生过，持续Ｘ天的严寒天气。<BR> 
　　第３８条　在办理保险时，应写明甲方人员和第三方人员在施工现场生命财产安全的保险内容，保<BR> 
　　险金额，由谁办理和承担费用。<BR> 
