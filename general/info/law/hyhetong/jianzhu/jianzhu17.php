      <p> 　　<BR> 
　　于１９＿＿年＿＿月＿＿日，联洋航运有限公司（简称甲方），与蓝天船用设备有限公司（简称乙方），签订安装主机的项目合同。<BR> 
　　１．安装工程名称：１２Ｖ－４００ＺＣ型主机。<BR> 
　　２．工程地点：甲方＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿港口。<BR> 
　　３．工程范围：本合同所附图纸及估价单。<BR> 
　　４．完工期限：本安装工程将配合甲方＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿在甲方港口建造工程完工后六十（６０）天内，完成安装工程，其中包括１２Ｖ－４００ＺＣ主机试验运行。<BR> 
　　５．安装工程总价格：＿＿＿＿＿＿＿＿＿＿＿＿＿美元整。<BR> 
　　６．安装工程付款进度：<BR> 
　　第１期：甲方在本合同开始实施时，付给乙方工程总价１５％，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整；<BR> 
　　第２期：甲方在Ａ港口造船工程开始时，付给乙方工程总价１５％，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整；<BR> 
　　第３期：在安装管路时，甲方付给乙方工程总价１０％，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整；<BR> 
　　第４期：在主机的主要设备运抵工地时，甲方付给乙方工程总价的４０％，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整；<BR> 
　　第５期：在安装工程完毕时，甲方付给乙方工程总价的１０％，即＿＿＿＿＿＿＿＿＿＿＿＿＿美元整；<BR> 
　　第６期：在工程验收合格后，甲方付给乙方工程总价余额的１０％，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整。<BR> 
　　７．违约罚款：因乙方原因未准时完工时，应由乙方负责，应付违约罚款，每天按总价的１％计算，即＿＿＿＿＿＿＿＿＿＿＿＿＿＿美元整。<BR> 
　　８．增加或减少工程：若甲方需修改、增加或减少其工程计划时，总价之增减，应按双方规定之订单计算。若需新增加工程量，双方应另行协商该项新增加工程之单价。若甲方因修改原计划而将已完工部份工程或已运抵工地之材料弃置，甲方在验收后，应按双方规定的单价对乙方已完成的工程费用及材料费，予以支付。<BR> 
　　９．监督进度：甲方派去的监督人员或代表对工程进度予以监督并有权予以监工、指导。乙方应按甲方人员或代表的指示正确施工，不得以任何借口置之不理。<BR> 
　　１０．工程停止：若甲方通知乙方将工程停止，其原因并非应由乙方负责者，乙方可停止并要求甲方按照已完成工程的数量、运抵工地的材料及其他合理费用，在已交款中增付或扣除。<BR> 
　　１１．工程保管：在工程开始时，完工移交前，已完工的工程及留在工地的材料、工具、设备等，均由乙方保管。除人力不能抵抗的灾害外，乙方应对保管中的一切，负损害之全责。如遇不可抵抗的天灾人祸，乙方应详列损害实情，向甲方提出恢复原状的价格及日期，以供甲方核对付款之用。若甲方决定不再继续施工，按合同第１０条规定结束。<BR> 
　　１２．工程保证：除天灾或因甲方使用不当的原因外，乙方应在甲方验收后保证工程质量良好１年。<BR> 
　　１３．附带条款：在工程进行中，若因乙方的过错，使甲方或其他人员受到损害，乙方应负责赔偿。但其损害原因是由甲方或与甲方订约之其他工程人员的过错所造成，则应由甲方完全负责。<BR> 
　　１４．附件：本合同附件应视为本合同的一部份，与其他条款有同等效力。<BR> 
　　附件包括：（１）图样；（２）估价单。<BR> 
　　１５．合同形式：本合同一式两份，甲乙双方各执一份。另各保留两份副本供双方存档备查。<BR> 
　　１６．附加条款：主机的保养－－乙方应在甲方验收后，提供免费及定期对１２Ｖ－４００ＺＣ主机保养服务１年，每六（６）个月派员一次维护正常运行。<BR> 
　　本合同按双方所签日期生效。<BR> 
　　甲方：＿＿＿＿＿＿＿＿＿＿＿＿＿＿　乙方：＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　由：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿　由：＿＿＿＿＿＿＿＿＿＿＿＿＿＿承包人（Ｃｏｎｔｒａｃｔｏｒｓ）。承包人与欲求安装工程的业主（Ｐｒｏｍｏｔｅｒｓ）双方经洽谈或招标、投标达成默契而签订合同。承包者必须根据工程图纸及业主要求的特殊条件予以施工，而且在一定的时间内承担维修业务。为完成安装施工任务，承包者还需提供施工设备、特殊工具、施工监督工程师及所需的劳动力等。<BR> 
　　工程承包有综合承包人（总承包）（Ｇｅｎｅｒａｌ　Ｃｏｎｔｒａｃｔｏｒｓ）和专业承包人（Ｓｐｅｃｉａｌｉｓｔ　Ｃｏｎｔｒａｃｔｏｒｓ）。承包人对工程进度、质量、保证、验收等承担责任。业主必须按时支付工程进度款。<BR> 
　　国际间的工程安装和工程承包是一种比较实用的而且广泛被人们所接受的经济技术的合作方式。<BR> 
