　　附加协议条款：<BR> 
　　<BR> 
　　《工程建设监理合同》使用说明<BR> 
　　<BR> 
　　《工程建设监理合同》包括《工程建设监理合同标准条件》和《工程建设监理合同专用条件》（以下简称为《标准条件》、《专用条件》）。<BR> 
　　《标准条件》适用于各个工程项目建设监理委托，各个业主和监理单位都应当遵守。《专用条件》是各个工程项目根据自己的个性和所处的自然和社会环境，由业主和监理单位协商一致后进行填写。双方如果认为需要，还可在其中增加约定的补充条款和修正条款。<BR> 
　　现对《专用条件》的填写说明如下：<BR> 
　　《专用条件》应当对应《标准条件》的顺序进行填写。<BR> 
　　例如：<BR> 
　　“第二条”，要根据工程的具体情况，填写所适用的部门、地方法规、规章。<BR> 
　　“第四条”，在协商和写明其“监理工程范围”时，一般要与工程项目总概算、单位工程概算所涵盖的工程范围相一致，或与工程总承包合同、分包合同所涵盖工程范围相一致。<BR> 
　　在写明“监理业务”时，首先要写明是承担哪个阶段的监理业务，或设计阶段的监理业务，或施工和保修阶段的监理业务，或全过程的监理业务；其次要详细写明委托阶段内每项具体监理工作，应当避免遗漏。其办法可按照《建设监理规定》中所列的监理内容和《监理大纲》所列的监理内容进一步细化。<BR> 
　　如果业主还要求监理单位承担一些咨询业务和事务性工作，也应当在本条款中详细列出。例如，建设项目可行性研究、编制概预算、编制标底、提供改造交通、供水、供电设施的技术方案等。又例如，办理购地拆迁，提供临时设施的设计和监督其施工等。<BR> 
　　“第十四条”，在填写业主提供的设施和监理单位自备的设备时，一般是指下列设施与设备：（１）检测试验设备；（２）测量设备；（３）通信设备；（４）交通设备；（５）气象设备；（６）照相录像设备；（７）电算设备；（８）打字复印设备；（９）办公用房；（１０）生活用房。<BR> 
　　在写明业主给予监理单位自备设备经济补偿时，一般应写明补偿金额。其计算方法为：补偿金额＝设施在工程上使用时间占折旧年限的比率×设施原值＋管理费。<BR> 
　　“第十五条”，如果双方同意，可在专用条件中设立此条款。在填写此条款时应写明提供的人数和时间。<BR> 
　　“第二十六条”，在写明“赔偿额”时，应写明其计算方法。<BR> 
　　“第三十七条”，在写明“监理任务酬金”时，按照国家物价局和建设部（９２）价费字４９７号文《工程建设监理费有关规定的通知》的规定计收。其支付时间应当写明某年某月某日支付数额。<BR> 
　　在写明“附加工作酬金”时，应当写明如果业主未按原约定提供职员或服务员，或设施，业主应当按照监理实际用于这方面的费用给以完全补偿。还应写明，如果由于业主或第三方的阻碍或延误而使监理单位发生附加工作，也应当支付酬金。计算方法为：酬金＝附加工作日数×监理任务平均酬金额。在写明其支付时间时，应当写明在其发生后的多少天内支付。<BR> 
　　在写明“额外工作酬金”时，应当写明如果由于非监理单位的原因所发生的监理业务暂停，其暂停时间和用于恢复执行监理业务的时间为额外工作时间。如果中途中止委托合同而必须进行的善后工作时间也属于额外工作时间。额外工作时间均应收取酬金。其计算方法为：酬金＝额外工作日数×监理业务日平均酬金额。在写明其支付时间时，应写明其后的多少天内支付。<BR> 
　　“第四十三条”，如果双方同意，可以在专用条件中设立此条款。在填写此条款时应当写明在什么情况下业主给予奖励以及奖励办法。例如，由于监理单位的合理化建议而使业主获得实际经济利益，其奖励办法可参照＿＿＿＿奖励办法。<BR> 
