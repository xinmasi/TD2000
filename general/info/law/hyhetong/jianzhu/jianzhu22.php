　　第一部分　合同条件<BR> 
　　<BR> 
　　一、词语含义及合同文件<BR> 
　　<BR> 
　　第一条　词语含义。在本合同，下列词语除协议条款另有约定外，应具有本条所赋予的含义：<BR> 
　　１．合同：是指为实施工程，发包方和承包方之间达成的明确相互权利和义务关系的协议。包括合<BR> 
　　同条件、协议条款以及双方协商同意的与合同有关的全部文件。<BR> 
　　２．协议条款：是指结合具体工程，除合同条件外，经发包方和承包方协商达成一致意见的条款。<BR> 
　　３．发包方（简称甲方）：协议条款约定的具有工程发包主体资格和支付工程价款能力的当事人。<BR> 
　　甲方的具体身份、发包范围、权限、性质均需在协议条款内约定。<BR> 
　　４．承包方（简称乙方）：协议条款约定的具有工程承包主体资料并被甲方接受的当事人。<BR> 
　　５．甲方驻工地代表（简称甲方代表）：甲方在协议条款内指定的履行合同的负责人。<BR> 
　　６．乙方驻工地代表（称简乙方代表）：乙方在协议条款内指定的履行合同的负责人。<BR> 
　　７．社会监理：甲方委托具备法定资格的工程建设监理单位对工程进行的监理。<BR> 
　　８．总监理工程师：工程建设监理单位委派的监理总负责人。<BR> 
　　９．设计单位：甲方委托的具备与工程相应资质等级的设计单位。<BR> 
　　本合同工程的装饰或二次及以上的装饰，甲方委托乙方部分或全部设计，且乙方具备相应设计资<BR> 
　　质，甲、乙双方另行签订设计合同。<BR> 
　　１０．工程：是指为使建筑物、构筑物内、外空间达到一定的环境质量要求，使用装饰装修材料，<BR> 
　　对建筑物、构筑物外表和内部进行修饰处理的工程。包括对旧有建筑物及其设施表面的装饰处理。<BR> 
　　１１．工程造价管理部门：各级建设行政主管部门或其授权的建设工程造价管理部门。<BR> 
　　１２．工程质量监督部门：各级建设行政主管部门或其授权的建设工程质量监督机构。<BR> 
　　１３．合同价款：甲、乙双方在协议条款内约定的、用以支付乙方按照合同要求完成全部工程内容<BR> 
　　的价款总额。招标工程的合同价款为中标价格。<BR> 
　　１４．追加合同价款：在施工中发生的、经甲方确认后按计算合同价款的方法增加的合同价款。<BR> 
　　１５．费用：甲方在合同价款之外需要直接支付的开支或乙方应承担的开支。<BR> 
　　１６．工期：协议条款约定的、按总日历天数（包括一切法定节假日在内）计算的工期天数。<BR> 
　　１７．开工日期：协议条款约定的绝对或相对的工程开工日期。<BR> 
　　１８．竣工日期：协议条款约定的绝对或相对的工程竣工日期。<BR> 
　　１９．图纸：由甲方提供或乙方提供经甲方代表批准，乙方用以施工的所有图纸（包括配套说明和<BR> 
　　有关资料）。<BR> 
　　２０．分段或分部工程：协议条款约定构成全部工程的任何分段或分部工程。<BR> 
　　２１．施工场地：由甲方提供，并在协议条款内约定，供乙方施工、操作、运输、堆放材料的场地<BR> 
　　及乙方施工涉及的周围场地（包括一切通道）。<BR> 
　　２２．施工设备和设施：按协议条款约定，由甲方提供给乙方施工和管理使用的设备或设施。<BR> 
　　２３．工程量清单：发包方在招标文件中提供的、按法定的工程量计算方法（规则）计算的全部工<BR> 
　　程的分部分项工程量明细清单。<BR> 
　　２４．书面形式：根据合同发生的手写、打印、复写、印刷的各种通知、证明、证书、签证、协<BR> 
　　议、备忘录、函件及经过确认的会议纪要、电报、电传等。<BR> 
　　２５．不可抗力：指因战争、动乱、空中飞行物坠落或其它非甲乙方责任造成的瀑炸、火灾，以及<BR> 
　　协议条款约定的自然灾害等。<BR> 
　　第二条　合同文件及解释顺序。合同文件应能互相解释，互为说明。除合同另有约定外，其组成和<BR> 
　　解释顺序如下：<BR> 
　　１．协议条款；<BR> 
　　２．合同条件；<BR> 
　　３．洽商、变更等明确双方权利、义务的纪要、协议；<BR> 
　　４．建设工程施工合同；<BR> 
　　５．监理合同；<BR> 
　　６．招标发包工程的招标文件、投标书和中标通知书；<BR> 
　　７．工程量清单或确定工程造价的工程预算书和图纸；<BR> 
　　８．标准、规范和其它有关的技术经济资料、要求；<BR> 
　　当合同文件出现含糊不清或不一致时，由双方协商解释，协商不成时，按协议条款第３５条约定的<BR> 
　　办法解决。<BR> 
　　第三条　合同文件使用的语言文字、标准和适用法律。合同文件使用汉语或协议条款约定的少数民<BR> 
　　族语言书写、解释和说明。<BR> 
　　施工中必须使用协议条款约定的国家标准、规范。没有国家标准、规范时，有行业标准、规范的，<BR> 
　　使用行业标准、规范；没有国家和行业标准、规范的，使用地方的标准、规范。甲方应按协议条款约定<BR> 
　　的时间向乙方提供一式两份约定的标准、规范。<BR> 
　　国内没有相应标准、规范时，乙方应按协议条款约定的时间和要求提出施工工艺，经甲方代表和设<BR> 
　　计单位批准后执行。甲方要求使用国外标准、规范的，应负责提供中文译本。本条所发生购买、翻译和<BR> 
　　制定标准、规范的费用，均由甲方承担。<BR> 
　　适用于合同文件的法律是国家的法律、法规（含地方法规），及协议条款约定的规章。<BR> 
　　第四条　图纸。甲方在开工日期７天之前按协议条款约定的日期和份数，向乙方提供完整的施工图<BR> 
　　纸。乙方需要超过协议条款双方约定的图纸份数，甲方应代为复制，复制费用由乙方承担。<BR> 
　　使用国外或境外图纸，不能满足施工需要时，双方在协议条款内约定复制、重新绘制、翻译、购买<BR> 
　　标准图纸等的责任和费用承担。<BR> 
　　<BR> 
　　二、双方一般责任<BR> 
　　<BR> 
　　第五条　甲方代表。甲方代表按照以下要求，行使合同约定的权利，履行合同约定的义务：<BR> 
　　１．甲方代表可委派有关具体管理人员，行使自己部分权利和职责，并可在任何时候撤回这种委<BR> 
　　派。委派和撤回均应提前７天通知乙方。<BR> 
　　２．甲方代表的指令、通知由其本人签字后，以书面形式交给乙方代表，乙方代表在回执行上签署<BR> 
　　姓名和收到时间后生效。确有必要时，甲方代表可发出口头指令，并在４８小时内给予书面确认，乙方<BR> 
　　应于甲方代表发出口头指令后７天内提出书面确认要求，甲方代表在乙方提出确认要求２４小时后不予<BR> 
　　答复，视为乙方要求已被确认。乙方认为甲方代表指令不合理，应在收到指令后２４小时内提出书面申<BR> 
　　告，甲方代表在收到乙方申告后２４小时内作出修改指令或继续执行原指令的决定，并以书面形式通知<BR> 
　　乙方。紧急情况下，甲方代表要求乙立即执行的指令或乙方加有异议，但甲方代表决定仍继续执行的指<BR> 
　　令，乙方应予执行。因指令错误而发生的追加合同价款和对乙方造成的损失由甲方承担，延误的工期相<BR> 
　　应顺延。<BR> 
　　３．甲方代表应按合同约定，及时向乙方提供所需指令、批准、图纸并履行其它约定的义务。否则<BR> 
　　乙方在约定时间后２４小时内将具体要求、需要的理由和迟误的后果通知甲方代表，甲方代表收到通知<BR> 
　　后４８小时内不予答复，应承担由此造成的追回合同价款，并赔偿乙方的有关损失，延误的工期相应顺<BR> 
　　延。<BR> 
　　甲方代表易人，甲方应于易人前７天通知乙方，后任继续履行合同文件约定的前任的权利和义务。<BR> 
　　第六条　委托监理。本工程甲方委托监理，应与监理单位签订监理合同。并在本合同协议条款内明<BR> 
　　确监理单位、总监理工程师及其应履行的职责。<BR> 
　　本合同中总监理工程师和甲方代表的职责不能相互交叉。<BR> 
　　非经甲方同意，总监理工程师及其代表无权解除本合同中乙方的任何义务。<BR> 
　　合同履行中，发生影响甲，乙双方权利和义务的事件时，总监理工程师应作出公正的处理。<BR> 
　　为保证施工正常进行，甲乙双方应尊重总监理工程师的决定。对总监理工程师的决定有异议时，按<BR> 
　　协议条款的约定处理。<BR> 
　　总监理工程师易人，甲方接到监理单位通知后应同时通知乙方，后任继续履行赋予前任的权利和义<BR> 
　　务。<BR> 
　　第七条　乙方驻工地代表。乙方任命驻工地负责人，按以下要求行使合同约定的权利，履行合同约<BR> 
　　定的义务：<BR> 
　　１．乙方的要求、请求和通知，以书面形式由乙方代表签字后送甲方代表，甲方代表在回执行上签<BR> 
　　署姓名及收到时间后生效。<BR> 
　　２．乙方代表按甲方代表批准的施工组织设计（或施工方案）和依据合同发出的指令、要求组织施<BR> 
　　工。在情况紧急且无法与甲方代表联系的情况下，可采取保护人员生命和工程、财产安全的紧急措施，<BR> 
　　并在采取措施后２４小时内向甲方代表送交报告。责任在甲方，由甲方承担由此发生的追加合同价款，<BR> 
　　相应顺延工期；责任在乙方，由乙方承担费用。<BR> 
　　乙方代表易人，乙方应于易人前７天通知甲方，后任继续履行合同文件约定的前任的权利和义务。<BR> 
　　第八条　甲方工作。甲方按协议条款约定的内容和时间，一次或分阶段完成以下工作：<BR> 
　　１．提供施工所需的场地，并清除施工场地内一切影响乙方施工的障碍；或承担乙方在不腾空的场<BR> 
　　地内施工采取的相应措施所发生的费用，一并计入合同价款内；<BR> 
　　２．向乙方提供施工所需水、电、热力、电讯等管道线路，从施工场地外部接至协议条款约定的地<BR> 
　　点，并保证乙方施工期间的需要；<BR> 
　　３．负责本工程涉及的市政配套部门及当地各有关部门的联系和协调工作；<BR> 
　　４．协调施工场地内各交叉作业施工单位之间的关系，保证乙方按合同的约定顺利施工；<BR> 
　　５．办理施工所需的有关批件、证件和临时用地等的申请报批手续；<BR> 
　　６．组织有关单位进行图纸会审，向乙方进行设计交底；<BR> 
　　７．向乙方有偿提供协议条款约定的施工设备和设施。<BR> 
　　甲方不按协议条款约定的内容和时间完成以上工作，造成工期延误，承担由此造成的追加合同价<BR> 
　　款，并赔偿乙方有关损失，工期相应顺延。<BR> 
　　第九条　乙方工作。乙方按协议条款约定的时间和要求做好以下工作：<BR> 
　　１．在其设计资格证书允许的范围内，按协议条款的约定完成施工图设计或与工程配套的设计，经<BR> 
　　甲方代表批准后使用；<BR> 
　　２．向甲方代表提供年、季、月度工程进度计划及相应统计报表和工程事故报告；<BR> 
　　３．在腾空后单独由乙方施工的施工场地内，按工程和安全需要提供和维修非夜间施工使用的照<BR> 
　　明、看守、围栏和警卫。乙方未履行上述义务造成工程、财产和人身伤害，由乙方承担责任及所发生的<BR> 
　　费用；<BR> 
　　在新建工程或不腾空的建筑物内施工时，上述设施和人员由建筑工程承包人或建筑物使用单位负<BR> 
　　责，乙方不承担任何责任和费用。<BR> 
　　４．遵守地方政府和有关部门对施工场地交通和施工噪音等管理规定，经甲方代表同意，需办理有<BR> 
　　关手续的，由甲方承担由此发生的费用。因乙方责任造成的罚款除外；<BR> 
　　５．遵守政府和有关部门对施工现场的一切规定和要求，承担因自身原因违反有关规定造成的损失<BR> 
　　和罚款；<BR> 
　　６．按协议条款的约定保护好建筑物结构和相应管线、设备；<BR> 
　　７．已竣工工程未交付甲方验收之前，负责成品保护，保护期间发生损坏，乙方自费予以修复。第<BR> 
　　三方原因造成损坏，通过甲方协调，由责任方负责修复；或乙方修复，由甲方承担追加合同价款。要求<BR> 
　　乙方采取特殊措施保护的分段或分部工程，其费用由甲方承担，并在协议条款内约定。甲方在竣工验收<BR> 
　　前使用，发生损坏的修理费用，由甲方承担。由于乙方不履行上述义务，造成工期延误和经济损失，责<BR> 
　　任由乙方承担。<BR> 
　　<BR> 
　　三、施工组织设计和工期<BR> 
　　<BR> 
　　第十条　施工组织设计及进度计划。乙方应在协议条款约定的日期，将施工组织设计（或施工方<BR> 
　　案）和进度计划提交甲方代表。甲方代表应按协议条款约定的时间予以批准或提出修改，逾期不批复，<BR> 
　　可视为该施工组织设计（或施工方案）和进度计划已经批准。乙方必须按批准的进度计划组织施工，接<BR> 
　　受甲方代表对进度的检查、监督。工程实际进展与进度计划不符时，乙应按甲方代表的要求提出措施，<BR> 
　　甲方代表批准后执行。<BR> 
　　第十一条　延期开工。乙方按协议条款约定的开工日期开始施工。乙方不能按时开工，应在协议条<BR> 
　　款约定的开工日期７天前，向甲方代表提出延期开工的理由和要求。甲方代表在７天内答复乙方。甲方<BR> 
　　代表７天内不予答复，视为已同意乙方要求，工期相应顺延。甲方代表不同意延期要求或乙方未在规定<BR> 
　　时间内提出延期开工要求，竣工工期不予顺延。<BR> 
　　甲方征得乙方同意并以书面形式通知乙方后，可要求推迟开工日期，承担乙方因此造成的追加合同<BR> 
　　价款，相应顺延工期。<BR> 
　　第十二条　暂停施工。甲方代表在确有必要时，可要求乙方暂停施工，并在提出要求后４８小时内<BR> 
　　提出处理意见。乙方应按甲方要求停止施工，并妥善保护已完工工程。乙方实施甲方代表处理意见后，<BR> 
　　可提出复工要求，甲方代表应在４８小时内给予答复。甲方代表未能在规定时间内提出处理意见，或收<BR> 
　　到乙方复工要求后４８小时内未予答复，乙方可自行复工。停工责任在甲方，由甲方承担追加合同价<BR> 
　　款，相应顺延工期；停工责任在乙方，由乙方承担发生的费用。因甲方代表不及时作出答复，施工无法<BR> 
　　进行，乙方可认为甲方已部分或全部取消合同，由甲方承担违约责任。<BR> 
　　第十三条　工期延误。由于以下原因造成工期延误，经甲方代表确认，工期相应顺延。<BR> 
　　１．甲方不能按协议条款的约定提供开工条件；<BR> 
　　２．工程量变化和设计变更；<BR> 
　　３．一周内，非乙方原因停水、停电、停气造成停工累计超过８小时；<BR> 
　　４．工程未按时支付；<BR> 
　　５．不可抗力；<BR> 
　　６．其它非乙方原因的停工。<BR> 
　　乙方在以上情况发生后７天内，就延误的内容和因此发生的追加合同价款向甲方代表提出报告，甲<BR> 
　　方代表在收到报告后７天内予以确认、答复，逾期不予答复，乙方可视为延期及要求已被确认。<BR> 
　　非上述原因，工程不能按合同工期竣工，乙方按协议条款约定承担违约责任。<BR> 
　　第十四条　工期提前。施工中如需提前竣工，双方协商一致后应签订提前竣工协议。乙方按协议修<BR> 
　　订进度计划，报甲方批准。甲方应在７天内给予批准，并为赶工提供方便条件。提前竣工协议包括以下<BR> 
　　主要内容：<BR> 
　　１．提前的时间；<BR> 
　　２．乙方采取的赶工措施；<BR> 
　　３．甲方为赶工提供的条件；<BR> 
　　４．赶工措施的追加合同价款和承担；<BR> 
　　５．提前竣工受益（如果有）的分享。<BR> 
　　<BR> 
　　四、质量与检验<BR> 
　　<BR> 
　　第十五条　工程样板。按照协议条款规定，乙方制作的样板间，经甲方代表检验合格后，由甲乙双<BR> 
　　方封存。样板间作为甲方竣工验收的实物标准。制作样板间的全部费用，由甲方承担。<BR> 
　　第十六条　检查和返工。乙方应认真按照标准、规范、设计和样板间标准的要求以及甲方代表依据<BR> 
　　合同发出的指令施工，随时接受甲方代表及其委派人员检查检验，为检查检验提供便利条件，并按甲方<BR> 
　　代表及其委派人员的要求返工、修改，承担因自身原因导致返工、修改的费用。因甲方不正确纠正或其<BR> 
　　他原因引起的追加合同价款，由甲方承担。<BR> 
　　以上检查检验合格后，又发现由乙方原因引起的质量问题，仍由乙方承担责任和发生的费用，赔偿<BR> 
　　甲方的有关损失，工期相应顺延。<BR> 
　　检查检验合格后再进行检查检验应不影响施工的正常进行，如影响施工的正常进行，检查检验不合<BR> 
　　格，影响施工的费用由乙方承担。除此之外影响正常施工的追加合同价款由甲方承担，相应顺延工期。<BR> 
　　第十七条　工程质量等级。工程质量应达到国家或专业的质量检验评定标准的合格条件。甲方要求<BR> 
　　部分或全部工程质量达到优良标准，应支付由此增加的追加合同价款，对工期有影响的应给予相应的顺<BR> 
　　延。<BR> 
　　达不到约定条件的部分，甲方代表一经发现，可要求乙方返工，乙方应按甲方代表要求返工，直到<BR> 
　　符合约定条件。因乙方原因达不到约定条件，由乙方承担返工费用，工期不予顺延。返工后仍不能达到<BR> 
　　约定条件，乙方承担违约责任。因甲方原因达不到约定条件，由甲方承担返工的追加合同价款，工期相<BR> 
　　应顺延。<BR> 
　　双方对工程质量有争议，请协议条款约定质量监督部门调解，调解费用及因此造成的损失，由责任<BR> 
　　一方承担。<BR> 
　　第十八条　隐蔽工程和中间验收。工程具备隐蔽条件或达到协议条款约定的中间验收部位，乙方自<BR> 
　　检合格后，在隐蔽和中间验收４８小时前通知甲方代表参加。通知包括乙方自检记录、隐蔽和中间验收<BR> 
　　的内容、验收时间和地点。乙方准备验收记录。验收合格，甲方代表在验收记录上签字后，方可进行隐<BR> 
　　蔽和继续施工。验收不合格，乙方在限定时间内修改后重新验收。工程符合规范要求，验收２４小时<BR> 
　　后，甲方代表不在验收记录签字，可视为甲方代表已经批准，乙方可进行隐蔽或继续施工。<BR> 
　　甲方代表不能按时参加验收，须在开始验收２４小时之前向乙方提出延期要求，延期不能超过两<BR> 
　　天，甲方代表未能按以上时间提出延期要求、不参加验收，乙方可自行组织验收，甲方应承认验收记<BR> 
　　录。<BR> 
　　第十九条　重新检验。无论甲方代表是否参加验收，当其提出对已经验收的隐蔽工程重新检验的要<BR> 
　　求时，乙方应按要求进行剥露，并在检验后重新隐蔽或修复后隐蔽。检验合格，甲方承担由此发生的追<BR> 
　　加合同价款，赔偿乙方损失并相应顺延工期。检验不合格，乙方承担发生的费用，工期也予顺延。<BR> 
　　<BR> 
　　五、合同价款及支付方式<BR> 
　　<BR> 
　　第二十条　合同价款与调整。合同价款及支付方式在协议条款内约定后，任何一方不得擅自改变。<BR> 
　　发生下列情况之一的可作调整：<BR> 
　　１．甲方代表确认的工程量增减；<BR> 
　　２．甲方代表确认的设计变更或工程洽商；<BR> 
　　３．工程造价管理部门公布的价格调整；<BR> 
　　４．一周内非乙方原因造成停水、停电、停气累计超过８小时；<BR> 
　　５．协议条款约定的其它增减或调整。<BR> 
　　双方在协议条款内约定调整合同价款的方法及范围。乙方在需要调整合同价款时，在协议条款约定<BR> 
　　的天数内，将调整的原因、金额以书面形式通知甲方代表，甲方代表批准后通知经办银行和乙方。甲方<BR> 
　　代表收到乙方通知后７天内不作答复，视为已经批准。<BR> 
　　对固定价格合同，双方应在协议条款内约定甲方给予乙方的风险金额或按合同价款一定比例约定风<BR> 
　　险系数，同时双方约定乙方在固定价格内承担的风险范围。<BR> 
　　第二十一条　工程款预付。甲方按协议条款约定的时间和数额，向乙方预付工程款，开工后按协议<BR> 
　　条款约定的时间和比例逐次扣回。甲方不按协议条款约定预付工程款，乙方在约定预付时间７天内后向<BR> 
　　甲方发出要求预付工程款的通知，甲方在收到通知后仍不能按要求预付工程款，乙方可在发出通知７天<BR> 
　　后停止施工，甲方从应付之日起向乙方支付应付款的利息并承担违约责任。<BR> 
　　第二十二条　工程量的核实确认。乙方按协议条款约定的时间，向甲方代表提交已完工程量的报<BR> 
　　告。甲方代表接到报告后７天内按设计图纸核实已完工程数量（以下简称计量），并提前２４小时通知<BR> 
　　乙方。乙方为计量提供便利条件并派人参加。<BR> 
　　乙方无正当理由不参加计量，甲方代表自行进行，计量结果视为有效，作为工程价款支付的依据。<BR> 
　　甲方代表收到乙方报告后７天内未进行计量，从第８天起，乙方报告中开列的工程量视为已被确认，作<BR> 
　　为工程款支付的依据。甲方代表不按约定时间通知乙方，使乙方不能参加计量，计量结果无效。<BR> 
　　甲方代表对乙方超出设计图纸要求增加的工程量和自身原因造成的返工的工程量，不予计量。<BR> 
　　第二十三条　工程款支付。甲方按协议条款约定的时间和方式，根据甲方代表确认的工程量，以构<BR> 
　　成合同价款相应项目的单价和取费标准计算出工程价款，经甲方代表签字后支付。甲方在计量结果签字<BR> 
　　后超过７天不予支付，乙方可向甲方发出要求付款通知，甲方在收到乙方通知后仍不能按要求支付，乙<BR> 
　　方可在发出通知７天后停止施工，甲方承担违约责任。<BR> 
　　经乙方同意并签订协议，甲方可延期付款。协议需明确约定付款日期，并由甲方支付给乙方从计量<BR> 
　　结果签字后第８天起计算的应付工程价款利息。<BR> 
　　<BR> 
　　六、材料供应<BR> 
　　<BR> 
　　第二十四条　材料样品或样本。不论甲乙任何一方供应都应事先提供材料样品或样本。经双方验收<BR> 
　　后封存，作为材料供应和竣工验收的实物标准。甲方或设计单位指定的材料品种，由指定者提供指定式<BR> 
　　样、色调和规格的样品或样本。<BR> 
　　第二十五条　甲方提供材料。甲方按照协议条款约定的材料种类、规格、数量、单价、质量等级和<BR> 
　　提供时间、地点的清单，向乙方提供材料及其产品合格证明。甲方代表在所提供材料验收２４小时前将<BR> 
　　通知送达乙方，乙方派人与甲方一起验收。无论乙方是否派人参加验收，验收后由乙方妥善保管，甲方<BR> 
　　支付相应的保管费用。发生损坏或丢失，由乙方负责赔偿。甲方不按规定通知乙方验收，乙方不负责材<BR> 
　　料设备的保管，损坏或丢失由甲方负责。<BR> 
　　甲方供应的材料与清单或样品不符，按下列情况分别处理：<BR> 
　　１．材料单价与清单不符，由甲方承担所有差价；<BR> 
　　２．材料的种类、规格、型号、质量等级与清单或样品不符，乙方可拒绝接收保管，由甲方运出施<BR> 
　　工现场并重新采购；<BR> 
　　３．到货地点与清单不符，甲方负责倒运至约定地点；<BR> 
　　４．供应数量少于清单约定数量时，甲方将数量补齐。多于清单数量时，甲方负责将多余部分运出<BR> 
　　施工现场；<BR> 
　　５．供应时间早于清单约定时间，甲方承担因此发生的保管费用。<BR> 
　　因以上原因或迟于清单约定时间供应而导致的追加合同价款，由甲方承担。发生延误，工期相应顺<BR> 
　　延，并由甲方赔偿乙方由此造成的损失。<BR> 
　　乙方检验难过之后仍发现有与清单和样品的规格、质量等级不符的情况，甲方还应承担重新采购及<BR> 
　　返工的追加合同价款，并相应顺延工期。<BR> 
　　第二十六条　乙方供应材料。乙方根据协议条款约定，按照设计、规范和样品的要求采购工程需要<BR> 
　　的材料，并提供产品合格证明。在材料设备到货２４小时前通知甲方代表验收。对与设计、规范和样品<BR> 
　　要求不符的产品，甲方代表应禁止使用，由乙方按甲方代表要求的时间运出现场，重新采购符合要求的<BR> 
　　产品，承担由此发生的费用，工期不予顺延。甲方未能按时到场验收，以后发现材料不符合规范、设计<BR> 
　　和样品要求，乙方仍应拆除、修复及重新采购，并承担发生的费用。由此延误的工期相应顺延。<BR> 
　　第二十七条　材料试验。对于必须经过试验才能使用的材料，不论甲乙双方任何一方供应，按协议<BR> 
　　条款的约定，由乙方进行防火阻燃、毒性反应等测试。不具备测试条件的，可委托专业机构进行测试，<BR> 
　　费用由甲方承担。测试结果不合格的材料，凡款采购的应停止采购，凡已采购运至现场的，应立即由采<BR> 
　　购方运出现场，由此造成的全部材料采购费用，由采购方承担。甲方或设计单位指定的材料不合格，由<BR> 
　　甲方承担全部材料采购费用。<BR> 
　　<BR> 
　　七、设计变更<BR> 
　　<BR> 
　　第二十八条　甲方变更设计。甲方变更设计，应在该项工程施工前７天通知乙方。乙方已经施工的<BR> 
　　工程，甲方变更设计应及时通知乙方，乙方在接到通知后立即停止施工。<BR> 
　　由于设计变更造成乙方材料积压，应由甲方负责处理，并承担全部处理费用。<BR> 
　　由于设计变更，造成乙方返工需要的全部追加合同价款和相应的损失均由甲方承担，相应顺延工<BR> 
　　期。<BR> 
　　第二十九条　乙方变更设计。乙方提出合理化建议涉及到变更设计和对原定材料的换用，必须经甲<BR> 
　　方代表及有关部门批准。合理化建议节约的金额，甲方双方协商分享。<BR> 
　　第三十条　设计变更对工程影响。所有设计变更，双方均应办理变更洽商签证。发生设计变更后，<BR> 
　　乙方按甲方代表的要求，进行下列对工程影响的变更：<BR> 
　　１．增减合同中约定的工程数量；<BR> 
　　２．更改有关工程的性质、质量、规格；<BR> 
　　３．更改有关部分的标高、基线、位置和尺寸；<BR> 
　　４．增加工程需要的附加工作；<BR> 
　　５．改变有关工程施工时间和顺序。<BR> 
　　第三十一条　确定变更合同价款及工期。<BR> 
　　发生设计变更后，在双方协商时间内，乙方按下列方法提出变更价格，送甲方代表批准后调整合同<BR> 
　　价款：<BR> 
　　１．合同中已有适用于变更工程的价格，按合同已有的价格变更合同价款；<BR> 
　　２．合同中只有类似于变更情况的价格，可以此作为基础确定变更价格，变更合同价款；<BR> 
　　３．合同中没有适用和类似的价格，由乙方提出适当的变更价格，送甲方代表批准后执行。<BR> 
　　设计变更影响到工期，由乙方提出变更工期，送甲方代表批准后调整竣工日期。<BR> 
　　甲方代表不同意乙方提出的变更价格及工期，在乙方提出后７天内通知乙方提请工程造价管理部门<BR> 
　　或有关工期管理部门裁定，对裁定有异议，按第３５条约定的方法解决。<BR> 
　　<BR> 
　　八、竣工与结算<BR> 
　　<BR> 
　　第三十二条　竣工验收。工程具备竣工验收条件，乙方按国家工程竣工验收有关规定，向甲方代表<BR> 
　　提供完整竣工资料和竣工验收报告。按协议条款约定的日期和份数向甲方提交竣工图。甲方代表收到竣<BR> 
　　工验收报告后，在协议条款约定的时间内组织有关部门验收，并在验收后７天内给予批准或提出修改意<BR> 
　　见。乙方按要求修改，并承担由自身原因造成修改的费用。<BR> 
　　甲方代表在收到乙方送交的竣工验收报告７天内无正当理由不组织验收，或验收后７天内不予批准<BR> 
　　且不能提出修改意见，视为竣工验收报告已被批准，即可办理结算手续。<BR> 
　　竣工日期为乙方送交竣工验收报告的日期，需修改后才能达到竣工要求的，应为乙方修改后提请甲<BR> 
　　方验收的日期。<BR> 
　　甲方不能按协议条款约定日期组织验收，应从约定期限最后一天的次日起承担保管费用。<BR> 
　　因特殊原因，部分工程或部位须甩项竣工时，双方订立甩项竣工协议，明确各方责任。<BR> 
　　第三十三条　竣工结算。竣工报告批准后，乙方应按国家有关规定或协议条款约定的时间、方式向<BR> 
　　甲方代表提出结算报告，办理竣工结算。甲方代表收到结算报告后应在７天内给予批准或提出修改意<BR> 
　　见，在协议条款约定时间内将拨款通知送经办银行支付工程款，并将副本送乙方。乙方收到工程款１４<BR> 
　　天内将竣工工程交付甲方。<BR> 
　　甲方无正当理由收到竣工报告后１４天内不办理结算，从第１５天起按施工企业向银行同期贷款的<BR> 
　　最高利率支付工程款的利息，并承担违约责任。<BR> 
　　第三十四条　保修。乙方按国家有关规定和协议条款约定的保修项目、内容、范围、期限及保修金<BR> 
　　额和支付办法，进行保修并支付保修金。<BR> 
