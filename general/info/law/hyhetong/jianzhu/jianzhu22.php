　　家没有统一标准、规范时，有行业标准、规范的，使用行业、标准规范；没有国家和行业标准、规范<BR> 
　　的，使用地方标准、规范。此条应写明使用的标准、规范的名称。并按照工程的主要部位或项目分别填<BR> 
