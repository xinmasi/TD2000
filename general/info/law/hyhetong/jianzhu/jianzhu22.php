　　法规或规章名称。如由甲方提供，还应写明提供时间。如由乙方自备，应写明费用由谁承担。<BR> 
　　第四条<BR> 
　　不同的工程对图纸的需要情况各不相同。本条应写明甲方提供的份数（必须包括竣工图和现场存放<BR> 
