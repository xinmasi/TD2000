　　８．４　本款应写明甲方应协调哪些施工单位之间的关系及协调内容和完成时间。<BR> 
　　８．５　本款应写明由甲方办理的各种证件、批件和其他需要批准的事项及完成的时间，其时间可<BR> 
　　以是绝对的年、月、日，也可以是相对的时间，如在某项工作开始几天之前完成。<BR> 
　　８．６　本款应写明甲方组织图纸会审的时间，如不能约定准确时间，应写明相对时间，如甲方发<BR> 
　　布开工令前多少天。<BR> 
　　第九条<BR> 
　　本条按具体工程和实际情况，逐款列出各项工作的名称、内容、完成时间和要求，实际需要而《合<BR> 
　　同条件》未列的，要对条款和内容予以补充。<BR> 
　　９．１　甲方如委托乙方完成工程施工图及配套设计，应在本款写明设计的名称、内容、要求、完<BR> 
　　成时间和设计费用计算方法。<BR> 
　　９．２　本款应写明乙方提供的计划、报告、报表的名称、格式、要求和提供的时间。<BR> 
　　９．３　甲方要求乙方提供的合同价款之外的照明、警卫、看守等工作，应在此款写明。<BR> 
