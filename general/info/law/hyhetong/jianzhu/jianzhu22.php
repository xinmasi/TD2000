　　本条写明甲方委托的监理单位名称、总监理工程师的姓名、职称和甲方授权范围。<BR> 
　　第八条<BR> 
　　本条按具体工程和实际情况，逐款列出各项工作的名称、内容、完成时间和要求，实际存在而《合<BR> 
　　同条件》未列入的，要对条款或内容予以补充。还应写明甲方不能按《协议条款》的要求完成有关工作<BR> 
　　时，应支付的费用金额和赔偿乙方损失的范围及计算方法。<BR> 
　　８．１　本款应写明使场地具备施工条件的各项工作的名称、内容、要求和完成的时间。<BR> 
　　８．２　本款应写明水、电、电讯等管线应接至的地点、接通的时间和要求。如上、下水管应在何<BR> 
　　时接至何处，每天应保证供应的数量，水质标准，不能全天供应的要写明供应的时间。<BR> 
