　　的格式。承发包双方针对工程的实际情况，把对《合同条件》的修改、补充和对某些条款不予采用的一<BR> 
　　致意见按《协议条款》的格式形成协议。《合同条件》和《协议条款》是双方统一意愿的体现，成为合<BR> 
　　同文件的组成部分。<BR> 
　　采用招标发包的工程，《合同条件》应是招标文件的组成部分，发包方对其修改、补充或对某些条<BR> 
　　款不予采用的意见，要在招标文件中说明。承包方是否同意发包方的意见及自己对《合同条件》的修<BR> 
　　改、补充和对某些条款不予采用的意见，也要在标书中一一列出。中标后，双方将协商一致的意见写入<BR> 
　　《协议条款》。不采用招标发包的工程，在要约和承诺时都要把对《合同条件》的修改、补充和对某些<BR> 
　　条款不予采用的意见一一提出，将达成一致意见写入《协议条款》。<BR> 
　　下面对《协议条款》的使用加以说明：<BR> 
　　发包方（以下简称甲方）：可以是具备法人资格的国家机关、事业单位、国有企业、集体企业、私<BR> 
　　营企业、经济联合体和社会团体，也可以是依法登记的个人合伙、个体工商户或个人。上列单位的名称<BR> 
　　和个人的姓名，应准确地写在《协议条款》甲方位置内，不得简称。<BR> 
　　承包方（以下简称乙方）：应是具备与工程相应资质和法人资格的国有企业、集体企业或私营企<BR> 
　　业，上述单位的名称应准确地写入《协议条款》乙方位置内，不得简称。<BR> 
　　第一条<BR> 
　　１．１　本款内应准确写出工程的名称、详细地址、承包范围和承包方式。承包范围主要指单位工<BR> 
　　程的装饰装修内容及等级。<BR> 
　　１．２　本款内开工日期应写明双方商定的开工日期，也可以将此规定为甲方代表发布开工令的日<BR> 
　　期；竣工日期指双方商定的乙方提交竣工报告的日期；总日历工期天数是包括法定节、假日在内的总日<BR> 
　　历工期天数。<BR> 
　　１．３　写明双方商定的工程应达到的质量等级（即合格还是优良）。<BR> 
　　１．４　应写明工期价款的金额和承包方式。<BR> 
　　工程为群体或小区工程时，以上各项应将单位工程或分部工程按附表一说明，作为协议条款的附<BR> 
　　件。<BR> 
