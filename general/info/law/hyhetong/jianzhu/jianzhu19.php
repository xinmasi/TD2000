　　建筑工程承包合同是以建筑工程为标的的合同，包括建筑工程施工合同、建筑工程勘察合同、建筑<BR> 
　　工程设计合同。建筑工程承包合同是很复杂的合同。涉及到工程技术标准、质量的监督检查、施工期限<BR> 
　　等问题，而且涉及的法律、法规也很多。因此，订立建筑工程承包合同，应当根据不同的情况作出具体<BR> 
　　规定，力求做到准确、明确和正确，确保工程顺利进行。特别是不同工程的技术术语、语词涵义往往都<BR> 
