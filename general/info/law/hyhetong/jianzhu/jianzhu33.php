　　特定风险<BR> 
　　<BR> 
　　６５．本合同任何条款均服从以下规定：<BR> 
　　６５．１任何责任，不论属于赔偿或其它，也不论其是有关或涉及工程（以下提及的任何特定风险发生前，已按本合同第３９条宣布为不适用的工程除外）、业主或第三当事人的财产的毁坏或损坏，或有关或涉及人员伤亡，只要是因以下所规定的特定风险导致，承包人概不负责。业主必须保证承包人不受上述情况以及由此所引起的任何索赔、诉讼、损害赔偿金、诉讼费、开支和费用的损害。<BR> 
　　６５．２如工程或任何在工地、或从附近运往工地的材料，或承包人其它用于或旨在用于工地的任何财产因上述特定风险受到毁坏或损坏，承包人有权得到以下偿付：<BR> 
　　因此而被毁坏或损坏的任何永久性工程或材料，<BR> 
　　以及，只要工程师要求，或工程完工所必需，按成本费加上工程师证明合理的利润为基准支付；<BR> 
　　恢复或补偿工程被如此毁坏或损坏的部分；<BR> 
　　恢复或补偿承包人用于或旨在于工程的此种材料或其它财产。<BR> 
　　６５．３因地雷、炸弹、炮弹、手榴弹、或其它射弹、导弹、弹药、或军用炸药的爆炸或冲击（不论时间和地点）所导致有毁坏、损害、受伤或死亡应被视为是上述特定风险的结果。<BR> 
　　６５．４除本条以下关于战争爆发的规定外，业主应偿还承包人因上述特定风险而发生的任何施工额外或附加费用，在任何特定风险发生前按合同第３９条视为不适用的工程的重建费不在此列，承包人一旦知道此种费用增加，必须立即书面通知工程师。<BR> 
　　６５．５特定风险指战争、战争状态（不论宣战予否）、侵略、外国敌对行为、本合同第２０条第２款规定的核危险和声波风险、或与正在或即将施工或维修的工程所在地国有关的反叛、革命、暴动、军事政变、篡权、内战，或（只是由承包人或其分包人的雇员和因施工引起的除外）骚乱、动乱或混乱。<BR> 
　　６５．６在合同执行时，如世界任何地方爆发战争，无论宣战予否，是否在财政或其他方面对工程造成了重大影响，除非本合同按本条款规定终止，否则承包人都应继续尽力完成工程。业主在战争爆发后随时有权书面通知承包人终止合同，一旦此种通知发出，除本条所规定的双方权利和第６７条的规定外，合同应当终止，但不得损害任何一方与以前任何违约有关的一切权利。<BR> 
　　６５．７如合同按上一款规定终止，承包人应迅速从工地移走所有建筑设备，并向其分包人提供同样的转移设施。<BR> 
　　６５．８如果合同如上终止，应承包人所得的暂付款项中尚未包括的在终止之日前已完成工作的款项，业主应按本合同所规定的价格支付给承包人，此外还应支付：<BR> 
　　所有有关临时单据应予支付的款项，只要单据所含的工作或服务已经完成，如此种单据所含的工作或服务只完成一部分，则只支付经工程师确认的应予支付的那一部分款项。<BR> 
　　因工程而订购的材料或货物的合理费用，包括已运交承包人的和承包人按法律有义务接收的材料或货物，业主向承包人付款后，这些材料或货物即成为业主的财产。<BR> 
　　经工程师确认的，承包人为完成整个工程而发生的合理费用。只要本款上述各种费用未将此费用包括在内。<BR> 
　　本条第１、第２和第４款所规定的追加付款。<BR> 
　　本条第７款所规定的搬迁建筑设备的合理费用，如果承包人要求，包括将此设备运回到承包人注册国的主要设备堆置场的费用，或运至其他地点的费用，但此费用不得超过运回注册国的费用。<BR> 
　　合同终止时，遣返承包人雇用在工程上或与之有关的职员和工人的合理费用。<BR> 
　　对本款项下业主应予支付的款项，业主有权用承包人的应付款来抵冲，包括建筑设备及材料的预付款的未偿余额，以及其它任何在合同终止日按合同条款业主有权向承包人回收的款项。<BR> 
