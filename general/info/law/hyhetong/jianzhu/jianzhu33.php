　　补救和权力<BR> 
　　<BR> 
　　６３．１如果承包人破产，或被法院下达了破产者产业管理接收令，或递交了破产申请，或因债权人而处置或转让财产，或同意在债权人决议委任的检查委员会监督下执行合同，或，如其是一个公司，将被清算（不是因合并或重组而进行的自愿清算），或承包人事先未征得业主的书面同意而转让合同，或其货物被四，或工程师书面向业主证明，他认为承包人：<BR> 
　　已经撤销合同，或<BR> 
　　没有正当理由但却不开工，或在收到工程师要求继续工程的书面通知后，停工＿＿天，或<BR> 
　　在收到工程师书面通知，说明某些材料不适用或某工程应予否决后＿＿天内未从工地搬走这些材料，或未拆毁此种工程重建，或<BR> 
　　不按合同施工，或长期或公然不履行其合同义务，尽管工程师已书面提出警告，或<BR> 
　　不顾工作质量，或无视工程师的指示，分包任何合同部分，<BR> 
　　此时，业主可在书面通知承包人＿＿天后进驻工地，并将承包人逐出，由此不会造成合同无效，或解除承包人的任何合同义务或责任，或影响合同赋予业主或工程师的权利和权力，业主可自己完成工程或另雇其他承包人完成工程。为完成工程，业主和其他承包人可充分使用其认为合适的，按本合同规定被视为是专门为施工而保留的建筑设备、临建工程和材料，业主可随时出售上述任何建筑设备、临建工程和未使用的材料，并将出售收入抵付按合同规定承包人应支付或可能支付给他的任何款项。<BR> 
　　６３．２业主进驻工地并逐走承包人之后，工程师应尽快单方面，或通过了解双方当事人，或在作过他所认为恰当的调查或咨询之后作出安排或决定，并证明到此种进驻与被逐时为止，按事实上已完成的合同工程，承包人已获得或理应获得的款项（如果有），以及计算出上述任何未使用或只部分使用的材料、建筑设备和临建工程的价值。<BR> 
　　６３．３如果业主按本条规定进驻工地并逐走承包人，他应在维修期满，且所有施工和维修费、延误完工赔偿费（如果有）、和业主所发生的其他费用被核实，且经工程师证明后，方才负责对承包人按合同进行支付。此时，承包人有权得到的款额（如果有）仅为工程师证明因他妥善完工而应得的款额减去上述款项之差。如上述费用超过承包人妥善完工应得的款额，经要求，承包人得向业主支付此超出部分款额，超出部分应视为承包人对业主的负债，且可照此收回。<BR> 
　　６４．如在施工或维修期间，因工程或部分工程发生或涉及任何意外、或事故、或其他事件，工程师或工程师代表认为需马上进行补救或其它工作或修理，以保证工程的安全，而承包人无法或不愿马上进行此种工作或修理，业主可雇用并支付他人进行工程师或工程师代表认为必要的此种工作或修理。如工程师认为，业主所进行的此种工作属于承包人按合同规定应自己付费完成的工作，业主由此所发生的一切费用应由业主向承包人收回，或从其应付给或可能应付给承包人的款项中扣除。在此种任何紧急事件发生后，工程师或工程师代表应根据情况，尽快书面告知承包人。<BR> 
