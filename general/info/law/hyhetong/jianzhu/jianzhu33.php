　　６２．３尽管签发了维修证书，承包人以及业主（但应符合本条第２款的规定）仍应对发生在证书签发前而在证书签发时尚未履行的合同义务负责，为决定此种义务的性质及范畴，应视本合同继续对合同双方有效。<BR> 
