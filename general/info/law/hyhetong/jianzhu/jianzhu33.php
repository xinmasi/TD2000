　　失效<BR> 
　　<BR> 
　　６６．如合同缔结后爆发战争，或出现双方无法控制的其它情况，使得任何一方无法履行其合同义务，或根据合同适用的法律，双方被解除了继续履约的义务，由业主对已完成的工程而支付承包人的款额应当等于按本合同第６５条规定之金额，即如果合同根据第６５条之规定终止时，应当支付的金额。<BR> 
