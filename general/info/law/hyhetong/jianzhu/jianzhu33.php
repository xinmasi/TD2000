　　争议的解决<BR> 
　　<BR> 
　　６７．业主与承包人或工程师与承包人因合同或工程施工而发生的或与之有关的任何争议或分歧，无论是在施工中还是在完工后，无论是在合同终止、撤销或违约前后，首先应当提交工程师解决，应任何一方要求后＿＿天内，工程师必须将其决定书面通知业主和承包人。除按以下规定提出仲裁外，此种有关上述所有事项的决定为最终决定，对业主和承包人均有约束力的，且立即对业主和承包人生效，承包人应继续施工并尽一切应有的注意，无论他或业主或承包人是否要按以下规定提请仲裁。在工程师将决定书面通知业主和承包人后，如＿＿天内未收到业主或承包人的仲裁申请，上述决定应作为最终决定，对业主和承包人均有约束力。如工程师未按上述规定应要求在＿＿天内通知其决定，或业主或承包人对此种决定不满意，在此后或此种情况下，业主或承包人可在收到决定通知＿＿天内，或在最先所说的＿＿天期满后＿＿天内，视情况而定，将争议事项按下述规定提交仲裁。所有争议或分歧，凡工程有关决定（如果有）如上所述不再是最终和有约束力的，均应由根据《国际商会调解和仲裁规则》所指定的一名或多名仲裁员按该规则规定予以最终解决。上述仲裁员（们）有权评论、修正和复审工程师的任何决定、意见、指示、证书或估价。在仲裁过程中，任何一方均可不受限制地向仲裁员（们）提供当时供工程师作出决定而采用的证据或论据。不论工程师按上述规定曾作过何种决定，均不得剥夺其被传唤作为证人，且就按上述规定提交仲裁的争议或分歧的任何有关事项向仲裁员（们）提供证据的资格。不论工程尚未或认为尚未完工，均可提请仲裁，但业主、工程师和承包人的义务不得因在施工进程中提起仲裁而改变。<BR> 
　　<BR> 
　　通知<BR> 
　　<BR> 
　　６８．１业主或工程师按合同条款给予承包人的所有证书、通知或书面命令均应邮寄或送交至承包人的主要营业地，或承包人指定的其他通讯地址。<BR> 
　　６８．２所有按本合同条款给予业主或工程通知均应邮寄或送交至第二部分有关通知条款所指的其各自的地址。<BR> 
　　６８．３任何一方均可把一指定地址改为在施工地国的另外一个地址，但事先应书面通知另一方，工程师在改变地址前需书面通知双方当事人。<BR> 
　　<BR> 
　　业主违约<BR> 
　　<BR> 
　　６９．１如果业主：<BR> 
　　在工程师证书所认定的应付款项按合同条款到期＿＿天内未向承包人支付，业主按合同有权所作的扣除部分除外，或<BR> 
　　干涉或阻碍或拒绝同意颁发任何此种证书，或<BR> 
　　破产或、如为公司，即将被清算，而此种清算不是为了重组或合并，或<BR> 
　　正式通知承包人，因不可预见的原因，鉴于经济混乱，其无法再履行其合同义务。<BR> 
　　承包人应有权在提前＿＿天书面通知业主，并将副本呈交工程师后，终止其合同雇佣关系。<BR> 
　　６９．２本条第１款规定的＿＿天通知期限到期后，尽管本合同第５３条第１款作有规定，承包人仍然应尽量迅速地从工地撤走他所带来的所有建筑设备。<BR> 
　　６９．３如果合同如此终止，业主必须向承包人承担本合同按第６５条规定终止时一样的支付义务，但除了第６５条第８款所规定的款项外，业主还必须向承包人支付因合同由此终止而造成的或有关的或导致损失费或损害费。<BR> 
　　<BR> 
　　费用与法规的变更<BR> 
　　<BR> 
　　７０．１合同价格的调整应与劳工或／和材料成本的增减，或其他任何影响施工费用的事项相关，应按第二部分第７０条规定执行。<BR> 
　　７０．２在提交工程标书最后期限前的＿＿天之后，如施工或即将施工工程所在地国的任何国家或州的法律、法令、政令或其它任何地方和其他权力机构的法律或条例或规章发生变化，或适用了使承包人的施工费用发生增减（非本条第１款所述的增减）的任何州的法规、法令、政令、法律、条例或规章，此种费用增减应经工程师证明，且由业主支付或记入贷方，合同价格也应作相应调整。<BR> 
　　<BR> 
　　货币和汇率<BR> 
　　<BR> 
　　７１．在提交标书最后期限前的＿＿天之后，如施工或即将施工工程所在地国的政府或政府授权机构对合同价格计价货币实行货币管制和／或货币兑换管制，业主应补偿由此对承包人造成的一切损失或损害，这种补偿不得影响承包人在此种情况下行使其它任何权利或补救措施的权利。<BR> 
　　７２．１如果合同规定，承包人的支付应全部或部分使用一种或多种外币，此种支付不得受特定外币与施工工程所在地国的货币之间的汇率变化的影响。<BR> 
　　７２．２如业主要求标书计价只用一种货币，而支付却用多种货币，且承包人已说明他要求用其他货币支付的比例或数额，用于计算此种比例或数额支付的货币兑换率应为现行汇率，即由施工所在地国的中央银行在提交标书最后期限前的＿＿天当日所决定的汇率，就此，业主必须在标书提交前告知承包人，或在投标文件中予以写明。<BR> 
　　７２．３如合同规定使用多种货币支付，凡涉及按本合同第５８和第５９条规定使用暂列款的项目，其用外汇支付的比例或数额安本条第１和第２款制定的原则予以确定。<BR> 
