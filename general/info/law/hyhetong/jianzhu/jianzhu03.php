      <p> 　　<BR> 
　　签订合同双方：<BR> 
　　征用土地单位：＿＿＿＿，以下简称甲方；<BR> 
　　被征土地单位：＿＿＿＿，以下简称乙方。<BR> 
　　根据＿＿＿＿（审批权力机关）批准的＿＿＿＿建设项目的计划任务书和工程选点报告等文件，按照《国家建设征用土地条例》和＿＿省（或自治区、直辖市）政府的有关规定，经甲方向征地所在地的土地管理机关申请和＿＿县（或市、省）人民政府审查批准，经甲乙双方实地查看、反复协商，特签订本合同，以供双方共同遵守。<BR> 
　　第一条　征用土地数量及方位<BR> 
　　甲方征用乙方土地共＿＿亩，其中，稻田＿＿亩，水塘＿＿亩，菜地＿＿亩，坡地＿＿亩，宅基地＿＿亩，林木＿＿亩，共有树木＿＿株。所征土地东起＿＿＿＿，南起＿＿＿＿，西起＿＿＿＿，北起＿＿＿＿。<BR> 
　　第二条　征用土地的各类补偿费和安置补助费<BR> 
　　１．根据＿＿省（或自治区、直辖市）政府关于征用土地的补偿规定，各类耕地（包括菜地）按该地年产值的＿＿倍（一般为该耕地年产值的三至六倍）补偿。征用无收益的土地，不予补偿。（征用园地、鱼塘、藕塘、苇塘、宅基地、林地、牧场草原等的补偿标准，按省、自治区、直辖市政府制定的办法执行；征用城市郊区的菜地，还应按当地政府的有关规定，向国家缴纳新菜地开发基金。）<BR> 
　　２．根据＿＿省（或自治区、直辖市）政府的规定，所征土地上的青苗按该地年产值的＿＿％补偿，所征土地上的水井、林木、水塘等附着物按＿＿办法补偿。房屋的补偿办法另订拆迁合同。乙方人员在开始协商征地方案以后抢种的作物、树木和抢建的设施，甲方一律不予补偿。<BR> 
　　３．各类耕地的年产值按耕地被征用前三年的平均年产量和国家规定的价格核定，稻田按平均亩产大米为＿＿公斤，每公斤计价＿＿元，年产值每亩核定为＿＿元；旱地按平均亩产玉米（或小麦）＿＿公斤，每公斤计价＿＿元，年产值每亩核定为＿＿元；菜地按平均亩产大白菜＿＿公斤计算，每公斤计价＿＿元，年产值每亩核定为＿＿元。<BR> 
　　４．根据《国家建设征用土地条例》的规定，乙方需要安置的农业人口数，按征地前农业人口和耕地面积的比例及征地数量计算，共计＿＿人；甲方对乙方农业人口的安置；补助费标准，按所征耕地每亩产值的二至三倍计算（年产值按被征用前三年的平均年产量和国家规定的价格计算，但每亩耕地的安置补助费，最高不得超过其年产值的十倍）；征用宅基地不付安置补助费（征用园地、鱼塘、藕塘、林地、牧场、草原等土地的安置补助费，按省、市、自治区政府的标准计算）。<BR> 
　　甲乙双方在本合同上签字，并实地勘验征用地界、定立永久性界桩后＿＿日内，甲方向乙方一次（或商定于某段时期内几次）支付全部各类补偿费、安置补助费共＿＿元（其中，土地补偿费和安置补助费的总和不得超过被征土地年产值的二十倍），付款均通过建设银行转帐托付。<BR> 
　　第三条　减免公、余粮交售任务<BR> 
　　甲乙双方按照＿＿人民政府的规定，根据被征土地的亩数，向＿＿人民政府呈递减免公、余粮交售任务的申请报告。实际减免量，以＿＿人民政府的批文为准。<BR> 
　　第四条　安置办法<BR> 
　　乙方因被征用土地造成农业剩余劳力，甲方应向有关单位联系，采取以下第（　）项办法解决：<BR> 
　　１．发展农业生产。甲方协助乙方改良土壤，兴修水利，改善耕作条件；在可能和合理的条件下，经县、市土地管理机关批准，适当开荒，扩大耕种面积；也可由甲方结合工程施工帮助造地，但要从安置补助费中扣除甲方的资助费用。<BR> 
　　２．发展社队工副业生产。在符合国家有关规定的条件下，甲方帮助乙方因地制宜，兴办对国计民生有利的工副业和服务性事业，但要从安置补助费中扣除甲方的资助费用。<BR> 
　　３．迁队或并队。土地已被征完或基本征完的生产队，在有条件的地方，可以组织迁队；也可按照自愿互利的原则，与附近生产队合并，甲方要积极为乙方迁队或并队创造条件。<BR> 
　　（按照上述途径确实安置不完的剩余劳动力，经＿＿省或自治区或直辖市人民政府批准，在劳动计划范围内，符合条件的可以安排到集体所有制单位就业，并将相应的安置补助费转拨给吸收劳动力的单位；甲方如有招工指标，经＿＿省或自治区或直辖市人民政府同意，也可以选招其中符合条件的当工人，并相应核减被征地单位的安置补助费。乙方的土地被征完，又不具备迁队、并队条件的，乙方原有的农业户口，经省或自治区、或直辖市人民政府审查批准，可转为非农业户口或城镇户口，乙方原有集体所有的财产和所有的补偿费、安置补助费，由县市以上人民政府与有关乡村商定处理，用于组织生产和不能就业人员的生活补助。）<BR> 
　　乙方用补偿费和安置补助费兴建生产生活设施所需建设物资，乡村能够解决的，由乡村自行解决；乡村不能解决的，由当地政府协助解决；地方无法解决的少数统配部管物资，经县（或市）土地管理机关审查核实后，由甲方随同建设项目向国家有关部门申请分配，物资价款由乙方支付。<BR> 
　　第五条　甲方的责任<BR> 
　　１．甲方征用土地上有青苗的，在不影响工程正常进行的情况下，应当等待乙方收获，不得铲毁；凡在当地一个耕种收获期内尚不要使用的土地，甲方应当与乙方签订协议，允许乙方耕种。<BR> 
　　２．甲方已征用二年还不使用的土地（铁路沿线以及因安全防护等特殊需要，符合国家规定的留用土地，不得视为征而未用的土地），除经原批准征地的机关同意延期使用的土地外，当地县（或市）人民政府有权收回处理，甲乙方均不得擅自侵占或处理。<BR> 
　　３．甲方如逾期不向乙方支付征地的各种补偿费和安置补助费，乙方可凭本合同正本申请建设银行从甲方银行帐户内拨付，并可请求甲方按银行关于延期付款的规定偿付违约金。<BR> 
　　第六条　乙方的责任<BR> 
　　１．自本合同订立之日起，乙方有责任告知所属村民不得在征用土地内种植作物，不得砍伐林木和损坏其它附着物，有违反者，乙方必须赔偿甲方的损失。经甲方同意在征地上种植作物的，乙方应统一安排，不得逾期。<BR> 
　　２．乙方在本合同订立后需在征用土地上架设电线、兴修沟渠等，应经甲方同意，乙方应在不影响建设工程的前提下动工，否则按侵犯公有财产报请有关机关处理。<BR> 
　　第七条　工程临时用地<BR> 
　　甲方在工程施工过程中，需要建设材料堆场、运输通路和其它临时设施的，应当尽量在征地范围内安排。确实需要另行增加临时用地的，由甲方向原批准工程项目用地的主管机关提出临时用地数量和期限的申请，经批准后，同乙方签订临时用地协议，并按乙方前三年土地平均年产值逐年给予补偿。甲方在临时用地上不得修建永久性建筑物。甲方使用期满，应当恢复乙方土地的耕种条件，及时归还乙方，或按恢复土地耕种条件的工作量向乙方支付费用。<BR> 
