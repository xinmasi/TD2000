　　价格年度至工程竣工的时间跨度在二年以内或工期在一年以内的工程。<BR> 
　　５．按投标中标价一次包死，不作调整，采用这种结算方式的条件同施工图预算加风险系数的结算<BR> 
　　方式。<BR> 
