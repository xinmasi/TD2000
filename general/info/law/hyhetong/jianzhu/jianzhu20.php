　　中间验收（含月末验收）。指工程施工过程中因中间结算的需要对分部分项工程质量和数量的验<BR> 
　　收，跨年度施工的单位工程还包括竣工前的年度验收。<BR> 
　　第六条　工程价款的支付和结算<BR> 
　　工程价款的结算方式，一般有以下五种方式可供选择：<BR> 
