        <p align=center >实施专利许可合同备案衰填表须知</p>
        <p >1．表格前面的专利管理机关或涉外专利代理机构名称和登记日期由该机关或机构填写，备案号和备案日期由专利局填写后通知专利管理机关和备案人。表格中的项日由专利权人填写。</p>
        <p >2．合同种类系指独占许可、排他许可、普通许可、交叉许可和分许可等。涉及几种许可证时，应依次列出。</p>
        <p >3．合同性质系指纯专利许可或混合许可。对混合许可应说明其内容，如专利、Know―How、商标。</p>
        <p >4．许可方就此项合同的标的已与他人签订合同时，应填入该合同备案号、生效和终止日期及被许司方姓名或名称。</p>
        <p >5．使用费总计系指已确定的使用费总额。预计总额系指酬金按提成率计算时，根据提成率、合同期限及其他支付款项估算的累计总额。</p>
        <p >6．一次总算系指双方在成交时一次算清使用费的总金额。一次总付或分期支付应填人分期付款栏日内。</p>
        <p >7．入门费系指在接受技术的初始阶段，被许可方向许可方所支付的费用。如此款项分多次支付，应在备注栏内写明次数及期限。</p>
        <p >8．提成率系指与产品产量、销售额、销售量、利润或与其他固定额相联系的支付率。</p>
        <p >9，最高提成费和最低提成费系指合同中规定的上、下限提成费。</p>
        <p >10．递减提成率系指随产品销售额等的增加而按一定比例降低的提成率或提成费递减率。</p>
        <p >11．若已向国外申请专利，则应在该专利在国外申请的专利情况栏内写明申请国别、申请闩、申请号。若已取得专利权，应写明批准日、专利号及国别。</p>
        <p >12．在使用费和支付方式两栏中、外汇应写明货币名称，人民币不用折合外汇计算。</p>
