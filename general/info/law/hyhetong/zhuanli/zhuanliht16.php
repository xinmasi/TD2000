        <p >本实用新型的目的是要提供一种简便实用、方便灵巧的收折式抽水坐便器。</p>
        <p >本实用新型的设计方案是在保证抽水式坐便器的使用功能和效果的前提下，最大限度地缩小坐便器在非使用状态所占用的面积和空间，满足小型卫生间的使用需要，同时又具有折收灵活方便之特点。本方案主要由坐框、坐盆架和盆体构成，同时配置了由活动连杆和支撑件构成的收折支撑机构。所述的盆体为一可折叠的软盆体，其上口固定在坐盆架内，其下口接有水封和排污接口，以防止盆底间底水的反溢，排污接口可直接与卫生间的主排污管连接，便于安装；所述的坐盆架内设有环形溢流式分配通道，并与进水管道或水箱连接；所述的活动连杆对称设在坐框和坐盆架的两侧，并分别与它们作动配合联接，其中，与坐框作前旋限位连接，以满足收折需要；所述的支撑件可设在坐盆架的前侧或后侧，以保证架体在使用状态时的撑力稳定。鉴于以上设计，本方案的坐盆架可上提作逆时针向收折和下拉作顺时针向展开工使用状态，井由支撑件保证其正常使用。</p>
