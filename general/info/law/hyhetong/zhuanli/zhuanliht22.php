        <p align=center style='
text-align:center;line-height:22.0pt;
'>填表注意事项</p>
        <p style='line-height:24.0pt;'>一、申请发明专利，应当提交发明专利请求书、权利要求书、说明书、有附图的应同时提交附图及摘要附图、摘要。申请文件应当一式两份。一份为原本，一份为复印件。</p>
        <p style='line-height:24.0pt;'>二、本表应使用中文填写，表中文字应当打字或者印刷，字迹为黑色。外国人名、地名无统一译文时；应同时注明原文。</p>
        <p style='line-height:24.0pt;'>三、本表中方格口供填表人选择使用，若有方格后所述内容的，应在方格内标上“√”号。</p>
        <p style='line-height:24.0pt;'>四、本表中所有地址栏，国内地址应写明省（直辖市或者自治区）、市、区、街道、门牌号码以及邮政编码。外国人地址应写明国别、州（市、县）。</p>
        <p style='line-height:24.0pt;'>五、填表说明：</p>
        <p style='line-height:24.0pt;'>1．本表第①、第②、第③、第④、第⒅栏由专利局填写。</p>
        <p style='line-height:24.0pt;'>2．本表第⑤、第⒂栏发明名称，应简单明确，不得超过25个字。</p>
        <p style='line-height:24.0pt;'>3．本表第⑥栏发明人应当是对本发明的实质性特点刊；出创造性贡献的自然人。发明人有两个以上的应先自左向右、再自上而下依次填写，发明人排列次序无法律效力。</p>
        <p style='line-height:24.0pt;'>4．本表第⑦栏申请人是单位的，应填写单位全称，并与公章中名称一致。申请人为个人，应填写本人真实姓名，不得写笔名等。申请人为多个，又未委托专利代理机构，应当指定一人为代表人，并填写在第一署名申诸人位置。代表人为单位，还应当填写单位联系人姓名。</p>
        <p style='line-height:24.0pt;'>5．本表第⑧栏，应填写已在专利局注册的专利代表机构名称并注明注册代码。专利代理机构指定的代理人不得超过两人，其中必须有一名专职代表人并注明代理人证书号。</p>
        <p style='line-height:24.0pt;'>6．申请涉及微生物发明专利的，还应当填写本表第⑨栏，并提交保藏证明和存活证明。</p>
        <p style='line-height:24.0pt;'>7．申请人提出分案申请时，还应填写本表第⑩栏。</p>
        <p style='line-height:24.0pt;'>8．申请人要求外国或者本国优先权的，还应填写本表第⑾栏。</p>
        <p style='line-height:24.0pt;'>9．申请人要求不丧失新颖性宽限期的，还应填写本表第⑿栏。自申请日起两个月内提交证明文件。</p>
        <p style='line-height:24.0pt;'>10．申请人要求保密处理的，还应填写本表第⒀栏。</p>
        <p style='line-height:24.0pt;'>11．申请人应当按实际提交的文件名称、份数、页数正确填写本表第<span style='font-size:
12.0pt;font-family:宋体'>⒁</span>、第⒃、第⒄栏，专利局将按实收的文件的数量逐项[核实。</p>
        <p style='line-height:24.0pt;'>12．申请人请求减缓申请费，必须在提交申请文件的同时提交费用减缓请求书及有关证明文件。</p>
        <p style='line-height:24.0pt;'>13，申请人委托专利代理机构的，在本表第⒆栏应盖代理机I扣公章。申请人未委托专利代理机构的，本表第⒆栏应由全体申请人签字或盖章，申请人为单位的，应盖单位公章。请求书原本中的申请人或专利代理机构的签字或盖章不得复印。</p>
        <p style='line-height:24.0pt;'>14，本表第⒇栏，填写收件人地址和姓名。委托专利代理机构的，收件人地址应写专利代理机构的地址和名称，收件人写代[理人姓名。未委托专利代理机构的，收件人地址应写第一署名申请人（代表人）的地址，若代表人为单位，在地址后面还应写单位名称，收件人应写第一署名申请人（代表人）姓名，若代表人为单位，应写明单位联系人姓名。</p>
        <p style='line-height:24.0pt;'>15．发明人、申请人、要求优先权声明的内容本表填写不下时，应使用专利局统一制定的附页续写。</p>
        <p style='line-height:24.0pt;'>16．申请人应当在收到受理通知书之后，最迟自申请日起两个月内缴纳申请费（含公布印刷费）和申请附加费。申请人要求优先权的，应当在缴纳中请费的同时缴纳优先权要求费。</p>
        <p style='line-height:24.0pt;'>17．一件专利申请的权利要求（包括独立权利要求和从属权利要求）数量超过10项的，从第11项权利要求起，每项权利甄求增收附加费20元；一件专利申请的说明书页数（包括附图页数）超过30页的，从第31页起，每页增收附加费15元，超述300页的，从第301页起，每页增收附加费30元。</p>
        <p style='line-height:24.0pt;'>18．各种专利费用可以通过邮局或银行汇付，也可以直接向专利局缴纳。银行汇付寄“专利局”开户银行：中国工商银硼京市海淀区北太平庄分理处，账号：144005―63；邮局汇付寄北京市海淀区蓟门桥西土城路六号国家知识产权局专利局费用管理处。汇款时应在汇单上写明费用名称、发明创造名称、申请人和申请号。切勿使用电汇。</p>
