        <p align=center >填表注意事项</p>
        <p >一、申请实用新型专利，应当提交实用新型专利请求书、权利要求书、说明书、说明书附图、摘要、摘要附图。申请文件应当一式两份。一份为原本，一份为复印件。</p>
        <p >二、本表应使用中文填写，表中文字应当打字或者印刷，字迹为黑色。外国人名、地名无统一译文时，应同时注明原文：</p>
        <p class=MsoBodyTextIndent>三、本表中方格<span
style='font-family:宋体'>□</span>供填表人选择使用，若有方格后所述内容的，应在方格内标上“√”号。</p>
        <p >四、本表中所有地址栏，国内地址应写明省（直辖市或自治区）、市、区、街道、门牌号码，及邮政编码。外国人地址应写明国别、州（市、县）。</p>
        <p >五、填表说明：</p>
        <p >1．本表第①、第②、第③、第⒃栏由专利局填写。</p>
        <p >2．本表第⑤、第⒀栏实用新型名称，应简单明确，不得超过25个字。</p>
        <p >3，本表第⑥栏设计人应当是对本实用新型的实质性特点作出创造性贡献的自然人。设计人有两个以上的应先自左向右、再自上而下依次填写，设计人排列次序无法律效力。</p>
        <p >4．本表第⑦栏申请人是单位的，应填写单位全称，并与公章中名称一致。申请人为个人，应填写本人真实姓名，不得写笔名等。申请人为多个，又未委托专利代理机构，应当指定一人为代表人，并填写在第一署名申请人位置。代表人为单位联系人姓名</p>
        <p >5．本表第⑧栏，应填写已在专利局注册的专利代理机构名称并注明注册代码。专利代理机构指定代理人不得超过两人，其中必须有一名专职代理入，并注明代理人证书号。</p>
        <p >6．申请人提出分案申请时，还应填写本表第⑨栏。</p>
        <p >7．申请人要求外国或者本国优先权的，还应填写本表第⑩栏。</p>
        <p >8．申请人要求不丧失新颖性宽限期的，还应填写本表第⑩栏。自申请日起2个月内提交证明文件。</p>
        <p >9．申请人应当按实际提交的文件名称、份数、页数正确填写本表第<span style='font-size:
12.0pt;font-family:宋体'>⑿</span>、第⒁、第⒂栏，专利局将按实收文件的数量逐项核实。</p>
        <p >10．申请人请求减缓申请费，必须在提交申请文件的同时提交费用减缓请求书及有关证明文件。</p>
        <p >11．申请人委托专利代理机构的，在本表第⒄栏应盖代理机构公章。申请人未委托专利机构的，本表第⒄栏应由全体申请人签字或盖章，申请人为单位的，应盖单位公章。请求书原本中的中请人或专利代理机构的签字或盖章不得复印。</p>
        <p >12．本表第⒅栏，填写收件人地址和姓名。委托专利代理机构的，收件人地址应写专利代理机构地址和名称，收件人写代理人姓名。未委托专利代理机构的，收件人地址应写第一署名申请人（代表人）的地址，若代表人为单位，在地址后面还应写单位名称，收件入应写第一署名申请人（代理人）姓名、若代表人为单位，应写明单位联系人姓名。</p>
        <p >13．设计人、申请人、要求优先声明的内容本表填写不予时，应使用专利局统一制定的附页续写。</p>
        <p >14．申请人应当在收到受理通知书之后，最迟自申请日起两个月内缴纳申请费和申请附加费。申请人要求优先权的，应当在缴纳申请费的同时缴纳优先权要求费。</p>
        <p >15．一件专利中请的权利要求（包括独立权利要求和从属株利要求）数量超过10项的，从第11项权利要求起，每项权利摹求增收附加费20元；一件专利申请的说明书页数（包括附图哭数）超过30页的，从第31页起，每页增收附加费15元，超述300页的，从第301页起，每页增收附加费30元。</p>
        <p >16．各种专利费用可以通过邮局或银行汇付，也可以直接向专利局缴纳。银行汇付寄专利局，开户银行：中国工商银行北京市海淀区太平庄分理处，账号：144005―63；邮局汇付寄北京确海淀区蓟门桥西土城路六号国家知识产权局专利局费用管理处。汇款时应在汇单上写明费用名称、发明创造名称、申请人和申请号。切勿使用电汇。</p>
