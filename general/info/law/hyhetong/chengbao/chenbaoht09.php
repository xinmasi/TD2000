　　第七条　乙方的违约责任<BR> 
　　１．乙方如不按合同规定的时间、规格、数量，完成对国家、集体的任务，每逾期一天，应按其价值的＿＿‰，向甲方偿付违约金。超过＿＿天仍未履行承包任务的，甲方除可以要求有关部门处理外，还可以提出解除合同，收回牲畜重新发包给他人。<BR> 
　　２．自合同期满，甲方提供的栏圈、工具等如有损坏或丢失，乙方应修复或赔偿；乙方饲养、牧养的牲畜如损坏国家、集体的山林、树木或集体、个人的庄稼等，应由乙方负责赔偿。<BR> 
　　３．乙方如将所承包饲养的生产用畜擅自出借、出租给他人使用，每出借或出租一头生产用畜一天，应向甲方偿付违约金＿＿元。<BR> 
