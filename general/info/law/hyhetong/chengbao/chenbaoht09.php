　　为了发展畜牧业，为城乡人民提供更多更好的食用畜，为发展农业生产提供生产畜，充分调动农民养好牲畜的积极性，增加集体和个人的收入，根据中央（８３）、（８４）１号文件的精神，经村民大会充分讨论和甲乙双方认真协商，特订立本合同，以供双方共同遵守。<BR> 
　　第一条　承包期限<BR> 
　　承包期共＿＿年，从＿＿＿＿年＿＿月＿＿日起至＿＿年＿＿月＿＿日止。<BR> 
　　第二条　承包牲畜数量<BR> 
　　甲方将＿＿（牲畜）发包给乙方饲养，共＿＿头（只）。其中，种母畜＿＿头（只），种公畜＿＿头（只），肉畜＿＿头（只）……。<BR> 
