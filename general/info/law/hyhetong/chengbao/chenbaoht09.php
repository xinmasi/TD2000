　　３．甲方必须合理分配国家扶持畜牧业的物资和贷款指标，不得截留国家奖励乙方完成派购牲畜任务的粮食、化肥等指标。<BR> 
　　第五条　乙方的义务<BR> 
　　１．乙方必须保持栏圈的清洁卫生，防止疾病传染。牲畜染病的医疗费用由乙方承担。<BR> 
　　２．乙方饲养、牧放的牲畜不得损害山林、树木、庄稼。<BR> 
　　３．乙方必须爱护栏圈。合同期满时，必须将甲方提供的栏圈、饲料地、牧场（如果有）、工具等如数交还甲方。栏圈内的粪肥＿＿％归甲方，＿＿％归乙方。<BR> 
　　４．乙方饲养的生产用畜，不得擅自出借、出租给他人使用。未经甲方同意，乙方不得将牲畜转包给他人饲养。<BR> 
　　第六条　甲方的违约责任<BR> 
　　１．甲方如不按时、按量向乙方提供栏圈、牧场（如果有）、饲料地、工具，应向乙方偿付违约金＿＿元，以致乙方不能进行正常饲养的，乙方可以提出解除合同。<BR> 
　　２．甲方如不按合同规定的品种、数量、时间向乙方提供草料和商品粮，应向乙方偿付所欠草料、商品粮价值＿＿％的违约金。如果因此造成乙方损失的，还应赔偿乙方的损失。<BR> 
