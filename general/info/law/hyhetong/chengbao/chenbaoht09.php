　　２．乙方按合同规定的品种、数量、规格交售给国家的＿＿（牲畜）的价金，甲乙双方按＿＿：＿＿分成，甲方得＿＿成，乙方得＿＿成。乙方超额完成国家交售任务和上交甲方任务以外的部分，归乙方所有。<BR> 
