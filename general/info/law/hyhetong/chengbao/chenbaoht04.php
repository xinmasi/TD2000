      <p> 　　订立合同双方：<BR> 
　　＿＿县＿＿乡＿＿村＿＿组，以下简称甲方：<BR> 
　　承包户主＿＿，以下简称乙方。<BR> 
　　为了落实联产承包责任制，充分调动农民的生产积极性，明确甲乙双方的权利义务，根据中央（８３）、（８４）一号文件精神，经村民大会讨论同意和甲乙双方充分协商，特订立本合同，以便双方共同遵守。<BR> 
　　第一条　承包土地的面积、地点、等级<BR> 
　　甲方将＿＿亩土地（田）发包给乙方，该土地位于＿＿，东至＿＿，西至＿，南至＿＿，北至＿＿。其中，一等地＿＿亩，二等地＿＿亩，三等地＿＿亩，四等地＿＿亩，……。承包土地的所有权属于集体，乙方只有土地使用权，不得买卖、出租、荒芜，不准转作宅基地和构筑其它建筑物。<BR> 
　　第二条　承包期限<BR> 
　　承包时间共＿＿年，从＿＿年＿＿月＿＿日起，至＿＿年＿＿月＿＿日止。<BR> 
　　第三条　乙方的权利义务<BR> 
　　１．乙方有权获得国家下拨用于发展农业生产的化肥、农药、良种、贷款指标和其它农业物资。<BR> 
　　２．乙方必须完成上级下达的种植计划，在完成种植计划的前提下，可以自己决定种植经营。<BR> 
　　３．乙方必须完成按土地亩数摊派的统购指标任务和上缴农业税的任务，每年向国家交售＿＿＿＿斤，＿＿＿＿，＿＿＿＿斤＿＿＿＿，＿＿＿＿斤＿＿＿＿，……。交售时间是＿＿＿＿。每年向国家缴纳农业税＿＿元，纳税时间是＿＿＿。乙方必须每年向甲方上交公积金＿＿元，公益金＿＿元，管理费＿＿元，上交时间为每年的＿＿月＿＿日以前。乙方必须完成甲方按承包土地亩数摊派的义务工日，或上交按义务工日折价的金额。<BR> 
　　（本条亦可采取列表形式，见后）<BR> 
　　４．乙方经甲方同意，可以自找对象转包土地，可以与转承包户协商，平价购买生活用粮。土地转包后，转包户必须承担原由乙方承担的所有统购任务和缴纳农业税任务，承担向甲方上交“三全”和完成义务工任务。<BR> 
　　５．乙方如遇生（不能违背计划生育规定）、死、嫁、娶，承包土地一般不变动但必须按规定调整所承担的义务。乙方如因劳动力减少无法继续承包土地又无人愿转承包时，可向甲方提出退回承包土地的一部或全部，由甲方另行统一安排。乙方对国家、集体承担的义务相应减少或免除。<BR> 
　　６．乙方有责任保护承包土地上的林木、排灌、通电等国家或集体设施。<BR> 
　　第四条　甲方的权利义务<BR> 
　　１．甲方有权监督乙方完成国家下达的种植计划、统购任务和纳税任务，有权敦促乙方完成对集体的义务。<BR> 
　　２．国家按计划分配用于农业生产的化肥、农药、良种、贷款指标和其它农用物资，甲方应及时按承包土地的数量和等级分配给乙方。<BR> 
　　３．甲方应定期公布集体财务帐目和公积金、公益金、管理费的使用情况，必须接受乙方的监督检查。<BR> 
　　第五条　违约责任<BR> 
　　１．乙方如不按时履行对国家和集体的事务，除应继续履行外，还应按迟延项目价值的＿＿％，向甲方偿付违约金。乙方如仍然拒不履行义务的，甲方除可请求有关部门处理外，有权收回乙方承包的土地。<BR> 
　　２．乙方如荒芜承包的土地，除应履行对国家、集体的一切义务外，甲方有权收回土地。乙方如果擅自出卖和租让承包的土地，甲方可宣布买卖与租让关系无效，并可收回土地。乙方如果在承包的土地上建房，甲方有权责令乙方拆除。乙方如果毁坏承包土地上的树木和集体设施，应照价（毁坏树木折价）赔偿。<BR> 
　　３．甲方如不及时拨给乙方国家分配的化肥、农药、良种、贷款指标和其它农用物资，应按其价值的＿＿％，向乙方偿付违约金，并应如数补发所欠物资。<BR> 
　　第六条　不可抗力<BR> 
　　乙方如因不可抗力的自然灾害，造成承包土地内农作物的减产或绝产，经调查核实后，可根据实际情况减少或免除乙方对国家或集体的义务。<BR> 
