　　第四条　违约责任<BR> 
　　１．甲方如不按合同规定向乙方提供合格的树种树秧，应按不合格部分树种树秧价值的＿＿％向乙方偿付违约金，并须相应减少乙方栽培树苗的数量。如果因此造成乙方损失的，还应赔偿乙方的损失。<BR> 
　　２．甲方如不按合同规定及时向乙方提供栽培树苗资金和化肥，应比照银行关于延期付款的规定，向乙方偿付违约金，并相应减少或免除乙方栽培树苗的义务。甲方如不能按时按量销售树苗，应根据合同规定的树苗售价，按乙方应该得到的提成比例，向乙方付给酬金。<BR> 
