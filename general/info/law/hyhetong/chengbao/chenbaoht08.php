　　１．在承包期内，乙方共为甲方栽培＿＿树苗＿＿株，树苗＿＿株，＿＿树苗＿＿株，……。其中，第一年出圃＿＿树苗＿＿株，＿＿树苗＿＿株，＿＿树苗＿＿株，……；第二年出圃……。<BR> 
　　２．甲方向乙方提供＿＿树种＿＿斤，＿＿树种＿＿斤、……，树种的出苗率应达到＿＿％以上；＿＿树＿＿秧＿＿株，＿＿树秧＿＿株，……，树秧长＿＿寸以上，不得有黑根、烂根的树秧。在承包期间，甲方应向乙方提供栽培树苗资金＿＿元，其中，第一年拨给＿＿元，第二年拨给＿＿元，……；甲方每年为乙方提供化肥＿＿斤。甲方向乙方拨款和提供化肥时间，均为每年＿＿月＿＿日以前。<BR> 
