　　甲方将＿＿树苗、＿＿树苗、＿＿树苗……的栽培发包给乙方承担，乙方根据承包任务划出＿＿亩＿＿分土地作为＿＿苗圃，地点在＿＿。<BR> 
　　第二条　承包期限<BR> 
　　承包期限为＿＿零＿＿月，从＿＿年＿＿月＿＿日起，到＿＿年＿＿月＿＿日止。<BR> 
