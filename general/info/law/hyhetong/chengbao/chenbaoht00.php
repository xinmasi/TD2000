　　第四条　甲方的权利义务<BR> 
　　１．甲方应将承包机动车辆完好地交给乙方，并随车交付下列工具给乙方使用：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　２．根据县石油公司分配给农村机动车辆的燃料指标，合理分配给乙方购买。<BR> 
　　３．甲方有权督促乙方履行合同规定的义务。<BR> 
　　第五条　乙方的权利义务<BR> 
　　１．按时按量完成承包费的交纳任务，负担车辆的维修费，配件、燃料、养路费、税金及车检费、保险费。<BR> 
　　２．爱护车辆，适时维修和保养车辆，保证车辆设备和车容完好，能够正常运转。<BR> 
　　３．遵守交通规则和机动车辆操作规程，严禁违章驾驶和酒后行车，保证行车安全，乙方应负担因乙方的责任造成的他人生命和财产损失。<BR> 
　　４．乙方在经营运输中不得违反国家的政策和法律。乙方应对与第三方之间的经济合同负独立责任。<BR> 
　　５．乙方完成每年上交甲方的承包费用后，超收部分归自己，甲方不得另派加收费项目。<BR> 
　　６．合同期满，乙方应将机动车辆检修后，交甲方检验接收，并随交修车工具。<BR> 
　　第六条　甲方的违约责任<BR> 
　　１．甲方交给乙方的机动车辆如有损坏，应承担一切修理费用，并赔偿乙方因此所受的损失。甲方提交的工具如与合同规定的种类、数量不符，应按所欠工具的价格补偿乙方。<BR> 
　　２．甲方如果克扣乙方应得的燃料指标，每克扣一公升，应向乙方偿付违约金＿＿元。<BR> 
　　第七条　乙方的违约责任<BR> 
　　１．如不按时按量完成向甲方上交承包费的任务，每拖欠一天，应按照银行关于拖延付费的规定向甲方偿付违约金。<BR> 
　　２．如不按时交付车辆修理费、配件、燃料、养路费、车检费及税金，乙方应承担有关单位的经济制裁。乙方如不按时向保险公司投保，甲方可代为投保，然后凭保险费收据要求乙方付款。<BR> 
　　３．乙方如因交通事故或意外事故造成车损人伤（亡），应从保险金额中支付赔偿。<BR> 
　　第八条　不可抗力<BR> 
　　如因不可抗力的原因造成乙方不能正常开展营运，经有关部门证实后，甲方应据实酌情减少乙方的承包费。<BR> 
