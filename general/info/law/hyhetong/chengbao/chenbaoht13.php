      <p> 　　<BR> 
　　订立合同双方：<BR> 
　　委托单位：＿＿县＿＿国营（或集体）林场，以下简称甲方；<BR> 
　　护林单位：＿＿县＿＿乡＿＿村＿＿村民，以下简称乙方。<BR> 
　　为了保护林业资源，减少山林火灾，防止盗伐林木，促进林木速生丰产，特订立本护林承包合同，以供双方遵守。<BR> 
　　第一条　护林面积及方位<BR> 
　　甲方将＿＿山的＿＿亩林木承包给乙方看护管理，其中，成林＿＿亩，中幼林＿＿亩。该片林木北起＿＿至＿＿，南起＿＿至＿＿，东起＿＿至＿＿，西起＿＿至＿＿。<BR> 
　　第二条　承包期限<BR> 
　　承包有效期为＿年，从＿＿年＿＿月＿＿日起至＿＿年＿＿月＿＿日止。<BR> 
　　第三条　报酬及收益分配<BR> 
　　１．现有成材木，如甲方在＿＿＿年以前砍伐的，甲方按每立方米＿＿元付给乙方护林费；＿＿年以后至承包期届满以前砍伐的，甲方按每立方米＿＿元付给乙方护林费。<BR> 
　　（护林费也可采取工资形式给付）<BR> 
　　２．中幼林间伐材（指胸径在＿＿公分以下的规格）和林木修枝材，由乙方采伐，在甲方派员参加下按＿＿∶＿＿比例分成，甲方得＿＿成，乙方得＿＿成。<BR> 
　　３．护林区内杂木些草归乙方受益，但不得伤害植被。<BR> 
　　４．乙方抓获盗伐林木者，按每盗伐一株＿＿元罚款。所罚款项，甲乙双方按＿＿∶＿＿分成，被盗伐成木归甲方所有。<BR> 
　　第四条　甲方义务<BR> 
　　１．甲方交给乙方的护林区必须四至清楚，山林没有所有权争议。<BR> 
　　２．甲方必须适时派技术人员对乙方进行技术指导。<BR> 
　　３．甲方砍伐成木验收后＿＿日内，必须向乙方支付护林费。<BR> 
　　第五条　乙方义务<BR> 
　　１．保护山林资源不受破坏，在护林区内，不准开荒种植作物，不准采石、采砂取土和埋葬坟墓，严禁盗伐林木，防止山林火灾。<BR> 
　　２．乙方必须服从甲方的护林指导，按甲方的要求进行补植、间伐、修枝、施肥、防治病虫害，开设防火沟路、封山育林及人工促进等。<BR> 
　　３．自本合同生效之日起＿＿日内，乙方应选派责任心强、身体健康、在群众中有一定威信的村民＿＿名组成护林队，订立护林公约。并经常对所属村民进行遵纪守法、爱护山林的思想教育。<BR> 
　　第六条　违约责任<BR> 
　　１．如在护林区内林木被盗伐乙方未抓获盗伐林木者，乙方除应原地补植外，成材木被盗伐一株，按其根径计算大小，罚乙方以国家木材价格的＿＿倍赔偿给甲方；中幼林木被盗伐一株，罚乙方赔偿甲方＿＿元。<BR> 
　　２．由于乙方失职引起山林火灾，乙方除应原地补植外，烧毁成木数，乙方应按国家木材价格赔偿甲方；烧毁中幼林木数，乙方除应即时补植外，每株赔偿甲方＿＿元。<BR> 
　　３．护林区内，如有人采石、取砂、取土或埋葬坟墓，罚乙方赔偿甲方＿＿元。<BR> 
　　４．甲方交给乙方管理的护林区如有所有权争议，造成乙方经济损失，由甲方负责赔偿。<BR> 
　　５．甲方不按期给付乙方护林费，逾期一日，应按该护林费的＿＿‰向乙方偿付违约金。<BR> 
　　６．甲方不按时派出技术人员对乙方进行指导，造成乙方的经济损失，由甲方负责赔偿。<BR> 
　　第七条　不可抗力<BR> 
　　如因不可抗力的自然灾害，造成乙方护林地带林木的损失，经调查证实后，不以乙方违约论。<BR> 
