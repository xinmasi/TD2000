      <p> 　　<BR> 
　　订立合同双方：<BR> 
　　发包方：＿＿＿＿＿＿<BR> 
　　承包方：＿＿＿＿＿＿<BR> 
　　为深化企业改革，提高劳动生产率，依据《全民所有制工业企业承包经营责任制暂行条例》，经＿＿人民政府批准，将＿＿厂实行招标承包经营。由＿＿管理局、＿＿财政局、＿＿税务局、＿＿劳动局和中国工商银行＿＿分行组成的＿＿工业发包委员会作为发包方（以下简称：发包方）。发包方通过规定的招标程序最终确定以＿＿为代表的（个人、合伙人、企、事业单位法人）投标者中标，作为本合同规定承包期限内＿＿厂的承包方（以下简称：承包方）。双方协商一致，签订本合同。<BR> 
　　<BR> 
　　第一章　总则<BR> 
　　<BR> 
　　第一条　招标承包经营是在坚持企业的社会主义全民所有制的基础上，按照所有权与经营权分离的原则，通过公开招标，选拔经营者，并以承包合同形式确定发包方、承包方、企业职工三者责、权、利关系，使企业真正成为自主经营、自负盈亏的社会主义商品生产者。<BR> 
　　第二条　承包经营期间，本厂必须坚持社会主义方向，贯彻执行党和国家的方针、政策、法规、法令。<BR> 
　　第三条　承包经营期间，本厂独立核算，照章纳税，自主经营，自负盈亏。所有制、原有行政隶属关系及财政、税收渠道不变。<BR> 
　　第四条　承包经营期间，本厂必须坚持生产＿＿产品经营方向，在此基础上可以实行多种经营。<BR> 
　　<BR> 
　　第二章　承包的期限、形式和主要指标<BR> 
　　<BR> 
　　第五条　本厂本次实行招标承包经营的期限为＿＿年，即从＿＿年＿＿月＿＿日起至＿＿年＿＿月＿＿日止。<BR> 
　　第六条　本厂本次招标承包经营的形式为：保上缴利润，保技术改造投资，职工工资总额与上缴利润挂钩的承包经营责任制。<BR> 
　　第七条　本厂本次招标承包经营的主要经济技术指标为上缴利润、技术改造投资、贷款归还、企业上等级目标。具体如下：<BR> 
　　第一款　以“包死基数、逐年递增、不足自补、超交分成”为原则，承包经营期间上缴利润总额为＿＿万元，其中＿＿年为＿＿万元，＿＿年为＿＿万元，＿＿年为＿＿万元，＿＿年为＿＿万元，……。<BR> 
　　第二款　承包经营期间，保技术改造项目完成，技术改造投资总额为＿＿万元。其中＿＿年为＿＿万元，＿＿年为＿＿万元，＿＿年为＿＿万元，＿＿年为＿＿万元。……<BR> 
　　第三款　承包经营期间，归还银行贷款总额为＿＿万元。其中＿＿年为＿＿万元，＿＿年为＿＿万元，＿＿年为＿＿，＿＿年为＿＿万元，……。<BR> 
　　第四款　本厂企业上等级目标为＿＿年底以前达到国家（市）级企业标准。<BR> 
　　<BR> 
　　第三章　承包方的权利与义务<BR> 
　　<BR> 
　　第一节　承包方的权利<BR> 
　　<BR> 
　　第八条　承包经营期间，承包个人或合伙承包的第一承包人（以下简称：第一承包人）为该厂法人代表，当然厂长，享受厂长负责制和本合同赋予厂长的全部权利，并承担其全部义务，如企、事业法人承包则由其指派的承包代理人（仅限一人）作为本厂的法人代表，行使厂长的职权，并代表承包方履行本合同。<BR> 
　　第九条　承包方在承包期间，对本厂的经营管理有如下权利：<BR> 
　　第一款　有权按国家规定自主聘任副厂长及副厂级行政干部，组成本厂的领导机构，并报主管局备案，承包期满或合同解除后，该领导机构即告解体。<BR> 
　　第二款　有权决定本厂的机构设置、人事任免和专业技术人员的聘任。<BR> 
　　第三款　有权依照有关规定奖惩、招用职工和辞退违纪职工。<BR> 
　　第四款　有权改革本厂内部分配制度，在上级核定的工资总额范围内有权自选工资形式，自定工资标准。<BR> 
　　第五款　有权根据实际需要购置新设备。<BR> 
　　第六款　在不违反本合同第四条的前提下，有权开发新产品。<BR> 
　　第十条　承包方有权依据本合同规定，取得其应得的合法收入。<BR> 
　　<BR> 
　　第二节　承包方的义务<BR> 
　　<BR> 
　　第十一条　承包方在承包期间应尽义务如下：<BR> 
　　第一款　对本厂的物质文明建设和精神文明建设负有全面责任。<BR> 
　　第二款　必须依照国家有关规定，按期如数缴纳应缴纳的各种税、费和统筹基金。<BR> 
　　第三款　必须按期完成本合同规定的各项经济技术指标和各项附加指标。<BR> 
　　第四款　在承包期间，应保证本厂厂房设备的完好，并按国家规定，分类按比例提取固定资产折旧基金和大修理基金，做到专款专用。<BR> 
　　第五款　应完成国家给本厂下达的各项指令性计划。<BR> 
　　第六款　应负责继续履行本厂与其它单位已签订的一切有效经济合同，并应承担本厂与其它单位一切原有合法债权、债务。<BR> 
　　第七款　自觉接受本厂党组织和本厂职工的监督，尊重和保护本厂职工的民主权利，定期向本厂职代会报告工作，听取职工的意见和建议。<BR> 
　　第八款　维护本厂职工的合法权益，改善职工的劳动条件，在不断提高经济效益的前提下，逐步提高本厂职工收入，不断改善职工福利待遇。<BR> 
　　第十二条　承包方须以一定的自有资金做抵押（或担保）。承包方的抵押金（或担保金）额为＿＿元。<BR> 
　　第十三条　承包方必须全面履行本合同中应由承包方履行的全部条款。<BR> 
　　<BR> 
　　第四章　发包方的权利与义务<BR> 
　　<BR> 
　　第十四条　发包方的权利如下：<BR> 
　　第一款　有权维护国家利益和本厂利益不受损害。<BR> 
　　第二款　有权监督本厂的产品经营方向。<BR> 
　　第三款　对本厂有财务监督权、审计权和产品质量检查权。<BR> 
　　第四款　　有权按国家法律、法规和本合同规定维护本厂职工的合法权益。<BR> 
　　第十五条　发包方的义务如下：<BR> 
　　第一款　不得违反本合同规定，干涉承包方的经营自主权。<BR> 
　　第二款　不得平调本厂资产。<BR> 
　　第三款　必须按本合同规定保障承包方的合法权益。<BR> 
　　第四款　必须全面履行本合同中应由发包方履行的全部条款。<BR> 
　　<BR> 
　　第五章　承包方的收入<BR> 
　　<BR> 
　　第十六条　承包经营者（系指承包个人、合伙承包的第一承包人、企、事业法人承包所指派的承包代理人，下同），在承包期间享受经营者收入，原有工资级别存入档案。本次承包结束后，承包经营者如不继续承包，按国家有关工资政策重新核定其工资级别。承包经营者在承包期间享受本厂职工的福利待遇，国家规定的各种补贴照发。<BR> 
　　第十七条　承包经营者在承包期间的收入按与本合同规定的各项经济技术指标和各项附加指标挂钩考核的原则确定。（附加指标系指：销售收入、产品质量、安全生产、劳动生产率、资金利税率。此项指标按＿＿主管局每年下达的计划执行）<BR> 
　　具体规定如下：<BR> 
　　第一款　承包经营者如完成本合同规定的各项年度经济技术指标和附加指标，其收入以本厂职工当年人均收入为基数，所得是基数的两倍，上缴利润每超当年指标１％，再累加相当基数＿＿％的收入，直至基数的四倍。<BR> 
　　第二款　发包方如认为承包经营者作出特殊贡献时，可另给予特殊奖励。<BR> 
　　第三款　承包经营者因病、事假实际工作不足九个月，只发给预支生活费。<BR> 
　　第四款　职工人均年收入的计算范围按＿＿劳字＿＿号文件规定执行。<BR> 
　　第十八条　企、事业单位承包，如超额完成本合同规定的各项年度经济技术指标和各项附加指标，其单位承包收入为超承包指标上缴利润财政返还地方部分的５０％，另外５０％归还本厂作为生产发展基础。　　<BR> 
　　第十九条　承方包的收入每年底结算一次，年底结算之前，承包经营者只能按每月＿＿元的标准预支生活费（不含国家规定的补贴）。年底结算以后，承包经营者的个人收入和企、事业法人收入，由发包方一次发给。<BR> 
　　<BR> 
　　第六章　合同的变更、解除或终止<BR> 
　　<BR> 
　　第二十条　本合同生效后即具有法律约束力，发包、承包双方均不得随意变更或解除。本合同需要变更或解除时，须经双方协商一致达成新的书面协议，在新的书面协议未达成之前，本合同依然有效。<BR> 
　　第二十一条　本合同履行期间，如国家有关政策与本合同签订时相比发包、承包任何一方利益受到重大影响，受影响的一方可以提出变更或解除本合同。<BR> 
　　第二十二条　承包方如经营管理不善或经营决策严重失误，给本厂造成重大损失或连续两年度未完成本合同规定的年度上缴利润指标，发包方有权解除合同，不负违约责任，并保留向承包方要求赔偿损失的权利。<BR> 
　　第二十三条　发包方如违反本合同规定，干扰承包方的经营管理活动，使承包方无法继续经营下去，或使承包方的合法收入得不到保障，承包方有权解除本合同并要求发包方承担违约责任。<BR> 
　　第二十四条　由于不可抗力的原因使本合同无法完全履行或无法履行时，须发包、承包双方协商一致，可以变更或解除本合同。<BR> 
　　第二十五条　本合同规定的承包期满，发包承包双方的权利、义务履行完毕后，本合同自行终止。<BR> 
　　第二十六条　本合同期满三十日以前，承包方应接受发包方派出的审计机构对其承包情况进行审核，确定无误后，双方代表在审计意见书上签字后，承包方方可离职。<BR> 
　　<BR> 
　　第七章　违约责任<BR> 
　　<BR> 
　　第二十七条　发包、承包双方应全面实际履行本合同，不履行或不完全履行的应负违约责任。<BR> 
　　第二十八条　承包方如未按期完成本合同规定的各项经济技术指标和各项年度附加指标，应承担违约责任和相应的经济处罚，具体规定如下：<BR> 
　　第一款　承包方如未完成本合同规定的年度上缴利润指标，按每降低１％扣减承包经营者个人预支生活费额的７％，直至扣减其档案工资额的５０％。<BR> 
　　第二款　承包方如未成本合同规定的其它年度经济技术指标和各项附加指标，则由发包方酌情相应扣减承包经营者个人当年收入总额，但最多不超过其当年收入总额的＿＿％。<BR> 
　　第三款　承包经营者在本合同解除或终止时如未完成上缴利润指标，除分别按本条一至三款扣减收入以外，还须以本人（含保人）抵押金抵补，直至抵清或全部抵尽为止。<BR> 
　　第四款　企、事业法人承包，如未完成年度上缴利润指标，须用本单位自有资金补足。<BR> 
　　第五款　企、事业法人承包，如完成年度上缴利润指标，但未完成其各项年度经济技术指标和附加指标，则由发包方酌情相应扣减承包单位超上缴利润分成收入，但最多不超过该收入的＿＿％。<BR> 
　　第六款　本条第一至五款所指扣减数额和抵补数额，即为承包方违反本条所应缴纳的违约金额数。此项违约金由承包方负责在违约的年度的次年三月底以前交付发包方，承包方对此项违约金如有滞纳行为，则须从滞纳之日起至补齐之日止，以天为单位按滞纳数额的万分之三向发包方支付滞纳金，此项滞纳金由承包方自理。<BR> 
　　第二十九条　发包方如违反本合同第二十一条的规定，应承担违约责任，赔偿违约给承包方造成的直接经济损失，并须向承包方支付其所造成的直接经济损失的百分之三的违约金。发包方的赔偿金和违约金应在违约年度的次年一月底以前交付承包方。发包方如有滞纳行为，则须从滞纳之日起至补齐之日止，以天为单位按滞纳数额的百分之三向承包方支付滞纳金，此项滞纳金由发包方自理。<BR> 
　　第三十条　发包、承包双方发生纠纷时，应按《全民所有制工业企业承包经营责任制暂行条例》执行。<BR> 
　　<BR> 
　　第八章　附则<BR> 
　　<BR> 
　　第三十一条　合伙承包的原定第一承包人（或企、事业法人承包的原指派承包经营者）如发生意外事故，无法继续履行本合同，则由承包合伙人（或企、事业法人）另行推选（或指派）第一承包人（或承包经营者），经发包方认定，继续履行本合同。<BR> 
　　第三十二条　本合同期满后，如本厂仍实行招标承包经营，且承包方履行本合同情况良好，承包方在同等条件下有优先再承包的权利。<BR> 
　　第三十三条　发包方的《招标书》、承包方的《投标书》、《答辩材料》（合伙人承包者《承包合伙人协议书》），均为本合同附件，上述文件材料如与本合同正文有矛盾之处，以本合同正文为准。<BR> 
　　第三十四条　本合同由发包方代表、承包方代表签字，并经＿＿公证处公证后生效。<BR> 
　　第三十五条　本合同正本＿＿份，发包方、承包方和＿＿公证处各执一份。<BR> 
　　本合同副本若干份，报企业承包指导委员会、发包方成员单位备案。本合同副本与正本具有同等法律效力。<BR> 
