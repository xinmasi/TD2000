　　、排他实施许可合同或者独占实施许可合同。<BR> 
　　普通实施许可合同是最常见的专利实施许可方式。指转让方许可受让方在一定<BR> 
　　范围内实施转让方所有或者持有的专利所订立的合同。<BR> 
　　排他实施许可合同，是转让方和受让方约定分享专利实施权的协议。转让方不<BR> 
　　得在已许可受让方实施专利的范围内，就该项专利与第三方订立专利实施许可合<BR> 
　　同，但可以保留实施该项专利的权利。<BR> 
　　独占实施许可合同，是转让方将专利实施权移交受让方所订立的协议。转让方<BR> 
　　不得在已许可受让方实施专利的范围内，就该项专利与第三方订立专利实施许可<BR> 
　　合同，自己也不得实施该项专利。<BR> 
　　２．在“实施许可的范围、期限和方式”一项，如果转让方许可受让方向第三<BR> 
　　方分售专利实施许可时，应约定分售的条件及有关事项。没有约定时，应视为受<BR> 
　　让方在整个的专利权存续期间实施专利，不受地区和方式的限制，但不得与第三<BR> 
　　方订立专利实施许可合同。<BR> 
　　３．“技术服务项目”一项，指转让方派出专业技术人员进行技术指导的项目<BR> 
　　、内容、工作期限和有关事项。<BR> 
