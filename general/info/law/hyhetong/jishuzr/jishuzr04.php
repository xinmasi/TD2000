　　２．说明<BR> 
　　<BR> 
　　技术引进合同是指在中华人民共和国境内的公司、企业、团体或个人（受方）为引进技术同中华人民共和国境外的公司、企业、团体或个人（供方）订立的明确相互权利义务关系的协议。根据《技术引进合同管理条例》及其实施细则的规定，签订技术引进合同应当注意的问题有：<BR> 
　　（１）要列明合同的基本情况，包括合同名称、当事人姓名（或名称）、住所（或法定地址）、国籍、签订时间和地点。合同名称要能够确切地反映出该合同的性质、特点和内容，便于人们对整个合同进行了解。当事人名称（或姓名）要与实际承担合同权利义务的单位或个人相一致，是我方与外方某一总公司的子公司签订时更应注意此问题。当事人法定地址（或住所）、签约的地点往往成为交付技术资料的地点，还可能成为合同发生争议进行仲裁或诉讼的仲裁地点或法律诉讼地点，因此应该明确、具体。<BR> 
　　（２）要界定合同中所使用的关键词语的含义。合同中所使用的关键词语，可用定义条款加以说明。技术引进合同的双方当事人所属国家不同、语言不同、法律不同，为防止合同执行过程中可能发生分歧，有必要对那些容易混淆的词语，如专有技术、技术资料、质量标准、净销售额、技术改进等用定义的方式加以明确和具体说明。<BR> 
　　（３）技术引进合同应贯彻平等互利的原则，未经审批机关特殊批准，合同不得有下列限制性条款：①要求受方接受同技术无关的附带条件，包括购买不需要的技术、技术服务、原材料、设备或产品；②限制受方自由选择从不同来源购买原材料、零部件或设备；③限制受方发展和改进所引进的技术；④限制受方从其他来源获得类似技术或与之竞争的同类技术；⑤双方交换改进技术的条件不对等；⑥限制受方利用引进的技术生产产品的数量，品种或销售价格；⑦不合理地限制受方的销售渠道或出口市场；⑧禁止受方在合同期满后，继续使用引进的技术；⑨要求受方为不使用的或失效的专利支付报酬或承担义务。<BR> 
　　（４）引进的技术要符合法律规定的条件，否则合同无效。关于引进的技术应当符合的条件，《技术引进合同管理条例》第３条明确规定，必须先进适用，并且应当符合下列一项以上的要求：①能发展和生产新产品；②能提高产品质量和性能，降低生产成本，节约能源或材料；③有利于充分利用本国的资源；④能扩大产品出口，增加外汇收入；⑤有利于环境保护；⑥有利于生产安全；⑦有利于改善经营管理；⑧有助于提高科学技术水平。当事人在签订合同时，应当注意是否符合这些要求。<BR> 
