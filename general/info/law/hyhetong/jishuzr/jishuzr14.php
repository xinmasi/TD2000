　　１．第三条１（１）款中所提的技术服务费的＿＿＿＿％应以＿＿＿＿币（即甲方的国币）支付给在＿＿＿＿的中国服务组，其余＿＿＿＿％应由甲方按银行当日公布的兑率折成美元（或英镑）。<BR> 
