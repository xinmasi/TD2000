　　第二条<BR> 
　　乙方人员在＿＿＿＿（甲方国名）期间，应服从甲方国家的法律，受甲方的管理规章制度约束。<BR> 
　　第三条<BR> 
　　１．（１）甲方负责向乙方人员支付乙方提出的每人每月＿＿＿＿（币别）的免税技术服务费。<BR> 
　　（２）上述免税技术服务费的＿＿＿＿％应以美元（或英镑）支付。<BR> 
　　（３）本条（１）款提及的技术服务费自乙方人员到达＿＿＿＿（甲方国名）之日起开始计算。<BR> 
　　２．（１）在甲方服务期间，乙方人员在工作或业余的全部时间内，应保证行为端正。<BR> 
