　　一、合同名称<BR> 
　　＿＿＿＿专有技术转让许可证合同<BR> 
　　二、签约时间与地点　本许可证合同于＿＿＿＿年＿＿＿＿月＿＿＿＿日在中国＿＿＿＿签订。<BR> 
　　三、合同当事人及法定地址　中华人民共和国中国技术进口总公司（以下简称受方）为一方，＿＿＿＿国＿＿＿＿技术公司（以下简称供方）为另一方，同意就下列条款签订本合同（以下称本合同）。<BR> 
　　（双方法定地址以及电报、电传号）<BR> 
　　四、鉴于条款　鉴于供方拥有设计、制造、安装＿＿＿＿产品的专有技术，供方是该项技术的合法所有者，愿将该技术转让给受方。<BR> 
　　五、合同所涉及的关键名词的定义　本合同所用下述用语的定义是：<BR> 
　　专有技术（Ｋｎｏｗ－Ｈｏｗ）系指为制造＿＿＿＿产品所需的，为供方所掌握的一切知识、经验和技能，包括技术资料和不能形成文字的各种经验和技能。<BR> 
　　技术资料系指上述专有技术的全部文字资料（或扼要指明资料的范围）。<BR> 
　　合同产品系指受方根据本合同使用供方所转让的专有技术制造和销售的产品。<BR> 
　　净销售价系指销售合同产品的发票金额扣除产品税、交易折扣以及因退货、拒收所引起的退款剩余的价款。<BR> 
　　合同期限系指本合同生效日起算至第十年为止的期限。<BR> 
　　六、合同范围与内容<BR> 
　　１．供方同意受方在中国设计、制造、使用和销售合同产品的专用技术（或专利技术）。在该地区受方享有利用该技术独占性制造产品和销售产品的权利（或这是一项非独占的许可证）。<BR> 
　　２．供方负责向受方提供＿＿＿＿技术的研究报告、设计、计算、产品图纸、制造工艺、质量控制、试验、安装、调试、运行、维修等一切技术数据、资料（详见附件×）和经验，以便受方能实施制造产品（产品的型号、规格、技术参数详见附件×）。<BR> 
　　３．供方负责自费派遣技术人员赴受方进行技术指导和参加＿＿＿＿的性能考核（详见附件×）。<BR> 
　　４．供方负责接受受方有关人员自费赴供方进行培训，使受方人员能掌握合同规定的上述技术（详见附件×）。<BR> 
　　５．在合同有效期限内，受方在合同产品上有权使用属于供方所有的＿＿＿＿商标（或牌号）。<BR> 
　　６．供方有责任（或同意）以最优惠的价格向受方提供为制造合同产品所必需的设备、测试仪器、原材料及零部件（或供方有责任帮助受方为实施制造合同产品所需的配套件，从第三方取得有关技术许可证，或者与第三方进行合作生产）。<BR> 
　　七、价格或许可证使用费<BR> 
　　１．根据本合同规定，供方向受方提供的技术和技术服务等，受方应向供方支付的合同总价为＿＿＿＿万美元，其中：技术使用费＿＿＿＿元；资料费＿＿＿＿元；技术服务费＿＿＿＿元。……上述价格为固定价格。<BR> 
　　２．受方有义务对根据许可证转让的技术支付下列费用；<BR> 
　　（１）入门费＿＿＿＿美元。<BR> 
　　（２）受方在合同有效期内应向供方支付常年提成费，其提成率为合同产品净销售价的３％。<BR> 
　　八、技术资料的交付<BR> 
　　１．供方应按本合同附件＿＿＿＿的规定向受方提供技术资料。<BR> 
　　２．供方用空运把技术资料送达中国＿＿＿＿机场。该机场在收到技术资料而在空运提单上加盖的印戳日期为技术资料的实际交付日期。受方将带有到达印戳日期的空运提单影印本一份寄送供方。<BR> 
　　３．在技术资料发运后的２４小时（或４８小时）内，供方应将合同号、空运提单号与日期、资料项号、件数、重量、航班号用电报或电传通知受方，并将空运提单正本一份、副本两份和技术资料装箱清单三份航空邮寄给受方。<BR> 
　　４．如受方收到技术资料后发现不符本合同附件×的规定，包括在空运中丢失或损坏，应在３０天内通知供方，说明所缺或损坏的资料，供方应在收到通知后立即（或３０天内）免费补寄或重寄给受方。如果受方在收到技术资料后６０天内没有提出资料不足或损坏的书面通知，即视为受方对技术资料验收。<BR> 
　　５．技术资料使用文字为英文（或其它文字），计量单位为米制，技术资料所适用的标准为＿＿＿＿工业标准。<BR> 
　　６．技术资料的包装要适应长途运输与搬运、防雨、防潮，每箱上应以英文标明下述内容：合同号（许可证合同编号ＬＩＳ８５００１）、收货人（中国技术进出口公司＿＿＿＿分公司），目的地（中国＿＿＿＿市机场）、毛重（＿＿＿＿公斤）、箱号（或件号）以及运输标志等。<BR> 
　　九、交换改进技术及对技术资料的修改<BR> 
　　１．供方在合同有效期内改进和发展的技术资料，应免费提供受方。受方改进和发展的技术也应按对等原则提供给供方，但改进和发展的技术所有权属于受方，对方不得去申请专利或转让给第三方。双方交换技术资料，均不附加任何限制。<BR> 
　　２．供方提供的技术资料，如有不适合于受方生产条件的，供方有责任协助受方修改技术资料，并加以确认。<BR> 
　　十、性能考核和验收<BR> 
　　１．在合同产品首批生产后，由双方根据本合同附件×的规定，共同进行产品性能考核。<BR> 
　　２．经考核合同产品的性能符合本合同技术文件规定的技术指标，即通过验收，双方签署合同产品性能考核合格证明书一式四份。每方各执二份。<BR> 
　　３．如经考核，合同产品性能不符本合同技术文件规定的技术指标时，双方应共同研究，分析原因，澄清责任。如责任在供方，供方应自负费用，采取措施，消除缺陷，缺陷消除后进行第二次考核。如第二次考核后仍不合格，供方应继续采取措施，消除缺陷，并进行第三次试验。如第三次考核有不合格时，受方有权终止本合同。如果考核不合格责任在于受方，受方在供方协助下，采取措施，消除缺陷，并进行第二次或第三次试验。如第三次考核仍不合格时，则由双方协商如何再执行合同的问题。供方协助受方消除缺陷派遣技术人员的交通和食宿费用由受方负担。<BR> 
　　十一、保证与索赔<BR> 
　　１．供方保证按本合同附件×的规定提供给受方的技术资料是供方掌握的最新资料，并保证向受方及时提供任何发展和改进的技术资料。<BR> 
　　２．供方保证所提供的资料是正确的、完整的、清晰的和可靠的，与供方生产使用的技术资料完全一样。<BR> 
　　３．供方应按本合同规定日期交付技术资料，如果未按时交付资料，按下述比例向受方支付罚款。<BR> 
　　（１）拖延一至四周，每周为本合同总价格（或技术使用费）的０．５％；<BR> 
　　（２）拖延五至八周，为本合同总价格（或技术使用费）的１％；<BR> 
　　（３）拖延八周以上，为本合同总价格（或技术使用费）的１．５％。<BR> 
　　４．供方向受方支付罚款并不解除供方继续交付技术资料的义务。<BR> 
　　５．如果供方迟交技术资料超过六个月，受方有权终止本合同，此时供方应将受方已支付的金额，并按年利＿＿＿＿％的利息，一并退还给受方。<BR> 
　　６．如果合同产品考核验收三次仍不合格时，受方除有权终止本合同外，受方还有权收回已付给供方的全部金额，并加年利＿＿＿＿％的利息。如果产品只有部分性能指标达不到合同的规定时，受方减少支付合同总价的＿＿＿＿％。<BR> 
　　７．供方保证合同转让中的一切权利，包括制造、使用、销售以及其它有关技术的合法性，并保证不受第三方的指控。如发生第三方指控侵权，供方应负责与第三方交涉，并承担法律上和经济上的一切责任。<BR> 
　　十二、税收<BR> 
　　１．凡因执行本合同有关的一切税款，在受方国内的由受方负担。在受方以外的则均由供方负担。<BR> 
　　２．供方因履行本合同而在中国境内取得的许可证使用费的收入，必须按中国税法（或按＿＿＿＿国与＿＿＿＿国的税收协定）纳税。<BR> 
　　十三、仲裁<BR> 
　　１．因执行本合同所发生的或与本合同有关的一切争议，应通过双方友好协商解决。如协商仍不能解决时，应提交仲裁解决。<BR> 
　　２．仲裁地点在瑞典的斯德哥尔摩，由斯德哥尔摩商会仲裁院根据该院的仲裁规则进行仲裁（或仲裁在被诉方的国家进行。如在中国由中国国际经济贸易仲裁委员会按该会仲裁程序暂行规则进行仲裁。如在＿＿＿＿国则由＿＿＿＿仲裁协会按该会的仲裁规则进行仲裁）。<BR> 
　　３．仲裁裁决是终局的，对双方均有约束力。<BR> 
　　４．仲裁费用，除仲裁另有规定外，由败诉方负担。<BR> 
　　５．在仲裁过程中，除进行仲裁的部分外，本合同的其它部分应继续执行。<BR> 
　　十四、不可抗力<BR> 
　　１．本合同的任何一方，由于战争、严重水灾、水灾、台风以及地震等不可抗力的事故，致使本合同不能执行时，可延迟履行本合同，延迟的期限相当于事故影响的期限。<BR> 
　　２．发生不可抗力事故后，受不可抗力影响的一方，应在十五天内以航空挂号信将有关当局出具的证明文件提交给另一方确认。<BR> 
　　３．如不可抗力事故持续一百二十天以上，双方应通过友好协商解决继续执行本合同的问题。<BR> 
　　十五、合同的生效、期限、终止及其它<BR> 
　　１．本合同由双方代表于＿＿＿＿年＿＿＿＿月＿＿＿＿日在＿＿＿＿市签字。签字后由各方分别向本国政府有关当局申请批准，争取在六十天内获得批准，以最后批准的日期为合同生效日期。如签字后六个月仍得不到批准，双方有权撤销本合同。<BR> 
　　２．本合同从生效日起＿＿＿＿年内有效，有效期满后合同自动失效。如合同期满前三个月内，经一方提出，另一方同意后可延长＿＿＿＿年。<BR> 
　　３．本合同期满时，债务人对债权人未了债务应继续予以支付。<BR> 
　　４．本合同条款的任何修改、补充，须经双方协商同意后授权代表签署书面文件，作为本合同的组成部分。<BR> 
　　５．本合同附件一至附件＿＿＿＿，为本合同的组成部分，与合同正文具有同等效力。<BR> 
　　６．本合同用中文和＿＿＿＿文两种文字写成，正本四份，具有同等效力，双方各执一式两份。<BR> 
