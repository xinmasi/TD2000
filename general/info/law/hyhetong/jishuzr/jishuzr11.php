　　<BR> 
　　１３．１　本合同由双方代表于＿＿＿＿签字。由各方分别向本国政府当局申请批准，以最后一方的批准日期为本合同生效日期。双方应尽最大努力在六十天内获得批准，用电传通知对方，并用信件确认。<BR> 
　　本合同自签字之日起六个月仍不能生效，双方有权取消合同。<BR> 
　　１３．２　本合同有效期从合同生效日算起共＿＿＿＿年，有效期满后本合同自动失效。<BR> 
　　１３．３　本合同期满时，双方发生的未了债权和债务，不受合同期满的影响，债务人应对债权人继续偿付未了债务。<BR> 
　　１３．４　本合同用英文和中文两种文字写成，具有同等效力。双方各执英文和中文本各一式二份。（日本国可以日文和中文两种文字）<BR> 
　　１３．５　本合同附件一至附件八，为本合同不可分割的组成部分，与合同正文具有同等效力。<BR> 
　　１３．６　对本合同条款的任何变更、修改或增减　经双方协商同意后授权代表签署书面文件，作为本合同的组成部分，具有同等效力。<BR> 
　　１３．７　在本合同有效期内，双方通讯以英文或中文进行，正式通知应以书面形式，用挂号信邮寄，一式二份。<BR> 
