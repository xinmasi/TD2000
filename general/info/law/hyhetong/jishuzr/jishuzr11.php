　　第二章　定义<BR> 
　　<BR> 
　　２．１　“合同产品”指本合同附件一中所列的全部产品。<BR> 
　　２．２　“蓝图”指乙方制造合同产品目前所使用的总图、制造图样、材料规范及零件目录等的复制件。<BR> 
　　２．３　“技术资料”是指为生产合同产品必须具有的乙方目前正用于生产合同产品的全部专用技术和其它有关设计图纸、技术文件等。<BR> 
　　２．４　“标准”指为制造合同产品向甲方提供的技术资料中，由乙方采用和制定的标准。<BR> 
　　２．５　“入门费”指由于乙方根据本合同第一章１．２条、１．３条、１．４条、１．６条、１．７条规定的内容以技术资料转让的形式向甲方提供合同产品的设计和制造技术，甲方向乙方支付的费用。<BR> 
　　２．６　“提成费”指在本合同有效期内，由于乙方所给予甲方连续的技术咨询和援助，以及甲方在合同有效期内连续使用乙方的商标和专有技术，甲方向乙方支付的费用。<BR> 
　　２．７　“合同有效”期指本合同开始生效的时间到本合同１４．３条规定的本合同终止时间的期间。<BR> 
