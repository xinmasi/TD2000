　　第三章　价格<BR> 
　　<BR> 
　　３．１　按本合同第一章规定的内容，甲方向乙方支付的合同费用规定如下：<BR> 
　　３．１．１　入门费为＿＿＿＿美元（大写：＿＿＿＿美元）这是指本合同产品有关的资料转让费和技术培训费，包括技术资料在交付前的一切费用。入门费为固定价格。<BR> 
　　３．１．２　合同产品考核验收合格后，甲方每销售一台合同产品的提成费为基价的＿＿＿＿％，甲方向乙方购买的零件不计入提成费。<BR> 
