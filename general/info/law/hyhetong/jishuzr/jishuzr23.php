　　第九章　最终验收检验测试<BR> 
　　<BR> 
　　９．１　技术文件和合同设备的最终验收<BR> 
　　９．１．１　为证明根据本合同附件二提供的专有技术和技术文件是完整的、正确的，证明转让方许可的合同材料是能组装、测试的，转让方应自费派遣其技术人员去合同工厂参加相应阶段合同设备的验收测试。被转让方的技术人员也应参加这些验收测试。<BR> 
　　９．１．２　双方应尽其最大努力完成验收测试。<BR> 
　　９．１．３　验收测试时，若合同设备达到了验收标准，则双方应签署四份合同设备验收合格证书，每方各执两份。<BR> 
　　９．１．４　若合同设备未达到验收标准，双方应共同分析验收测试失败的原因，澄清验收测试失败的责任。<BR> 
　　１．若验收测试失败的责任在于转让方负责，则转让方应在第一次验收测试后４周内，再派其技术人员进行第二次验收测试，并承担其在第二次验收测试期间所发生的一切费用。<BR> 
　　２．若验收测试失败的责任在于被转让方负责，则被转让方应为转让方技术人员提供往返机票并承担第二次验收测试期间其在华的食宿费用和当地交通费。<BR> 
　　第二次验收测试时，若合同设备达到了验收标准，则双方应签署四份合同设备验收合格证书，每方各执两份。<BR> 
　　３．若因转让方的责任第二次验收测试时，合同设备未达到验收标准，转让方应采取各种措施并在第三次验收测试后六周内，再派其技术人员进行第三次验收测试，并承担其在第三次验收期间所发生的一切费用。<BR> 
　　４．若第二次验收测试失败的责任在被转让方，则被转让方应为转让方技术人员提供往返机票，并承担第三次验收测试期间他们在华的食宿费和当地交通费。<BR> 
　　第三次验收测试时，若合同设备达到了验收标准，则双方应签署四份合同设备的验收合格证，每方各执两份。<BR> 
　　９．１．５　第三次验收测试时，若合同设备未达到验收标准，双方应共同分析原因并继续验收测试。若二个月后，还达不到验收标准，转让方将对有缺陷的合同设备进行修理，或更换以符合本合同规定的技术指标、质量和性能标准的新设备。转让方与被转让方应在合同工厂座谈讨论解决此类问题的方法。<BR> 
　　９．２　合同材料的最终验收<BR> 
　　９．２．１　合同材料每次运抵合同工厂后，被转让方人员应立即对其进行检验。<BR> 
