　　评述<BR> 
　　<BR> 
　　一个“计算机系统”是设备（硬件）与程序（软件）的组合，程序是指示或指挥硬件执行以下操作：（１）接收信息；（２）根据软件所提供的特殊指令处理输入的信息；（３）根据操作者所要求的形式和内容产生输出信息。<BR> 
　　计算机软件程序主要有两类。第一类称为“系统”程序或“操作”程序，它用于控制计算机系统的一般而不专门涉及操作者的特殊使用目的。第二类称为“应用”程序，它由操作者选择并为系统执行特殊功能要求提供所需的命令。<BR> 
　　中央处理机（ＣＰＵ）是系统操作的中心，它解释所有输入的指令，并依相应顺序指挥硬件操作。<BR> 
　　“程序设计”是程序开发的一种过程，它要写出每条指令的类型及其执行次序。这样，当计算机系统按照程序所规定的顺序执行这些指令时，它就会产生所要的结果。程序的编写（编码）是由经过训练从事这种智力劳动的“程序员”来完成。原始书写的程序叫“源码”。源码必须转换（编译）成“机器可读”的形式，即计算机能够认识的并能够执行的形式，这后一种形式叫“目标码”。<BR> 
　　在ＣＰＵ中，数据位是以组的形式出现，称之为“字节”。有关其分组及其“地址”编码，以及系统中数据传送的速率的各种标准，都随不同厂家所制造的不同型号的ＣＰＵ而出版，这些厂家或ＣＰＵ的供应商一般都提供控制ＣＰＵ的操作程序。但是近几年，大量增加的计算机软件许可主要是应用软件许可，硬件厂商虽然也可以供应某些这类程序，但由于可应用领域的广阔，所以提供这种程序的供应源就大量增加。<BR> 
　　本协议书实例就是一种应用程序许可。<BR> 
　　计算机软件的属性是它很容易被复制和未经授权的行销及使用，而价钱又很便宜。因此，从许可方的观点来看，计算机程序许可的“核心”是保护程序免受这种未经授权行为的侵害。在软件许可的早期，唯一可用来保护软件的是保密。而今天，在许多国家，甚至可以说是大多数国家，计算机程序也可以获得版权保护。即使可利用版权保护，保密的方法对程序保护仍然是很重要的。<BR> 
　　被许可方主要感兴趣的是获得能执行所需任务的软件。在许多情况下，被许可方的商务主要是依靠该许可软件能正确地不中断运作。与被许可方所指望的这种性能相比，保证书虽然有用，但其重要性毕竟稍逊一筹。因此，正如大多数谨慎的被许可人那样，合同实例中的被许可方也得到了在试用期内使用该许可软件的权利，以便实示观察它是否能胜任工作，如果达不到要求，则可以终止此项许可（第４－２款）。<BR> 
　　任何被许可方也都想获得许可方帮助的保证，这种帮助是必须的，它确保该软件在被许可方的环境中功能正常，工作运转顺利，如果需要的话，还能提供帮助以更换该软件。大多数程序开发者都有广阔的市场潜力，他们可向被许可方提供这种安装及维护服务。维护是根据另外的合同来提供。在本合同实例中，维护费是按许可费的百分比来提取。流行程序的许可方通常是不断地改进这些程序，并向其被许可方提供这种改进版本。这种改进版本是根据维护合同提供的。<BR> 
　　此外，被许可方还可能对自行修改该许可软件感兴趣，或者是把该软件与其他软件合并感兴趣。应用程序的大多数被许可方改正或修改程序的能力都比程序开发者弱。如果被许可方无法接触到源码，那情况更是这样。原来在编写程序时，程序的改正或修改都是以源码的形式进行的。从根本上说，在不知道源码的情况下是可以设法将目标码再转换成源码，但要碰到一些麻烦。因此，对修改程序或将它与其他程序合并使用来说，接触源码是很重要的，对维护、改进和以其他方式改变许可软件属性及使用感兴趣或有能力的被许可方因而也就极力想得到源码。一方面，拒绝提供源码也是一种方法，这样，许可方就可以对其许可程序实行保密。下面的作法也不少见，如果提供源码不包括在许可之列，则在合同中就要包括某些特殊的防范措施，以使许可方无论由于何种原因在改正及修改许可程序方面未能提供所需的帮助时，被许可方能够使用源码。例如，源码可以被寄存在信托公司或寄存代理事务所，授权它在许可合同规定的某些条件下向被许可方提供利用。<BR> 
　　在本许可实例中，被许可方获得源码是作为许可内容的一部分（第３－１款）。这里要注意的是，在第７条中，被许可方还获得对许可软件进行修正和提高（改进）的权利，并对这种改进拥有所有权。获取这些权利是要与许可方进行一番艰苦地讨价还价，它取决于特殊的谈判环境。很多的程序许可方都想自己保持有修改其程序的权利，以便更好地对程序加以控制。同时还要注意，对被许可方利用这种改进权利的限制是要遵守许可方关于保密信息的要求。这种限制在大多数软件许可上都可以见到。<BR> 
　　即使被许可方有了源码，而且通过一次总付的方式也获得使用许可软件并对其进行改进的权利，但为稳妥起见，它还是希望能获得许可方的维护服务，根据被许可方的选择，这种维护服务是可终止的。<BR> 
　　如同大多数应用程序的许可一样，在本许可中，许可软件也只限制在一台指定的ＣＰＵ上使用。这种限制用于保护该软件的许可价值。更为重要的，它为许可方保留这样的机会，即它可以向同一个被许可方再次发放在其他ＣＰＵ上的使用许可。由于对使用地点及被许可方人员接触该软件数量加以限制，这也有利于限制未经授权而拷贝该软件的机会。<BR> 
　　正如其他许可证一样，价钱总是一个很重要的因素。在合同实例中，提成费采用随许可证的发放而一次总付方式。这种支付方法几乎都是用于价钱不太高的程序许可。对于复杂的和价格高的程序，当它作为协议书的标的时，常常是采用提成费的方式。只要许可方使用该许可程序，就必须支付提成费。这种支付方式的时间比较长，但它可能被那些小单位被许可方所接受，它不可能或不愿意随许可证的发放而一下子支付这么多的钱。采用提成费许可的方式还可以有这样的做法，当所规定的提成费限额全部支付完时，该使用许可就已付清。<BR> 
