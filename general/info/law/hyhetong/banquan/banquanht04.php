　　第三条　许可方提供的服务<BR> 
　　３－１　许可程序和许可资料<BR> 
　　许可方将以源码和目标码二种形式向被许可方提供各一份许可程序，并提供不少于二份的许可资料。<BR> 
　　３－２　安装、初级培训及调整<BR> 
　　许可方应根据附件二向被许可方提供安装和初级培训。如果需要的话还应提供初始调整服务，附件二附属于本协议书并作为其一部分。为此目的，被许可方应在正常上班时间向许可方提供使用指定ＣＰＵ的合理机时。<BR> 
　　３－３　附加培训<BR> 
　　除了附件二第Ｂ节规定的最大培训时间外，如果被许可方以书面形式向许可方提出附加的培训要求，许可方应按被许可方的要求尽最大努力及时地提供这种培训服务，培训地点可设在指定ＣＰＵ的所在地或双方可接受的其他适当地方。<BR> 
　　３－４　交付<BR> 
　　本协议生效后，双方应就上述所规定的提供许可软件和许可资料以及许可方提供的各种服务进行协商，并作出双方都一致同意的安排。<BR> 
　　３－５　其他顾问性服务<BR> 
　　３－５－１　除了根据上述第３－２款，第３－３款及附件二Ａ－１段所规定的培训服务外，根据被许可方的书面要求，许可方还应该向被许可方提供有关许可软件的顾问性服务。<BR> 
　　３－５－２　在开始提供任何附加服务之前，许可方应与被许可方共同制定一个满足许可软件要求和（或）其他特殊服务要求的附加的许可软件调整清单。<BR> 
　　３－５－３　被许可方应指明这些附加服务的优先次序及何时要利用这些服务。<BR> 
