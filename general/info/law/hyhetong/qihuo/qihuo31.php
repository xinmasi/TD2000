        <p class=MsoBodyTextIndent>三、验证笔录是发行人向中国证券监督管理委员会（以下简称证监会）申请公开发行股票所必需具备的法定文件之一。</p>
        <p class=MsoBodyTextIndent>四、验证笔录是主承销商律师（以下简称律师）对招股说明书所述内容进行验证的记录。制作验证笔录的目的在于保证招股说明书的真实性、准确性。</p>
        <p class=MsoBodyTextIndent>五、验证笔录的结构由重要信息、依据或确认、确认人三部分构成。</p>
