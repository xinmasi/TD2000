        <p class=MsoBodyTextIndent>一、主要资料</p>
        <p class=MsoBodyTextIndent>本节由律师对招股说明书主要资料部分进行验证，确认人为发行人的董事（筹委会成员）及相关中介机构。</p>
        <p class=MsoBodyTextIndent>二、绪言</p>
        <p class=MsoBodyTextIndent>本节由律师对绪言中所载重要信息进行验证，确认人为发行人的董事（筹委会成员）及主承销商。</p>
        <p class=MsoBodyTextIndent>三、发售新股的有关当事人</p>
        <p class=MsoBodyTextIndent>本节由律师对与本次发行、上市有关当事人的真实性、合法性进行验证，确认人为发行人的董事（筹委会成员）及主承销商。</p>
        <p class=MsoBodyTextIndent>四、风险因素及对策</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人的发展前景、产品销售、市场份额、财务状况、经营效益等方面的风险与对策所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）及主承销商，若涉及有关财务和法律问题的，应同时向财务审计机构及发行人律师进行验证。</p>
        <p class=MsoBodyTextIndent>五、募集资金运用</p>
        <p class=MsoBodyTextIndent>本节由律师对本次募股资金的运用计划所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商及财务审计机构。若涉及有关评估和法律问题的，应同时向资产评估机构及发行人律师进行验证。</p>
        <p class=MsoBodyTextIndent>六、股利分配政策</p>
        <p class=MsoBodyTextIndent>本节由律师对股利分配的各项政策所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商及发行人律师。</p>
        <p class=MsoBodyTextIndent>七、验资报告</p>
        <p class=MsoBodyTextIndent>本节由律师对验资的真实性、合法性进行验证，确认人为会计师事务所、发行人的董事（筹委会成员）及主承销商。</p>
        <p class=MsoBodyTextIndent>八、承销</p>
        <p class=MsoBodyTextIndent>本节由律师对本次承销和发行有关的全部重要信息进行验证，确认人为主承销商、发行人的董事（筹委会成员）及相关中介机构。</p>
        <p class=MsoBodyTextIndent>九、发行人情况</p>
        <p class=MsoBodyTextIndent>本节由律师对与发行人有关的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商及发行人律师。若涉及有关财务问题的，应同时向财务审计机构进行验证。</p>
        <p class=MsoBodyTextIndent>十、发行人公司章程、公司章程草案的摘录</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人公司章程或公司章程草案摘录的准确性进行验证，确认人为发行人的董事（筹委会成员）、主承销商及发行人律师。</p>
        <p class=MsoBodyTextIndent>十一、董事、监事、高级管理人员及重要职员</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人的董事、监事、高级管理人员及重要职员的简单情况、任职膏格以及是否存在或是可能产生双重任职问题进行验证，确认人为发行人律师、发行人的董事（筹委会成员）及主承销商。</p>
        <p class=MsoBodyTextIndent>十二、经营业绩</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人过去至少3<span style='font-family:宋体;
"Times New Roman"'>年中的经营业绩问题进行验证，确认人为发行人的董事（筹委会成员）、主承销商及财务审计机构。</span></p>
        <p class=MsoBodyTextIndent>十三、股本</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人的股本结构及演变过程等方面所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商、财务审计机构及发行人律师。</p>
        <p class=MsoBodyTextIndent>十四、债项</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人在特定日期的主要借款情况（包括银行贷款、公司债、对内部人员和关联企业负债以及或有负债、主要合同承诺）进行验证，确认人为发行人董事（筹委会成员）、主承销商及财务审计机构。</p>
        <p class=MsoBodyTextIndent>十五、主要固定资产</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人拥有或占有的主要固定资产的种类、原值、用途、折旧情况和所在地等重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商、资产评估机构或财务审计机构。若涉及非经营性资产剥离问题的，应同时向发行人律师进行验证。</p>
        <p class=MsoBodyTextIndent>十六、财务会计资料</p>
        <p class=MsoBodyTextIndent>本节由律师对招股说明书中全文引用的审计报告、发行人编制的财务报表及附注、其他财务资料的真实性、准确性进行验证，确认人为发行人的董事（筹委会成员）、主承销商及财务审计机构。</p>
        <p class=MsoBodyTextIndent>十七、资产评估</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人进行资产评估的真实性、合规性进行验证，确认人为发行人董事（筹委会成员）、主承销商及资产评估机构。</p>
        <p class=MsoBodyTextIndent>十八、盈利预测</p>
        <p class=MsoBodyTextIndent>本节由律师对盈利预测所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）、主承销商及财务审计机构。</p>
        <p class=MsoBodyTextIndent>十九、公司发展规划</p>
        <p class=MsoBodyTextIndent>本节由律师对公司发展规划中所涉及的全部重要信息进行验证，确认人为发行人的董事（筹委会成员）及主承销商。</p>
        <p class=MsoBodyTextIndent>二十、重要合同及重大诉讼事项</p>
        <p class=MsoBodyTextIndent>本节由律师对发行人已签订的重要合同和作为一方当事人的尚未执行完早的重大诉讼事项进行验证，确认人为发行的董事（筹委会成员）、主承销商及发行人律师。</p>
        <p class=MsoBodyTextIndent>二十一、其他重要事项</p>
        <p class=MsoBodyTextIndent>本节由律师对招股说明书披露的其他重要事项进行验证，确认人为发行人的董事（筹委会成员）、主承销商及其他相关中介机构。</p>
        <p class=MsoBodyTextIndent>二十二、确认人签字、盖章</p>
        <p class=MsoBodyTextIndent>律师应要求发行人的董事（筹委会成员）、主承销商及其他相关中介机构仔细阅读验证笔录，并在验证笔录上签字、盖章。若律师对验证笔录进行修改，必须征得相关确认人同意。</p>
