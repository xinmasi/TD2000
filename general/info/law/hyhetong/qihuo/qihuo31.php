        <p class=MsoBodyTextIndent>2<span style='font-family:
宋体;"Times New Roman"'>．说明已经对确认人提供的资料的真实性、准确性及上述资料是否足以证明重要信息的真实、准确进行审核，若有疏忽，愿承担相应的法律责任。</span></p>
        <p class=MsoBodyTextIndent>3<span style='font-family:
宋体;"Times New Roman"'>．确认人提供的为副本或复印件的，律师已经证明副本与正本及复印件与原件的一致性。</span></p>
        <p class=MsoBodyTextIndent>4<span style='font-family:
宋体;"Times New Roman"'>．确认人直接进行确认的，律师没有再作进一步审查验证。</span></p>
        <p class=MsoBodyTextIndent>5<span style='font-family:
宋体;"Times New Roman"'>．说明是依据招股说明书签署日前已经发生的事实进行验证工作。</span></p>
        <p class=MsoBodyTextIndent>6<span style='font-family:
宋体;"Times New Roman"'>．说明已经对招股说明书所栽全部重要信息进行验证，若有遗漏，愿承担相应的法律责任。</span></p>
