        <p >（二）说明本次配股已经依法取得或者尚待取得有权部门同意发行、上市的许可事项。</p>
        <p >三、本次发行、上市的实质条件</p>
        <p >（一）逐项说明本次配股是否符合《证券法》、《公司法》以及国务院证券管理部门关于公司配股的具体规定。</p>
        <p >（二）如股东以实物资产或者其他非现金资产作价抵作股款，说明此类资产是否已取得完备的权属证书，此种行为的实施是否存在法律障碍。</p>
        <p >（三）说明各股东在配售过程中是否受到公平对待。</p>
        <p >四、说明本次配股所涉及的关联交易及同业竞争</p>
        <p >（一）说明发行人有哪些关联企业。</p>
        <p >（二）说明发行人与其关联企业在本次配股中是否存在关联交易，若存在关联交易，还需说明关联交易的内容、数量和金额。</p>
        <p >（三）说明这种关联吏易是否会耦害发行人及其股东的利益。</p>
        <p >（四）若关联交易一方是公司大股东，还需说明是否已采取必要措施对小股东的利益进行保护。</p>
        <p >（五）说明股东大会关于本次配股涉及的关联交易的表决程序是否符合法律、法规和《公司章程》的决定。</p>
        <p >（六）说明发行人本次配股涉及的关联企业间是否存在同业竞争。若存在同业竞争，还需说明是否已采取必要措施解决同业竞争。</p>
        <p >（七）说明是否对关联吏易和同业竞争进行充分披露。</p>
        <p >五、发行人募股资金的运用</p>
        <p >（一）说明发行人前次募集资全的使用是否与原募集计划一致。如果发行人改变前次募集资金的用途，应当说明谊改变是否依法定程序获得批准。</p>
        <p >（二）说明发行人本次募股所筹资金的使用是否需要得到有权部门要求的批准或授权；如需要，是否已经得到批准或者授权。</p>
        <p >（三）如果本次募集资金的运用涉及兼并、收购其他企业的，说明兼并、收购的方式及其法律性质，并说明是否存在法律障碍、是否存在法律纠纷。</p>
        <p >六、本次配股所涉及到的重大债权、债务关系</p>
        <p >（一）审查本次配股涉及的重大合同的合法性。</p>
        <p >（二）说明发行人在上述合同项下的义务对本次配股是否构成法律障碍。</p>
        <p >（三）说明发行人与股东之间是否存在重大债权债务关系及相互提供担保的情况。</p>
        <p >（四）说明发行人正在进行或将要进行的重大诉讼、仲裁或行政诉讼事项，并说明对本次配股是否构成法律障碍。</p>
        <p >七、发行人的税务问题</p>
        <p >（一）说明发行人执行的税种、税率是否符合现行法律、法规及规范性文件的要求。</p>
        <p >（二）说明发行人近三年是否依法纳税，是否被税务部门处罚的情形。</p>
