        <p >（三）说明已经按照本准则的要求对本次发行上市的合法性及对本次发行上市有重大影响的法律问题发表法律意见，法律意见书中不存在虚假、严重误导性陈述及重大遗漏，否则愿承担相应的法律责任。</p>
