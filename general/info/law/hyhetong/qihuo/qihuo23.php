        <p >（二）说明发行人是否依法有效存续。即根据法律、法规及其章程，说明发行人是否有需要终止的情形出现。</p>
        <p >（三）说明发行人章程的内容是符合现行法律、法规的规定，是否按《上市公司章程指引》修订。</p>
