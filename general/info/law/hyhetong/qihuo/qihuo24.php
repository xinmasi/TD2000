'>申请公开发行股票公司报送材料标准格式</p>
        <p >为进一步做好公开发行股票公司申请材料的核准工作，中国证券监督管理委员会根据《中华人民共和国公司法》等有关法律、法规，对《关于印发〈申请公开发行股票公司报送材料标准格式〉的通知》（证监发字[1999]422号）作了修订。今后，拟公开发行股票公司（A股）申请材料报我会核准时，应按下列标准格式制作：</p>
        <p >一、发行申请材料的纸张、封面及份数</p>
        <p >（一）纸张 </p>
        <p >应采用幅面为209×295毫米规格的纸张（相当于A4纸张规格）。</p>
        <p >（二）封面</p>
        <p >1．标有“公开发行股票申请材料”字样；</p>
        <p >2．申请企业名称；</p>
        <p >封面后应附发行人及所有与发行有关的中介机构联系表，材料侧面必须标注企业名称。</p>
        <p >（三）份数</p>
        <p >申请材料首次报送三份，其中一份为原件。发审会召开之前，根据我会要求的份数补报材料。发审会通过后，除原件和复印件各一份留我会存档外，其余申请材料退该企业。</p>
        <p >二、发行申请材料目录</p>
        <p >第一章<span>&nbsp; </span>省级人民政府或国务院有关部门推荐企业申请发行的文件</p>
