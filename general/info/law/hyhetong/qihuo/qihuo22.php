        <p >1．重要会计政策的变动；</p>
        <p >2．会计师事务所的变更说明；</p>
        <p >3．市场和商品价格重大变化；</p>
        <p >4．企业购并；</p>
        <p >5．企业迁移；</p>
        <p >6．公司主要生产协作关系更换；</p>
        <p >7．较大技术项目投产；</p>
        <p >8．重大投资决策及产生效益情况；</p>
