        <p >十、董事会上市承诺</p>
        <p >本节列示自公司股票上市之日起，发行人董事会自愿作出的如下承诺：</p>
        <p >本公司董事会将严格遵守《公司法》、《股票发行与交易管理暂行条例》、《公开发行股票公司信息披露实施细则》、《……证养吏易所交易规则》和有关法律、法规的规定，并自股票上市之日起作出如下承诺：</p>
        <p >1．按照法律、法规的规定程序和要求披露重大的信息、并接受证券主管机关、……证券交易所的监督管理。</p>
        <p >2．及时、真实、准确地公布中期报告和年度报告、并备置于规定场所供投资者公众查阅。</p>
        <p >3．本公司董事、监事及高级管理人员如发生人事吏动或持本公司股票发生变化时，在报告证券主管机关、证券交易所的同时向投资者公布。</p>
        <p >4．在任何公共传播媒介中出现的消息可能对本公司股票的市场价格产生误导性影响时，本公司知悉后将及时对该消息予以公开澄清。</p>
        <p >5．本公司董事、监事及高级管理人员将认真听取社会公众的意见和批评，不利用巳获得的内幕消息和其他不正当手段直接或间挂从事股票买卖活动。</p>
        <p >6．本公司无记录负债。</p>
