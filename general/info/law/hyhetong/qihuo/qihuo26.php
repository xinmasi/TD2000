'>关于发布公开发行股票公司</p>
        <p align=center style='
text-align:center;line-height:16.0pt;
'>信息披露的内容与格式准则第一号</p>
        <p align=center style='
text-align:center;line-height:16.0pt;
'>《招股说明书的内容与格式》的通知</p>
        <p align=center >1997年1月7日&nbsp; 证监[1997]2号</p>
        <p >各省、自治区、直辖市、计划单列市证管办（证监会），国务院有关部门，上海、深圳证券交易所：</p>
        <p >为了维护证券市场的健康发展，规范公开发行与上市公司的住处披露行为，保护投资者的合法权益，中国证监会对公开发行股票公司信息披露的内容与格式准则第一号《招股说明书的内容与格式（试行）》进行了补充、修改和完善，现在正式颁布《招股说明书的内容与格式》，于1997年4月1日起开始执行。各公开发行与上市公司应按准则要求履行其披露义务。执行中有什么问题，请及时报告中国证监会。</p>
        <p >附件：《招股文明书的内容与格式》</p>
        <p >（附件一）</p>
        <p align=center >招股说明书的内容与格式</p>
        <p >（一）根据《中华人民共和国公司法》（以下简称《公司法》）与《股票发行与交易管理暂行条例》（以下简称《股票条例》制定本准则。</p>
        <p >（二）凡在中华人民共和国境内公开发行股票的发行人，在申请公开发行股票时，应当按照本准则编制招股说明书。</p>
        <p >本招股说明书作为发行人向中国证券监督管理委员会（以下简称证监会）申请公开发行申报材料的必备部分。</p>
        <p >（三）本准则规定的内容与格式包括：</p>
        <p >1．招股说明书封面；</p>
        <p >2．招股说明书目录；</p>
        <p >3．招股说明书正文；</p>
        <p >（1）主要资料</p>
        <p >（2）释义</p>
        <p >（3）绪言</p>
        <p >（4）发售新股的有关当事人</p>
        <p >（5）风险因素与对策</p>
        <p >（6）募集资金的运用</p>
        <p >（7）股利分配政策</p>
        <p >（8）验资报告</p>
        <p >（9）承销</p>
        <p >（10）发行人情况</p>
        <p >（11）发行人公司章程摘录</p>
        <p >（12）董事、监事、高级管理人员及重要职员</p>
        <p >（13）经营业绩</p>
        <p >（14）股本</p>
        <p >（15）债项</p>
        <p >（16）主要固定资产</p>
        <p >（17）财务会计资料</p>
        <p >（18）资产评估</p>
        <p >（19）盈利预测</p>
        <p >（20）公司发展规划</p>
        <p >（21）重要合同及重大诉讼事项</p>
        <p >（22）其他重要事项</p>
        <p >（23）董事会成员及承销团成员的签署意见</p>
        <p >4．招股说明书附录；</p>
        <p >5．招股说明书备查文件。</p>
        <p >（四）本准则的基本原则是要求发行人将一切对投资者进行投资判断有重大影响的信息予以充分披露，以利于投资者更好地做出投资决策。发行人应据此原则编制招股说明书。</p>
        <p >1．凡对投资者做出投资决策有重大影响的信息，不论本准则是否有规定，均应予以披露；</p>
        <p >2．发行人认为有助于投资者做出投资决策的信息，如果本准则没有规定，发行人可增加这部分内容；</p>
        <p >3．本准则某些具体要求对发行人确实不适用的，发行人可根据实际情况做出适当修改，同时以书面形式报告证监会，并在招股书中予以说明。发行人成立不足3年的，应提供其自成立之日起，至进行股票公开发行准备工作之时止的经营业绩及其他资料。</p>
        <p >如果发行人由原有企业经改制而设立，且改制不足3年，则发行人在根据本准则的要求对其历史情况进行披露时，应包括原有企业情况。</p>
        <p >境内上市外资股的发行人，应当增加关于中国经济、政治、法律等有助于外国投资人了解中国一般情况的资料，以及有助于对发行人增加了解的其他资料。有必要时，境内上市外资股发行人还应编制招股说明书的外文文本。发行人应当保证两种文本内容的一致性。在对两种文本的理解上发行歧义时，以中文文本为准。</p>
        <p >发行人尚未成立董事会、监事会，而由筹备机构代行其权力的，本招股说明书中凡要求董事会、监会在股票发行过程中以及对本招股说明书所应承担的责任与义务，由筹备机构承担，对董事、监事有关情况的披露改为对筹备机构成员有关情况的披露。</p>
        <p >（五）招股说明书有效日期为6个月，自招股说明书签署之日起计算。发行人不得使用过期的招股说明书发行股票。发行人在招股说明书有效期内未能发行股票，必须修订招股说明书，补充最新的财会资料和其它信息。这些修改、补充的信息，须先经承销商、推荐人以及与该等信息有关的中介机构（例如：律师、注册会计师或资产评估人员）的认可，再报证监会审核后，发行人方可发行。</p>
        <p >（六）招股说明书不得刊登任何个人、机构或企业的题字，任何有祝贺性、恭维性或推荐性的词句，以及任何广告、宣传性用语。</p>
        <p >（七）招股说明书中的数字应当采用阿拉伯数字。招股说明书中有关货币金额的资料除特别说明之外，应指人民币金额。</p>
        <p >（八）有关地方法规中凡与本准则相抵触的部分，应以本准则为准。</p>
        <p >（九）本准则自公布之日起实施。</p>
        <p >一、招股说明书封面</p>
        <p >招股说明书的封面应载明下列事项：</p>
        <p >（一）发行人的名称及公司住所；</p>
        <p >（二）“招股说明书”字样，送交证监会审核的稿件，必须标有“送审稿”显著字样；</p>
        <p >（三）说明发行股票的类型，例如：普通股、优先股或者境内上市外资股等；如果同时发行认股证，还须列明认股证与股票的比例。</p>
        <p >（四）重要提示，必须按照本准则附件一规定的文字列示；</p>
        <p >（五）发行量、每股面值、每股发行价、发行费用、募集资金，采用上网竞争价方式发行股票的，应标明发行底价；</p>
        <p >（六）发行方式及发行期；</p>
        <p >（七）拟上市证券交易所；</p>
        <p >（八）主承销商；</p>
        <p >（九）推荐人；</p>
        <p >（十）签署日期。</p>
        <p >招股说明书必须用幅面为209×295毫米规格的纸张（等于A4纸规格），封面必须为浅色，除可以印有发行人的标志之外，不应当有其他图案。具体格式见附件一。</p>
