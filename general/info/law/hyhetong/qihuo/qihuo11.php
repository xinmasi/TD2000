      <div style='layout-grid:15.6pt'>
<p align=center style='
text-align:center;line-height:16.0pt;
'>公开发行股票公司信息披露的内容</p>
        <p align=center style='
text-align:center;line-height:16.0pt;
'>与格式准则第六号《法律意见书的内容与格式》（修订）</p>
        <p align=center >第一部分<span>&nbsp; </span>基本要求</p>
        <p >一、根据《中华人民共和国证券法》（以下简称《证券法》）、《中华人民共和国公司法》（以下简称《公司法》）的有关规定，制定本准则。发行人申请公开发行股票和上市（含配股，下同），其所聘请的律师应当按照本准则的要求，出具法律意见书。</p>
        <p >二、法律意见书是发行人向中国证券监督管理委员会（以下简称证监会）申请公开发行股票所必须具备的法定文件之一。</p>
        <p >三、律师应当严格履行法定职责，对发行人股票发行、上市的合法性及对发行、上市有重大影响的法律问题发表法律意见，保证法律意见书的真实性、准确性和完整性。</p>
        <p >四、律师应当对出具法律意见书所依据的事实和材料进行核查和验证。若有过错，应承担相应的法律责任。</p>
        <p >五、法律意见书不仅表述结论性意见，而且应当说明上述结论性意见的依据。</p>
        <p >六、律师出具法律意见书的内容与格式，应当符合本准则规定。本准则的某些具体规定确实不适用的，律师可以根据实际情况对有关内容与格式作出适当修改，但应当说明修改的原因及理由。</p>
        <p >七、本准确未明确要求，但对发行、上市有重大影响的法律问题，律师应当发表法律意见。</p>
        <p >八、律师出具法律意见书，不应使用“基本符合条件”一类的措辞。对于不符合条件的事项或者律师已经勤勉尽责仍不能对其法律性质或其合法性作出准确判断的事项，应当发表保留意见。</p>
        <p >九、律师可以要求发行人或相关当事人就特定事宜作出书面说明、确认或承诺；但无论有无书面说明、确认或承诺，律师仍受勤勉尽责义务的约束，不得出具有虚假、严重误导性内容或者有重大遗漏的法律意见。</p>
        <p >十、为了维护法律意见书的严肃性，律师应当在发行、上市申报材料正式上报时，方可签署法律意见书。法律意见书上报后，不得对该文本进行任何修改。如需要作出补充、说明或更正，应另行出具专项法律意见书。</p>
        <p >报送证监会的法律意见书应当是经两名以上具有证券从业资格的律师及其所在的律师事务所签字、盖章的正式文本。</p>
        <p >十一、发行申报材料上报后，如有任何修改，发行人必须立即通知律师。上述修改对法律意见有影响的，律师应当就该项修改的内容出具专项法律意见书。</p>
        <p >十二、本准则适用于A股发行和上市，发行境内上市外资股，可参照本准则制作法律意见书。</p>
        <p >十三、本准则由证监会负责解释。</p>
        <p >十四、本准则自1999年7月1日起施行。1994年10月28日证监会《关于发布〈公开发行股票公司信息披露的内容与格式准则第六号〉的通知》（证监发字[1994]162号）同时废止。</p>
