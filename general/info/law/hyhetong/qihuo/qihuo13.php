        <p >三、股份公司本次发行、上市的实质条件</p>
        <p >（一）股份公司本次发行股票属于发起设立的股份有限公司增资发行。</p>
        <p >（二）股份公司本次发行、上市的实质条件：</p>
        <p >股份公司本次股票发行、上市符合《公司法》、《证券法》和《股票条例》及其他规范性文件规定的实质条件：</p>
        <p >1．股份公司主要从事<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>生产及经营，符合国家产业政策；</p>
        <p >2．股份公司发行的股份仅限于普通股一种，符合同股同权，同股同利的规定；</p>
        <p >3．股份公司设立时发起人认购的<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>万股股份占股份公司总股本的100%，拟占本次公开发行A股后股本总额的<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>%。符合发起人认购的部分不少于30000万元及认购数额不少于拟发行股本总额35%的规定；</p>
        <p >4．股份公司本次拟向社会公开发行<u>&nbsp;&nbsp;&nbsp; </u>万股，占发行后股本总额的<u>&nbsp;&nbsp;&nbsp; 
          </u>%。符合向社会公众发行的部分不少于公司拟发行股本总额25%，及股本总额超过40，000万股的，其向社会公众发行的部分不少于公司拟发行股本总额15%的规定；</p>
        <p >5．根据股份公司的保证和本所律师的调查，股份公司及其发起人在近三年内没有重大违法行为；</p>
        <p >6．按照<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>会计师事务所出具的（<u>&nbsp;&nbsp;&nbsp; </u>）<u>&nbsp;&nbsp;&nbsp; </u>师股审字（<u>&nbsp;&nbsp;&nbsp; 
