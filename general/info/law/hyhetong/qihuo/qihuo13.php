          </u>总公司、<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </u>有限公司、<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>有限公司四家发起人共同发起设立股份公司。1999年7月，<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>省人民政府以<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </u>政函[1999]<u>&nbsp;&nbsp;&nbsp; 
          </u>号正式推荐股份公司为股票发行上市企业。</p>
