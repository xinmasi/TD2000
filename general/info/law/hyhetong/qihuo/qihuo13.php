          </span></u>日召开的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>年度第二次临时股东大会作出了审议通过董事会拟定的公司章程修订草案的决议，并决定待股份公司本次股票发行成功并经工商行政管理机关核准登记后正式生效实施。</p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>本所律师依法对股份公司的章程修订草案进了审查。本所律师认为，该章程修订草案符合《公司法》、《上市公司章程指引》及其他现行有关法律、法规及有关主管部门的规定，规定了股东的各项权利，包括表决权、股份转让权、资料查阅权、监督权、股利及剩余财产分配权等；公司章程修订草案亦未对股东行使权利进行任何限制，并建立了关联股东表决回避制度，充分保护了股东（特别是小股东）的权利；未发现公司章程修订草案中有与《上市公司章程指引》重大不一致的条款。</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>股份公司的章程修订草案尚待股份公司本次股票发行成功后经工商行政管理机关注册登记后方能正式生效。</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>五、关联交易及同业竞争</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>（一）股份公司的关联企业</span></p>
        <p class=MsoBodyTextIndent>股份公司是由集团公司作为主发起人，联合四家发起人共同发起设立的股份有限公司，并申请公开发行、上市社会公众股。其中集团公司以经评评估确认的净资产<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元按<u><span>&nbsp;&nbsp;&nbsp; </span></u>%的折股比例认购<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>万股，占股份公司本次股票发行前股份总额的<u>&nbsp;&nbsp;&nbsp; </u>%，拟占股份公司本次股票发行后股份总额的<u>&nbsp;&nbsp;&nbsp; 
          </u>%，为股份公司的绝对控股股东；其他四家发起人共以现金<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </u>万元人民币认购<u>&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>万股，占股份公司本次股票发行前股份总额的<u>&nbsp;&nbsp;&nbsp; </u>%，拟占股份公司本次股票发行后股份总额的<u>&nbsp;&nbsp;&nbsp; 
          </u>%。因而，股份公司的关联企业主要为集团公司。</p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>（二）股份公司与集团公司之间的关联交易</span></p>
        <p class=MsoBodyTextIndent>因地理环境、历史渊源关系等客观因素的影响，股份公司与集团公司之间在生活服务、生产辅助等方面难以避免地存在着若干关联交易。集团公司向股份公司提供<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>服务、电费代缴、通信服务、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>设备租赁、生产辅助材料供应等方面的服务。除有关法律、法规和政策要求采用政府定价外，集团公司和股份公司之间的各项服务费用标准依提供服务的市场价格或成本价予以确定，非因政府或行业定价调整或市场状况发生重大变化，双方将保持该等价格四年不变。</p>
        <p class=MsoBodyTextIndent>预计<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年上述各项服务发生费用分别为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>万元、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>万元、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元，共计<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元。</p>
        <p class=MsoBodyTextIndent>股份公司承租使用集团公司<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>宗土地，租赁面积共计<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>平方米，租期为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年，年租金价格平均为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>元/平方米，<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>年全部租金共计为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </u>万元。</p>
        <p class=MsoBodyTextIndent>经股份公司<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年度第一次、第二次临时股东大会批准及<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>省工商管理局、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>省<u><span>&nbsp;&nbsp;&nbsp; 
          </span></u>厅<u><span>&nbsp;&nbsp;&nbsp; </span></u>研字（<u><span>&nbsp;&nbsp;&nbsp; 
          </span></u>）第<u><span>&nbsp;&nbsp;&nbsp; </span></u>号文批准同意，股份公司拟在募集资金到位后，收购集团公司下属的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂和<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>厂经剥离后的经营性资产（含相关土地使用权和<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂采矿权）。为此，双方签订了《资产收购协议》。依该协议，股份公司与集团公司以经评估并确认后的净资产值为基础，协商确定收购价格为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元。该协议待股份公司开始发行A股成功后正式实施。</p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>经本所律师审查，未发现上述关联交易有违反《公司法》、《证券法》等法律、法规之处，未发现上述关联交易存在损害股份公司及其股东利益的内容。</span></p>
        <p class=MsoBodyTextIndent>根据股份公司提供的材料和本所律师的调查，集团公司是股份公司的绝对控股大股东，在上述关联交易中，双方根据“公平、公正、等价、有偿”的市场原则，按照一商业条款签订了《综合服务协议》及补充协议、《土地使用权租赁协议》及补充协议和《资产收购协议》。股份公司采取书面合同的方式与控股股东集团公司确定存在的关联交易及双方的权利义务关系；<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年<u><span>&nbsp;&nbsp;&nbsp; </span></u>月<u><span>&nbsp;&nbsp;&nbsp; 
          </span></u>日召开的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>年度第二次临时股东大会对资产收购事项进行表决时，集团公司予以回避；同时集团公司保证其向股份公司提供的服务优先于其向任何第三方（包括除股份公司外的集团附属企业）提供的服务，且不利用自己的优势或垄断地位强制股份公司接受不合理的条件，以公平维护股份公司及其小股东的正当权益。</p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>（三）同业竞争问题</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>根据公司提供的材料和本所律师的调查，目前集团公司的主要产品与股份公司的主要产品在品种、销售市场上存在着较大的差异，不存在相互竞争，而且在本次股票发行上市，股份公司完成资产收购事项后，股份公司的主营产品在品种、销售市场上与集团公司也不存在站任何同业竞争。</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>本所律师为认，集团公司与股份公司间不存在同业竞争问题。</span></p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>经本所律师审查，股份公司本次《招股说明书》（送审稿）对关联交易和同业竞争进行了充分披露。</span></p>
