        <p >四、股份公司的章程（修订草案）</p>
        <p >股份公司现行章程经股份公司创立大会暨第一次股东大会审议通过，并经</p>
        <p ><u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>省工商行政管理局核准登记，业已生效。该章程符合我国《公司法》和其他法律、法规的规定。</p>
        <p >为适应股份公司本次股票公开发行上市需要，股份公司根据《公司法》和中国证监会颁布的《上市公司章程指引》对原公司章程进行了修改。股份公司<u><span>&nbsp;&nbsp; 
          </span></u>年</p>
        <p ><u><span>&nbsp;&nbsp; </span></u>月<u><span>&nbsp;&nbsp; 
          </span></u>日召开的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>年度第一次临时股东大会及<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
