        <p >13．1&nbsp; 甲、乙双方如果因履行和解除本协议而发生争议时，应首先通过友好协商解决。</p>
        <p >13．2&nbsp; 右双方在30天内未能协商一致的情况下，任何一方均有权向<u>&nbsp;&nbsp;&nbsp; 
          </u>市中级人民法院提起诉讼。</p>
