        <p >2．甲方已以出让方式取得本协议所列的各宗土地的使用权，土地使用期限为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>年；</p>
        <p >3．乙方在正式设立后将使用本协议附件所列的名宗土地和有关房屋。经双方协商，甲方同意向乙方出租上述的各宗土地使用权，乙方同意向甲方承租该等土寺使用权。</p>
        <p class=MsoBodyTextIndent><span style='font-family:宋体'>为明确甲、乙双方的权利义务，保护双方的合法权益，根据有关法律、法规的规定，甲、乙双方当事人本着平等、自愿、等价有偿的原则，订立本土地使用权租凭协议。</span></p>
        <p >1．租凭范围</p>
        <p >1．1&nbsp; 甲方向乙方出租的土地共<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>宗，总面积为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </u>平方米。甲方租给乙方的土地的使用证号、宗地位置、宗地面积、批准用途等内容以附件表格形式列出（详见本协议附件）。</p>
        <p >1．2&nbsp; 地下资源、埋藏物和市政公用设施均不属于本协议租凭范围。</p>
        <p >2．租凭期限</p>
        <p >本协议项下的土地使用权租赁期限为<u><span>&nbsp;&nbsp;&nbsp; 
          </span></u>年，自乙方正式设立之日起计算。</p>
        <p >3．租金标准</p>
        <p >3．1&nbsp; 乙方每年应向甲方支付租金总额为<u>&nbsp;&nbsp;&nbsp; 
          </u>元。</p>
        <p >3．2&nbsp; 本协议项下各宗土地租金标准详见附件。该租金标准是依据<u>&nbsp;&nbsp;&nbsp; 
          </u>省地产咨询评估中心《土地评估报告》确定的。</p>
        <p >3．3&nbsp; 租凭期间，如遇有国家政策调整，则租金应作相应调整。</p>
        <p >4．土地用途</p>
        <p >本协议项下的各宗土地应用于股份公司生产经营，即必须作与其生产经营活动相配套或相关的用途使用（租凭土地被批准的用途见附件）。</p>
        <p >5．甲方的权利、义务</p>
        <p >5．1&nbsp; 甲方的权利；</p>
        <p >5．1．1&nbsp; 甲方有权按本协议约定向乙方收取相关的租金。</p>
        <p >5．1．2&nbsp; 在租凭期间，甲方有权对各宗地的开发、利用情况进行检查、监督。</p>
        <p >5．2&nbsp; 甲方的义务：</p>
        <p >5．2．1&nbsp; 甲方有义务按本协议的约定向乙方移交租凭物进行查验。</p>
        <p >5．2．2&nbsp; 甲方有义务协助乙方办理相关手续。</p>
        <p >6．乙方的权利、义务</p>
        <p >6．1&nbsp; 乙方的权利</p>
        <p >6．1．1&nbsp; 乙方有权按本协议的约定要求甲方移交租赁物并进行查验。</p>
        <p >6．1．2&nbsp; 乙方有权利在本协议约定的范围、时间内使用附件所列各宗土地。</p>
        <p >6．1．3&nbsp; 乙方有权要求甲方协助办理相关手续。</p>
        <p >6．2&nbsp; 乙方的义务</p>
        <p >6．2．1&nbsp; 乙方有义务按本协议约定的时间、方式和数量向甲方支付租金。</p>
        <p >6．2．2&nbsp; 乙方有义务按照本协议约定的范围内使用土地。乙方如果需要改变土地用途的，应事先征得甲方同意并由甲方按有关规定报批后，重新订立合同。</p>
        <p >6．2．3&nbsp; 乙方未经甲方的同意，不得将本协议项下的各宗地转租或许可任何第三人使用。</p>
        <p >7．甲方的承诺</p>
        <p >7．1&nbsp; 甲方保证在本协议存续间合法拥有该等土地的使用权，并有权出租该等土地使用权。</p>
        <p >7．2&nbsp; 甲方同时保证该等土地使用权没有进行任何抵押。</p>
        <p >7．3&nbsp; 如果因该等土寺使用权存在异议，致使乙方在本协议项下的权利无法实现或者遭到其他损失，甲方应给予赔偿，而且乙方此时有权解除本协议。</p>
        <p >8．声明</p>
        <p >8．1&nbsp; 本协议存续期间，甲方不得随意取回其土地使用权。但在特殊情况下，根据社会公共利益的需要，甲方可以依照法定程序提前收回某宗土地使用权，但应按有关规定及乙方开发利用土地的实际情况给予乙方相应的补偿。</p>
        <p >8．2&nbsp; 本协议项下的某宗土地使用权租赁关系的终止，不影响其他宗地的租赁关系。</p>
        <p >9．续租</p>
        <p >9．1&nbsp; 租凭期限届满后，在同等条件下，乙方享有优先承租权。</p>
        <p >9．2&nbsp; 乙方如需继续使用各宗地，应在各宗地租赁期满前6个月向甲方提出续租申请，双方应重新签订租赁合同。</p>
        <p >10．租金的支付</p>
        <p >10．1&nbsp; 本协议项下的土地使用权租金在依本协议第3条确定后，乙方应于每年12月31日将租金划入甲方指定的帐户上。</p>
        <p >10．2&nbsp; 有关土地的土地使用税、费等由甲方自行负担。</p>
        <p >11．违约责任</p>
        <p >甲、乙双方任何一方不履行本协议项下的任一义务，均构成违约，应承担违约责任，即应向对方赔偿其违约行为造成的一切直接的和可预见的损失。若双方均有过错的，按双方各自过错大小来承担违约责任。</p>
        <p >12．本协议的解除</p>
        <p >12．1&nbsp; 双方均有权解除：</p>
        <p >12．1．1&nbsp; 双方同意可以解除本协议。</p>
        <p >12．1．2&nbsp; 因不可抗力，本协议已无法履行或履行已没有意义，双方均有权解除。</p>
        <p >12．2&nbsp; 甲方有权解除：</p>
        <p >12．2．1&nbsp; 乙方未经甲方同意擅自转租、转借本协议项下的土地使用权；</p>
        <p >12．2．2&nbsp; 乙方违反协议规定欠交租金3年以上；</p>
        <p >12．2．3&nbsp; 乙方利用有关土地进行违法活动时。</p>
        <p >12．3&nbsp; 乙方有权解除：</p>
        <p >12．3．1&nbsp; 由于本协议生效以后国家有关立法的变动，乙方须提前解除本协议的；</p>
        <p >12．3．2&nbsp; 由于国家产业政策的变更，乙方须提前解除本协议的；</p>
