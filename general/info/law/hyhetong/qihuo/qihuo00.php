　　南京石油交易所汽油标准合约<BR> 
　　<BR> 
　　２．０１　商品名称：汽油（Ｇａｓｏｌｉｎｅ）<BR> 
　　定义：原油经直馏或直馏与二次加工制得的汽油馏分的混合物，加有适当抗爆剂和抗氧防胶剂制成的，用于点火式发动机的液体燃料。<BR> 
　　２．０２　期货合约的要求<BR> 
　　（１）月份和时间：按交易所规定时间并在指定月份交货。<BR> 
　　（２）交易的计量单位：以手计算，每一“手”为１００吨。<BR> 
　　（３）质量标准（见附表）<BR> 
　　（４）最小变动价格：每吨为２元人民币或每交易单位为２００元人民币。<BR> 
　　（５）合约的价格：南京港离岸价。<BR> 
