        <p >根据《公开发行股票公司信息坡露实施细则》规定，发行人应以不少于一万字的篇幅编制《招股说明书概要》（以下简称《概要》，在承销期开始前2至5个工作日内刊登在至少一种证监会指定的报刊上及发行人选择的其他报刊上。《概要》应简要地提供招股说明书的主要内容，但不得误导投资人。</p>
