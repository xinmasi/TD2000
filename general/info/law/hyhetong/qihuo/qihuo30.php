        <p >2．发售新股有关当事人（《公开发行股票公司信息披露的内容与格式准则》第一号关于招股说明书正文（四）的全部内容。《公开发行股票公司信息披露的内容与格式准则》第一号以下简称第一号准则，招股说明书以下简称招股书）；</p>
