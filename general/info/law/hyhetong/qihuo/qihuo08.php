        <p >8．1&nbsp; 甲方的权利</p>
        <p >8．1．1&nbsp; 甲方有权要求乙方按本协议的规定勤勉，尽责地承销股票，并有权对整个承销活动进行监督。</p>
        <p >8．1．2&nbsp; 甲方有权要求乙方按本协议的规定时间和方式支付全部股票款项（在扣除承销手续费和上市推荐费以后）。</p>
        <p >8．2&nbsp; 甲方的义务</p>
        <p >8．2．1&nbsp; 甲方有主务向乙方提供本次股票发行所需的所有详尽的、真实的、完整的资料和文伯，以便乙方的工作顺利地进行。</p>
        <p >8．2．2&nbsp; 甲方有义务协助乙方的本次股票发行工作，包括但不限于为乙方对其企业进行实地考察提供方便，协助办理有关手续等。</p>
        <p >9．乙方的权利义务</p>
        <p >9．1&nbsp; 乙方的权利</p>
        <p >9．1．1&nbsp; 乙方有权要求甲方对本次股票发行工作给予配合。</p>
        <p >9．1．2&nbsp; 乙方有权要求甲方提供的与本次股票发行有关的所有资料文件为真实、完整、准确的。如果甲方未尽此项义务而致使本次股票发行受阻或失败，乙方有权终止本协议。</p>
        <p >9．1．3&nbsp; 乙方有权按本协议的约定收取有关承销手续费、上市推荐费和其他有关费用。</p>
        <p >9．2&nbsp; 乙方的义务</p>
        <p >9．2．1&nbsp; 乙方有义务按本协议的规定，尽责、勤勉地完成本次股票承销活动。</p>
        <p >9．2．2&nbsp; 乙方应积极协助甲方向上级主管部门办理发行股票的申报手续，制订发行时间表。</p>
        <p >9．2．3&nbsp; 乙方负责组建承销团，制订股票发行具体方案，编制招股说明书等相关文件或资料，并与有关方面签订分销合同，资料备忘录以及相关的其他法律文件，并负责处理、协调本次股票发行过程中出现的有关问题。</p>
        <p >9．2．4&nbsp; 乙方负责为甲方进行上市辅导（具体协议以后再签）和推荐上市等服务工作。</p>
        <p >9．2．5&nbsp; 乙方对甲方负有保密义务，即其对在本次股票发行过程中所知道甲方的商业秘密等不欲让外人知道的秘密负有保密的责任。</p>
        <p >10．甲、乙双方声明</p>
        <p >甲、乙双方保证，双方自本协议签署之日起一直到本次承销结束日为止，在事先未与对方协商并取得对方书面同意的情况下，不能以任何形式，对外披露招股说明书以外的可能影响本次股票发行的信息，否则应承担违约责任。</p>
        <p >11．甲方的承诺</p>
        <p >11．1&nbsp; 甲方保证其为乙方提供的与本次股票发行有关的资料文件为真实、完整、准确，不存在重大误解、虚假性陈述和重要遗漏。</p>
        <p >11．2&nbsp; 甲方承诺，将视本次A股发行的进程，同意乙方提出的相关合理的要求，包括采取相应的行为和提供相应的文件。以上目的是为了本次A股的顺利发行。</p>
        <p >12．违约责任</p>
        <p >甲、乙双方应严格遵守本协议的各项规定，如有违反，则为违约行为，应承担违约责任。给对方造成损失的，要予以全额赔偿（有其他具体规定除外）；双方均有过错的，按过错大小分别承担相应的责任。以下为具体违约责任的规定：</p>
        <p >12．1&nbsp; 甲方在与乙方签订本协议后又选择其他证券公司担任相同或类似的角色的，其与其他证券公司签订的协议无效，而且还应履行本协议下之全部义务；如果已无法履行该义务，则应支付股金总额的<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>‰，作为赔偿金；</p>
        <p >12．2&nbsp; 由于乙方的过失造成成本次A股未能发行，应向甲方支付股金总额的<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>‰，作为赔偿金；</p>
        <p >12．3&nbsp; 乙方延迟划拨股款的，按同期银行利率+<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>%来计收利息。</p>
        <p >13．争议的解决</p>
        <p >13．1&nbsp; 双方就本协议的解释、履行，以及与本协议有关的其他争议，应首先通过友好协商解决；</p>
        <p >13．2&nbsp; 如果双方自友好协商开始后30日内未能达成一致，则应将争议提交中国证券监督管理委员会指定的仲裁机构仲裁，其仲裁裁决为终局的，对双方有约束力。</p>
        <p >14．不可抗力</p>
        <p >自本协议签署日到承销结束日之间，如果发生任何政治、军事、经济、金融、法律等方面的重大变故，而且此等变故为双方所不可预料，不可避免，而且已经或者将会对甲方的业务、财务状况以及本次A股发行产生重大不利影响，则任何一方可以决定暂时中止（不可超过30天）本协议或者终止本协议。</p>
        <p >15．未尽事宜</p>
