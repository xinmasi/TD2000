        <p >甲方委托乙方为甲方A种股票发行的主承销商和股票上市推荐人，由乙方负责总承销股票业务。本次股票发行采用包销方式，即在承销期限内未能全数发行完毕，未售出部分股票由乙方全额购入。</p>
