        <p >2．1&nbsp; 甲方本次委托乙方代为承销的股票种类为人民币普通股（即A股），每股面值人民币1元。</p>
        <p >2．2&nbsp; 甲方本次委托乙方向社会公共发行的股票总额为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>股。</p>
        <p >2．3&nbsp; 每股发行价格为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>元。</p>
        <p >2．4&nbsp; 发行总市值为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>元（即每股发行价格×发行总额）。</p>
        <p >3．承销期限及起止日期</p>
        <p >3．1&nbsp; 本次股票承销期为<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>天（不超过90天）</p>
        <p >3．2&nbsp; 承销期限自刊登公开招股说明书之日起至第<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>天为止。</p>
        <p >4．承销付款方式及日期</p>
        <p >在本次承销期届满后，30个工作日内无论是否全部发行完毕，乙方均应将全部股票款项（如未全部发行完缥，也包括自己包销部分的股款）在扣除承销费用后，一次划向甲方指定的银行帐号。</p>
        <p >5．承销手续费的计算、支付方式</p>
        <p >5．1&nbsp; 甲、乙双方经协商同意，甲方向乙方支付发行总值的<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>%作为主承销费用（按证监会的收费标准，为1.5%～3%之间）。该费用包括但不限于文件制作费（如招股说明书、上市公告书等文书的制作）、主承销协调费等。</p>
        <p >5．2&nbsp; 承销费用由乙方在向甲方支付股票款项时扣除。</p>
        <p >6．其他费用与承销有关的费用</p>
        <p >与本次股票发行（承销）有关的其他费用，如广告宣传费、印刷费等费用由甲方确认后由其承担。该等费用不包含在第7条所指的主承销手续费中。</p>
        <p >7．上市推荐费</p>
        <p >上市推荐费按本次发行面值<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>%收取，由乙方一并在移交股款时扣除。</p>
