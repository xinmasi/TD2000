      <p>一、现将本文书的制作要点介绍如下：</p>
      <div style='layout-grid:15.6pt'>
<p >1．主体。写明双方当事人，同时注意对双方当事人作一界定。</p>
        <p >2．服务的内容；3．费用的计算或支付办法；4．声明或承诺；5．违约责任；6．争议的解决；7．协议的期限；8．协议的变更。</p>
        <p >二、格式：</p>
        <p align=center style='
text-align:center;line-height:22.0pt;
'>综合服务协议</p>
        <p >本协议由以下双方在××省××市签署：</p>
        <p >甲方：××集团公司</p>
        <p >住所：××省××市××路××号</p>
        <p >乙方：××股份有限公司（筹）</p>
        <p >住所：××省××市××路××号</p>
        <p >鉴于：</p>
        <p >（1）甲方为具有中国法人地位的国有独资公司，持有工商行政管理部门颂发的《企业法人营业执照》。</p>
        <p >（2）乙方是经政有关主管机构批准，由甲方作为独家发起人拟设立的股份有限公司。</p>
        <p >（3）乙方在设立后，需要甲方提供某些相关的服务或供应。据有关法律、法规，甲、乙双方在平等、自愿、等价有偿的基础上，签订本协议，以资共同遵照执行。</p>
        <p >1．定义</p>
        <p >1．1&nbsp; 甲方，除××集团公司以外，还包括其附属企业。</p>
        <p >1．2&nbsp; 乙方，即××股份有限公司（筹），还包括其成立后所拥有的附属企业。</p>
        <p >1．3&nbsp; 附属企业，是指甲、乙双方现在及以后（亦即存续期间内）所拥有的全资企业、控股公司、参股公司以及控制的其他经济实体。</p>
        <p >2．其本原则</p>
        <p >2．1&nbsp; 甲、乙双方确认，将依照市场原则，即公平竞争、等价有偿的原则提供相关服务和供应。</p>
        <p >2．2&nbsp; 本协议项下的相关服务或供应均为非固定、非排他性的，乙方有权委托第三方提供以上服务和供应。</p>
        <p >3．甲方提供的服务或供应</p>
        <p >3．1&nbsp; 甲方应按照本协议及其附件的要求为乙方提供服务和供应。</p>
        <p >3．2&nbsp; 甲方提供的服务和供应包括就餐、宿舍、取暖等后勤保障服务及生产辅助方面的某些服务和供应，具体见附件。</p>
        <p >3．3&nbsp; 甲、乙方双方确认，除已列入本协议及其附件的相关服务和供应外，如果乙方在其生产经营和生活中还需要甲方提供其他服务或供应，甲方在拥有该等设备及能力的前提下，不得无故拒绝。双方应根据本协议确立的原则签订补充协议，以明确双方的权利和义务。此补充协议构成本协议的补充附件，与其他附件拥有相同效力。</p>
        <p >4．乙方支付费用的义务</p>
        <p >4．1&nbsp; 乙方应就甲方所提供的服务或供应，按双方约定的支付时间和方式向甲方支付相关费用。</p>
        <p >4．2&nbsp; 如果未对某一项服务的付款时间作出约定的，乙方付款时间便为甲方提供服务或供应后1个月内。</p>
        <p >4．3&nbsp; 乙方以人民币支付服务费。</p>
        <p >5．服务或供应费用的确定</p>
        <p >5．1&nbsp; 甲方向乙方提供的各项服务或供应的费用应按公正、公平、等价、合理的原则予以确定，甲方不得利用自己的优势或地位要求或迫使乙方接受任何违反上述原则的条件。</p>
        <p >5．2&nbsp; 甲、乙双方同意，确定每项服务或供应的费用时应参照以下标准和顺序：</p>
        <p >5．2．1&nbsp; 关于该项服务有国家价格，即根据法规规定的强制价格时，应按照此价格标准执行；</p>
        <p >5．2．2&nbsp; 有可适宜和的市场标准价格时则按此价格标准来确定；</p>
        <p >5．2．3&nbsp; 若无以上两种标准则按公平、等价、公正、合理的原则由双方协商确定。</p>
        <p >5．3&nbsp; 某项服务或供应的市场价格，应按以下方法确定：</p>
        <p >5．3．1&nbsp; 首先应参照本市第三方提供的类似服务或供应的正常的、公平的价格予以确定；</p>
        <p >5．3．2&nbsp; 若没有第三方提供类似的服务或供应可资参照，则原则上依据甲方提供服务或供应的实际成本（含营业税、印花税等税费）加5%的利润予以确定。</p>
        <p >5．4&nbsp; 除甲、乙方双方另有约定外，该等服务或供应价格由双方按上述原则确定后，在三年内不变，但有下列情形之一的，则有关价格应作出相应的调整：</p>
        <p >5．4．1&nbsp; 某项服务或供应的国家价格被取消，则双方应根据市场价格重新定价，具体生效时间自取消国家价格之日开始。</p>
        <p >5．4．2&nbsp; 某项服务的或供应的国家价格发生变更，则根据变更之日起执行变更后的国家价格。</p>
        <p >5．4．3&nbsp; 若某项服务或供应在签订本协议时并无国家定价，后又有了国家定价，按颁布国家定价之日起执行。</p>
        <p >5．4．4&nbsp; 若市场价格发生变动，则相应做出调整。</p>
        <p >5．5&nbsp; 若双方对服务或供应价格不能协商一致，则应将该等事宜提交<u><span>&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市场价管理部门确定，该部门的决定为最终结论，对双方均有约束力。</p>
        <p >6．声明和承诺</p>
        <p >6．1&nbsp; 甲方承诺。将不会以优于其向乙方提供服务或供应的条件向第三方提供类似的服务和供应；且甲方向乙方提供的服务或供应应优于其向第三人提供的服务或供应，服务或供应的质量也不应低于其向第三方提供的类似服务或供应的质量。</p>
        <p >6．2&nbsp; 双方承诺。将根据本协议及其附件的约定来完全履行各自应承担的义务：</p>
        <p >6．2．1&nbsp; 甲方承诺。保证有足够数量和质量的员工设施来向乙方提供服务或供应。</p>
        <p >6．2．2&nbsp; 乙方承诺。保证按约定的时间和方式向甲方支付相关费用。</p>
        <p >6．3&nbsp; 除甲、乙双方另有约定以外，乙方如果可以以优于甲方的条件（包括价格条件、质量条件）从第三方获得所需的某项服务或供应时，有权要求甲方提供同等条件的服务或供应。甲方如果拒绝或在2个月内未作出回答，乙方有权终止由甲提供该项服务或供应。</p>
        <p >6．4&nbsp; 如果发生不可抗力，甲方不能提供（包括完全不能和部分不能）某项服务或供应时，甲方应及时通过乙方，并可以在以书面形式通知乙方向的适当时间内终止提供该项服务或供应。</p>
        <p >6．5&nbsp; 如果乙方需要甲方增加或减少服务或供应项目或数量时，应提前1个月通知甲方。</p>
        <p >6．6&nbsp; 终止某项服务或供应将不影响本协议项下的甲方向乙方应当提供的其他服务或供应。</p>
        <p >7．违约责任</p>
        <p >7．1&nbsp; 甲方应当按照约定的质量、数量、时间、方式向乙方提供相应的服务或供应，否则，应承担违约责任，乙方有权要求甲方赔偿损失。</p>
        <p >7．2&nbsp; 乙方应当接受甲方按上述规定提供的服务或供应，并按约定的时间和方式向甲方支付相应的费用，否则，乙方应承担违约责任，甲方有权要求乙方赔偿损失。</p>
        <p >7．3&nbsp; 任何一方违反本协议而给另一方造成损失，应对另一方负违约责任；如果双方均有过错，则按各自过错大小承担相应的责任。</p>
        <p >8．不可抗力</p>
        <p >8．1&nbsp; 本协议中的不可抗力是指双方当事人所不能预见，不能控制的事件或情况。该等事件包括（但不限于）火灾、地震、暴风雨、暴风沙、洪水、战争等。</p>
        <p >8．2&nbsp; 任何一方当事人因不可抗力造成部分或全部不能或迟延履行本协议时，无须承担违约或赔偿责任，但该方应尽量采取一切合理措施以减少对方损失，并尽可能快地通知对方。</p>
        <p >8．3&nbsp; 任何一方当事人均应在不可抗力事件或情况结束后尽快地恢复履行其义务。</p>
        <p >9．本协议的终止</p>
        <p >在本协议的有效期间内，存在下列情形之一的，本协议终止：</p>
        <p >9．1&nbsp; 甲、乙双方任何一方破产、解散或依法被撤销；</p>
        <p >9．2&nbsp; 乙方不再需要甲方提供本协议下的所有服务或供应；</p>
        <p >9．3&nbsp; 甲方无力承担向乙方提供本协议项下的所有服务或供应；</p>
        <p >9．4&nbsp; 甲、乙双方合意终止本协议。</p>
        <p >10．争议的解决</p>
        <p >10．1&nbsp; 甲、乙双方如就本协议的解释和履行发生争议时，应通过友好协商的方式解决。</p>
        <p >10．2&nbsp; 若甲、乙双方在30日内协商不成的，任何一方均有权向<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市人民法院提起诉讼。</p>
        <p >11．本协议的有效期</p>
        <p >本协议自生效之日起计算，有效期为<u><span>&nbsp;&nbsp;&nbsp; </span></u>年。有效期届满后，如果乙方需要甲方继续提供该服务或供应的，应重新合同。</p>
