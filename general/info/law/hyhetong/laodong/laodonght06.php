　　二、工作岗位<BR> 
　　１．甲方根据生产和工作的需要，并参照乙方的工作技能或特长，经考核后择优上岗或安排适当的工作。上岗前应按照《上海竟成印刷厂岗位聘用实施办法》与所在部门签订上岗聘约。上岗聘约为本合同附件。<BR> 
　　２．甲方因生产和工作需要或根据乙方的工作能力和表现情况，可调动乙方的工作部门工作岗位，在征求乙方意见时，如无特殊情况，乙方应以服从为原则。<BR> 
　　３．双方有关岗位聘用、解聘等事中项按《上海竟成印刷厂岗位聘用实施办法》和《上海竟成印刷厂职工下岗待聘的暂行规定》办理。<BR> 
　　三、双方的责任和义务<BR> 
　　１．甲方应根据国家有关劳动保护、安全生产的法规制度，采取有效措施，为乙方提供良好的劳动环境和工作条件，加强对职工的安全、卫生和劳动保护，并根据生产和实际工作需要发给乙方必要的劳防用品和保健营养待遇。同时，对女职工应酌情实行特殊保护。<BR> 
　　２．甲方根据企业生产和经济发展情况，不断提高和改善职工生活福利待遇，并提供必要的集体生活设施和娱乐场所。<BR> 
　　３．甲方根据生产和工作需要，对职工提供必要的专业技术培训和业务进修条件，并进行政治文件学习、安全生产和厂规厂纪教育等。<BR> 
　　４．乙方有参加甲方民主管理，获得政治荣誉和物质奖励的权利。<BR> 
　　５．乙方上岗后应按照甲方的生产和工作要求，掌握本岗位的工作技能和操作规程，按质按量地完成各项规定的生产和工作任务，并接受甲方职能部门的有关考核。<BR> 
　　６．乙方在合同期内，应树有良好的职业道德和主人翁精神风貌，维护企业声誉，爱护集体财产。<BR> 
　　四、劳动报酬<BR> 
　　１．甲方实行本企业的内部工资分配形式并根据“按劳分配”的原则，按照岗位的劳动技能高低、工作责任大小、劳动强度和劳动条件优劣情况，确定不同工种的劳动报酬，随着生产经营发展和经济效益增长情况，逐步提高乙方劳动报酬和有关福利待遇。<BR> 
　　２．乙方工资、奖金、浮动工资、岗位工资、加班工资和相应的福利津贴等，仍按甲方现行的规定按月发放。<BR> 
　　３．乙方在生产或工作中有突出贡献或特殊成绩的，甲方可给予必要的精神鼓励和物质鼓励或晋级工资。<BR> 
　　五、福利待遇和劳动保险<BR> 
　　１．在劳动合同期间，乙方仍享受统一规定的有关津贴，物价补贴、计划生育、住房补贴、养老保险、独生子女费以及法定的公休节假日、探亲假、婚丧假、产假和甲方规定的职工休假等。<BR> 
　　２．在劳动合同期间内，乙方因工或非因工死亡的待遇以及家属劳保待遇等仍按国家现行政策规定执行。<BR> 
　　３．劳动合同期间，乙方患病或非因工负伤期间的有关待遇仍按国家现行的有关政策和本单位规章制度执行。对乙方的停工医疗期按《上海竟成印刷厂实行全员劳动合同制的暂行规定》中的有关条款执行。<BR> 
　　４．被列入下岗待聘范围的人员，其各种待遇按照《上海竟成印刷厂下岗待聘的暂行规定》执行。<BR> 
　　５．乙方到达离退休年龄，其离退休待遇仍按国家现行政策规定执行。<BR> 
　　六、劳动纪律<BR> 
　　乙方在劳动合同期间内必须自觉地遵守国家的有关法规法纪、遵守劳动纪律和甲方制定的各种规章制度，如有违纪违章行为，甲方有权按有关厂规厂纪规定给予必要的处罚。<BR> 
　　七、合同的变更、终止和解除<BR> 
　　１．凡有固定期限的劳动合同，期限届满时即为终止，甲乙双方经协商后可续订劳动合同。<BR> 
　　２．合同双方在履行劳动合同过程中，如发生特殊情况，无法履行劳动合同的有关内容，经双方协商一致后，可变更劳动合同的有关内容，但必须办理变更手续。<BR> 
　　３．职工到达规定的离退休年龄或因病丧失劳动能力提前退休时，劳动合同自然终止。<BR> 
　　４．在劳动合同期间，任何一方要求解除劳动合同，除属《上海竟成印刷厂实行全员劳动合同制暂行规定》中的第十六条第一、二、三、四款之外，必须提前一个月以书面形式通知对方，方可办理解除劳动合同的手续。<BR> 
　　５．在劳动合同期限内，乙方如属《暂行规定》中第十六条规定之一的，甲方可以解除劳动合同。<BR> 
　　６．乙方在劳动合同期限内，如遇有《暂行规定》第十八条规定之一的，甲方不得解除劳动合同。<BR> 
　　７．乙方在劳动合同期限内，如遇有《暂行规定》第十七条规定之一的，可向甲方提出解除劳动合同。<BR> 
　　８．被解除劳动合同的乙方人员，甲方根据《暂行规定》的有关条款办理有关手续。<BR> 
　　八、违约责任<BR> 
　　１．在合同期内，甲方除《暂行规定》第十六条、第十九条、乙方除《暂行规定》第十七条规定的条件外，均不得解除合同或自行离职，否则应支付违约金５００元。<BR> 
　　２．甲乙双方必须严格履行劳动合同，除遇有特殊情况，经双方协商一致不能履行劳动合同的有关内容外，任何一方违反合同给对方造成经济损失的，应根据其后果和责任大小，给对方赔偿经济损失。赔偿金额按有关规定或实际情况确定。<BR> 
　　３．凡由甲方出资对乙方进行培训、学历学习、进修或分房的人员，其有离职、调动或违约时，均按《上海竟成印刷厂关于职工在服务期内离岗及违约赔偿办法》有关规定执行。<BR> 
　　九、双方需要约定的有关条款<BR> 
　　１．本合同未尽事宜，均按《暂行规定》的有关规定办理。<BR> 
　　２．本合同的有关规定在执行过程中，如与国家新颁布的有关规定相抵触时，应按国家规定执行。甲乙双方需要修订或补充的，可协商修订补充。<BR> 
