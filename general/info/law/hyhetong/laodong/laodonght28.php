　　１．乙方所有人员在ＥＭＳ国发生工伤或疾病时，其医疗及住院费由甲方支付。<BR> 
　　２．现场医务室需用的常用药品和器具，由乙方向甲方提出购置计划，经甲方同意后，由乙方在其本国或其他地方采购，费用由甲方支付。<BR> 
