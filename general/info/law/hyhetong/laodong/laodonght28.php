　　ＰＭＨ公司（其总部设在××××，以下称“甲方”）与ＣＨＣ工程公司（其总部设在××××，以下称“乙方”），经过友好协商，乙方同意为甲方在ＥＭＳ国的ＺＺＺ项目的施工提供劳务，为此与甲方签订本劳务合同。<BR> 
　　（一）合同目的<BR> 
　　本合同的目的：乙方根据本合同条款向甲乙提供技术工人、工程技术人员和其他人员（以下称为派遣人员），甲方向乙方支付报酬。<BR> 
　　为保证甲方工程的顺利完成，双方应互相协作，认真执行合同。<BR> 
　　（二）人员派遣<BR> 
　　１．应按双方商定的计划派遣人员（详见附表２）。甲方对所需派遣的人员应提前两个月用书面正式通知乙方。乙方同意在派出前一个月向甲方提交派遣人员一览表，包括姓名、出生年月日、工种、护照号码及ＥＭＳ国申请入境所需要的资料。<BR> 
　　２．乙方负责办理乙方人员（从其居住国）的出境手续，并承担与此有关的各项费用。在ＥＭＳ国的入境和居住手续由甲方办理，并负担与此有关的各项费用。<BR> 
　　３．根据工程计划的需要，派遣人员可随时增加或减少。<BR> 
　　４．如要增加派遣人员时，甲方同意提前两个月向乙方总部提出派遣人员计划。增加人员的工资，按本协议附件１所列工资标准支付。增加如系新工程，其工资标准应由双方驻工地的代表商定。<BR> 
　　５．根据工程进度，如现场需要减少人员，则应由双方现场代表商定后实施。<BR> 
　　（三）准备费<BR> 
　　甲方同意付乙方派遣人员的准备费每人＿＿＿＿美元。准备费应在向乙方提交派遣计划的同时电汇乙方＿＿＿＿银行＿＿＿＿帐号。<BR> 
　　（四）工资<BR> 
　　１．派遣人员原工资应按附件中所商定的工资表支付。工资的计算应从派遣人员离开乙方所在国ＳＴＵ机场之日起到离开ＥＭＳ国＿＿＿＿机场之日止。乙方同意在安排航线时尽可能取最短路线，缩短时间。<BR> 
　　２．派遣人员的基本工资详见附件１。<BR> 
　　３．基本工资以月计算，凡不满一个月的按日计算，日工资为月工资的２５％。<BR> 
　　４．根据ＥＭＳ国目前的经济情况，派遣人员基本工资每年应增长１５％。<BR> 
　　（五）工作时间及加班<BR> 
　　１．乙方人员的工作时间为每月２５天，每周６天，每天８小时。<BR> 
　　２．每周休假一天，具体休假日期可由双方在现场安排。<BR> 
　　３．由于材料短缺、气候条件等影响不能正常施工时，经双方协商可以临时调整工作内容，如因上述及其他甲方原因造成停工时，甲方同意支付乙方人员的工资。<BR> 
　　４．如工作需要并经双方同意，乙方人员可以加班。甲方按下列标准支付加班工资；<BR> 
　　（１）平时加班工资为基本工资的１２５％；<BR> 
　　（２）平时夜间加班（２２点至次日晨５点）以及休假日加班，工资为基本工资的１５０％；<BR> 
　　（３）节日加班工资为基本工资的２００％；<BR> 
　　（４）加班工资计算方法如下：<BR> 
　　（月基本工资／２００小时）×加班小时数×加班工资的百分率<BR> 
　　（５）上述加班工资和基本工资同时支付。<BR> 
　　（六）伙食<BR> 
　　１．甲方同意向乙方提供厨房全套炊餐具及冷藏设备，由乙方自行办理伙食。<BR> 
　　２．甲方同意付给乙方每人每天＿＿＿＿美元的伙食费，包干使用。<BR> 
　　３．食堂用水、用电和燃料以及生活物资采购用车由甲方提供并支付费用。<BR> 
　　（七）节日和休假<BR> 
　　１．所有乙方人员有权享有ＥＭＳ国政府的法定节日。<BR> 
　　２．所有乙方人员在工作满１１个月零１０天后，应享受２０天的回国探亲假，其ＥＳＭ国＿＿＿＿机场至ＳＴＵ机场的往返机票由甲方支付。应尽可能安排最短的航线。<BR> 
　　３．如果现场施工需要乙方人员推迟回国休假，乙方同意说服其人员延期休假，甲方同意为了补偿乙方人员的损失，应给予适当的报酬。<BR> 
　　４．关于补偿上述损失的报酬，可根据当时的情况由双方现场代表商定。但这项补偿不应少于ＥＭＳ国＿＿＿＿机场至ＳＴＵ机场之间的单程机票价金额。<BR> 
　　５．乙方人员由于家属不幸等原因，工作满半年以上时，经双方现场代表协商同意，可以提前享用探亲假。如有关人员已享受回国休假，其往返旅费应由乙方负担，对这一事假甲方不支付工资。<BR> 
　　（八）旅费及交通<BR> 
　　１．甲方负担乙方人员从ＳＴＵ机场至工程现场之间的往返旅费和航空公司招待之外的必须的食宿费，但乙方应努力减少这项额外费用的开支，甲方同意支付乙方人员进入ＥＭＳ国的入境费用（例如机场税等）。<BR> 
　　２．甲方负责提供乙方人员上下班交通工具，同时也提供现场代表、工程师及其他管理人员的工作用车。<BR> 
　　３．乙方应凭机票或收据（按购票当日银行公布的外汇牌价）向甲方结算。<BR> 
