　　１．乙方负责派遣身体健康、技术熟练的合格人员到ＥＭＳ国现场工作，如甲方认为派遣的人员不能胜任工作，经双方现场代表同意后，由乙方负责替换，由此而发生的费用应由乙方负责。<BR> 
