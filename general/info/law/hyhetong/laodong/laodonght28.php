　　乙方人员应在＿＿＿＿（其原居住国）交纳的一切税金由乙方量；乙方人员在ＥＭＳ国交纳的一切税金由甲方负担。<BR> 
　　（十）社会保险<BR> 
　　１．乙方人员在合同有效期内的人身保险，由乙方自行办理。甲方同意支付乙方派遣人员每月＿＿＿＿美元的人身保险费。<BR> 
