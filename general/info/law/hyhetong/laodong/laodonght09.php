　　甲方因生产（工作）需要，经劳务市场介绍，同意招用＿＿＿＿为临时工。经双方协商同意签订本合同，并共同遵守如下条款：<BR> 
　　一、合同期限：<BR> 
　　自一九＿＿年＿＿月＿＿日起至一九＿＿年＿＿月＿＿日止。合同期满，本合同自行终止。如甲方需要继续留用，经乙方同意，双方可以续订合同，并办理鉴证手续。<BR> 
　　二、生产（工作）任务：<BR> 
　　甲方根据生产（工作）需要，安排乙方在＿＿岗位，承担＿＿临时生产（工作）任务。乙方若同意，须服从。在合同期内甲方因调整生产（工作）任务，需要变更乙方的岗位和任务，需经乙方同意。如乙方不同意，可提出辞职，双方办理解除合同手续。<BR> 
　　三、劳动时间与劳动报酬：<BR> 
　　（１）劳动时间：甲方实行每周＿＿日，每日＿＿小时工作制。严格控制加班加点，确因生产（工作）需要加班加点的，甲方应从流水作业和台机人员的操作等问题考虑，提前通知乙方并征得同意，按规定发给加班加点费。<BR> 
　　（２）劳动报酬：按国家有关规定和单位的实际情况，根据乙方的岗位和承担任务，甲乙双方协商定为每日＿＿＿＿元×２５．５天＝每月＿＿＿＿元。加班工资，按不低于国家规定的标准执行。奖金，根据单位效益和劳动贡献，定期发给。实行计件工资制的，月工资按计件单价结算。具体办法在本合同双方约定栏中约订。从事夜班的，应发给夜餐费＿＿＿＿元。<BR> 
　　（３）在合同期间，如发生停工待料情况，甲方每天发给乙方基本生活费＿＿＿＿元。<BR> 
　　四、劳动保护、保险福利待遇：<BR> 
　　（１）从城镇招用的临时工，实行社会养老保险制度。保险金缴纳办法与本单位劳动合同制工人相同。临时工本人负担标准工资的百分之三，先由甲方统一支付，后在其当月工资中扣除。<BR> 
　　（２）根据工作岗位需要，并参照国家有关规定，甲方发给乙方劳动保护用品：＿＿＿＿、劳动保健食品费＿＿＿＿元。从事起重、电焊、司炉等特殊工种作业的，需持有关部门核发的《专业岗位操作证》方可上岗。<BR> 
　　（３）在合同期内，临时工或农民工发生工伤、致残、工亡或非因工伤、残、亡以及疾病的，其待遇应分别按国务院第４１号令、第８７号令及内蒙古自治区人民政府第２９号令的有关规定执行。<BR> 
　　（４）合同期间，甲方发给乙方每月副食品补贴＿＿＿＿元。冬季取暖期间，按规定每月发取暖津贴＿＿＿＿。<BR> 
　　五、劳动纪律：<BR> 
　　乙方在合同期必须遵守以下纪律：<BR> 
　　（１）按时上下班，不得迟到早退。<BR> 
　　（２）严格遵守操作规程，保证生产。<BR> 
　　（３）爱护单位财产，不无故损坏公物。<BR> 
　　（４）上班工作（生产）时间不得做私事不得做与生产工作无关的事。<BR> 
　　（５）听从指挥，服从调动。<BR> 
　　（６）保质保量完成任务，不得投机取巧。<BR> 
　　（７）有事及时请示报告，不得擅自主张。<BR> 
　　六、劳动合同的终止、解除和辞职<BR> 
　　（１）有下列情形之一的，可以解除劳动合同：<BR> 
　　①双方一致同意的；<BR> 
　　②符合本合同第六条第三项和第六条第四项规定的；<BR> 
　　③乙方培训（熟练）期满，不符合录用条件或本人不愿意供职的；<BR> 
　　④乙方非因工负伤，治疗终结仍不能复工的。<BR> 
　　（２）有下列情形之一的，本合同自行解除：<BR> 
　　①甲方宣告破产；<BR> 
　　②乙方被除名开除、劳动教养或判处徒刑的。<BR> 
　　（３）乙方在合同期内有下列情形之一的，甲方可以辞退；<BR> 
　　①严重违犯劳动纪律，影响生产、工作秩序的；<BR> 
　　②违反操作规程，损坏设备、工具，浪费原材料、能源，造成经济损失的；<BR> 
　　③服务态度恶劣，损害消费者利益的；<BR> 
　　④有贪污、盗窃、赌博，营私舞弊等违法行为，尚未构成刑事责任的；<BR> 
　　⑤无理取闹、打架斗殴，严重影响社会秩序或犯有其他严重错误的。<BR> 
　　（４）甲方有下列情形之一的，乙方可以辞职：<BR> 
　　①经国家有关部门确认，劳动安全卫生条件恶劣，无有效劳动保护措施，严重损害工人身体健康的；<BR> 
　　②甲方不按合同规定发放工资或连续两个月不支付工资的；<BR> 
　　③人格受到甲方负责人侮辱的；<BR> 
　　④甲方不履行劳动合同，或违反国家劳动法律、政策，侵害工人合法权益的；<BR> 
　　⑤自费考入中等专业以上学校学习的；<BR> 
　　⑥经国家批准应征入伍的。<BR> 
