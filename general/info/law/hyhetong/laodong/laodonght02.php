　　第九条　违反劳动合同应承担的违约责任：<BR> 
　　１．甲乙双方任何一方违反合同时，应向对方支付违约金，违约金标准是：＿＿＿＿。<BR> 
　　２．甲乙双方任何一方违反合同，给对方造成经济损失的，应根据后果及责任大小给予赔偿。<BR> 
　　３．甲方出资培训乙方，应事先与乙方商定在本企业的服务期限。乙方服务期未满，欲辞职或另谋职业的，应支付甲方培训费：＿＿＿＿元。<BR> 
　　第十条　劳动争议处理：<BR> 
　　甲乙双方发生劳动争议后，应先行协商解决。协商不能解决的，可在法定申诉时效内向有管辖权的劳动争议仲裁委员会申请仲裁。<BR> 
