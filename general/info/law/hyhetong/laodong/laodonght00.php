　　劳动合同是指用人单位与劳动者之间为建立一定的劳动关系而签订的有关劳动权利义务关系内容的协议。签订劳动合同应当注意的问题有：<BR> 
　　（１）劳动合同条款必须齐全，符合国家法律的要求。根据劳动法的规定，劳动合同的主要条款应当包括：合同期限和试用期限；劳动者的工种、职务和职称；工作时间；劳动报酬、保险和福利待遇；生产和工作条件；教育与培训；政治待遇和劳动纪律；劳动合同的变更和解除；违约责任；双方商定的其他事项。<BR> 
　　（２）签订劳动合同的主体资格要合适。根据劳动法的规定，用人单位不能招用童工，也就是说劳动者必须是达到法定年龄的有劳动能力的人。<BR> 
　　（３）双方商定的权利义务必须符合国家的规定。例如，甲方作为用人单位必须保证雇工的劳动安全，提供必要的劳动安全条件，不得克扣劳动者的工资等。<BR> 
　　（４）劳动合同应当是书面形式的。<BR> 
