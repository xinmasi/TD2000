　　２．说明<BR> 
　　<BR> 
　　个人合伙是指两个及其两个以上公民按照协议，各自提供资金、实物、技术等，共同经营、共同劳动、共担风险、共负盈亏的自愿联合。其法律特征是：①合伙须有两个及其以上的公民；②合伙是按合伙合同联合起来的经济单位；③合伙人必须共同出资、共同经营、共同劳动、共担风险；④合伙财产归全体合伙人共有，合伙人对合伙债务承担连带责任。个人合伙应当签订合伙协议。合伙协议是指明确合伙人之间权利义务关系的协议。《民法通则》规定，合伙人应当对出资数额、盈余分配、债务承担、入伙、退伙、合伙终止等事项，订立书面协议。当事人未订立书面协议，但具备合伙条件，又有两个以上无利害关系人证明有口头协议的，人民法院可以认定其具有合伙关系。签订合伙协议应当注意的问题有：<BR> 
　　（１）个人合伙可以起字号，依法经核准登记，在核准登记的经营范围内从事经营。合伙人应当对出资数额、盈余分配、债务承担、入伙、退伙、合伙终止等事项，订立书面协议。合伙人的权利有：①合伙事务的经营权、决定权和监督权，合伙的经营活动由合伙人共同决定，无论出资多少，每个人都有表决权；②合伙人享有合伙利益的分配权；③合伙人分配合伙利益应以出资额比例或者合同的约定进行，合伙经营积累的财产，归合伙人共有；④合伙人有退伙的权利。合伙人的义务有：①按照合伙协议的约定维护合伙财产的统一；②分担合伙的经营损失和债务；③为合伙债务承担连带责任。<BR> 
　　（２）个人合伙的经营活动，由合伙人共同决定，合伙人有执行或监督的权利。合伙人可以推举负责人。合伙负责人和其他人员的经营活动，由全体合伙人承担民事责任。合伙的债务，由合伙人按照出资比例或者协议的约定，以各自的财产承担清偿责任。合伙人对合伙的债务承担连带责任，法律另有规定的除外。偿还合伙债务超过自己应当承担数额的合伙人，有权向其他合伙人追偿。<BR> 
