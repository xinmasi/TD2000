　　８．公司的组织法和章程，或其证明无误的副本。<BR> 
　　９．董事长姓名：＿＿＿＿<BR> 
　　１０．资产负债表，关于借项、贷项、固定资产、投资、股份和在其他公司合作及盈亏状况等，并附过去３年经审计的资产负债表和盈损计算书。<BR> 
