      <p> 　　<BR> 
　　一、投标答辩<BR> 
　　１．投标答辩，由＿＿＿＿公司《＿＿＿＿厂租赁经营》评标委员会组织，公开进行，允许旁听。<BR> 
　　２．《＿＿＿＿厂租赁经营》评标委员会邀请＿＿市公正机关和有关部门的代表参加投标答辩会议。<BR> 
　　３．投标答辩人，由《＿＿＿＿厂租赁经营》评标委员会经过初步筛选后产生。没有取得投标答辩权的投标人，可以参加投标答辩会议旁听。<BR> 
　　４．投标答辩的顺序，由投标答辩人抽签决定。答辩开始后，尚未轮到答辩的投标人，不准参加答辩会（包括合伙人）。答辩完毕的投标人，可以列席旁听其他人的答辩。<BR> 
　　５．投标答辩人在答辩前，认为个别评委对自己的评判可能产生不公正的结果，有权向评标委员会主任提出请求该评委回避，评标委员会主任认为有必要时，告知被请求人，在对该投标答辩人的《投标方案》评议和评分时回避。<BR> 
　　６．从投标答辩会议开始，到投标答辩会议结束，评标委员会委员不准和任何投标答辩人接触，如发现有徇私舞弊行为，撤销其评委资格和有关投标人的答辩、中标资格。<BR> 
　　７．投标答辩人应当遵守答辩会议的各项规则，按顺序进行答辩，到时不参加答辩的以弃权论。<BR> 
　　８．投标答辩人的答辩时间相等，答辩分为：《投标方案》解释阶段和答辩阶段，每个阶段结束前三分钟，会议主持人应当提醒投标答辩人，到时应当通知答辩人停止发言。<BR> 
　　９．投标答辩人的发言要言简意赅、突出重点，以《投标方案》为准，离题太远时，评委会主任应当提醒答辩人进行纠正。<BR> 
　　评委向投标答辩人提出的问题，只限于《投标方案》的范围内。投标答辩人对评委提出的与《投标方案》无关的问题可以拒绝回答。<BR> 
　　１０．投标答辩人应以自己的最佳方案，进行一次答辩。<BR> 
　　１１．《投标方案》中有关投标人的家庭财产部分，评委有为其保密的义务，如因评委泄密，造成严重后果，泄密人应负行政或者法律责任。<BR> 
　　二、评标与决标<BR> 
　　１２．评标委员会委员在评标和决标中，要立场公正、实事求是、合理评分、确切评议，对所有投标答辩人，一视同仁。<BR> 
　　１３．评委在投标答辩人评分时，禁止相互协商。<BR> 
　　评委应当在投标答辩人答辩后十分钟内，就地按时以无记名方式作出自己的评分和评语表，交工作人员进行统计和计算，由公证机关代表进行监督。<BR> 
　　１４．招标工作人员应当在投标答辩后一日内，对每个投标答辩人的得分及评语要点，整理好后交评委会主任。<BR> 
　　１５．评委会经过评议，预选四名中标候选人，向＿＿厂职工大会作冶厂方演说。<BR> 
　　预选的中标候选人，应认真准备书面演说词，一式两份，交评委会，并按演说词进行演说。<BR> 
　　评委会应专门听取＿＿厂职工代表对中标候选人演说的意见。<BR> 
　　１６．决标由全体评委采用无记名投标方式进行，得票超过评委人数三分之二多数的中标，评委会主任宣布中标人。<BR> 
　　投标表决时，应有公证机关的代表参加验票和计算票数。<BR> 
　　表决结果如均未超过三分之二的票数时，则依得票数额取其前两名，由评委会进行再次评议表决，得票多者中标。<BR> 
　　１７．少数评委对中标人有重大异议，认为自己的意见足以说明中标人无经营能力和条件的，可在决标会上要求重新表决。评委会主任认为有必要时，可重新投票表决一次，重新表决后的中标人即为中标人，决标终止。<BR> 
　　１８．投标人放弃中标权的，应在决标前向评委提出。<BR> 
　　三、其它事项<BR> 
　　１９．投标答辩、评标及决标的一切资料，由招标工作人员分门别类，整理存档备查。<BR> 
　　２０．中标人的《投标方案》和答辩记录作为签订租赁经营合同的依据之一，在签订租赁经营合同时，中标人反悔的应负行政责任，情节严重的要负法律责任，还要赔偿招标单位本次招标及再行招标活动的一切费用。<BR> 
　　合伙承租的投标人，合伙人负连带责任。<BR> 
　　２１．本《投标答辩、评标、决标程序及规则》的未规定事宜，评委会主任有权处置。<BR> 
　　２２．租赁经营合同生效后，招标工作全部结束。<BR> 
　　２３．本规则从＿＿年＿＿月＿＿日生效。<BR> 
