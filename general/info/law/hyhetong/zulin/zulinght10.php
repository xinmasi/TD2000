      <p> 　　<BR> 
　　北京外交人员房屋服务公司（以下简称甲方）自＿＿＿＿年＿＿月＿＿日起，将＿＿＿＿＿＿外交人员公寓＿＿楼＿＿＿单元＿＿＿号公寓＿＿＿套租给＿＿＿国驻华大使馆（以下简称乙方）作住宅之用。如房屋结构、装置或设备有缺陷由甲方负责修理并承担费用。<BR> 
　　双方兹订立租赁合同如下：<BR> 
　　<BR> 
　　第一条<BR> 
　　<BR> 
　　自本合同生效之日起，乙方须按季度预付租金（外汇支票）＿＿＿＿美元（或其他外币，或外汇兑换券＿＿＿元）。乙方接到甲方的收租通知单后，应在三十天内一次付清。逾期须按日支付千分之＿＿＿＿的滞纳金，以补偿甲方所受的损失。<BR> 
　　甲方根据北京地区物价和房屋修缮费用提高的幅度，可以对乙方承租的公寓租金进行调整，但应提前三个月通知乙方。<BR> 
　　<BR> 
　　第二条<BR> 
　　<BR> 
　　甲方负责向乙方承租的房屋提供水、电、煤气和热力。乙方须按仪表数据缴付电费和煤气费。由于甲方对乙方承租的房屋设备维修不及时，引起水、电和热力的供应中断，甲方将酌情减收相应的费用。<BR> 
　　<BR> 
　　第三条<BR> 
　　<BR> 
　　甲方对出租房屋进行定期检查，并负责下列自然损耗的免费维修：<BR> 
　　１．屋顶漏雨，地板糟朽；<BR> 
　　２．门窗五金配件、门窗纱的修理和更新；<BR> 
　　３．管道堵塞、漏水，阀门、水咀和水箱零件失灵，上下水管道、暖气片及零件、卫生设备的修理和更换；<BR> 
　　４．灯口、插座、电门和电线的修理和更换；<BR> 
　　５．公用楼道的粉刷，首层每二年一次，二层以上每三年一次；<BR> 
　　６．电梯故障的及时排除和对乘员的人身安全保险。<BR> 
　　乙方应将承租的公寓和设备损坏的情况及时通知甲方，甲方在接到通知后应尽快修理，甲方未能及时修理的，乙方可以自行修理，修理费用由甲方承担。<BR> 
　　<BR> 
　　第四条<BR> 
　　<BR> 
　　甲方对出租的公寓进行检查、维修前，应征得乙方同意，乙方应给予必要的方便和协助。如遇住房发生水灾、大量跑水、煤气漏气，在甲方无法及时通知乙方的危急情况下，甲方可进入乙方住宅处置。<BR> 
　　甲方要定期检查和保养楼内的消防器材。<BR> 
　　<BR> 
　　第五条<BR> 
　　<BR> 
　　由于甲方对乙方承租的住房检查不周、维修不及时而造成房屋结构损坏，发生安全事故，使乙方遭受损失时，由甲方负责赔偿。如房屋大修，不能继续使用，必须腾出时，甲方应给乙方提供条件大体相当的住房，乙方应按期从承租的公寓中迁出。如果乙方借故拖延不迁出，造成一切损失，由乙方负责。乙方如发现承租的房屋危及自身安全或健康，可以随时解除合同。<BR> 
　　<BR> 
　　第六条<BR> 
　　<BR> 
　　乙方对承租的公寓和设备有爱护保管的责任。由于使用不当造成的损失或损坏要负责修复或赔偿。乙方应遵守《住户须知》（另发）中的规定。乙方对公寓住房结构或装置设备的自然损耗不负赔偿责任。<BR> 
　　<BR> 
　　第七条<BR> 
　　<BR> 
　　乙方在征得甲方同意后，对承租的公寓可进行下列各项自费工程：<BR> 
　　１．在不损坏房屋结构的条件下，改变室内装修原样；<BR> 
　　２．增添固定装置和设备。<BR> 
　　乙方在改变室内装修原样、拆除或更换原有的炉具、灯具、卫生设备等应事先通知甲方并负担相应的损失费。在租赁关系结束时，乙方增添或更换的设备可以拆除。乙方可以自费油漆粉刷房间。<BR> 
　　<BR> 
　　第八条<BR> 
　　<BR> 
　　乙方在征得甲方同意后，可以对承租的公寓增加电容量。但施工费用由乙方负担。乙方不得使用超负荷的电气设备和擅自改动供电设备。否则，由此造成的损失，由乙方负全部责任。<BR> 
　　乙方应自行设置必要的消防器材。<BR> 
　　<BR> 
　　第九条<BR> 
　　<BR> 
　　遇有不可抗拒的自然灾害，致使乙方承租的公寓遭到破坏，甲方应视房屋破坏的程度，免收部分或全部租金，直至该公寓修复时为止。<BR> 
　　<BR> 
　　第十条<BR> 
　　<BR> 
　　乙方承租的房屋不得私自转让或转租。退房时须提前半个月通知甲方，经甲方检验住房及其设备，确认完好（除自然损耗外）后，双方办理终止租赁手续。租金按终止日结算。<BR> 
　　乙方主动申请调换公寓时，须缴付原租公寓的油漆粉刷费。<BR> 
　　<BR> 
　　第十一条<BR> 
　　<BR> 
