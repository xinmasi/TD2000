      <p> 　　<BR> 
　　租赁单位＿＿＿＿＿<BR> 
　　经过租赁经营指导小组和答辩委员会的答辩审查，＿＿＿（以下简称甲方）决定将＿＿＿＿租赁给＿＿＿（以下简称乙方）个人经营，经甲乙双方协商签订合同如下：<BR> 
　　１．租赁期为＿＿年，即自＿＿＿＿年＿＿月＿＿日起，至＿＿＿＿年＿＿月＿＿日止。<BR> 
　　２．自本合同生效之日起，乙方为＿＿＿＿＿厂长，具有法人代表资格。<BR> 
　　３．乙方需请两名保证人，共同以个人财产作为抵押，其中乙方为＿＿＿＿元，保证人共为２２５８元。乙方及保证人用以抵押的个人财产经甲方和公证处共同调查并登记造册后，在租赁期内不得转移或变卖。<BR> 
　　４．乙方的权利和义务：<BR> 
　　（１）对＿＿＿＿＿拥有完全的生产经营自主权，包括机构设置、人员配备、行政干部任免、规章制度的制定，以及分配形式、生产经营方式的确定等。<BR> 
　　（２）依法有权从事多种经营并决定其经营方向。<BR> 
　　（３）有权按照有关政策和规定对职工进行表彰、奖励和处分、辞退等奖惩处理。<BR> 
　　（４）有权按政策规定招收租赁期内有关的临时合同工。<BR> 
　　（５）必须认真贯彻执行党和国家的各项方针、政策、法令和法规；接受主管部门的领导和综合部门的指导、监督；自觉接受企业党组织和职代会的监督，保证职工的主人翁地位和合法权益；定期向职代会汇报工作，听取职工的意见和建议。<BR> 
　　（６）对企业的财产、必须进行社会保险；要按国家规定标准提取折旧基金和大修理基金，维修和更新设备、房产。合同履行期满，要按固定资产清单如数返还，并保证设备完好率达到＿＿＿＿％以上。<BR> 
　　（７）按规定交纳个人所得税。<BR> 
　　５．甲方权利和义务；<BR> 
　　（１）甲方有权对乙方的工作进行监督和检查。<BR> 
　　（２）甲方应积极支持乙方行使正当职权，帮助、指导乙方依法管理和经营企业。<BR> 
　　６．租赁经营的分配：<BR> 
　　（１）＿＿＿＿＿＿＿应照章纳税，并按规定缴纳能源交通基金。<BR> 
　　（２）乙方应向甲方缴纳基数租金＿＿＿＿元，当＿＿＿＿钢球厂年实现利润大于租赁的基数利润时，以下形式确定数额缴纳租金：基数利润×利率＝基数租金＿＿＿＿＿×＿＿＿＿＿＝＿＿＿＿＿元<BR> 
　　租赁年度实现利润<BR> 
　　租金＝基数租金＝－－－－－－－－－－－<BR> 
　　租赁基数利润<BR> 
　　当＿＿＿＿＿年实现利润小于租赁基数利润时，按规定的基数租金１１００元缴纳，不足部分由承租人和保证人用个人资产抵补。<BR> 
　　（３）甲方收到租金后，应按有关规定全部返给＿＿＿＿作为企业留利。<BR> 
　　（４）乙方对本企业的留利必须合理使用，百分之四十作为企业发展基金，百分之六十作为福利基金。<BR> 
　　（５）＿＿＿＿＿＿的盈利，按规定交纳所得税后，按税后利润提＿＿＿＿％作为乙方个人收入，乙方从租赁收入中除按规定向甲方交纳租金外，每年应提留＿＿＿％作为风险基金；乙方每年从个人收入中扣除租金、风险基金后，其余全部为自己个人所得，对保证人的获利额，由乙方与保证人议定并从乙方个人所得中分给。<BR> 
　　（６）租赁期间停发乙方的工资和奖金（保留原工资级别，如遇调整工资，可按规定给予调整级，但不发薪金）乙方可按每月预支生活费用，数额可略高于其原来工资和资金之和，但最高不得超过原工资和与奖金之和的二倍。<BR> 
　　（７）乙方提留的风险基金应存入＿＿＿＿＿＿，并按银行存款利率计息，存入企业期间可用于生产经营，租赁期满或解除合同后由乙方全部（含利息）提走。<BR> 
　　（８）＿＿＿＿＿的租赁年度实现利润，除按规定交纳所得税、租金及支付乙方个人收入外，其余部分为企业留利基金，核定＿＿＿＿基数留利基金为＿＿＿＿＿＿万元，租赁期间企业留利基金应逐年增长，如达不到基数留利基金＿＿＿＿万元时，乙方应用私人财产赔偿减少额的＿＿＿＿％。<BR> 
　　７．甲乙双方对＿＿＿＿＿要遵守如下经济政策：<BR> 
　　（１）按集体企业管理，并按集体企业征收工商所得税和奖金税。<BR> 
　　（２）以企业＿＿＿＿＿年工资总额＿＿＿＿万元，产品销售收入＿＿＿＿万元（即百元销售收入的工资含量＿＿＿＿元）为基数，各租赁年内职工工资总额随销售收入的增减而浮动，只要人均利润、上交利税和劳动生产率增长幅度高于工资增长幅度，允许职工工资（含奖金，计件超额工资）据实列支，计入成本，因国家统一调整职工工资而需增加的工资总额另行增补。<BR> 
　　（３）用自有资金进行技术改造，项目投产后的新增利润一年内由企业留用，经财税部门批准还可适当延长留用期；用贷款进行技术改造，允许用新增利润税前还款，还贷后，企业可按核定的比例提留福利基金。将自有资金与贷款合并使用所获得的新增利润，按自有资金所占比例提留给企业，继续用于技术改造。<BR> 
　　（４）以前年度应该归还的技术贷款，在租赁期内逐年归还，因归还技术贷款而减少的利润允许提取福利基金，由于归贷而减少的利润均不影响乙方的提成基数，其资金来源应由增提的福利基金支付。<BR> 
　　（５）因生产经营需要对房屋进行改造，只要投资额和面积没有超过国家有关规定，免征建筑税。<BR> 
　　（６）乙方除交纳租金外，不再交纳承包费。<BR> 
　　８．违约责任：<BR> 
　　（１）由于甲方非法干预对乙方造成的损失，应由甲方负责。<BR> 
　　（２）乙方若不能按本合同规定如期如数缴纳租金，其不足部分由乙方和保证人用个人的抵押财产抵补。抵补原则为：①先乙方后保证人；②据低补数额需要，对保证人按登记财产赔尽为止；③要保证乙方个人及家庭必需的生活费；④抵补不够部分由乙方同甲方签订债务合同，经公证处公证生效。<BR> 
　　９．合同的变更或解除：<BR> 
　　（１）甲乙双方无正当理由均不得随意要求变更或解除合同。<BR> 
　　（２）因国家政策的变化或自然灾害的影响，致使租赁经营无法按本合同规定执行时，甲乙双方均有权要求变更或解除合同。<BR> 
　　（３）因乙方违反合同规定或其它原因损害了甲方的利益，或因甲方违反合同规定，严重干扰乙方生产经营自主权，致使乙方无法自主经营，合法收入得不到保证，甲乙任何一方均可要求解除合同。<BR> 
　　（４）如乙方发生意外而无力执行合同时，甲方有权要求终止合同。<BR> 
　　（５）本合同的变更或解除应由甲乙双方协商达成书面协议。并经公证处认可方为有效。若双方达不成协议，经公证处调解无效时，任何一方均可向法院起诉，由法院依法判决。<BR> 
　　１０．本合同需由甲乙双方及保证人签字，并需经公证处公证后方可生效。<BR> 
　　１１．本合同公证费用由＿＿＿＿＿支付。<BR> 
　　１２．本合同未尽事宜，由双方随时协商解决。<BR> 
　　１３．本合同正本六份，甲乙双方及保证人和公证处各一份；副本九份，报市经委、市体改委、市财政局、市税务局、市审计局、市劳动局、市工商银行、＿＿＿＿＿区税务局、人行＿＿＿＿办各一份备案。<BR> 
