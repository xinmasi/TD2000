　　日期：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿　日期：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿注：买卖合同是指卖方提供货物，买方支付货款的，以买卖为目的双务合同。卖方将货物的所有权转移给买方，买方将相应的金额支付给卖方，这是双方履行合同的权利和义务。若一方不履行其义务，另一方有权拒绝执行合同的义务。货物（Ｇｏｏｄｓ）买卖或称合同的标的物系指有形动产（Ｃｏｒｐｏｒａｌ　Ｍｏｖａｂｌｅ　Ｐｒｏｐｅｒｔｙ），但不包括股票（Ｓｔｏｃｋ　Ｃｅｒｔｉｆｉｃａｔｅ）、流通票据（Ｃｅｒｔｉｆｉｃａｔｅ　ｏｆ　Ｉｎｄｅｂｔｅｄｎｅｓｓ）、财产权利（Ｒｉｇｈｔ　ｏｆ　Ｐｒｏｐｅｒｔｙ）和不动产（Ｒｅａｌ　Ｐｒｏｐｅｒｔｙ）等。<BR> 
　　货物买卖合同属于诺成合同，买卖双方达成协议，经签字即可生效。有些国家对货物买卖合同的最低交易额作了明文规定。买卖双方所签的合同，一般来讲，以书面合同为主，若无书面合同，一旦发生争执，仲裁委员会（或法院）不予以受理。在国际间采用的买卖合同是由律师事务所书就，以示对买卖双方的权利和义务平等互利。<BR> 
