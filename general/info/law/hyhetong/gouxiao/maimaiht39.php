　　１９＿＿＿＿年＿＿＿月＿＿＿日＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿为卖方和＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿为买方。双方同意买卖＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿，其条款如下：<BR> 
　　１．合同货物：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　２．产　　地：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　３．数　　量：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　４．商　　标：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　５．合同价格：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿Ｆ．Ｏ．Ｂ．<BR> 
　　６．包　　装：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　７．付款条件：签订合同后买方于７个银行日内开出以卖方为受益人的，经确认的、不可撤销的、可分割、可转让的、不得分批装运的、无追索权的信用证。<BR> 
　　８．装船：从卖方收到买方信用证日期算起，４５天内予以装船。若发生买方所订船舶未按时到达装货，按本合同规定，卖方有权向买方索赔损毁／耽搁费，按总金额＿＿＿％计算为限。因此，买方需向卖方提供银行保证。<BR> 
　　９．保证金：卖方收到买方信用证的１４个银行日内，向买方寄出＿＿＿％的保证金或银行保函。若卖方不执行合同其保证金买方予以没收。<BR> 
　　１０．应附的单据：卖方向买方提供：<BR> 
　　（１）全套清洁提货单；<BR> 
　　（２）一式四份经签字的商业发票；<BR> 
　　（３）原产地证明书；<BR> 
　　（４）装箱单；<BR> 
　　（５）为出口＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿所需的其他主要单据。<BR> 
　　１１．装船通知：卖方在规定的装货时间，至少１４天前用电报方式将装船条件告知买方。买方或其代理人将装货船估计到达装货港的时间告知卖方。<BR> 
　　１２．其他条款：质量、数量和重量的检验可于装货港一次进行。若要求提供所需的其他证件，其办理手续费、领事签证费应由买方负担。<BR> 
