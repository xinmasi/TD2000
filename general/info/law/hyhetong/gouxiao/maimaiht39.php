　　１５．延期费／慢装卸罚款：对于＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿载重吨船来说，每天＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿Ｕ．Ｓ．Ｄ．。<BR> 
　　１６．不可抗力：签约双方的任何一方由于台风，地震和双方同意的不可抗力事故而影响合同执行时，则延迟履行合同的期限应相当于出事故所影响的时间。<BR> 
