      <p> 　　<BR> 
　　订立合同双方：<BR> 
　　供电单位：＿＿市（县）供电局（所），以下简称供电方；<BR> 
　　用电单位：＿＿，以下简称用电方。<BR> 
　　为了协调电力供、用双方的关系，明确双方的责任，确立正常的供用电秩序，安全、经济、合理地使用电力，根据《全国供用电规则》的规定，经供、用电双方充分协商，特订立本合同，以便共同遵守。<BR> 
　　一、受电地点、受电电压、受电容量及限期<BR> 
　　１．受电地点：＿＿＿＿＿＿＿＿＿<BR> 
　　２．受电电压：＿＿千伏＿＿线三相交流＿＿千伏。其中，３５千伏及以上供电和对电压质量有特殊要求的电压变运幅度为额定电压的±５％；１０千伏及以下高压供电和低压电力的电压变动幅度为额定电压的±７％；低压照明用电的电压变动幅度为额定电压的＋５％、－１０％。电网容量在３００万千瓦及以上者，供电周率允许偏差为±０．２周／秒；电网容量在３００万千瓦以下者，供电周率允许偏差为±０．５周／秒。<BR> 
　　３．受电容量：三相交流＿＿千伏安，其中＿＿千伏安＿＿台，＿＿千伏安＿＿台，……。<BR> 
　　４．合同期限：合同期限为＿＿年，从＿＿年＿＿月＿＿日起至＿＿年＿＿月＿＿日止。<BR> 
　　二、用电方新装、增容与变更用电<BR> 
　　１．用电方新装或增加用电，均应向供电方办理用电申请手续，并按规定办理有关事项。<BR> 
　　２．供电方为新装或增加用电的用电方确定的供电方案，高压的有效期限为１年，低压的有效期限为３个月，逾期注销。用电方如有特殊情况，应及时与供电方协商延长。<BR> 
　　３．用电方新装或增加用电，应按国家有关规定，向供电方交纳帖费，以分担电力部门为适应用电增加而进行的输电、变电、配电工程建设或改造的部分费用。专线供电或用户已列入基建项目的工程，由用户投资建设。<BR> 
　　４．用电方投资建设的输电、变电、配电设施，建成送电后，其产权归属，按《全国供用电规则》的规定办法确定。<BR> 
　　５．用电方提出减少用电容量，供电方应根据用电方所提的期限，保留其原容量，保留期最长不得超过２年。在保留期限内恢复用电时，不再交付帖费；超过保留期限要求恢复用电时，按新装、增容手续办理。按变压器容量计算基本电费的用电方，必须停止整台或整组变压器的运行，方算暂停用电。自暂停用电期满之日起，无论用电方申请恢复用电与否，都应交付全部基本电费。　　　<BR> 
　　６．用电方变更用电性质、变更户名、减少用电容量、暂停或停止用电、移动表位和迁移用电地址，均应事先向供电方办理手续。<BR> 
　　停止用电时，应将电费结清。<BR> 
　　迁移用电地址而引起供电点变更时，新址用电按新装用电办理。<BR> 
　　三、设计、安装、试验与接电<BR> 
　　１．用电方新装、增装或改装电气装置的设计、安装和试验，应符合国家的有关标准，国家尚未制定标准的，应符合原水利电力部或＿＿省（或自治区、直辖市）电力部门的规定和规程。<BR> 
　　２．高压方式供电的用电方，应向供电方提供下列电气装置的设计文件和资料：<BR> 
　　（１）电气设计说明；<BR> 
　　（２）用电负荷分布图；<BR> 
　　（３）负荷组成、性质及保安电力；<BR> 
　　（４）用电功率因数的计算和无功补偿及容量；<BR> 
　　（５）高压设备的一次接线方式和布置；<BR> 
　　（６）过电压保护、继电保护和计量装置的方式。<BR> 
　　低压方式供电的用电方应提供负荷组成和用电设备清单，１００千伏安（千瓦）及以上低压用电方还应提供用电功率的计算和无功补偿资料。<BR> 
　　用电方提供的设计文件和资料应一式２份，供电方审核提出书意见后，退还用电方１份据以施工。用电方如改变设计，应将变更方案再交供电方审核。用电方安装竣工后，应向供电方提供高压电气设备试验及继电保护装置整定记录，经供电方检查，直至合格。<BR> 
　　３．无功电力应就地平衡。用电方应在提高用电自然功率因数的基础上，设计和装置无功补偿设备，并做到随其负荷和电压变动及时投入或切除，防止无功电力倒送。用电方在供电方规定的电网高峰负荷时的功率因数应达到下列规定：<BR> 
　　（１）高压供电的工业用电和高压供电装有带负荷调整电压装置的用电，功率因数为０．９０以上；<BR> 
　　（２）其他１００千伏安（千瓦）以上用电（包括大、中型电力排灌站），功率因数为０．８５以上；<BR> 
　　（３）趸售和农业用电，功率因素为０．８０。<BR> 
　　（凡功率因数不能达到上述规定的新用电方，供电方可拒绝接电。未达到上述规定的现有用电方，应在二、三年内增添无功补偿设备，达到上述规定。对长期不增无功补偿设备又不申明理由的用户，供电方可停止或限制供电。供电方应督促和帮助用电方采取措施，提高功率因数。）<BR> 
　　４．用电方在供电前应申请用电指标，并就供电方式、装接容量、用电时间、产权划分、调度、通讯、计量方式和电费计收等项，与供电方签订供用电合同（或协议），供电方即可装表接电。<BR> 
　　５．用电方的冲击性负荷、不对称负荷和整流用电等对供电质量和安全经济运行有影响者，应采取技术措施消除影响，否则供电方可不供电。<BR> 
　　四、安全用电、计划用电、节约用电<BR> 
　　１．安全用电：<BR> 
　　（１）供电方供电设施的计划检修，校验和试验工作应统一安排，需要对用电方停电时，３５千伏以上每年一般不超过１次；１０千伏每年一般不超过３次。计划检修停电应在７天前通知用电方。<BR> 
　　（２）用电方应定期进行电气设备和保护装置的检查、检修和试验，防止电气设备事故和错误操作；用电方的电气设备危及人身和运行安全时，应立即检修；多路电源供电的用电方应加装连锁装置，并按照双方签订的协议进行调度操作；装有自备发电机组的供电方备案，并应采取保安措施，防止在电网停电时向电网反送电。<BR> 
　　用电方发生人身触电伤亡、主要电气设备损坏及用电方的原因引起电网停电等事故时，应立即向供电方报告，并在７天内提出事故分析报告。　　<BR> 
　　（３）用电方与电力系统的继电保护方式，应相互配合，并按照原水利电力部颁发的有关规程进行整定和检验。由供电方整定、加封的继电保护装置及其二次回路和供电方规定的继电保护整定值，用电方不得自行变动。<BR> 
　　（４）供电方对用电方的安全用电工作应督促检查，并积极协助有关主管部门及用电方共同做好对用电方电工的技术培训和管理工作，定期进行安全技术考核。<BR> 
　　２．计划用电：<BR> 
　　（１）用电方应定期提出计划用电指标的申请，内容包括：计划期内的生产任务、单位产品电耗定额、需用电量、最高电力负荷、生产班次和节约用电措施等。<BR> 
　　（２）用电方设备的检修应尽量安排在枯水期（用电方工业设备的检修应尽量安排在农业排灌季节）。<BR> 
　　（３）供电方和用电方都应服从电网统一调度，严格按指标供电和用电，不得超分超用。供电方应认真执行“谁超限谁”、“超用扣还”的原则。供电方装设电力定量装置，用电方不得拒绝。<BR> 
　　３．节约用电：<BR> 
　　（１）用电方应定期编制节约用电措施计划，完成节约用电任务；供电方应督促、检查、帮助用电方的节约用电工作。<BR> 
　　（２）用电方应积极采用节约用电的技术措施，推广行之有效的节约经验。用电方因此节约用电，“三电”办公室不得减少其用电指标。凡国家推广的节约用电技术措施，用电方必须纳入节约用电措施计划，付诸实施。用电方如不采用，“三电”办公室可相应扣除用电指标。<BR> 
　　（３）供电方和用电方应加强非生产用电的管理，取消对家庭生活用电的包用、包费制，一律按实用电量由个人缴费。使用非生产性民炉，应经供电局批准。<BR> 
　　五、维护管理与产权分界<BR> 
　　１．供电方与用电方电气设备的维护管理范围按产权分界点划分，其确定原则如下：<BR> 
　　（１）低压供电的，以供电接户线的最后支持物为分界点，支持物属供电方；<BR> 
　　（２）１０千伏及以下高压供电的，以用电方界外或配电室前的第一断路器或进线套管为分界点（第一断路器或进线套管的维护管理责任，由双方协商确定）；<BR> 
　　（３）３５千伏及以上高压供电的，以用电方界外或用电方变电站外第一基电杆为分界点，第一基电杆属供电方；<BR> 
　　（４）产权属于用电方的线路，以分支点或以供电方变电所外第一基电杆为分界点（第一基电杆维护管理责任由双方协商确定）。<BR> 
　　（采用电缆供电的，本着便于维护管理的原则，由双方协商确定。）<BR> 
　　２．供电方和用电方分工维护管理的供电、用电设备，未经分管单位同意，不得操作或更动。如因紧急事故必须操作或更动者，事后应迅速通知分管单位。<BR> 
　　３．供电方由于工程施工或线路维护上的需要，在用电方处凿墙、挖沟、掘坑、巡线等时，用户应给予方便。供电方人员应遵守用电方的有关安全保卫制度。用电方到供电方维护的设备区工作，应征得供电方同意，并在供电方人员监护下工作。竣工后，均应及时修复。<BR> 
　　六、电度计量与收费<BR> 
　　１．计费电度表及其附件的购置、安装、移动、更换、校验、拆除、加封、启封等，均由供电方负责办理。高压电用电方的成套设备装有自备电度表及其附件的，经供电方同意并检验合格后，可用作计费电度表，并办理固定资产无偿转移手续，用电期间由供电方负责维护管理。用电终了后，再办理资产无偿转还手续。<BR> 
　　装设在６３千伏及以上计量点的计费电度表应使用互感器的专用二次回路；装设在６３千伏以下计量点的计费电度表应设置专用的互感器，不得与保护、测量等回路共用，现已共用的，应逐步改造。<BR> 
　　２．计费电度表应装在产权分界处，变压器的有功、无功损耗和线路损失由产权所有者负担。<BR> 
　　３．用电方对供电方安装的计费电度表及附件应负责保管，如遗失或因用电方责任损坏，应赔偿或负担修理费。由于用电方原因需要移动表信时，工料费由用电方负担。<BR> 
　　４．用电方要求校验计费电度时，供电方应尽速办理，经校验合格者，应收校验费；不合格者，不收校验费。用电方对校验结果仍有异议时，可要求供电方上级计量监督机构直至国家计量局参加处理。用电方自备的分表，供电方应接受修理校验，收取费用。<BR> 
　　５．计费电度计量装置误差超过允许范围或记录不准，供电方应按实际误差及起讫时间，退还或补收电费。起讫时间查不清时，可按《全国供用电规则》的规定办法计算。<BR> 
　　６．供电方应固定抄表日期，按期抄表收费。用电方应按供电方规定的期限交付电费。对逾期不交者，按规定加收迟纳金，并可停止供电?　　供电方对用电量较多的用电方，由银行分次划拨电费，月末抄表结算。供电方可委托银行、农村信用社托收或代收电费。<BR> 
　　七、违约责任<BR> 
　　１．供电方未按计划指标向用电方供电时，事后应补还少供的电力、电量，应向用电方偿付少供电量电费的＿＿％的违约金，违约金不足以赔偿用电方损失的，供电方并应赔偿用电方的损失；用电方超计划指标用电时（包括低容少用电力），供电方除扣还其超用电量外，并征违约金，违约金按多用电量电费的＿＿％计算。<BR> 
　　２．供电方由于运行、操作的责任事故造成用电方停电时，供电方应按用电方在停电时间内可能用电量的电度电费的５倍（单一制电价为４倍）给予赔偿，该可能用电量按停电前用电方正常用电量计算。但电力系统开关掉闸，经自动重合闸重合良好或对有备用电源的用电方，只停其中一路电源，其它电源可以满足用电方备用供电设备能力时，供电方不负赔偿责任。<BR> 
　　３．由于用电方的责任造成供电方对外停电，用电方应按少供电量电费予以赔偿。用电方引起的事故，因供电方的责任而扩大停电范围，则用电方不负事故扩大部分的赔偿责任。<BR> 
　　４．供电电压超出本合同规定的变动幅度时，供电方应按用电方实际所用的不合格电量电费的２０％给予赔偿。但用电方用电的功率因数未达到本合同规定，或其他用电方的内部原因引起电压波动，供电方不负责任。<BR> 
　　电压波动超出允许变动幅度的时间，以用户自备并经供电方校验合格的电压自动记录仪的记录为准；如用电方未装此仪表，则以供电方变电所的电压记录为准。<BR> 
　　５．供电周率超出本合同规定的允许偏差时，供电方应按用电方实际所用不合格电量电费的２０％予以赔偿。<BR> 
　　周率变动超出允许偏差的时间，以用电方自备并经供电方检验合格的周率自动记录仪表记录为准。<BR> 
　　６．供电方如因施工错误或由于供电方的责任导致高压供电线路断落连接到低压供电线路，造成用电方用电设备烧毁时，应对该设备修复或给予合理赔偿。<BR> 
　　７．用电方如在电价低的供电线路上，私自接用电价高的用电设备或私自改变用电类别，按实际使用时间向供电方补交差额电费，并处以１－２倍差额电费的罚金。对使用起讫日期难以确定者，至少按３个月计算。<BR> 
　　８．用电方超过报装容量私自增加用电容量，应追补电费，处以每千瓦（千伏安）２０元的违约金，并拆、封私增设备。用电方擅自使用已报暂停电气设备或启用封存电气设备，应追补电费，处以每千瓦２０元的罚金，并再次封存擅自启用的电气设备。<BR> 
　　９．用电方如私自迁移、更动和擅自操作供电方的电度计量装置、电力定量装置、线路或其他供电设施，处以２０至５０元的罚金。用电方经供电方同意，自行引入备用电源，按接用容量处以每千瓦５０元的罚金。<BR> 
　　八、其它<BR> 
