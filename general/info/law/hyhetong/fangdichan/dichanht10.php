　　<BR> 
　　第一章　总则<BR> 
　　<BR> 
　　第一条　本合同当事人<BR> 
　　委托方（以下简称甲方）：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　受托方（以下简称乙方）：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　根据有关法律、法规，在自愿、平等、协商一致的基础上，甲方将＿＿＿＿＿（物业名称）委托于乙方实行物业管理，订立本合同。<BR> 
　　第二条　物业基本情况<BR> 
　　物业类型：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　座落位置：＿＿＿＿＿＿市＿＿＿＿区＿＿＿＿＿路（街道）＿＿＿＿＿＿号<BR> 
　　四至：东＿＿＿＿＿＿南＿＿＿＿＿＿西＿＿＿＿＿＿北＿＿＿＿＿＿＿＿＿<BR> 
　　占地面积：＿＿＿＿＿＿＿＿平方米<BR> 
　　建筑面积：＿＿＿＿＿＿＿＿平方米<BR> 
　　委托管理的物业构成细目见附件一。<BR> 
　　第三条　乙方提供服务的受益人为本物业的全体业主和物业使用人，本物业的全体业主和物业使用人均应对履行本合同承担相应的责任。<BR> 
　　<BR> 
　　第二章　委托管理事项<BR> 
　　<BR> 
　　第四条　房屋建筑共用部位的维修、养护和管理，包括：楼盖、屋顶、外墙面、承重结构、楼梯间、走廊通道、门厅、＿＿＿＿＿＿。<BR> 
　　第五条　共用设施、设备的维修、养护、运行和管理，包括：共用的上下水管道、落水管、垃圾道、烟囱、共用照明、天线、中央空调、暖气干线、供暖锅炉房、高压水泵房、楼内消防设施设备、电梯、＿＿＿＿＿。<BR> 
　　第六条　市政公用设施和附属建筑物、构筑物的维修、养护和管理，包括道路、室外上下水管道、化粪池、沟渠、池、井、自行车棚、停车场、＿＿＿＿＿。<BR> 
　　第七条　公用绿地、花木、建筑小品等的养护与管理。<BR> 
　　第八条　附属配套建筑和设施的维修、养护和管理，包括商业网点、文化体育娱乐场所、＿＿＿＿＿。<BR> 
　　第九条　公用环境卫生，包括公共场所、房屋共用部位的清洁卫生、垃圾的收集、清运、＿＿＿＿＿。<BR> 
　　第十条　交通与车辆停放秩序的管理。<BR> 
　　第十一条　维持公共秩序，包括安全监控、巡视、门岗执勤、＿＿＿＿＿＿。<BR> 
　　第十二条　管理与物业相关的工程图纸、住用户档案与竣工验收资料。<BR> 
　　第十三条　组织开展社区文化娱乐活动。<BR> 
　　第十四条　负责向业主和物业使用人收取下列费用：<BR> 
　　１．物业管理服务费；<BR> 
　　２．＿＿＿＿＿＿＿；<BR> 
　　３．＿＿＿＿＿＿＿。<BR> 
　　第十五条　业主和物业使用人房屋自用部位、自用设施及设备的维修、养护，在当事人提出委托时，乙方应接受委托并合理收费。<BR> 
　　第十六条　对业主和物业使用人违反业主公约的行为，针对具体行为并根据情节轻重，采取批评、规劝、警告、制止、＿＿＿＿＿＿＿等措施。<BR> 
　　第十七条　其它委托事项<BR> 
　　１．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　２．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　３．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　<BR> 
　　第三章　委托管理期限<BR> 
　　<BR> 
　　第十八条　委托管理期限为＿＿＿＿年。自＿＿＿＿年＿＿月＿＿日时起至＿＿＿＿年＿＿月＿＿日时止。<BR> 
　　<BR> 
　　第四章　双方权利义务<BR> 
　　<BR> 
　　第十九条　<BR> 
　　Ａ　甲方权利义务（适用于业主委员会）<BR> 
　　１．代表和维护产权人、使用人的合法权益；<BR> 
　　２．制定业主公约并监督业主和物业使用人遵守公约；<BR> 
　　３．审定乙方拟定的物业管理制度；<BR> 
　　４．检查监督乙方管理工作的实施及制度的执行情况；<BR> 
　　５．审定乙方提出的物业管理服务年度计划、财务预算及决算；<BR> 
　　６．在合同生效之日起＿＿日内向乙方提供＿＿＿＿平方米建筑面积的经营性商业用户，由乙方按每月每平方米＿＿＿＿元租用，其租金收入用于＿＿＿；<BR> 
　　７．在合同生效之日起＿＿日内向乙方提供＿＿＿平方米建筑面积管理用房（产权属甲方），由乙方按下列第＿＿项执行；<BR> 
　　（１）无偿使用；<BR> 
　　（２）按建筑面积每月每平方米＿＿＿元租用，其租金收入用于＿＿＿＿＿。<BR> 
　　８．负责收集、整理物业管理所需全部图纸、档案、资料，并于合同生效之日起＿＿日内向乙方移交；<BR> 
　　９．当业主和物业使用人不按规定交纳物业管理费时，负责催交或以其它方式偿付；<BR> 
　　１０．协调、处理本合同生效前发生的管理遗留问题：<BR> 
　　（１）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　（２）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　１１．协助乙方做好物业管理工作和宣传教育、文化活动；<BR> 
　　１２．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　Ｂ　甲方权利义务（适用于房地产开发企业）<BR> 
　　１．在业主委员会成立之前，负责制定业主公约并将其作为房屋租售合同的附件要求业主和物业使用人遵守。<BR> 
　　２．审定乙方拟定的物业管理制度；<BR> 
　　３．检查监督乙方管理工作的实施及制度的执行情况；<BR> 
　　４．审定乙方提出的物业管理服务年度计划、财务预算及决算；<BR> 
　　５．委托乙方管理的房屋、设施、设备应达到国家验收标准要求。<BR> 
　　如存在质量问题，按以下方式处理：<BR> 
　　（１）负责返修；<BR> 
　　（２）委托乙方返修，支付全部费用；<BR> 
　　（３）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　６．在合同生效之日起＿＿日内向乙方提供＿＿＿＿平方米建筑面积的经营性商业用房，由乙方按每月每平方米＿＿＿元租用，其租金收入用于＿＿＿＿＿＿；<BR> 
　　７．在合同生效之日起＿＿＿日内向乙方提供＿＿＿＿平方米建筑面积管理用房（产权属甲方），由乙方按下列第＿＿＿项执行：<BR> 
　　（１）无偿使用；<BR> 
　　（２）按建筑面积每月每平方米＿＿＿元租用，其租金收入用于＿＿＿＿。<BR> 
　　８．负责收集、整理物业管理所需全部图纸、档案、资料，并于合同生效之日起＿＿＿日内向乙方移交；<BR> 
　　９．当业务和物业使用人不按规定交纳物业管理费用时，负责催交或以其它方式偿付；<BR> 
　　１０．协调、处理本合同生效前发生的管理遗留问题：<BR> 
　　（１）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿；<BR> 
　　（２）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　１１．协助乙方做好物业管理工作和宣传教育、文化活动；<BR> 
　　１２．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　第二十条　乙方权利义务<BR> 
　　１．根据有关法律法规及本合同的约定，制订物业管理制度；<BR> 
　　２．对业主和物业使用人违反法规、规章的行为，提请有关部门处理；<BR> 
　　３．按本合同第十六条的约定，对业主和物业使用人违反业主公约的行为进行处理；<BR> 
　　４．可选聘专营公司承担本物业的专项管理业务，但不得将本物业的管理责任转让给第三方；<BR> 
　　５．负责编制房屋、附属建筑物、构筑物、设备、设备、绿化等的年度维修养护计划和大中修方案，经双方议定后由乙方组织实施；<BR> 
　　６．向业主和物业使用人告知物业使用的有关规定，当业主和物业使用人装修物业时，告知有关限制条件，订立书面约定，并负责监督；<BR> 
　　７．负责编制物业管理年度管理计划、资金使用计划及决算报告；<BR> 
　　８．每＿＿个月向全体业主和物业使用人公布一次管理费用收支帐目；<BR> 
　　９．对本物业的公用设施不得擅自占用和改变使用功能，如需在本物业内改、扩建或完善配套项目，须与甲方协商经甲方同意后报有关部门批准方可实施；<BR> 
　　１０．本合同终止时，乙方必须向甲方移交全部经营性商业用房、管理用房及物业管理的全部档案资料；<BR> 
　　１１．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　<BR> 
　　第五章　物业管理服务质量<BR> 
　　<BR> 
　　第二十一条　乙方须按下列约定，实现目标管理。<BR> 
　　１．房屋外观：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　２．设备运行：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　３．房屋及设施、设备的维修、养护：＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　４．公共环境：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　５．绿化：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　６．交通秩序：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　７．保安：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　８．急修：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　小修：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　９．业主和物业使用人对乙方的满意率达到：＿＿＿＿＿＿＿＿<BR> 
　　具体的物业管理服务质量要求见附件二。<BR> 
