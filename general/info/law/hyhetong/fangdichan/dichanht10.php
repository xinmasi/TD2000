　　第六章　物业管理服务费用<BR> 
　　<BR> 
　　第二十二条　物业管理服务费<BR> 
　　１．本物业的管理服务费，住宅房屋由乙方按建筑面积每月每平方米＿＿＿元向业主或物业使用人收取；非住宅房屋由乙方按建筑面积每月每平方米＿＿＿元向业主或物业使用人收取。<BR> 
　　２．管理服务费标准的调整，按＿＿＿调整。<BR> 
　　３．空置房屋的管理服务费，由乙方按建筑面积每月每平方米＿＿＿＿元向＿＿＿＿＿＿收取。<BR> 
　　４．业主和物业使用人逾期交纳物业管理费的，按以下第＿＿＿＿项处理：<BR> 
　　（１）从逾期之日起按每天＿＿＿元交纳滞纳金；<BR> 
　　（２）从逾期之日起按每天应交管理费的万分之＿＿交纳滞纳金；<BR> 
　　（３）＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿。<BR> 
　　第二十三条　车位使用费由乙方按下列标准向车位使用人收取：<BR> 
　　１．露天车位：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　２．车库：＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　３．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　第二十四条　乙方对业主和物业使用人的房屋自用部位、自用设备、毗连部位的、维修、养护及其它特约服务，由当事人按实发生的费用计付，收费标准须经甲方同意。<BR> 
　　第二十五条　其它乙方向业主和物业使用人提供的服务项目和收费标准如下：<BR> 
　　１．高屋楼房电梯运行费按实结算，由乙方向业主或物业使用人收取；<BR> 
　　２．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　３．＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿＿<BR> 
　　第二十六条　房屋的共用部位、共用设施、设备、公共场地的维修、养护费用：<BR> 
　　１．房屋共用部位的小修、养护费用，由＿＿＿承担；大中修费用，由＿＿＿承担；更新费用，由＿＿＿承担。<BR> 
　　２．房屋共用设施、设备小修、养护费用，由＿＿＿承担；大中修费用，由＿＿＿承担；更新费用，由＿＿＿承担。<BR> 
　　３．市政公用设施和附属建筑物、构筑物的小修、养护费用，由＿＿＿承担；大中修费用，由＿＿＿承担；更新费用，由＿＿＿承担。<BR> 
　　４．公用绿地的养护费用，由＿＿＿承担；改造、更新费用，由＿＿＿承担。<BR> 
