　　<BR> 
　　第七章　违约责任<BR> 
　　<BR> 
　　第二十七条　甲方违反合同第十九条的约定，使乙方未完成规定管理目标，乙方有权要求甲方在一定期限内解决，逾期未解决的，乙方有权终止合同；造成乙方经济损失的，甲方应给予乙方经济赔偿。<BR> 
　　第二十八条　乙方违反本合同第五章的约定，未能达到约定的管理目标，甲方有权要求乙方限期整改，逾期未整改的，甲方有权终止合同；造成甲方经济损失的，乙方应给予甲方经济赔偿。<BR> 
　　第二十九条　乙方违反本合同第六章的约定，擅自提高收费标准的，甲方有权要求乙方清退；造成甲方经济损失的，乙方应给予经济赔偿。<BR> 
　　第三十条　甲乙任一方无正当理由提前终止合同的，应向对方支付＿＿＿元的违约金；给对方造成的经济损失超过违约金的，还应给予赔偿。<BR> 
