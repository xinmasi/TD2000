      <p> 　　<BR> 
　　根据《深圳经济特区土地管理条例》（以下简称“条例”），深圳市国土局为一方（以下简称“市国土局”），深圳福田建设开发有限公司为另一方（以下简称“用地者”）（“用地者”一词在本土地使用合同以及附件一的土地使用规则中的上下文许可的情况下亦包括用地者的继承人或受让人）。双方达成如下协议，于一九八九年＿＿月＿＿日在＿＿＿＿签订本土地使用合同，明确双方的权利和义务。<BR> 
　　一、本土地使用合同签订日期起三十（３０）天内，由市国土局将编号为福田壹号地段，土地面积约＿＿平方米的地块（以下简称“甲地段”）的土地使用权，连同其上的权益，一次性出让和转移给用地者使用。甲地段位置图见本土地使用合同附件二宗地图红线范围。甲地段之面积以实际批出的宗地图为准。<BR> 
　　土地使用年限伍拾（５０）年从签署本土地使用合同日期起计，即＿＿年＿＿月＿＿日至＿＿年＿＿月＿＿日。<BR> 
　　土地用途如附件四之发展大纲图，包括标准厂房、专业厂房、单身职工宿舍以及商业楼宇（上述各项定义见土地使用规则）。<BR> 
　　二、用地者同意支付下列款项成交甲地段：（Ⅰ）地价总额为＿＿＿＿万港币；（Ⅱ）征地费总额＿＿＿＿万港币；以及（Ⅲ）为单身职工宿舍用地另付＿＿＿＿万港币。上述（Ⅰ），（Ⅱ）和（Ⅲ）之总和称为“用地价款”。<BR> 
　　三、本土地使用合同所附之土地使用规则是本土地使用合同的不可分割的组成部分。用地者愿意遵守土地使用规则。<BR> 
　　四、双方同意按土地使用规则第二条和第三条的方式支付上述第二条的款项。<BR> 
　　五、在用地者付清上述第二条的用地价款后，市国土局应即发给国有土地使用证，并将甲地段的空地占有交付给用地者。<BR> 
　　六、用地者应每年向国土局支付土地使用费，根据土地使用规则第四条界桩定点所确定的甲地段的确实面积，每平方米支付人民币＿＿＿＿元。<BR> 
　　七、当用地者取得国有土地使用证后，可以按照土地使用规则规定的规划用途和程序，进行赠与、出售、交换、抵押和出租其全部或部分土地，无论上盖建筑物完成与否。<BR> 
　　八、市国土局及一切属下部门于行使土地使用规则项下的任何有关之权力或采取任何行动前，必须提前二十一日发出书面通知用地者方为有效。<BR> 
　　九、交付于用地者的必须是空地占有，没有任何未经授权的建筑物或任何其他非法占有。<BR> 
　　十、用地者取得国有土地使用证后可即按本土地使用合同附件二的土地使用规则，开发建设甲地段。<BR> 
　　十一、本土地使用合同的订立、效力、解释、履行和争议的解决均受中华人民共和国公布的法律的保护和管辖。如果中华人民共和国法律对有关本合同的某一事项未加以规定时，应参照国际商业惯例。<BR> 
　　十二、本土地使用合同经双方签字盖章后生效。<BR> 
　　十三、本土地使用合同正本一式三份，市国土局一份、用地者一份、市公证处一份。本合同副本八份，市计划局一份，市国土局一份，市税务局一份，市建设局一份，市财政局一份，用地者两份。<BR> 
