　　２．说明<BR> 
　　<BR> 
　　房屋租赁合同是指出租人与承租人之间达成的有关房屋租赁权利义务关系的协议。根据该协议，出租人有义务将房屋交付给承租人使用，承租人有义务缴纳租金并保证租赁的房屋完整。签订房屋租赁合同应当注意的问题有：<BR> 
　　（１）出租人必须是房屋的所有人或者有权出租的使用人。如果是转让租房屋必须经过原出租人的同意并取得相应的法律证明文件。<BR> 
　　（２）承租人的义务中应当明确要对房屋的完整负全部责任。不得破坏房屋的整体结构，不得影响房屋的原有的质量，以保证出租人的合法权益。<BR> 
　　（３）合同的条款要齐全、完备。要明确租赁的期限、租金交付的方式、出租人和承租人的基本权利和义务等内容。<BR> 
