　　第一条　总则<BR> 
　　合作分两期进行。<BR> 
　　第一期，乙方首先提供四组月产５４０００部收音机和电子钟收音机所需的全部设备和检验仪器，这些设备和仪器是新的、先进的、价格是优惠的，并须经甲方确认为准。设备和仪器的价款，在甲方收取的装配费内分期偿付。<BR> 
　　乙方并负责不作价提供装配上述产品所需的全部元件、器件和包装物料；甲方负责提供相应的厂房、电力，并负责组织装配，收取加工费，产品全部交给乙方。<BR> 
　　进行步骤：<BR> 
　　（１）甲方于９月３０日前将负责装配任务的工厂平面（草）图提交乙方，而乙方则于１０月２０日前派出工程技术人员携带厂房调整方案和设备安装图纸到佛山市无线电一厂，与甲方共同商定施工计划。<BR> 
　　（２）从１１月１日起，乙方应于９０天到１２０天内将有关设备和仪器运抵上述工厂；甲方应于同时期内完成厂房调整，并即着手进行设备安装工程。<BR> 
　　（３）乙方应于设备和仪器运抵上述工厂一个月内，将不作价提供的够三个月用的装配上述产品的全部元件、器件和包装物料运抵上述工厂；甲方则应于收到上述元件、器件和包装物料之日起一个月内为试装阶段，第二个月起开始正式装配。<BR> 
　　（４）双方一致同意，对计算机、电视机等其他电子产品的装配任务，待上述两项产品的装配正式投产后便立即进行。<BR> 
　　第二期，为降低产品成本，提高竞争能力，双方一致商定将逐步采用甲方生产的元件、器件，为此，在收音机的装配正式投产后，乙方立即提供制造上述产品的元件、器件所需的全部，设备和仪器，对提供这些设备和仪器的条件与价款偿付办法，按提供上述设备和仪器的条件与价款偿付办法办理。<BR> 
　　第二条　装配费计算<BR> 
　　因每批装配的具体品种不同，工序的繁简、难易也不同，双方同意每项品种的装配费装在有关合同中具体规定。<BR> 
　　第三条　损耗率<BR> 
　　由于所装配的产品之不同品种、款式，其损耗率也不尽相同。因此，双方一致同意在签订有关合同时，再另行商定合理的损耗定额。（此损耗定额系指元器件在装配前经过检验的全部合格品，在装配过程中的自然损耗率）。<BR> 
　　第四条　交货条件<BR> 
　　有关设备、仪器，ＦＯＢ香港交货。元件、器件和包装物料运抵佛山的运费由乙方承担（卸货费用由甲方负担）；甲方承担将装配成品运抵香港或九龙交货，卸装费用由乙方负担。<BR> 
　　第五条　交货期<BR> 
　　为使甲方能正常、持续生产，乙方提供的元件、器件和包装物料应于生产月前１５天运抵甲方工厂；装配成品的交货期在每批合同中具体规定。<BR> 
　　第六条　责任问题<BR> 
　　如因装配技术而发生的产品质量与合同规定不符或因延期交货而引起的损失，由甲方负担；如果元件、器件、包装物料不合格、不配套或不能按时运交甲方而引起的产品质量不合格或因延期交货而引起的损失由乙方负担。<BR> 
　　第七条　技术交流<BR> 
　　本协议签订后，乙方在接到甲方通知时，须尽快派出工程技术人员到佛山市无线电一厂负责设备安装和装配技术指导，直至该厂能全部掌握设备安装和装配技术进行正常生产时为止。<BR> 
　　上述工程技术人员在厂工作期间的生活费用由甲方负担，工资由乙方负担；必要时，甲方亦可派出工程技术人员到乙方所属工厂交流技术，其所需生活费用，由乙方负担。<BR> 
　　第八条　协议有效期限<BR> 
　　本协议用中、英文书就正本各一式二份，甲、乙双方各执一份，两种文本具有同等效力，自１９９８年９月２２日起正式生效，有效期限为１０年。任何一方如须延长本协议，则须提前半年向另一方提出，经双方协商确定。<BR> 
　　第九条　本协议如有未尽事宜，须经双方友好协商一致后，方得进行必要的修改和补充。<BR> 
