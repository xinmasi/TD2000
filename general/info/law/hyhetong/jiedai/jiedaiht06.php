　　第七条　贷款的偿还<BR> 
　　<BR> 
　　一、“借款人”应按照“贷款人”提出的还款时间表中列明的金额和日期，按期如数偿还贷款本息和支付费用。“借款人”应提前　　个工作日将应偿付款项支付给“贷款人”以保证“贷款人”按时向国外贷款银行还款。<BR> 
　　二、“借款人”应以与贷款货币相同的货币还本付息，支付费用。<BR> 
　　<BR> 
　　第八条　提前还款<BR> 
　　<BR> 
　　一、政府贷款可以提前还款。“借款人”应将提前还款金额、方式事先通知“贷款人”，“贷款人”应在五个工作日内做出答复，如在规定的期限内未做答复，即视为同意“借款人”提前还款。<BR> 
　　二、买方信贷提前还款时，“借款人”应提前５０天通知“贷款人”。在得到“贷款人”书面同意后，从还款时间表列明的最后一期还款开始倒顺序提前还款。提前还款应在付息日进行，提前还款金额应为一期还款额的完整倍数。<BR> 
　　三、“借款人”提前还款的通知为不可撤销的。已提前还款的部分不得要求再贷。<BR> 
　　四、因提前还款所发生的费用由“借款人”负担。<BR> 
　　<BR> 
　　第九条　逾期利息<BR> 
　　<BR> 
　　“借款人”如未能按本协议第六、七条的规定偿还任何到期的款项（包括本金、利息及费用），“贷款人”将向“借款人”计收应付未付部分款项的逾期利息。该利息的具体计收方法为：逾期之日起（包括这一日）至“贷款人”实际收到该笔应付款项日止（不包括这一日），“借款人”按逾期之日“贷款人”公布的半年期现汇贷款利率和适用的买方信贷利率之较高者向“贷款人”支付逾期利息。借款逾期１年以上，贷款人则在上述利率基础上，向借款人收取２０％至５０％的加息。　　<BR> 
　　第十条　保险<BR> 
　　<BR> 
　　“借款人”在“商务合同”生效后，应向“承保人”就该合同项下的设备在到货、建设和还款期间的风险进行投保。保险金额应不低于“贷款人”贷给“借款人”全部贷款的本金。投保险别包括运输险、安装工程险和财产险。“借款人”在投保后，必须将保险单项下的权益转让给“贷款人”。保险赔款应首先用于偿还贷款本息及费用。但“贷款人”可视“项目”实际情况通知“承保人”将保险赔款付给“借款人”，继续用于本协议项下的项目建设。<BR> 
　　<BR> 
　　第十一条　税收<BR> 
　　<BR> 
　　国家税收部门在“国外贷款协议”及本协议项下征收的任何税收（包括但不限于利息预提税、印花税等）均由“借款人”负担。<BR> 
　　<BR> 
　　第十二条　保证<BR> 
　　<BR> 
　　一、“借款人”在此保证：<BR> 
　　１．每年及时向“贷款人”提供年度的项目建设和资金使用计划；<BR> 
　　２．每半年向“贷款人”提供“项目”进度和资金使用的实际情况的资料；<BR> 
　　３．随时将可能影响“项目”建设的重大情况，决定和事件以书面形式通知“贷款人”；<BR> 
　　４．在每年度结束后６０天以内向“贷款人”提供“借款人”以及“项目”的该年度的财务报表及有关资料，包括但不限于资产负债表、损益表和项目工程支出年度决算报告；<BR> 
　　５．应“贷款人”的要求，向“贷款人”提供其它有关资料；<BR> 
　　６．在每次偿还本协议项下到期应付本息前６０天，将与应付款项等额的资金存入按第四条规定开立的存款帐户中；<BR> 
　　７．“借款人”对其与其它银行、金融机构或单位所签贷款协议或担保协议项下所发生的任何违约事件应立即通知“贷款人”。<BR> 
　　二、“借款人”在此声明并承诺：<BR> 
　　１．“借款人”在其它任何贷款或担保协议项下，未曾给予其它银行和金融机构优惠于本贷款协议的偿还条件。“借款人”在本协议项下及其与“贷款人”签订的所有其它贷款或担保协议项下所欠“贷款人”的全部款项，在偿还方面与“借款人”所有其它债务均处于同等受偿地位；<BR> 
　　２．“借款人”今后在其它银行和金融机构签订任何贷款协议和担保协议时，决不接受比本协议项下贷款优先偿还和损害或违背“贷款人”利益的条件和条款；<BR> 
　　３．未经“贷款人”书面同意，“借款人”不将其现存的和将来获得的任何资产和权益抵押或转让给他人。<BR> 
　　<BR> 
　　第十三条　违约事件<BR> 
　　<BR> 
　　下列事件之一即构成“借款人”在本协议项下的违约：<BR> 
　　一、“借款人”未能按本协议的规定按时偿还到期本金、利息、费用及其它任何应付款项；<BR> 
　　二、“借款人”违反了本协议项下的任何规定；<BR> 
　　三、“借款人”在其与“贷款人”所签订的其它贷款协议或担保协议项下发生了任何违约事件；<BR> 
　　四、“借款人”在其与其它任何银行或金融机构所签的贷款或担保协议项下发生了任何违约事件。<BR> 
　　五、“贷款人”有理由判定本协议下贷款的担保人已丧失担保能力或担保资格时，在发生上述违约事件之一的情况下，“贷款人”有权采取下述一项或几项措施：<BR> 
　　１．限期“借款人”纠正违约事件；<BR> 
　　２．中止用款；<BR> 
　　３．宣布全部贷款立即到期并要求偿还全部借款。<BR> 
　　<BR> 
　　第十四条　放弃<BR> 
　　<BR> 
　　一、“商务合同”项下买卖双方如发生争执或发生其它事件，将不影响“借款人”在本协议项下应承担的全部义务。即“借款人”在本协议项下的义务具有独立性，不受其它事件的影响。<BR> 
　　二、“贷款人”在本协议执行过程中未行使或延迟行使本协议项下的任何权利，不应视为放弃该项权利，并且不影响“借款人”在本协议项下应承担的任何义务。<BR> 
　　<BR> 
　　第十五条　转让<BR> 
　　<BR> 
　　一、未征得“贷款人”书面同意，“借款人”不得将其在本协议项下的任何权利、义务转让给第三者。<BR> 
　　二、如经“贷款人”书面同意，“借款人”将其在本协议项下的权利、义务转让给第三者时，则任何继承人、代理人、受让人或接管人应无条件地遵守本协议的任何条款，并应无条件地承担“借款人”在本协议下的全部义务。<BR> 
　　<BR> 
　　第十六条　协议的修改、补充和解释<BR> 
　　<BR> 
　　一、本协议在“贷款人”和“借款人”双方书面同意的情况下，可以予以修改和补充，但在修改或补充协议生效之前，原协议仍然有效。<BR> 
　　二、如因国家法律、法规或规章发生变化，而影响本协议的正常履行时，则应按国家当时的法律、法规或规章修改或补充本协议。<BR> 
　　三、经“贷款人”和“借款人”双方确认后，对本协议的任何修改或补充均构成本协议不可分割的部分，与本协议同等有效。<BR> 
　　四、本转贷协议中未明确的内容，应按“国外贷款协议”有关条款的规定解释。<BR> 
　　<BR> 
　　第十七条　争执<BR> 
　　<BR> 
　　一、借贷双方在执行本协议过程中如发生任何争执，应首先通过友好协商解决。如协商无效时，当事人的任何一方可依法直接向有管辖权的人民法院起诉，通过司法诉讼程序解决。<BR> 
　　二、诉讼费用，按人民法院的调解书或判决书的规定办理。<BR> 
　　三、在诉讼期间，本协议凡不涉及争议部分的条款仍继续有效，任何一方不得借口解决争执而拒不执行本协议的具体条款。<BR> 
　　<BR> 
　　第十八条　协议生效<BR> 
　　<BR> 
　　一、在下列条件具备时，本协议正式生效，生效时间以迟者为准：<BR> 
　　１．本协议业经借贷双方有权签字人签署并加盖公章。<BR> 
　　２．“国外贷款协议”业经有关当局批准生效。<BR> 
　　二、对本协议的任何修改或补充应经借贷双方授权代表签字或盖章并加盖公章后生效。<BR> 
　　三、本协议正本一式两份，由“借款人”和“贷款人”各执一分，副本分送。<BR> 
　　四、本协议项下未尽事宜，均按照中国人民银行及中国银行总行的有关规定办理。<BR> 
