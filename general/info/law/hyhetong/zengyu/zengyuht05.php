　　２．说明<BR> 
　　<BR> 
　　赠与合同是指赠与方将自己所有的物品、现金或者某种权利赠送给他人的行为。赠与合同通常是单务合同，即合同生效后，赠与方负有将赠与物交给受赠人的义务而不享有任何权利；受赠方享有领取赠与物的权利而不承担义务。赠与也可以附条件，即赠与一方要求受赠方履行某种义务，受赠方不履行义务赠与方有权撤销赠与。但这种条件一般是与赠与人的利益无关的条件，而不是要求有偿地赠送。如果将某种物送给他人并要求给付一定的金钱就不是赠与关系。因此，附条件的赠与仍然是单务行为。签订赠与合同应当注意的问题有：<BR> 
　　（１）赠与标的必须是赠与人所有的或者赠与人有权处理的财产或者某种权利，赠与人不能把不履于自己或者自己无权处理的财产和权利赠送他人，否则，构成侵害他人权益的行为。赠与标的可以是物，可以是货币（包括外国货币），也可以是有价证券、某种权利。例如专利权人可以将专利权赠与他人，房屋所有人可以将房屋赠与他人等。<BR> 
　　（２）权利转移涉及到有关部门批准的，应当在合同中订立清楚。例如，赠与房屋合同就需要到房管部门办理房屋所有权转移手续，没有办理手续的，其转让无效。<BR> 
　　（３）合同中要写清楚赠与人有权处理赠与财产的证明文件，确保赠与行为的合法有效。<BR> 
