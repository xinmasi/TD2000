      <p> 　　<BR> 
　　根据我国现行法律和《深圳市投资信托基金管理暂行规定》的有关精神和要求，中国人民建设银行深圳分行和深圳蓝天基金管理公司本着共同发起“蓝天基金”，将募集的资金投资于能产生良好效益的经济领域，以使投资人获得尽可能高的投资回报和较丰厚的资本增值的宗旨，经平等协商并形成共识后，达成以下协议：<BR> 
　　第一条　释义<BR> 
　　除本契约其他条款另有规定外，下列词语具有如下意义：<BR> 
　　１．本基金<BR> 
　　指根据本契约规定所设立的蓝天基金。<BR> 
　　２．基金单位<BR> 
　　指代表本基金一定资产份额的最小等份。<BR> 
　　３．人<BR> 
　　指我国民法通则所指的民事活动的参与者，包括自然人和法人。<BR> 
　　４．受益人<BR> 
　　指持有本基金所发行的单位份额并依据本契约规定享有相应的资产实际所有权、收益分配权、剩余资产分配权、受益人大会表决权等权益及承担本契约和本基金章程和现行法规规定的义务的人。<BR> 
　　５．主管机关<BR> 
　　指对基金实施行政管理职能的政府机构，本契约中通指中国人民银行深圳经济特区分行。<BR> 
　　６．经理人<BR> 
　　根据基金管理规定，作为本基金的主要发起人和本契约的缔结人之一，受信托人委托将本基金资产做投资管理活动的人。在本契约中专指深圳蓝天基金管理公司或其继任人。<BR> 
　　７．投资<BR> 
　　指经理人将本基金资金以购买任何股票、债券、银行票据、商业票据及其它有价证券及其所应占的资金份额，或以对企业进行参股等项目为资金运用的对象进而获取相应之利益的行为。<BR> 
　　８．信托人<BR> 
　　指根据基金管理规定，作为本契约的缔约人之一，按本契约规定对本基本资产进行保管的人，在本契约中专指中国人民建设银行深圳市分行或其继任人。<BR> 
　　９．会计年度<BR> 
　　指公历每年的１月１日起至当年的１２月３１日止的期间。<BR> 
　　１０．年初资产净值<BR> 
　　指本基金在每会计年度开始后第一个估值日的资产净值。<BR> 
　　１１．受益凭证<BR> 
　　指根据基金管理规定由信托人和经理人为募集本基金资金而向受益人签发的有价证券，在本契约中通指经主管机关批准的证券存折。<BR> 
　　１２．单位持有人<BR> 
　　指登录在受益人名册上的每一基金单位的实际持有人，同时通指本基金的所有受益人。<BR> 
　　１３．受益人名册<BR> 
　　指本契约第四条所述的记载有关本基金受益人名称、地址、持有单位份额等资料的登记名册。<BR> 
　　１４．登记人<BR> 
　　指受信托人委托负责受益人名册登录和保管工作的人或机构，本契约中专指深圳证券登记公司。<BR> 
　　１５．交易日<BR> 
　　指国内有合法证券交易活动的证券交易所或证券商或银行的任一营业日。<BR> 
　　１６．关连人<BR> 
　　泛指下列三种人：<BR> 
　　（１）直接或间接拥有经理人或信托人总股份额３０％或以上普通股或表决权的人；<BR> 
　　（２）受上述（１）项所指的人控股的人；<BR> 
　　（３）由经理人或信托人直接或间接拥有总股份额３０％或以上普通股或表决权的人。<BR> 
　　１７．估值<BR> 
　　指根据本契约第六条规定所对本基金有关资产或债权的价值进行评估的活动。<BR> 
　　１８．估值日<BR> 
　　指经理人按本契约规定进行资产净值计算并公布的任一交易日。<BR> 
　　１９．首次发行费<BR> 
　　指本契约第七条所指的首次发行费。<BR> 
　　２０．经理年费<BR> 
　　指经理人根据本契约第十三条三款规定所应享有的款额。<BR> 
　　２１．资产净值<BR> 
　　指根据本契约第六条规定的本基金或本基金发行的一个基金单位（根据上下文要求而定）所拥有或代表的资产价值。<BR> 
　　２２．信托年费<BR> 
　　指信托人根据本契约第十二条（３）款规定所应享有的款额。<BR> 
　　２３．核数师<BR> 
　　指根据本契约规定由经理人经信托人同意后所指定的对本基金会计帐目进行核数的公认专业机构及其专业人员。<BR> 
　　２４．一般决议<BR> 
　　指在根据本契约第十条规定召开的受益人大会上由占表决权总票数５０％或以上票数通过的决议。<BR> 
　　２５．特别决议<BR> 
　　指根据本契约第十条规定召开的受益人大会上占表决权总票数７５％或以上票数通过的决议。<BR> 
　　２６．会计结算日<BR> 
　　指本契约规定的本基金存续期间的每年１２月３１日。<BR> 
　　２７．待分配收益帐户<BR> 
　　指本契约规定的专供存放待分配收益资金的帐户。<BR> 
　　２８．收益分配日<BR> 
　　指在本基金存续期内每会计年度终了后经信托人提出并由受益人大会确定的该会议年度的收益分配过户截止日，该日不得超过该会计年度的会计结算日后三个月期间。<BR> 
　　２９．基金管理规定<BR> 
　　指《蓝天基金章程》及其今后的修改或增补部分。<BR> 
　　３０．基金管理规定<BR> 
　　指《深圳市投资信托基金管理暂行规定》。<BR> 
　　３１．计价日<BR> 
　　指本基金每一估值日以前最近之证券交易日或股权估价日或房地产评估日。<BR> 
　　第二条　本基金<BR> 
　　１．本基金名称：蓝天基金。<BR> 
　　２．本基金发行规模：本基金最高发行限额为３００００万单位，最低发行额为１８０００万单位，每一基金单位面额为人民币壹元。本基金在初始封闭期内不上市交易，亦不对外追加发行基金单位，但在存续期内可以增加基金单位总额并相应增加受益人持有的单位数额的形式来派发每年的收益。<BR> 
　　３．本基金存续期：本基金自成立起初始三年为封闭式单位信托投资基金，期满前根据受益人大会决议并经主管机关批准可以延长封闭期（最长不超过十年），或转为开放式投资基金，及或转为经理人发起的另一名下的投资基金。<BR> 
　　４．本基金资产由下列部分构成：<BR> 
　　（１）发售基金单位的资金收入；<BR> 
　　（２）利用上述资金所进行的投资或该等投资所形成的财产；<BR> 
　　（３）出售或转让上述投资或财产的资金收入；<BR> 
　　（４）上述投资或财产在存续过程中所形成的增值、溢价及其它非营业性收入；<BR> 
　　（５）将有关收益进行再投资所获得的收入；<BR> 
　　（６）除上述项目外的其它杂项收入。<BR> 
　　５．本基金的上述资产（以下统称本基金资产）由信托人按信托原则在名义上持有，本基金的所有投资人（或受益人）为实际持有人。<BR> 
　　６．信托人应为上述资产开立独立于自有资产或他人资产之外的单独帐户进行保管和持有；本基金的一切对外业务活动均以本基金名义出现。<BR> 
　　７．信托人、经理人或受益人均不能依据本契约对本基金资产享有留置权、抵押权或其它优先权。<BR> 
　　８．本基金出于投资经营上的需要可以随时向外借入以现金或其它资产形式体现的资金；本基金借款余额不能超过本资金在该会计年度的年初资产净值。<BR> 
　　本基金的上述借款可来源于信托人、经理人或关连人，但该等借款利率一般不应高于当期同业的通常利率水平。<BR> 
　　第三条　本基金单位的发行、受益凭证和持有人<BR> 
　　１．本基金单位的发售对象、发行办法和发行期限按主管机关核准的招募说明书所列实施。<BR> 
　　２．每一基金单位的面值为人民币壹元，发售价格为人民币壹元，另加收发售价格３％的首次发行费。<BR> 
　　３．本基金单位和受益凭证均以记名的书面凭证形式发行，每一受益凭证（交易手）为１０００基金单位。<BR> 
　　４．本基金所发行的受益凭证是表示单位持有人享有对本基金一定份额资产的所有权、受益权或其它权益并承担相应义务的证明，不是保障收益的凭据。<BR> 
　　５．经理人须认购并持有本基金单位发行总额５％的份额，且在本基金清盘前未经主管机关批准不得低于上述原始数额。<BR> 
　　６．本基金单位的实际持有人同时为本基金的受益人，每一受益人按本契约规定所享有的对本基金的权益大小，与该受益人所持有的本基金单位份额大小成正比，但任何受益人增不享有对本基金某一特定资产的特别权利。<BR> 
　　７．本基金单位的受益人在本基金封闭式存续期间不得向本基金赎回其所持的基金单位。<BR> 
　　８．如果本基金在规定的发行期限内所售出的基金单位数额达不到最低限额的要求，则本基金不能成立，届时将由发起人将已募集的金额加上按人民银行规定的活期存款利率计算的利息，在扣除相应发行费用后一并退还给认购人。<BR> 
　　第四条　受益人名册和基金单位的转让<BR> 
　　１．本基金在未上市前或开放式运作期间，受益人名册由经理人以书面或电子信息形式制作、登录、变更和保存，上市后可交由登记人办理。受益人名册记录的内容为：<BR> 
　　（１）受益人的姓名和供分配现金收益用的银行帐号或存折号。<BR> 
　　（２）受益人所持有的基金单位份额；<BR> 
　　（３）该受益人认购或受让基金单位的日期及转让人的有关资料；<BR> 
　　（４）基金单位转让的过户日期。<BR> 
　　２．受益人如认为需要更改上述记录内容时，应向经理人或登记人提出更改申请，由经理人或登记人按有关规定程序进行审核，并视情况决定是否在受益人名册上作出相应变更。<BR> 
　　３．受益人身份的确认和其所持单位份额的大小一般应以受益人名册的记录为准。<BR> 
　　４．本基金成立满三年后，如属延长封闭期，则本基金单位可申请在证券交易所上市交易。<BR> 
　　本基金单位上市后的交易规则按照上市的证券交易规则执行。<BR> 
　　５．任何受益人均有权将其名下的基金单位通过经理人或其委托的证券交易所或证券商有偿转让给他人。<BR> 
　　６．上述基金单位的交易或转让价格按该交易日的市场价格确定，并由转让方和／或受让方另行支付主管机关规定的印花税和手续费。<BR> 
　　７．上述转让中的受让人须持转让凭证向经理人或登记人办理已转让的基金单位过户手续后，方能享有该份额基金单位的收益分配等权益。<BR> 
　　第五条　基金的投资目标、投资范围及投资限制<BR> 
　　１．本基金作为面向社会的新型金融投资工具，目标是根据证券等市场的变化情况，将投资人所汇集的资金以多元化组合投资的方式投放于不同类别的已上市或非上市的股票和债券等有价证券上，或企业的股权、房地产或期货等投资项目上，以求分散和降低投资风险并达到本基金资本的较大增值。<BR> 
　　２．本基金在初始阶段，证券投资将以上市交易的股票和债券为主，并以深圳、上海两地市场为主。为了更好地利用投资机遇，本基金也可以参与非上市公司的股权收购及合作开发经营房地产等业务。<BR> 
　　３．本基金的投资规模或范围有下列限制：<BR> 
　　（１）投资于股票的资金不超过本基金该会计年度初资产净值的８０％，投资于债券的比例不超过４０％，投资于非上市公司的股权不超过３０％，房地产和期货等其它类投资不超过４０％；<BR> 
　　（２）投资于任一上市公司的股票或其它任何一种证券的总金额不超过本基金年初资产将值的１０％；投资于任一上市公司股票或其它证券的数额不超过该公司该种股票或证券发行总数的１０％；<BR> 
　　（３）本基金不能投资于其它各类基金所发行的单位或受益凭证，不能以本基金资产为他人提供担保，不能投资于承担无限责任的项目；<BR> 
　　（４）本基金不能对外放款，但等待投资时机的款项可对外短期拆借，拆借余额不能超过本基金年初资产净值的２５％；<BR> 
　　（５）经理人在未获得信托人书面同意前，不得将本基金资产投资于经理人自己的股东拥有３０％以上股权的企业或项目上。<BR> 
　　４．在下列情况下，如果本基金的任何投资已超出上述投资限额，经理人无须对该投资额进行调整（包括无须减少或追加投资或作其它资产配置）；<BR> 
　　①由于本基金全部或部分资产升值或贬值，或由于某种市价变动的原因而造成该项投资额超过上述限额时；<BR> 
　　②由于受投资的企业出现兼并、重组、分立、转让等情况而造成该项投资额超限时；<BR> 
　　③由于本基金在经营活动中支出或收入一定款额而造成该项投资额起限时。<BR> 
　　５．经理人或其关连人在未获得信托人书面同意前，不得以其本人名义与本基金发行投资往来关系。<BR> 
　　６．本基金存放在信托人或经理人或关连人处的现金存款的利率不得低于当期同业的存款利率水平。<BR> 
　　７．经理可以随时根据经营需要决定某一投资项目或随时将部分或全部投资项目变现后将资金转作其它用途。<BR> 
　　８．上述一切投资活动均应遵循市场通常交易方式进行，除非经理人认为有必要采用对本基金并无不利的其它交易方式，但须得到信托人同意。<BR> 
　　９．信托人有权拒绝接受其认为不符合本契约规定的投资或其它财产，并可要求经理人及时采取措施，将不符合本契约规定的投资或财产加以替换。<BR> 
　　１０．经理人只有在本基金成立并经信托人书面同意后才可开展投资经营活动。<BR> 
　　第六条　资产估值规则<BR> 
　　１．本基金单位上市后每月的第一个证券交易日为本基金资产和基金单位的估值日。<BR> 
　　２．本基金资产和基金单位的估值遵循深圳市的会计准则、参照国际惯例并遵照本契约的有关规定进行。<BR> 
　　３．本基金资产价值的数额应按下列基准确定：<BR> 
　　（１）任何上市的或挂牌的证券投资项目的价值应按该投资市场计价日相等投资份额的收市价计算，但下列情况除外：<BR> 
　　①如果上述投资市场不止一处，则按主要投资市场的收市价计算；<BR> 
　　②如果出于某种原因在一定时间内无法从上述投资市场中获得价格数据，则按前一计价日价格计算或视需要暂停估值；<BR> 
　　③如果投资项目须负担利息而上述价格资料并未包括该应计利息部分，则在对该投资项目计算时应将至计价日止的应付利息部分预先扣除。<BR> 
　　注：如果经理人循上述途径所获得的并据称是最新资料的价格与实际价格有误差，经理人不对差错承担任何责任。<BR> 
　　（２）任何未上市债券或短期商业票据以买进成本加上自买进次日起至计价日止应收利息为准计算。<BR> 
　　（３）银行存款及类似货币性资产的价值应按其票面值加上至计价日止应收的利息为准计算。<BR> 
　　（４）任何已达成价格协议但尚未实施的需要依约交割的投资或其它财产的价值应按该投资或财产权责发生后应具有的价值来计算。<BR> 
　　（５）股权或其它未清算的投资项目按该项投资原始成本加上依预期利润值计至计价日止应能获取的利润数来计算。<BR> 
　　（６）除上述投资或财产项目之外应收的债权债务按至计价日止应收回或应付的数额进行计算。<BR> 
　　（７）按规定在本基金资产中待摊或预提的所有规费或其它支出金额，应按上月月末余额减去上月月末计价日止应摊销部分或加上至计价日止应预提部分后，余额计入本基金资产总额来计算。<BR> 
　　４．本基金资产按上述方法估值后，如果资产价值大于帐面值，增值部分作为重估盈余入帐；如小于帐面值，则从重估盈余中扣除。<BR> 
　　５．将按上述方式所计算出的本基金资产总额减去本基金负债总额后的余额，即为本基金在该估值日的资产净值。<BR> 
　　将上述资产净值除以本基金已发行的单位总额后的得数，即为每一基金单位资产净值。<BR> 
　　６．经理人可以在经信托人同意后，将上述投资项目或其它财产的计值方式作适当调整，或采用其它计值方式，以能真实反映资产的实际价值。<BR> 
　　７．本基金的单位资产净值每月在《深圳特区报》或《深圳商报》上公布一次。<BR> 
　　第七条　本基金的费用<BR> 
　　１．首次发行费，包括发起费用、佣金、承销费、宣传广告费和文件资料印制和派发费等，其费率按基金单位发售价格的３％计算，由认购人在认购基金单位时一并支付。<BR> 
　　２．基金经理年费，其费率为本基金年资产帐面净值的１．２％，逐日累计，并在每月１０日前由信托人从本基金资产中直接支付给经理人。<BR> 
　　３．基金信托年费，其费率为年资产帐面净值的０．３％，逐日累计，每月１０日前由基金信托人从本基金资产中直接提取。<BR> 
　　４．基金受益人名册登记费、受益凭证托管费、上市交易费和分配基金收益所发生的费用，由信托人根据本契约或有关规定的标准从信托资产中按时支付给经理人登记人或证券商或证券交易所。<BR> 
　　５．公告或通知本基金有关事项及编制年报、季报等文件的费用及律师费、核数师费、公证费等应付费用，由信托人依据经理人的指定或有关合同并核实后从本基金资产中支付。<BR> 
　　６．基金在进行投资和管理运作中，向外借款或办理财产保险中所发生的一切费用或利息支出，由信托人依据有关合同从本基金资产中支付。<BR> 
　　７．本基金投资经营中或获得的收益中应缴纳的各种税费及地方性规费等，由信托人从本基金资产中支付。<BR> 
　　８．除上述项目之外的临时性必需开支或法规或当地政府规定的缴费，由信托人认可后按实际发生额从本基金资产中支付；<BR> 
　　９．经理人或信托人在将本基金资金进行投资和管理活动中所发生的必须开支由信托人从本基金资产中支出，但经理人和信托人本身的日常行政管理开支不得由本基金承担。<BR> 
　　第八条　会计和审计报告<BR> 
　　１．本基金单独立帐，独立核算，自负盈亏。<BR> 
　　２．本基金会计制度按深圳经济特区会计准则执行；特区会计准则未作规定的，参照国际惯例执行。<BR> 
　　３．经理人在每季终了后的一个月内，须向信托人提交本季度信托报告，信托人在每半年终了后的一个半月内，应公开本基金的中期财务报告，在每会计年度终了后的三个月内，向受益人大会提交经核数师公允审计过的年度财务报告。<BR> 
　　４．本基金的投资和管理活动中的会计帐簿以及资产保管会计帐簿均由信托人建立和保管；经理人负责建立和保管本基金的一切投资或管理活动事项记录。<BR> 
　　经理人与信托人均应为对方查看和复制所建帐簿或记录资料提供方便条件。<BR> 
　　５．上述一切会计凭证或帐簿或投资管理记录资料的保存年限为本基金清盘终结后满五年。<BR> 
　　６．本基金的核数师应为在中国大陆或香港注册的专业会计机构及其专业人士，且必须独立于信托人、经理人或关连人。<BR> 
　　第九条　收益分配<BR> 
　　１．本基金在每一个会计结算日止所实现的该会计年度收益总额是指该会计年度内本基金投资经营中所获得的任何股息、红利、利息、利润和／或其它种类的收入（包括已／应收回的债权）的总收入部分，扣除当年应支出的所有投资经营成本和应纳税额、地方性规费等款项后的余额部分。<BR> 
　　２．本基金的上述每年收益总额扣除按规定或可由经理人提取的业绩奖金部分后的余额，为该会计年度的可分配收益总额。<BR> 
　　３．上述当年的可分配收益总额和分配方式由信托人确定并经受益人大会通过后，按本基金单位的总数额计算出每一基金单位可分配收益数，并依照收益分配日在册的受益人名单由经理人或登记人以现金形式直接划拨入每一受益人在受益人名册上登录的银行户头内，或以送红利单位的形式打印出受益凭证发给受益人。通常情况下，本基金每年的收益分配均采用派送红利单位的形式。<BR> 
　　４．本基金每年分配收益一次，通常于每个会计年度终了后的３个月内进行，一般不进行中期分配；如果该会计年度的收益总额低于本基金年初资产净值５％的比例或无收益，则该年不进行收益分配。<BR> 
　　５．上述每年收益存放在待分配收益帐户中可计取的利息收入，应转入下一会计年度的可分配收益数额内，但在该年收益分配日后的分配过程中均不再计提利息。<BR> 
　　６．如果因受益人自身原因造成信托人或经理人或登记人无法在一定期限内（一般为本基金清盘前达到５年或到本基金清盘开始日止）将应分配给该受益的收益及时派发出去，则该受益人被视为已自行放弃上述收益的受益权，且在上述期限后无权再向信托人或经理人索偿在上述情况下，上述无法分配出的收益应构成基金资产的一部分。<BR> 
　　第十条　受益人大会及决议<BR> 
　　１．受益人大会每年至少召开一次，通常在该会计年度终了后的２个月内召开，或者是根据代表本基金单位份额总数１０％或以上的受益人书面建议后临时召开。<BR> 
　　２．持有或代表本基金单位一定份额（视发行情况而定）的受益人有权出席大会，不足上述份额的受益人可以书面委托其它有权出席人发表有关意见或代行表决权，经理人和信托人的董事和获授权的高级职员均可出席大会。<BR> 
　　３．大会召开的时间、地点、出席人资格和会议议题经信托人确定后，应至少提前２０天以在《深圳特区报》或《深圳商报》上刊登公告和寄发信函的方式通知受益人。任何受益人无论出于何原因未见到或收到上述方式通知，均不影响该次大会决议对其产生的约束力。<BR> 
　　４．大会须有持有或代表本基金单位发行总份额３０％或以上的受益人或其委托人出席方可正式召开并可讨论和表决一般决议；如需讨论和表决特别决议；则出席人所代表的本基金单位份额应达到发行总额的５０％或以上。<BR> 
　　５．如果通过的开会时间已过十分钟而签到的出席人所代表的基金单位份额达不到上款的要求，则大会必须顺延１５日后召开，时间和地点由大会主席或信托人决定后仍按上述通知受益人。凡属延期召开的大会对出席人所持有或代表的基金单位份额不作任何要求，且可讨论和表决所有原定的议案。<BR> 
　　６．大会主席由信托人事先书面指定；如果信托人未指定或已指定的大会主席迟到１５分钟以上，则由出席人当场推举出一位出席人担任大会主席。<BR> 
　　７．除非大会主席根据表决需要或根据代表本基金发行单位总份额１０％或以上的出席人的提议而决定采用每一基金单位一票的表决方式，大会表决决议一般以每一出席人一票的表决方式进行。<BR> 
　　８．一般决议的通过或否决须有上述出席人表决权总额５０％或以上赞同或反对方为有效。特别决议的通过或否决须有上述出席人表决权总份额７５％或以上赞同或反对方为有效。<BR> 
　　大会通过的任何决议对全体受益人（包括未出席大会的受益人）、经理人和信托人均有约束力。<BR> 
　　９．大会的会议纪要应详细纪录大会召开的时间、地点、出席人名单、议题和大会讨论和通过或否决的决议，并由大会主席签名。上述会议纪要正本交信托人保存，副本交经理人留存。<BR> 
　　１０．下列事项须经大会作为特别决议进行讨论和表决：<BR> 
　　（１）对本基金章程进行修改或增补；<BR> 
　　（２）对本契约进行修改或增补；<BR> 
　　（３）已终结会计年度的收益分配议案；<BR> 
　　（４）本基金存续期的延长或转变为开放式基金的提案；<BR> 
　　（５）本基金期满清盘或提前清盘事项；<BR> 
　　（６）经理人或信托人的辞职或撤换；<BR> 
　　（７）其它有关本基金的重大事项。<BR> 
　　上述所通过的特别决议必要时须经主管机关核准。<BR> 
　　第十一条　本基金的结业的清盘<BR> 
　　１．本基金在封闭期届满未获延长时或延长封闭期届满时，或受益人大会决定不转变为另一名下的投资基金时，或在开放式运作期间受益人大会决定终止本基金时，经主管机关批准，本基金应结业。<BR> 
　　２．在出现下列情况之一时，本基金经受益人大会通过决议并经主管机关批准，可以提前结业：<BR> 
　　（１）由于现行法规的变更或新法规的实施使本基金不能继续合法存在或运作时；<BR> 
　　（２）经理人因故退任或被撤换而在２个月内无新的经理人继任时；<BR> 
　　（３）信托人因故退任或被撤换而在２个月内无新的信托人继任时；<BR> 
　　（４）因不可抗力致使本基金不能正常运作达二个月以上时；<BR> 
　　（５）本基金的资产净值连续六个月以上降至已发行基金单位面额总值７０％以下时。<BR> 
　　３．信托人、经理人或代表本基金已发行单位总份额５０％或以上的受益人均有权书面提出本基金提前结业的建议。<BR> 
　　４．本基金的结业经主管机关批准后，信托人应尽快以公告或信函的方式将本基金的结业日和基金单位的过户截止日通过受益人，同时由信托人负责组织本基金清算小组，经理人在本基金决定结业后不得再进行任何新的投资，且本基金单位停止转让。<BR> 
　　５．本基金清算小组的组成人员应包括信托人、经理人、注册会计师或核数师、法律顾问等。<BR> 
　　６．本基金清算小组的职责为：<BR> 
　　（１）清理、核对和保管好本基金所有资产；<BR> 
　　（２）清理本基金未结的债权债务；<BR> 
　　（３）以其认为适当的方式尽快变现一切未以现金形式存在的资产。<BR> 
　　（４）向主管机关提出本基金结余资产的分配方案并在获批准后负责该方案的实施；<BR> 
　　（５）负责本基金结业过程中的其它事宜。<BR> 
　　７．本基金清算小组及其受托人在履行上述职责时有权获取合理的报酬，且该项报酬经主管机关核准后可优先从本基金结余资产中受偿。<BR> 
　　８．受益人在上述本基金结余资产分配开始达一定期限（通常为１２个月）后未领取的部分，应上交主管机关处理。<BR> 
　　第十二条　信托人<BR> 
　　１．信托人必须履行下列职责：<BR> 
　　（１）协助经理人发起本基金；<BR> 
　　（２）配备专职人员负责妥善保管好本基金资产；<BR> 
　　（３）设立本基金资产的单独帐户并将之区别于自有资产或他人资产之外进行登录和核算；<BR> 
　　（４）建立并保存单位受益人名册，召集受益人大会以及负责收益分配；<BR> 
　　（５）负责本基金证券交易中的交割、清算和过户并负责股权及其它项目的投资清算；<BR> 
　　（６）监督监理人履行其职责并监督其投资管理活动符合本契约和本基金章程的规定；<BR> 
　　（７）确认经理人在履行职责中不违反本契约规定的投资行为后依其指示办理有关收支的财务手续；<BR> 
　　（８）审查和公开本基金的财务报告及其它公开性文件；<BR> 
　　（９）本契约或本基金章程中规定的信托人应履行的其它职责。<BR> 
　　２．信托人的有关义务如下：<BR> 
　　（１）以维护所有投资人或受益人的利益为行为准则，尽心尽职，遵约守法；<BR> 
　　（２）对其受托的本基金资产承担保管和监督责任；<BR> 
　　（３）注意保守本基金商业保密，任何时候不得向外（包括向属下无关职员）泄漏本基金现时的和未来的投资计划、意图和状况；<BR> 
　　（４）无权干涉或不执行经理人的未违反本契约规定的投资决策，否则应承担对本基金或经理人所受损失的赔偿责任；<BR> 
　　（５）有责任及时将对本基金或经理人有重大影响的事由通报经理人，并积极协助经理人采取相应措施，有责任为经理人查阅本基金资产和受益人等有关资料提供便利条件。<BR> 
　　３．信托人具有的权益：<BR> 
　　（１）有权取得本基金信托年费和其它为本基金或经理人垫支金额或遭受非自身责任引致的经济损失的合理补偿；<BR> 
　　（２）有权审查经理人的投资计划或方案，逐笔核查每笔占本基金年初资产净值５％以上金额的投资项目；<BR> 
　　（３）有权拒绝接受经理人不符本契约规定的投资或经营行为；<BR> 
　　（４）有权自费委托他人办理信托人负责的有关事务；<BR> 
　　（５）信托人作为本基金所有投资人或受益人资产的名义所有人和权益代表人，其与经理人缔结的本契约对上述投资人或受益人均有约束力。<BR> 
　　（６）本契约中规定的其它权益。<BR> 
　　第十三条　经理人<BR> 
　　１．经理人必须履行下列职责：<BR> 
　　（１）参与并主要负责本基金的发起工作；<BR> 
　　（２）分析研究各有关投资市场动向或趋势，制订出可行性强的投资计划或方案；<BR> 
　　（３）组织专业人士独立地对本基金资产进行投资和管理；<BR> 
　　（４）经信托人授权，代表本基金对外签订和履行一切投资合同或协议；<BR> 
　　（５）定期对本基金资产和单位进行估值并予以公布；<BR> 
　　（６）负责本基金已发行的基金单位的上市交易工作；<BR> 
　　（７）定期编制和递交经理报告，并向受益人大会报告工作；<BR> 
　　（８）提出每一会计年度的收益分配建议和初步方案；<BR> 
　　（９）准备、印制和公布本基金有关情况的公开性文件资料；<BR> 
　　（１０）本契约规定的其它职责。<BR> 
　　２．经理人的有关义务如下：<BR> 
　　（１）以努力实现本基金的宗旨为行为的最高目标，尽心尽职，遵约守法；<BR> 
　　（２）谨慎选择和决定每一个投资项目，注意发挥多元化组合投资的优越性，对本基金承担投资管理责任；<BR> 
　　（３）注意保守本基金商业秘密，任何时候不得向外（包括向属于无关职员）泄本基金现时的和未来的投资计划、意图和状况；<BR> 
　　（４）有责任及时将对本基金可信托人有重大影响的事由通报信托人，并主动积极采取相应措施；有责任为信托人查阅本基金投资和管理等有关资料提供便利条件；<BR> 
　　（５）对其受托人或属下职员的相应代理行为承担责任。<BR> 
　　３．经理人的有关权益如下：<BR> 
　　（１）有权取得本基金的经理年费和其它为本基金可信托人垫支金额的合理补偿，有权对为本基金进行投资和管理活动中非经理人责任所遭受的损失获得合理的补偿；<BR> 
　　（２）有权取得经理人业绩奖金。本契约规定经理人业绩奖金数额为该年度末本基金资产净值超过年初资产净值２５％以上部分中按２５％的比例提取，并按照上述原则分解为每月计提一次。具体为从本基金在该会议年度内每一估值日之前一月实现的收益达到该月额度后的超额部分中按２５％比例预提，逐月调整，年终平衡后视情况决定是否实提。计提公式如下：<BR> 
　　计提额＝（Ｖｎ－Ｖｏ－ｎ／４８×Ｖｏ）×２５％<BR> 
　　式中：Ｖｏ表示基金的年初资产净值；<BR> 
　　Ｖｎ表示本估值日前一月的资产净值；<BR> 
　　ｎ表示计值月数。<BR> 
　　（３）在不违反本契约规定条件下，有权选择决定每一笔投资或采取每一项管理行为；<BR> 
　　（４）在本基金存续期内，享有时本基金资产的投资充分经营权和处分权；<BR> 
　　（５）本契约规定的其它权益。<BR> 
　　第十四条　信托人和经理人的免责<BR> 
　　１．信托和经理人在履行各自职责中均不对下列非自身违法或违规或违约所引致的情况或产生的后果承担任何责任：<BR> 
　　（１）在正常业务交往中接受对方当事人签署、盖章和／或交付给信托人或经理人的任何通知、决议、指令、信函、凭证、声明、说明、票证、重组计划或其它代表所有权的凭证或其它据信为真件的投资文件资料等而使本基金、信托人或经理人遭受的损失；<BR> 
　　（２）经理人或信托人根据现行法律或政策或当地政府或主管机关的规定或要求（无论是否具有法律效力）所采取的或不能采取的行动及其后果；<BR> 
　　（３）由于客观条件的原因而无法或不可能执行本契约的有关规定；<BR> 
　　（４）非信托人或经理人指定或委托的任何代理人、托管人或经纪人擅自而为的行为（但对因信托人或经理人对上述人的选择、指定或委托错误所造成的损失仍应负责）；<BR> 
　　（５）在所收到的任何权益证书、转让证书、申请表格或其它有关书面凭证或文件上的签名或盖章，或其他人在上述证书上伪造的或无权所为的签名或盖章（信托人或经理人有权利但无义务将上述签名或印章送有关方面辨别真伪），或依据上述签名或盖章所采取的行动或实施的作为；<BR> 
　　（６）对履行受益人大会上表决通过的任何已记入由大会主席签名的会议纪要中的决议（尽管该决议在事后发现不符合大会召开或形成决议的有关要求，或该决议并非对所有受益人均有约束力）；<BR> 
　　（７）由于下款所列的任何银行业者、注册会计师、律师、经纪人、代理人或其它有关人或经理人或信托人（非本契约有规定）的任何失误行为、忽略行为、判断错误、遗忘、失慎等所造成的，可因信托人、经理人善意相信和信赖上述人的建议或信息而使本基金、信托人或经理人所遭致的任何损失。<BR> 
　　２．信托人和经理人在履行本契约所规定职责过程中，可根据来自任何作为信托人或经理人的代理人或顾问的银行业者、注册会计师、律师、经纪人、代理人或其它人士以书信、电话、电传、传真形式传达的任何建议或信息而采取投资或管理行为，且不为上述建议或信息中的失误之外承担责任。<BR> 
　　３．除非本契约前面部分有相反明确规定，信托人和经理人应视为已获得履行各自职责的充分授权，可自行决定或采取履行职责的方式方法，并对非自身故意或疏忽的作为或不作为而给本基金造成的任何资产损失、损坏或经营支出增加或经营困难不承担任何责任。<BR> 
　　４．信托人和经理人可：<BR> 
　　（１）接受其认为合格的任何人、机构或联合组织所出具的据信足以证明本基金有关资产价值的或资产购入或售出价格的或资产上市价格的证明文件；<BR> 
　　（２）依据任何行业／专业协会或官方机构内已形成的惯例和规律来进行投资或其它财产的买卖。<BR> 
　　５．本契约不阻止经理人或信托人的下列行为：<BR> 
　　（１）除了本契约第三条第（５）款规定之外，以自己的名义和资金购入、持有或处分本基金单位；<BR> 
　　（２）以自己的董事或属下职员个人名义和资金购入、持有、售出或处分不构成本基金资产的任何投资项目或其它财产；<BR> 
　　（３）各自与其他人或与本基金资产的持有人或受益人（包括自然人、法人、其他机构或组织）缔结经济合同或进行经济往来活动；<BR> 
　　（４）以经理人或信托人的名义再参与或合作创立一个与基金完全独立的新的基金，且无需将从新基金获得的任何收益交付给本基金。<BR> 
　　６．信托人可：<BR> 
　　（１）对因其善意按照经理人的任何投资或经营要求所采取或不采取的行为或经理人所为或所不为的行为或因上述行为造成的损失不承担任何责任；<BR> 
　　（２）除了根据本契约规定而由其从本基金资产中支付的项目外，信托人再无任何义务支付任何开支项目；<BR> 
　　（３）信托人无义务以本基金名义提起或参与其认为可能会造成由其承担经济支出或经济责任的任何与本契约规定或本基金有关的法院起诉、庭审、或答辩活动（除非经理人提出书面要求且根据本契约规定应由本基金负责足额补偿其可能遭受的经济损失）。<BR> 
　　７．经理人可：<BR> 
　　（１）如非出于在履行本契约规定的职责中本身故意或疏忽方面的原因，经理人对其按照本契约规定所为或所不为的行为及其结果不承担任何法律责任；<BR> 
　　（２）除非本契约有明确规定，经理人对信托人所为或所不为的任何行动及其后果不承担任何法律责任；<BR> 
　　（３）有权按照本契约的有关规定，将自己应承担的全部或部分工作任务、享有的权利或可自行决定的事务交给信托人同意的第三人履行或实施。在此情况下，经理人仍有权全额享有本契约规定应支付给经理人的经理年费、业绩奖金以及其它一切有关补偿。<BR> 
　　８．经理人或信托人有权销毁归自己保存的并已超过一定期限的本基金的有关会计帐簿、凭证及其它文件资料（保存期限为本基金清盘终结后满五年）。<BR> 
　　９．本契约所明确规定的对信托人或经理人在履行职责中所遭受的经济赔偿均为补足性的，并不影响到法律规定或法院判决书、调解书或仲裁机构裁决书规定的其他人对信托人或经理人所作的赔偿（但信托人或经理人必须充分证明其在履行职责时并不存在故意、疏忽、失职、违约的情况和责任）。<BR> 
　　第十五条　信托人或经理人的辞职或撤换<BR> 
　　１．信托人或经理人可以根据自身的意愿在本基金存续满１０年并继续存续时辞去信托人或经理人的职务，但须按下列程序进行：<BR> 
　　（１）信托人或经理人在未指定新的信托人或经理人前不得自行退任。<BR> 
　　（２）信托人或经理人在欲辞时所选定的新信托人或经理人必须符合信托人或经理人的资格和能力要求，并应取得本契约另一方缔约人的同意和受益人大会特别决议的批准。在此情况下，留任的本契约缔约人应与新加入的缔约人签订一份本契约的补充协议，以确认由新加入的缔约人或继任人取代欲退出的缔约人的位置和履行其职责。<BR> 
　　（３）上述替换程序完成后，新旧缔约人均应将上述情况尽快通知各受益人。<BR> 
　　２．信托人或经理人在下列情况下可建议受益人大会表决并报经主管机关批准后，撤换经理人或信托人：<BR> 
　　（１）经理人或信托人本身非自愿地被清盘；<BR> 
　　（２）经理人或信托人严重失职或严重违反现行法律或基金管理规定或本契约或本基金章程的规定；<BR> 
　　（３）信托人或经理人以充足理由相信并以书面形式指出从维护大多数受益人的利益考虑应撤换掉经理人或信托人；<BR> 
　　（４）代表本基金已发生单位总份额＿＿＿＿％或以上（经理人或信托人持有或视为持有的单位份额除外）的受益人书面要求撤换经理人或信托人。在此情况下，如果被撤换的是经理人，则其所持有的基金单位份额全数由新的经理人按最近的估值日价格收购。<BR> 
　　３．在上述替换或撤换程序中完成前，欲辞职或被撤换的经理人或信托人必须继续履行本契约规定的有关职责，不得自行退任，否则应承担由此而给本基金或另一方造成的经济损失的赔偿责任。<BR> 
　　经理人或信托人的退任或被撤换日期通常应为新的经理人或信托人的上任日期。<BR> 
　　第十六条　争议的解决<BR> 
　　１．本基金信托契约在履行过程中如在当事人之间出现意见分歧或争端，首先应由争执双方通过友好和平等协商的方式解决争端，如果无法解决，则将争执情况向主管机关报告，由主管机关进行调解或裁决。任何不接受上述调解或裁决的争执方均可向深圳市的人民法院提起民事诉讼。<BR> 
　　２．解决上述争端的准据法为中华人民共和国现行法规和有关地方性法规：上述法规未及之处，参照相应国际惯例。<BR> 
　　第十七条　本契约的修改或增补<BR> 
　　１．本契约可以根据现行法规的变更或新法规的实施或地方性的新规定或主管机关的新要求或本基金受益人的提议或受益人大会的决议或运作形势的需要而经信托人和经理人平等协商达成一致意见后予以修改。<BR> 
　　２．本契约可因信托人或经理人一方的辞职或被撤换而由留任的一方与新的经理人或信托人平等协商达成一致意见后签订补充协议。<BR> 
　　３．本契约的重大修改或任何补充协议均须经受益人大会通过并报主管机关批准方为有效，并构成本契约不可分割的组成部分。<BR> 
　　第十八条　本契约的生效及准据法<BR> 
　　１．本契约经双方缔约人签字盖章并报主管机关批准后即行生效并具有相应法律效力，同时对本基金成立后的所有受益人均有约束力。<BR> 
　　２．本契约一式六份，主管机关和公证处各留存一份。缔约人各保存二份。每份均具同等效力。<BR> 
　　３．本契约内容可印制成册并对外公开发售（收回工本费）或供投资人在有关场所免费查阅，但其效力应以本契约正本为准。<BR> 
　　４．本契约的准据法为中华人民共和国的现行法规和地方性法律规定；对于上述法律或规定未及之处，应参照相应国际惯例。<BR> 
　　第十九条　本契约的终止<BR> 
　　１．如果本基金未能在规定的期限内成立或本基金已清盘完毕，则本契约视为终止。<BR> 
　　２．本契约的任何方式的终止均不得违反本契约的有关规定。<BR> 
　　第二十条　其他<BR> 
　　１．除非本契约中有明确规定，本契约当事人在本基金封闭式运作期间均应严格按本契约的规定执行。<BR> 
　　２．本契约的对外解释权由经理人和信托人共同行使。<BR> 
