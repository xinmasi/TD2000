　　１９．２．　本合同修订须经双方讨论通过，形成正式文件。经主管部门审批，审批后的文件为本合同不可分割的组成部分。<BR> 
　　１９．３．　本合同内书写的标题，仅为醒目所列，不影响条款的意义和解释。１９．４．　本合同及附件用中文、英文书写，而两种文字具有同等法律效力。１９．５．　公司全部重要文件，一律用中、英两种文字书写。两种文字本均具有同等效力。<BR> 
