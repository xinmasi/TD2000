　　第二十三章　期限、解散、清算<BR> 
　　<BR> 
　　第２３．０１条　合营公司的合营期限为＿＿＿＿＿年，从合营公司营业执照签发之日开始。<BR> 
　　第２３．０２条　在合营期满前两年，经一方提议，董事会会议一致通过并报审批机构批准，可以延长合营期限，延长期限批准之后，要向工商行政管理局变更登记。<BR> 
　　第２３．０３条　合营公司在下列情况之一时，将解散，其中（ｂ）￣（①）各项可能发生在合营期满之前。<BR> 
　　１．合营期满，不再延长。<BR> 
　　２．合营双方一致认为提前解散合营公司于双方有利。<BR> 
