　　３．甲方应对合营公司或乙方对其所披露的保密资料，专有技术和技术保守保密。未经乙方事先书面授权，不得向其他任何第三方披露。<BR> 
　　４．乙方应对合营公司或甲方对其所披露的保密资料，专有技术和技术保守秘密，未经甲方事先书面授权，不得向其他任何第三者披露。<BR> 
　　第２２．０２条　合营公司，其任何雇员，甲方和乙方在下列情况下，不承担保密义务：<BR> 
