　　第十八章　财务会计制度<BR> 
　　<BR> 
　　第１８．０１条　合营公司的财务会计制度将按照中国财政部《中外合资企业财务会计制度》参照有关国际会计标准的惯例制定。<BR> 
　　第１８．０２条　合营公司将采用国际通用的权责发生制和借贷记帐法记帐。<BR> 
　　第１８．０３条　１．合营公司的全部帐簿和财务记录应合理、详细、完整和准确并公平地反映财务结果以及其制作之日的合营公司财务现状。<BR> 
　　２．合营公司的全部凭证、帐簿，报表将用中文制作，主要财务、会计文件，报表（包括月季和年度报表）将译成英文并其内容上与中文文件、报表相符。<BR> 
　　第１８．０４条　合营公司的会计年度将采用日历年制，自公历一月一日起十二月三十一日止为一个会计年度，但合营公司的第一个会计年度从合营公司成立之日，即领取营业执照之日起，最后一个会计年度截止于合营公司解散或合营期满。<BR> 
　　第１８．０５条　合营公司将采用人民币为记帐本位币，同时并用实际收付的货币记帐。外汇按中国国家外汇管理局当日公布的外汇比价值折算成人民币。<BR> 
　　第１８．０６条　合营公司将在中国银行＿＿＿＿＿分行分别开立人民币和外汇帐户，并可在中国国家外汇管理局批准的其他银行开立外汇帐户。<BR> 
　　第１８．０７条　１．合营公司的总会计师负责合营公司的财务会计工作。<BR> 
　　２．总会计师将按期向董事会提供合营公司的财务报告（按月、季和年度）。<BR> 
　　第１８．０８条　１．合营公司将聘请一名独立的来自于注册的会计师事务所的中国注册的审计师负责年度验证合营公司的报表及财务报告。<BR> 
　　该审计师的报告将提交给董事会的总经理。<BR> 
　　２．各方有权在任何时候聘请会计师审查合营公司的报表和财务报告，费用处理，合营公司将为此种审查提供便利。<BR> 
