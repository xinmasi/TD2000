　　第十六章　工会<BR> 
　　<BR> 
　　第１６．０１条　合营公司的职员、工人有权按《合资法》和《中华人民共和国工会法》的规定组织工会，开展工会活动。合营公司对工会的工作将给予支持，给予其房屋、设备的使用权，以便工会的办公，开会及开展其他活动。<BR> 
　　第１６．０２条　合营公司的工会在本合营公司内享有《公司章程》第九章所规定的权利及义务。<BR> 
　　第１６．０３条　合营公司将拨出合营公司职员、工人工资总额的２％作为工会活动经费。工会将按全国总工会的有关规定使用这笔经费。<BR> 
