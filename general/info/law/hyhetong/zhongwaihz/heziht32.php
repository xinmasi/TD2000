　　ｉ）任何一方的董事或推荐的高级职员被有效地排除参与董事会或对合营公司的管理。<BR> 
　　ｊ）合营的任何一方或合营公司的全部或大部分资产或财产被国家没收或征用。<BR> 
　　ｋ）合营各方通过其在董事会的代表未能就有关合营公司的全面政策，经营中的重大问题达成协议，这种情况对于合营公司继续有盈利地经营造成实质性和不利的影响。<BR> 
