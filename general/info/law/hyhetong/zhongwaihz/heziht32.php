　　第三章　成立合资经营公司<BR> 
　　<BR> 
　　第３．０１条　甲、乙双方根据"合资法"和中国的其他有关法律和规定，同意在中国境内建立合资经营的制药有限公司。<BR> 
　　第３．０２条　１．合营公司名称是：＿＿＿＿＿（以下简称合营公司）。<BR> 
　　其英文名称：＿＿＿＿＿<BR> 
　　为此，合营公司与乙方将签订一个许可使用"＿＿＿＿＿"名称的合同。<BR> 
　　无论什么原因，如果乙方在合营公司中不再有＿＿＿＿＿％的股份，甲方同意改变合营公司的名称，以使合营公司的中英文名称中不再出现"＿＿＿＿＿"的字样。<BR> 
　　２．合营公司的法定地址：＿＿＿＿＿<BR> 
　　第３．０３条　合营公司在中国具有法人资格，受中国法律的管辖和保护，其一切活动必须遵守中国法律和有关规定。<BR> 
　　第３．０４条　合营公司的组织形式为有限责任公司。甲、乙方以各自认缴的出资额对合营公司的债务承担责任。<BR> 
　　各方按其出资额在注册资本中的比例分享利润和分担风险及亏损。<BR> 
　　除各自认缴的注册资本的出资额外，各方均不对合营公司的债务负有更多的责任，合营公司的债权人只能向合营公司的财产求偿。<BR> 
