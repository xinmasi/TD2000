　　第五章　投资总额和注册资本<BR> 
　　<BR> 
　　第５．０１条　合营公司投资总额为相当于＿＿＿＿＿美元的人民币或＿＿＿＿＿币。<BR> 
　　第５．０２条　合营公司注册资本为＿＿＿＿＿美元。<BR> 
　　甲方出资额占注册资本的＿＿＿＿＿％。<BR> 
　　其中：以土地使用权出资，作价为＿＿＿＿＿美元。现金出资为相当于＿＿＿＿＿美元的人民币。<BR> 
　　乙方出资额占注册资本的＿＿＿＿＿％。<BR> 
　　其中：以工厂设施的设计及服务出资，作代价为＿＿＿＿＿美元。现金出资为相当于＿＿＿＿＿美元的＿＿＿＿＿币。<BR> 
　　第５．０３条　合营公司总投资额与注册资本之间差额将由合营公司向中国境内的银行或其他经合营公司选择并经中国国家外汇管理局批准的金融机构贷款解决。从甲方和／或乙方要求的对合营公司的贷款的担保或担保物应由双方按各自在注册资本的出资额的比例给予提供。<BR> 
　　第５．０４条　１．甲方除以现金对合营公司的注册资本出资外，还以＿＿＿＿＿平方米场地（以下称"场地"）使用权作为出资额出资。场地使用年限为＿＿＿＿＿年。场地使用权的出资作价为＿＿＿＿＿美元。<BR> 
　　２．乙方除以现金对合营公司的注册资本出资外，还以如本合同第１２．０１条及本合同附件四所述的条款和条件进行设计的设计工作和服务，并以此作为出资额出资，作价为＿＿＿＿＿美元。<BR> 
　　第５．０５条　双方应制定对注册资本分阶段的、同等出资的初步计划。一旦董事会正式成立，董事会应根据合营公司的实际要求调整该出资计划，但最后的出资应在合营公司厂房土建完成之前支付。以现金出资时，甲、乙双方应按出资计划规定的出资日期和出资以现金存入合营公司在中国银行所立的人民币帐户和外币帐户。<BR> 
　　甲方和乙方的出资是按美元折算的。运用的外汇兑换率为实际出资日中国国家外汇管理局公布的人民币对美元、瑞士法郎对美元的兑换率。出资后外汇兑换率的变化不影响双方出资额在注册资本中所占的比例。<BR> 
　　任何一方如果推迟了应交纳的资金时，应交付拖欠利息，利率比出资日中国银行公布的年度贷款的利率高＿＿＿＿＿％，直到交足资金并全部付清拖欠应付利息为止。<BR> 
　　第５．０６条　甲方和乙方应在出资计划规定特定事项完成后分别向注册资本出资。<BR> 
　　第５．０７条　合营公司的双方投资额需经中国的注册会计师验资，出具验资证明。合营公司据此给出资者出具有董事长、副董事长共同签署的出资证明。<BR> 
　　第５．０８条　合营期内，合营公司不得减少注册资本的数额。合营公司注册资本的增加须经甲、乙双方一致同意，并经审批机构批准。<BR> 
　　第５．０９条　任何一方转让其全部或部分出资额、事先都需取得对方书面同意。一方转让时，对方有优先购买权。<BR> 
　　第５．１０条　合营公司注册资本的增加或转让经董事会一致通过后，报审批机构批准，并向工商行政管理局办理变更登记手续。<BR> 
　　第５．１１条　当双方的出资额达到注册资本后，合营公司一旦取得了为使合营公司能有效地经营所需要的各种许可，合营公司将请求甲、乙双方协助合营公司安排所需的长期贷款。<BR> 
