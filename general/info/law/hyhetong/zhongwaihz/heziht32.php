　　第十七章　税收<BR> 
　　<BR> 
　　第１７．０１条　合营公司将按《中华人民共和国中外合资企业所得税法》及中国其他有关法律、规定缴纳税款。<BR> 
　　第１７．０２条　合营公司的高级职员、职员、工人将按《中华人民共和国个人所得税法》及有关法律缴纳个人所得税。<BR> 
　　第１７．０３条　本合同签字之后，甲方和乙方将立即将本合同、附件和"合营公司合营方关于税务待遇的申请书"提交给中国税务部门以争取早日取得有关税务通知。<BR> 
