　　第四章　生产经营目的、范围和规模<BR> 
　　<BR> 
　　第４．０１条　１．合营公司的目的是：根据平等互利的原则和长期真诚合作的愿望，努力吸取合营双方各自的专长采用适宜的先进技术以及科学的管理方法，将合营公司建成一个现代化的制药企业，使其在产品的品种、质量及价格方面在国内外市场上具有竞争能力，并使甲方和乙方获得满意的经济效益。合营公司应依照世界卫生组织规定的药品生产管理规范（"ＧＭＰ"）以及乙方制定的内部的质量规格条例，在符合《中华人民共和国药品管理法》和中国卫生部的有关规定的条件下从事生产和推销医药产品。<BR> 
　　２．为了达到上述的主要目的，合营公司可以单独或依照中国法律和有关规定与各种形式和性质的公司、企业、经济组织、经济实体、机构及个人合作，根据"合资法"与本合同在国内外成立分公司、子公司。<BR> 
　　第４．０２条　合营公司的经营范围是制造和销售各种剂型的药品。药品的包装包括大包装（例如粉、颗粒、片剂、胶囊剂等）和适合消费者的需要的小包装。<BR> 
　　为了达到它的主要目的，合营公司有权开展自己的经营活动。<BR> 
　　第４．０３条　合营公司将生产如在不断调整的本合同附件中列出的产品。<BR> 
　　Ａ类：用中国国内生产的原料药生产的产品，由合营公司通过乙方包销的形式在国外市场销售。<BR> 
　　Ｂ类：用乙方或其子公司提供的原料药生产的产品，由合营公司通过乙方包销的形式在国外市场销售。<BR> 
　　Ｃ类：用乙方或其子公司提供的原料药生产的产品，由合营公司利用甲方的销售机构，根据合营公司和甲方签订的代销合同在国内市场销售。<BR> 
　　Ｄ类：董事会可于将来决定Ｄ类产品。包括下述产品：<BR> 
　　（１）用乙方或中国国内原料药生产乙方开发的产品，使用乙方的商标，由合营公司在国内国外销售。出口产品应由乙方包销。<BR> 
　　（２）用中国国内的原料，生产合营公司开发的产品，包括先进的中草药制剂产品，使用合营公司的商标，由合营公司在国内国外销售，按董事会的决定出口产品可由合营公司直接或通过乙方销售。<BR> 
　　生产Ｂ类、Ｃ类及部分Ｄ类产品所需进口的原料药，合营公司按不高于乙方及其子公司或其团体公司之间购买同类原料药时的平均价格从乙方或其子公司购买。<BR> 
　　第４．０４条　按合营公司工厂的设计能力，合营公司初期的生产规模为年产量＿＿＿＿＿至＿＿＿＿＿片／粒。根据市场情况，今后再增加约＿＿＿＿＿美元的投资。合营公司的年产量可增至＿＿＿＿＿片／粒。<BR> 
　　第４．０５条　合营公司生产经营所需外汇主要由出口Ａ、Ｂ类以及部分Ｄ类产品来解决。如外汇仍有不足，特别是当合营公司未能成功地按合理的条款和条件出口产品时，合营公司也可以按本合同第十九章所述通过其他途径解决。<BR> 
　　第４．０６条　合营公司今后将努力进行研究和开发工作。其研究和开发的成果和产品均属合营公司所有。这些成果和产品，可按照董事会决定的条款和条件，分配给或转让给甲方或乙方，或双方。<BR> 
