　　第十五章　劳动管理<BR> 
　　<BR> 
　　第１５．０１条　１．合营公司的职员、工人的雇用、招聘、解雇和辞职，以及他们的工资、福利待遇、劳动保险、劳动保护、劳动纪律及其他事宜将按《中华人民共和国中外合资企业劳动管理规定》及其实施条例和本公司章程的有关规定办理。<BR> 
　　２．合营公司职员、工人的工资和报酬应根据中国政府的有关规定制定，其个人实得工资水平是＿＿＿＿＿地区国营医药企业职员、工人实得工资收入的＿＿＿＿＿，该工资应全部付给每一个职员、工人。<BR> 
　　３．在合营公司职员工人不能适合合营公司的要求条件下，合营公司将尽力保持将其职员、工人的长期雇用政策，接受特殊培训职员、工人的雇用期不得少于＿＿＿＿＿年。<BR> 
　　４．如果职员、工人过剩或经过培训后仍不断适合合营公司的要求，合营公司可解雇他们，但将依法给予补偿。<BR> 
　　５．上述１、２、３和４款的所述事宜按董事会的决定将在合营公司与职员、个人集体或个人所签订的劳动合同中作出具体规定。劳动合同应向市劳动部门备案。<BR> 
　　第１５．０２条　合营公司的奖励、福利基金只能用于支付合营公司职员和工人的奖金、福利，不得它用。<BR> 
