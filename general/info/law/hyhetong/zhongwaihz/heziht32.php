　　第六章　合营各方责任<BR> 
　　<BR> 
　　第６．０１条　甲方责任如下：<BR> 
　　１．向有关中国机关申请批准本合同及其附件，代表合营公司进行登记和取得营业执照以及办理有关合营公司建立的其他事项。<BR> 
　　２．根据本合同第五章的规定对合营公司的注册资本进行出资。<BR> 
　　３．协助合营公司办理有关场地的开发事宜。<BR> 
　　４．协助合营公司对场地获得的接通水、电和燃料，接通通讯、交通及其他有关的基础设施。<BR> 
　　５．根据本合同第９．０１条的规定，向甲方已有客户代销合营公司的内销产品。<BR> 
　　６．协助合营公司招聘合格雇员，及时任命合营公司的董事和董事长，推荐第１４．０１条的副总经理和第１４．０３条规定的其他高级职员。<BR> 
　　７．协助合营公司申请并取得根据《中华人民共和国药品管理法》和其他有关法律所必须的批准。<BR> 
　　８．协助合营公司办理合营公司从中国境外购置的所有机器设备的进口及海关手续。<BR> 
　　９．协助合营公司申请确认附于本合同后的"合营公司和合营各方的税务待遇的申请书"中提出的税务待遇。<BR> 
　　１０．协助合营公司与中国境内的银行或其他金融机构进行贷款谈判。<BR> 
　　１１．协助合营公司和乙方的外国雇员和职工获得他们进入中国从事有关合营公司业务所要求的签证和工作许可。<BR> 
　　１２．严格遵守本合同及其附件的所有规定。<BR> 
　　１３．办理合营公司委托甲方的其他事项。<BR> 
　　第６．０２条　乙方的责任如下：<BR> 
　　１．根据本合同第十二章负责工厂设施设计，并就该设计工作与中国设计院密切合作。<BR> 
　　２．为合营公司推荐在海外购置所需机器设备。<BR> 
　　３．根据本合同附件三"技术转让协议"的条款和条件技术转让和提供技术服务。<BR> 
　　４．协助合营公司申请并取得根据《中华人民共和国药品管理法》和其他有关法律所有必须的批准。<BR> 
　　５．直接或通过其子公司向合营公司出售合营公司根据本合同第１１．０３条为Ｂ类、Ｃ类和部分Ｄ类产品的生产所需要的所有原料药。<BR> 
　　６．协助合营公司招聘合格雇员和及时任命合营公司董事及副董事长，推荐１４．０１条的总经理和１４．０３条规定的高级职员。<BR> 
　　７．协助合营公司同在中国境外的金融机构进行贷款谈判。<BR> 
　　８．协助合营公司和甲方的中国雇员、职工取得他们在中国境外从事有关合营公司事务的旅行所要求的去往国境外的国家或地区的签证和工作许可。<BR> 
　　９．根据第９．０２条规定的合营公司和乙方签订的包销合同，通过合营公司产品的出口以及通过第１９．０１条（ｂ）（ｉｉｉ）、（ｉｖ）、（ｖ）（ｖｉ）条规定的其他方法协助合营公司获得足够的外汇。<BR> 
　　１０．严格遵守合同及其附件的所有规定。<BR> 
　　１１．办理合营公司委托乙方的其他事项。<BR> 
