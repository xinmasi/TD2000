　　第二十章　利润分配<BR> 
　　<BR> 
　　第２０．０１条　合营公司将从其税后利润中提取储备基金，企业发展基金，职工奖励福利基金（三项基金）。提取比例由董事会决定，但提取职工奖励福利基金的比例为税后利润的＿＿＿＿＿％。<BR> 
　　第２０．０２条　１．每个会计年度的前三个月，总经理负责准备上一年度的收支平衡表，损益报告书及利润分配方案并提交董事会审查批准。<BR> 
　　２．董事会将决定是否将提取三项基金后的税后利润分配给各方。任何利润的分配将按合营各方出资额在合营公司注册资本中所占比例进行分配。<BR> 
　　第２０．０３条　原则上，合营公司将用外汇支付乙方分得的利润，在特殊情况下，合营公司的外汇不足以支付乙方的利润，合营公司应选择下列之一：<BR> 
　　１．乙方同意后，以人民币支付乙方的利润或：<BR> 
　　２．直至合营公司获得足够的外汇，合营公司将：<BR> 
　　３．提取应付乙方同等金额的人民币并将该笔人民币在中国存入有利息帐户，一旦获得充裕的外汇后，合营公司将以外汇支付应付乙方的利润，以人民币支付存款的利息。或：<BR> 
　　４．提取应付乙方的同等金额的人民币作为合营公司的流动资金。一旦获得充裕的外汇，合营公司将以外汇支付乙方的利润以人民币支付其利息。利率将按合营公司决定使用这项资金之日中国银行贷给其他中国国营企业的类似贷款利率而定。　　<BR> 
