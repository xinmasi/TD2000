　　第十九章　外汇<BR> 
　　<BR> 
　　第１９．０１条　合营公司将在法律允许范围内采用各种适当的办法努力保护外汇收支平衡。<BR> 
　　（１）通过出口合营公司的产品取得外汇，乙方将根据本合同第９．０２条规定由乙方与合营公司签定包销合同，乙方负责出口合营公司的产品。在开始商业性生产起＿＿＿＿＿年内该出口作为外汇的主要来源，该＿＿＿＿＿年后合营公司的产品的出口将继续增加以创所需的外汇。<BR> 
　　（２）在合营期间内，如上述ａ）的办法尚不足时则合营公司或乙方将使用下列办法创外汇。<BR> 
　　（ａ）根据《国务院关于中外合资经营企业外汇收支平衡问题的规定》（以下简称"外汇平衡规定"）的第六条，经有关部门批准后，合营公司可利用乙方的销售渠道推销国内产品出口。"国内产品"包括甲方所生产并同意出口的任何产品和乙方认为可以成功地销往国外的其他任何第三方生产的任何产品。<BR> 
　　（ｂ）根据外汇平衡规定的第八条，经有关部门批准后合营公司可以向有外汇支付能力的企业销售产品，并以外币计价结算。<BR> 
　　（ｃ）根据外汇平衡规定的第九条，乙方（包括乙方的所有其他部门）已在中国境内设立的其他合营企业的合法收入的外汇有余额时，经有关部门批准后，乙方可调剂解决合营公司与乙方所设立的其他合营企业的外汇问题。<BR> 
　　①在特殊情况下，乙方同意，合营公司可以用人民币支付乙方的利润。根据外汇平衡规定的第十条，经有关部门批准后乙方可用人民币再投资于国内能够新创外汇或新增加外汇收入的企业并享受外汇平衡规定第十条所给予的优惠。<BR> 
　　②根据外汇平衡规定第五条，经有关部门批准后，合营公司可向中国国内用户销售其产品，替代进口，收取外汇。<BR> 
　　③在其他现行或将来的规定允许范围内，合营公司或乙方可采用其他手段以求其外汇收支平衡。<BR> 
　　第１９．０２条　合营公司的一切外汇事宜，按《中华人民共和国外汇管理暂行条例》和其他有关规定及本合同的规定办理。<BR> 
　　第１９．０３条　合营公司的一切外汇收入将存入在中国银行开户或经中国国家外汇管理局批准的其他银行的外汇存款帐户。合营公司的一切外汇支出从该外汇存款帐户中支出。<BR> 
　　第１９．０４条　根据合营公司债务和需要，董事会应决定合营公司外汇支付顺序。<BR> 
