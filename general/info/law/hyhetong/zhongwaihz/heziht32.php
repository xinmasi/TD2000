　　技术转让协议<BR> 
　　<BR> 
　　前　　言<BR> 
　　<BR> 
　　本技术转让协议于＿＿＿＿＿年＿＿＿＿＿月＿＿＿＿＿日，由＿＿＿＿＿（简称"乙方"）和＿＿＿＿＿、＿＿＿＿＿（简称"甲方"）在中华人民共和国（简称"中国"）＿＿＿＿＿签订。甲方、乙方签订本协议仅为保证在合营公司－－＿＿＿＿＿（简称"公司"）正式成立后，促使公司正式授权代表与乙方签订技术转让合同。<BR> 
　　总则<BR> 
　　本协议是乙方将其拥有的专用技术转让给公司，商标专用权许可给公司。乙方的这些技术应是先进的、适用、连续的和动态的技术，以及将使公司的技术能力和产品质量达到世界先进水平，产品在国际国内市场上在技术质量和经济方面具有竞争力。为此目的，双方经过友好协商，在平等互利的原则上达成如下协议：<BR> 
　　第一条　定义<BR> 
　　"＿＿＿＿＿"意指世界卫生组织制定和随时修改的"＿＿＿＿＿"指标，以及由乙方制定和随时修改的关于质量标准的内部规定。<BR> 
　　"制造"意指从活性物质开始，并把它们做成适当剂型的制造过程。如药片、糖衣药丸、胶囊、药膏、栓剂、溶液和其他制剂。<BR> 
　　"包装"意指成品用出售的包装材料包装并贴标签。<BR> 
　　"产品"意指在合营公司所述的Ａ、Ｂ、Ｄ和Ｄ类产品中，所提到的和随时补充的乙方的药物产品。指着该Ａ、Ｂ、Ｃ、或Ｄ类产品时，各自以下称为"Ａ类产品"、"Ｂ类产品"、"Ｃ类产品"或"部分Ｄ类产品"（来自乙方的Ｄ类产品）。"技术资料"意指由乙方所有或支配的制造或包装产品所要求的数据、资料和知识（包括但不限于制造说明、分配方、质量控制和有关安全、卫生、生态学方面的资料）。<BR> 
　　"医学／科学资料"意指由乙方所有或支配的有关产品的数据、资料和知识。包括特别是包含在对产品基本登记档案材料中的科学、医学、临床、药理学和病理学等的研究资料。"净销售额"意指帐单的发票价格减去任何折扣、回扣和工商统一税。所有这些应根据适用于公司年度总帐目的公司标准会计原则进行计算。<BR> 
　　第二条　协议的范围和内容<BR> 
　　１．为达到本协议上述的总则中所提到的目的，乙方同意提供给公司必要的产品的技术资料、医学／科学资料和技术协助，如在本协议随时修改的附件中，更具体列出的内容。乙方同意提供下列技术和专有技术的转让。<BR> 
　　（１）乙方将用文件、图纸和说明的方式提供给公司技术资料，所有这些资料都用英文写成；<BR> 
　　（２）乙方将用它的产品基本登记档案材料的方式提供给公司医学／科学资料，这些资料都用英文写成；<BR> 
　　（３）乙方将授与公司如在第三条所规定的使用由乙方拥有使用权的某些商标的使用许可；<BR> 
　　（４）乙方将协助公司取得如第四条规定所需的一定资料、材料和设备，以使公司进行产品的临床试验和产品报批；<BR> 
　　（５）如第五条规定的，乙方将定期检验公司制造和包装的产品样品，以决定它们是否符合"＿＿＿＿＿"和乙方的规格；<BR> 
　　（６）如第六条规定的。乙方将为公司和乙方所挑选的合格的人员提供培训。乙方将派他自己的技术人员到公司，以便在公司的工厂内提供现场培训。<BR> 
　　２．本协议不包括公司自己开发的Ｄ类产品。公司应按照合营合同第７．０２条来处理这些产品。<BR> 
　　第三条　商标使用许可<BR> 
　　１．乙方必须遵照《中华人民共和国商标法》及其实施细则到中国有关部门进行商标注册。<BR> 
　　２．在本协议规定的条件下（特别是分配在第五条和第十一条中所规定的质量控制和保密要求的条件下），乙方特此授与公司为销售在本协议期内，由公司制造和／或包装的Ａ类、Ｂ类、Ｃ类和部分Ｄ类产品，而使用在本协议"附表"中列举的商标（以下称"商标"）的使用许可。根据这些条件，（ａ）公司应仅使用该商标来销售它制造和包装的Ａ类、Ｂ类、Ｃ类和部分Ｄ类产品；（ｂ）每当使用该商标时（例如在容器上、包装上、说明上和广告上）应附上Ｒ标志（中英译文）和参考符合"合营公司＿＿＿＿＿的注册商标"（中英译文）和（Ｃ）包装材料（包括包装插页）上的第一产品的标签和说明应用清楚的字迹标明的特许下制造和／或包装（中英译文）。关于产品的、包装形式、包装插页、标签和宣传材料应由公司和乙方一致同意。<BR> 
　　３．乙在中国注册的商标受中国法律保护。对该商标，乙方享有独家所有权，在所授与的使用许可期限届满或提前终止后，公司不得使用该商标或给出使用许可。公司没有就对乙方对商标的任何权利的任何要求权。<BR> 
　　第四条　产品登记、临床试验／验证以及试制<BR> 
　　在本协议期间。为了登记产品。根据《新药审批办法》所需的程序，乙方应协助公司进行临床试验／验证。乙方应协助公司进行产品试制，为了遵照《中华人民共和国药品管理法》和其他有关规定，并得到所有销售所必需的特许和许可。临床试验／验证只能通过公司与乙方共同商定的试验／验证程序进行。公司将负担有关此事宜所发生的一切费用。<BR> 
　　第五条　制造、包装、质量控制和安全<BR> 
　　为保证公司能够达到根据"＿＿＿＿＿"和乙方的规格制造和销售它的产品的目的，如在本条所规定的乙方应提供给公司它的质量控制程序，公司应在这些程序的履行方面与乙方合作。为此，乙方和公司承担以下责任：<BR> 
　　１．公司应严格地依据"＿＿＿＿＿"和乙方的规格以及所有适用的法律和规则如《中华人民共和国药品管理法》和被普遍接受的安全标准来制造和包装产品。公司应实施乙方提供的特定的安全措施。<BR> 
　　２．公司应始终依照乙方提供的制造、包装、质量控制、贮藏和安全程序进行工作。<BR> 
　　３．在公司对产品、新的药品剂型或新的剂量进行首次制造或包装时，公司应免费交付给乙方试制产品的若干代表性样品。在乙方还没有对该批产品签署书面意见并交给公司以前，不得开始进行批量生产。但乙方应在交付给乙方有关样品后６０天内把它的决定通知公司（或用书面通知公司任何延迟的原因）。<BR> 
　　４．按乙方的要求，公司应在任何产品制造后和包装前，立即免费提供给乙方足够数量的由公司制造的某一批产品的样品。乙方应对该样品进行分析。以决定公司制造的该产品是否严格地符合"＿＿＿＿＿"和乙方的规格。<BR> 
　　５．乙方应定期检验产品的生产、包装、质量控制和／或贮藏和收到有关这些材料，并决定是否符合"＿＿＿＿＿"和乙方的规格。乙方作出的任何不合格的决定应通知公司，由此：（ａ）公司应立即暂停这些产品的销售，和（ｂ）公司应尽最大努力来补救任何已经由公司卖出和推销了的产品。公司同意在公司未收到乙方事先的书面批准（此批准将是根据以后的检验结果认为对乙方来说是合理地符合要求的）前，不能在任何条件下直接或间接地销售任何有关产品（此产品是乙方根据本款已经作出质量不合格决定并已通知公司的产品）。<BR> 
　　６．在本协助期间内，如公司发觉它未能保持或不能保证严格地按照乙方为了"＿＿＿＿＿"、安全、健康或其他目的所提出的规模和指导来制造、包装、质量控制、贮藏和运输任何产品时，公司应尽量大努力减少或在可能的情况下防止损失，并立即把以上情况通知乙方。<BR> 
　　７．根据乙方规定由公司制造和／或包装的产品，公司应保存参考样品以及产品的完整的资料。<BR> 
　　第六条　培训<BR> 
　　乙方应尽最大努力来保持公司的工作人员能够掌握和使用乙方转让的技术和专有技术，以便公司可以达到本协议的目的。为此，乙方应提供以下培训服务：<BR> 
　　１．在本协议期间内，按董事会的要求，乙方应为公司和乙方所同意挑选的有经验的合格的技术人员提供培训，培训地在乙方在＿＿＿＿＿的工厂或乙方选择的其他乙方的一个或多个联合机构的工厂。技术专家应是在药品制造方面有资格的，公司应尽最大努力来保证他们在完成了本条规定的培训后至少五年内继续被公司雇用。公司应支付培训期间的和与培训有关的公司的技术专家的旅行、食宿、医药费和其他费用。乙方应支付它本身的费用。包括乙方的技术人员的工资。<BR> 
　　２．在本协议期内，按董事会的要求，乙方应派遣有经验的合格的技术人员到公司的工厂，对公司工作人员进行现场培训。公司应付乙方的技术专家的商人等级来回飞机票，以及其在中国境内的食宿和交通费用，其标准应对乙方来说是合理的满意的。乙方应付其技术专家在他们逗留中国期间的工资和医药费。<BR> 
　　３．培训的范围、内容、要求、方法和具体的培训计划等由乙方和公司协商制订和同意，经董事会批准执行。<BR> 
　　４．公司和乙方将互为对方的派遣人员办理签证，居留和其他准许的手续。<BR> 
　　第七条　改进和交换资料及新技术<BR> 
　　１．乙方应将所有的改进和产品的新技术通知公司。这些改进是乙方已经发展和／或得到的在技术资料中所包括的所有的内容如在本协议附件中更具体列出的内容以及它经常提供给它的联合公司的那些改进。<BR> 
　　２．乙方还应在医学／科学资料的范围内把它的所有经常地提供给它的联合公司的新认识通知公司。<BR> 
　　３．公司及时将所有得到和收到的对产品和原料的制造、包装、质量控制、贮藏和运输产生有利或不利影响的有关技术和医学的问题（包括但不限于安全、健康和生态学）报告乙方<BR> 
　　第八条　活性物质／辅料和包装材料<BR> 
　　为了达到本协议的技术目标和保持最高的生产标准，如在合营合同和将由乙方和公司制订的供应合同中的规定，公司就从乙方购买活性物质以进行生产Ｂ类、Ｃ类和部分Ｄ类产品。在符合乙方质量规格和质量控制条件下，公司可以从中国或外国的来源购买辅料和包装材料。<BR> 
　　第九条　提成费<BR> 
　　１．作为本协议规定的乙方转让技术和继续发展该技术的报酬公司应在单项产品开始商业性销售后的＿＿＿＿＿年期间，按该单项产品的净销售率额的＿＿＿＿＿％向乙方支付该单项产品提成费。＿＿＿＿＿年的提成期间过后，不再对该产品支付提成费。公司有权无偿继续使用所转让的技术和生产销售所转让的产品。<BR> 
　　２．对用于乙方转让给公司的产品的属于乙方拥有的（不论在中国登记与否）具有专利权的技术，公司按运用该有专利权的技术的单项产品净销售额的＿＿＿＿＿％给乙方支付附加技术提成费，该附加技术提成费应由公司和乙方通过考虑评价该具有专利技术的价值的一切有关事宜达成协议。该附加技术提成费应在该专利有效期内支付。但支付该附加技术提成费最长不超过自该单项产品开始商业性销售后＿＿＿＿＿年期限。＿＿＿＿＿年提成期过后，不再对该产品支付任何提成费，公司有权无偿继续使用该项产品的具有专利权的技术。<BR> 
　　３．该提成费应每＿＿＿＿＿年向乙方支付一次，并应在每＿＿＿＿＿年期后的＿＿＿＿＿天内交付。该提成费应附上一份由公司的审计员开出的证明，该提成费应基于的产品的名称和净销售额以及应支付提成费的金额的财务报表。<BR> 
　　４．根据乙方的要求，乙方将自费任命一名独立的审计员，对公司的帐簿进行审计以证实公司提供的情况，公司应对审计工作提供方便。<BR> 
　　５．提成费应用＿＿＿＿＿币支付乙方。兑换应以汇给乙方之日前两个工作日由中华人民共和国国家外汇管理局公布的汇率为准。<BR> 
　　第十条　乙方的保证、责任<BR> 
　　１．乙方保证竭尽所知，使其根据本协议将向公司提供的技术资料和医学／科学资料是完整的、无错误的，并且对公司的情况是适合的。并且技术培训、技术指导，技术服务能合理地满足公司要求。<BR> 
　　２．公司收到乙方的技术资料后，根据乙方的技术资料清单对资料名称、数量进行审查时（该审查应在收到日后＿＿＿＿＿天内进行），如果发现有一部分遗漏或者有错误，公司应立即以书面通知乙方。乙方应在接到该通知后＿＿＿＿＿天内，免费向公司提供失去部件或正确部分。<BR> 
　　３．在公司严格按照乙方在工艺条件，操作规程和技术指导的前提下进行生产时，乙方应保证提供的专有技术能稳定达到乙方指定的生产水平，质量符合标准，原材料消耗定额不超过经验过的实际限度。<BR> 
　　４．乙方应帮助公司进行技术性能考核验收。如果由于乙方责任双方制订的技术目标达不到时：<BR> 
　　（１）首先公司和乙方通过友好协商改正解决。如果资料遗漏或有错误，乙方应提交失去部分或正确部分。<BR> 
　　（２）如果由于乙方责任技术目标或要求达不到，由乙方应免费派人参加试验改进，直到消除缺陷达到目标为止。如果在试车过程中发生问题，乙方应在与公司商定的期限内给以解决。如果经反复试验仍达不到规定的技术目标和指标，由乙方应负责补偿公司为实现该技术目标和指标所发生的费用。<BR> 
　　５．赔偿方法：<BR> 
　　（１）对任何一种产品的赔偿，赔偿金额只能从该产品的提成费扣除。当赔偿金额不超过提成费时，由本年度提成费扣除。如果超过本年度提成费时，先退回本年度提成费，不足部分由该产品的下一年度提成费补足，直到全部赔完为止。<BR> 
　　（２）如果乙方未从该产品得到足够的提成费进行赔偿，则乙方应用其他办法来赔偿公司因乙方违反其保证而引起公司发生的费用。<BR> 
　　６．在乙方支付公司如上所述赔偿后，乙方有义务继续履行本协议。<BR> 
　　７．乙方保证根据本协议乙方提供给公司的一切权利和技术不侵犯任何第三者的权益。<BR> 
　　８．由于公司不适当地使用或运用乙方向公司提供的技术、专有技术和转让的产品，而导致任何损害、损失或伤害的情况下，乙方不对公司承担任何责任。<BR> 
　　第十一条　保密<BR> 
　　１．乙方向公司所提供的所有技术资料和医学／科学资料应由公司严格保密，也应对甲方保持保密义务，并只能如合营公司所规定的，在公司的业务范围内被公司使用。公司在为了登记目的的需要和得到制造、包装和销售产品所需的允许，以及保持该登记和允许。<BR> 
