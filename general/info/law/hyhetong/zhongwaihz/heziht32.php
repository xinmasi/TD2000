　　第七章　技术合作<BR> 
　　<BR> 
　　第７．０１条　在合营期内，根据合营公司生产经营的需要，乙方应向合营公司转让其产品的先进技术，以及乙方今后对这些产品的改进。该技术转让的详细内容和条件规定在本合同附件三"技术转让协议"中。<BR> 
　　（１）乙方应以技术资料和医学／科学资料的形式向合营公司转让Ａ类、Ｂ类、Ｃ类和部分Ｄ类产品的生产配方、工艺技术、质量控制等专有技术的数据、资料和知识，包括今后的改进和进一步的发展，以使合营公司可能根据"ＧＭＰ"和乙方质量规格和不断修改的"药品生产指南"进行生产、包装和销售该产品。<BR> 
　　（２）乙方准予合营公司使用属于乙方的商标的使用许可，其条件和条款规定在本合营附件"技术转让协议"中。<BR> 
　　（３）作为乙方转让技术和继续发展该技术如上述（１）项和（２）项的报酬，合营公司应在单项产品开始商业性销售后的＿＿＿＿＿年期间，按该单项产品的净销售额的＿＿＿＿＿％向乙方支付该单项产品技术提成费。＿＿＿＿＿年的提成期过后，不再支付提成费。合营公司有权无偿继续使用转让的技术和生产销售所转让的产品。<BR> 
　　（４）对用于乙方转让给合营公司的产品的属于乙方拥有的，但未在中国专利局登记的具有专利权的技术，乙方应向合营公司提交有关专利证书。经乙方与合营公司董事会同意，根据不同情况，合营公司按运用该有专利权的技术的单项产品的净销售额的＿＿＿＿＿％￣＿＿＿＿＿％给乙方支付附加技术提成费。该附加技术提成费应在专利有产期内支付，但支付该附加技术提成费最长不超过自该单项产品开始商业性销售后的＿＿＿＿＿年期间，＿＿＿＿＿年期间过后不再支付任何提成费，合营公司有权无偿继续使用该项具有专利权的技术。<BR> 
　　（５）对用于乙方转让给合营公司的产品的属于乙方拥有的并在中国专利局登记批准的具有专利的技术，合营公司将根据（４）的原则与乙方另行签订专利许可合同。<BR> 
　　（６）乙方与合营公司的签订的按本合同附件三的形式的技术转让合同期限与合营合同期限相同，原则上董事会认为必要时经与乙方协商同意可对技术转让合同进行修改。<BR> 
　　（７）合营公司在使用乙方转让的技术时，对于第三者提出的权益要求，不负任何责任。<BR> 
　　第７．０２条　合营公司开发的产品作如下规定：<BR> 
　　１．合营公司将来按董事会批准所开发的Ｄ类产品应具有疗效、稳定性、有效期内的安全性。合营公司应严格地依据"ＧＭＰ"和乙方的标准操作程序和不断修改的"药品生产指南"以及所有适用的法律和规则（如《中华人民共和国药品管理法》）和被普遍接受的安全标准来制造和包装产品。<BR> 
　　２．在合营公司对该Ｄ类产品或该Ｄ类产品的新的药品剂型或新的剂量进行首次制造或包装前，合营公司应交给甲方和乙方完整的产品文件，包括全部技术资料和全部医学／科学资料，让甲方和乙方作出意见和批准，即确认所有文件是否完整。<BR> 
　　３．在合营期限内，如合营公司发觉它未能保持或不能保证严格地按照合营公司为了"ＧＭＰ"，安全健康或其他目的所提出的该Ｄ产品的产品规格来制造，包装、质量控制、贮藏和运输任何产品时，合营公司应尽最大努力减少或在可能的情况下防止损失，并立即把以上情况通知甲方和乙方。<BR> 
　　４．根据乙方规定由合营公司制造和／或包装的产品，合营公司应保存参考样品，所用原料、包装材料及产品的完整的资料，也根据乙方规定由合营公司进行随后的稳定性的检验。<BR> 
　　５．合营公司自己开发的产品属于合营公司所有，并使用合营公司自己的商标。<BR> 
　　６．除上述１、２、３、４、５规定外，如果合营公司要求从甲方或乙方给予附加的技术帮助或先进技术。对此，合营公司应就适应的报酬与甲方或乙方达成协议。<BR> 
　　第７．０３条　经董事会同意并根据中国有关法律规定，合营公司可从第三者引进甲方和乙方所没有的先进技术。合营公司也可向第三者转让合营公司自己开发的技术。<BR> 
　　<BR> 
　　第八章　场地使用<BR> 
　　<BR> 
　　第８．０１条　甲方保证合营公司在第２３．０１条所规定的合营期间中享有对场地的使用权。<BR> 
　　第８．０２条　合营公司承担场地的开发费，即取得场地的占地所发生的费用（劳动力安置，土地补偿费、青苗补偿费、新菜田开发费、拆迁费等）以及接通公用设施的费用。甲方和乙方估计总的开发费为人民币＿＿＿＿＿元左右。<BR> 
　　第８．０３条　合营公司应委托一个合适的机构负责安排和办好所有有关劳动力安置、土地补偿、青苗补偿、新菜田开发及拆迁等事宜。委托该机构在六个月内完成这些事宜。<BR> 
　　<BR> 
　　第九章　产品销售<BR> 
　　<BR> 
　　第９．０１条　合营公司应负责在国内销售其产品，并委托甲方作为甲方已有的客户的销售代理人。由甲方代销的条款和条件应在合营公司与甲方签证的销售代理合同中给予规定，或应包括下列原则：<BR> 
　　１．甲方应是合营公司产品在国内销售给甲方已有客户的销售代理人。<BR> 
　　２．产品的宣传和广告工作应由合营公司进行。<BR> 
　　３．产品的销售价格应由合营公司决定并且能够使产品在国内市场具有竞争力。<BR> 
　　４．甲方应享有销售佣金，该佣金占净销售额的比例由合营公司和甲方协议决定。<BR> 
　　第９．０２条　计划由合营公司出口的乙方的Ａ类、Ｂ类和部分Ｄ类产品以及合营公司开发并由董事会决定由乙方在国外销售的Ｄ类产品，由乙方在中国境外包销。由乙方包销的条款和条件应在合营公司与乙方签订的包销合同中给予规定，并应包括下列原则：<BR> 
　　１．乙方应为独家的出口产品包销商。<BR> 
　　２．乙方应以出厂价ＦＯＢ北京的条件，从合营公司购买出口产品，该价格应能使乙方按照国际市场有竞争力的价格转售出口产品。乙方应定期地向合营公司提供有关在中国境外销售的市场资料。<BR> 
　　３．合营公司应负责为该出口产品取得出口许可证，乙方应负责在出口产品销售的国家和地区取得销售许可。<BR> 
　　第９．０３条　由合营公司开发的Ｄ类产品亦可由合营公司直接出口。<BR> 
　　第９．０４条　合营公司应进行市场调查，以使决定当地市场条件和了解为国内市场服务的最佳途径和方法，据此，合营公司应在晚些时候运用该市场调查资料建立其自己的销售组织。<BR> 
　　<BR> 
　　第十章　设备、辅料、包装材料的购置<BR> 
　　<BR> 
　　第１０．０１条　董事会已经做出有关合营公司生产产品的最终决策之后，应根据乙方推荐购置机器设备。乙方应提供机器设备的型号、规格和供应者。为了确保工厂设施的经营根据"ＧＭＰ"和乙方的规格，合营公司应从乙方推荐的可靠性、信誉好的供应者购置机器设备。乙方应协助合营公司从海外定购机器设备。<BR> 
　　第１０．０２条　关于购买零件、分析测定仪器、机械设备、交通工具和办公用品等，如果能符合规格、保证要求、与其他部件配套，可靠的并在其他方面如服务、维修、维护及改进的服务和质量符合要求，可以对在中国购买给予优先考虑。<BR> 
　　第１０．０３条　在符合乙方质量规格和质量控制条件下，合营公司可以从中国或外国的来源购买辅料和包装材料。<BR> 
　　<BR> 
　　第十一章　原料药的供应<BR> 
　　<BR> 
　　第１１．０１条　为Ａ类产品的生产和合营公司开发的Ｄ类产品所要求的原料药和物质应在中国购买。<BR> 
　　第１１．０２条　为获得技术转让合同的技术目标以及维持最高的生产标准。合营公司应从乙方购买所有其需要的原料药，以生产Ｂ类、Ｃ类和部分Ｄ类产品。<BR> 
　　第１１．０３条　乙方应向合营公司按乙方同合营公司签订的供应合同规定的条款和条件供应第１１．０２条提到的原料药，该供应合同应包括下列原则：<BR> 
　　１．乙方对原料药的报价不应高于乙方向其子公司的并不时修改的报价的平均价格，其价格条件应是ＣＩＦ。<BR> 
　　２．向合营公司供应的原料药应符合乙方的标准规格。<BR> 
　　３．所有原料需求的预测、订货和支持应根据供应合同的规定。<BR> 
　　４．合营公司同意它应仅从乙方或其子公司购买所有它所需要的原料药。<BR> 
　　５．合营公司应用双方同意的自由兑换的货币为该原料药支付。<BR> 
　　６．合营公司应负责为该原料药获得必要的进口许可和政府批准，并应负责为该进口支付的任何关税和税款。<BR> 
　　<BR> 
　　第十二章　工厂设施的设计准备和建筑<BR> 
　　<BR> 
　　第１２．０１条　１．为确保合营公司将拥有和经营一个有先进的设计特点的现代化的药品（生产）工厂设施以便遵循"ＧＭＰ"和＿＿＿＿＿方规格，并符合中国政府有关设计的规范要求，＿＿＿＿＿方应为该工厂设施准备设计。<BR> 
　　合营公司与＿＿＿＿＿方应根据本合同附件"设计协议"的形式及条款和条件签订设计合同。＿＿＿＿＿方与一个＿＿＿＿＿设计院合作来完成该项设计工作。合营公司将与＿＿＿＿＿设计院签订一个设计合同，明确规定设计分工、协作、责任和报酬。＿＿＿＿＿方积极地参加该设计合同的谈判。<BR> 
　　２．＿＿＿＿＿方应通过准备工厂的初步设计和实施设计（护大初步设计），指导并监督＿＿＿＿＿设计院的设计是否符合＿＿＿＿＿方的设计规格。＿＿＿＿＿方对该项设计工作负有全面的责任。需要＿＿＿＿＿方确认的设计和图纸，应由合营公司负责安排译成＿＿＿＿＿文。<BR> 
　　３．上述第２款中所述的＿＿＿＿＿方的设计工作和服务，连同＿＿＿＿＿方由于设计工作需要派专家／技师来往＿＿＿＿＿的飞机票费（飞机票最多应不超过＿＿＿＿＿人次），应根据第５．０４条作为＿＿＿＿＿方对合营公司的注册资本出资，其作价为＿＿＿＿＿美元。合营公司应支付该专家／技师的食宿费（每次不超过两周，最多＿＿＿＿＿人次）。合营公司应负责支付＿＿＿＿＿设计院的设计费。<BR> 
　　第１２．０２条　本合同批准日后的一个月内，合营公司应建立一个建设和筹备办公室（筹备办公室）。董事会应委托该筹备办公室的工作人员。筹备办公室应在合营公司总经理和副总经理的领导下工作。<BR> 
　　第１２．０３条　筹备办公室的一般责任为：<BR> 
　　１．在本合同批准日后三个月内，准备总设计费的预算。为了便于主管部门对设计的审批协助＿＿＿＿＿方工作。<BR> 
　　２．根据设计与工厂建筑的总承包商就建筑合同（"建筑合同"）进行谈判。<BR> 
　　３．组织购置和检验工厂的建筑所要求的设备和材料，并在＿＿＿＿＿码头办理所有进口手续和海关申报。<BR> 
　　４．组织所有设置及设施的安装并在＿＿＿＿＿方指导监督下进行技术投试。<BR> 
　　５．决定项目建设的总进度。<BR> 
　　６．编制开支计划，并进行项目的财务管理。<BR> 
　　７．编制有关管理程序。<BR> 
　　８．保存和管理所有在建筑阶段期间的文件、图纸、档案和资料。<BR> 
　　９．定期准备由董事会审查的建筑报告。<BR> 
　　第１２．０４条　该工厂设施设计批准后，合营公司应与筹备办公室选中的总承包商签订建筑合同。该建筑合同应根据批准的设计和董事会满意的条款和条件来进行。<BR> 
　　第１２．０５条　筹备办公室应监督工程的实施以确保其符合设计和建筑合同的规定。<BR> 
　　第１２．０６条　筹备办公室的费用和其工作人员的报酬应包括在合营公司建设预算中。<BR> 
　　第１２．０７条　工厂建筑完工后，筹备办公室应安排董事会进行工程的验收。<BR> 
　　在工厂设施根据批准的设计的完工以及对交接程序的完成表示满意后，董事会应解散筹备办公室。<BR> 
　　第１２．０８条　除上述工作外，合营公司在其建设期间的其他生产准备工作应由总经理和副总经理根据实际情况安排。<BR> 
　　<BR> 
　　第十三章　董事会<BR> 
　　<BR> 
　　第１３．０１条　１．董事会是合营公司最高权力机构，董事会的权力和职责在《公司章程》中予以规定。<BR> 
　　２．合营公司的重大事项应由董事会全体一致决定。该重大事项在《公司章程》中第二十九条予以规定。<BR> 
　　３．除上述条款外的其他事项应由多数票通过决议决定，但是至少各方委派的一名董事投了赞成票。该事项在《公司章程》第三十条予以规定。<BR> 
　　第１３．０２条　董事会应由＿＿＿＿＿名董事组成，各方应各委派＿＿＿＿＿名董事。甲方应在其董事中委派一名董事长，乙方应在其董事中委派一名副董事长。<BR> 
　　董事、董事长和副董事长任职期限应为四年，经委派方决定可以连任。<BR> 
　　第１３．０３条　合营公司的营业执照颁发之日为其董事会成立之日。<BR> 
　　第１３．０４条　董事会的董事长是合营公司的法定代表人，如果董事长因故不能履行其职责，副董事长就被暂时授权来履行董事长的职责。<BR> 
　　第１３．０５条　董事会会议应每年举行＿＿＿＿＿次，并由董事长召集和主持，会议程序、法定人数要求、代理、投票等事宜在《公司章程》第四章中规定。　　<BR> 
　　第十四章　管理机构<BR> 
　　<BR> 
　　第１４．０１条　合营公司应设一名总经理和一名副总经理。总经理和副总经理应由董事会任命。总经理应由＿＿＿＿＿方推荐，副总经理应由＿＿＿＿＿方推荐。他们的任期为四年，同样可根据董事会的决定连任。<BR> 
　　第１４．０２条　总经理应对董事会负直接责任。他应执行董事会的各种决定并应组织和领导合营公司的日常工作和管理。副总经理应协助总经理进行工作，当总经理缺席时，副总经理就代表总经理履行其职责。关于主要事项的决定需要总经理和副总经理共同签署，在《公司章程》第三十三条中予以规定。<BR> 
　　第１４．０３条　１．合营公司应建立在总经理和副总经理领导下的，由生产经营部经理、质量控制总经理、车间工程师、人事部经理、财务管理经理（即：总会计师）以及销售和物料供应部（材料管理）经理组成的管理机构，上述人员均应由董事会任命，各高级职员的任期应为四年，亦可根据董事会的决定连任。<BR> 
　　２．甲方应推荐质量控制部经理、人事部经理和总会计师。乙方应推荐生产经营部经理、车间工程师、销售和物料供应部（材料管理）经理。在晚些时候董事会可对甲乙双方推荐的职位作调整。<BR> 
　　第１４．０４条　高级职员有营私舞弊和严重渎职的情况，可由董事会的决议随时解聘，触犯刑法者应对其犯罪行为根据中国刑法承担责任。<BR> 
　　第１４．０５条　合营公司高级职员工资和报酬应由董事会根据下述原则决定：<BR> 
　　（ａ）合营公司高级职员中的外国雇员的工资和报酬应与中国的医药合营公司的同样职位的同类职员的平均工资标准相似，并应以双方同意的可自由兑换的货币支付。如果法律允许，在中国境内一般日常开销所需那一部分工资和报酬，经董事会决定应以人民币支付。<BR> 
　　（ｂ）合营公司高级职员中的中国当地雇员的工资和报酬应与中国的医药合营公司的同样职位的同类职员的平均工资标准相似。该工资和报酬应以人民币支付。<BR> 
　　第１４．０６条　如董事会决定，合营公司应自费或支付住房补贴为合营公司的外国高级职员提供住房。该提供的住房或支付的住房补贴应有一个合理的标准，该标准与中国其他医药合营公司为外国管理人员提供的住房或住房补贴标准相似。<BR> 
　　第１４．０７条　所有其他事项，如合营公司的中外高级职员的津贴、福利、旅行费用标准等等应由董事会决定。<BR> 
