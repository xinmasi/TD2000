　　第一条　销售方式和区域<BR> 
　　１．１　在合同期间，乙方是合营公司向中国境外销售合同产品的独家代理，乙方负责包销所产量的＿＿＿＿＿％，但最多每年＿＿＿＿＿只，销售到国际市场。<BR> 
　　合营公司负责销售其余的＿＿＿＿＿％，在事先与乙方协商同意后合营公司可向国外销售合同产品。<BR> 
　　１．２　凡属由乙方返销的＿＿＿＿＿钻头，应由乙方直接与合营公司签订订货合同，乙方在每季末前１５天将预测外销钻头型号尺寸及数量提供合营公司，作为备料的依据。<BR> 
　　乙方应根据用户需要及时以电传订货方式通知合营公司，如电传所订的钻头系标准型、数量在＿＿＿＿＿只以下，合营公司自收到电传订货后，必须在＿＿＿＿＿天内将钻头发出，运往＿＿＿＿＿国际机场。如所订的钻头系非标准型，或数量在＿＿＿＿＿只以上的，根据实际情况，双方协商确定发运期限。<BR> 
　　１．３　乙方负责销售到国际市场的钻头，所有对产品销售后的技术服务全部由乙方承担；合营公司负责销售的钻头，销售后的产品的技术服务由合营公司自行负责。<BR> 
　　１．４　合营公司经乙方销售的钻头，如在使用中失效，经合营公司鉴定并确认是由于制造质量缺陷而造成的，由合营公司根据情况，给予乙方以折扣或免费更换。<BR> 
　　１．５　合营公司按订货合同制造的钻头，必须符合乙方制订的并已为合营公司所采用的质量标准，如需要更改标准，或要求高于乙方制定的质量标准时，必须经双方协商，达成书面协议，作为订货合同补充协议。<BR> 
　　１．６　合营公司在正式投产＿＿＿＿＿年后，将采用中国国内制造的＿＿＿＿＿片及＿＿＿＿＿切削块，如上述切块尚不能全部达到乙方的质量标准时，返销给乙方的钻头仍采用进口的切削块。<BR> 
　　第二条　产品销售价格<BR> 
　　２．１　合营公司外销的＿＿＿＿＿钻头价格应按乙方于年初公布的国际市场价目表中所列的＿＿＿＿＿洲的销售出厂价格减＿＿＿＿＿％，所减的＿＿＿＿＿％包括乙方全部的销售技术服务及其管理费用，返销给乙方的价格不得低于国内销售价格。<BR> 
　　合营公司内销价格＝产品销售成本＋销售费用＋管理费用＋税收＋＿＿＿＿＿％左右的税后利润。<BR> 
　　２．２　为适应国际市场行情的变化，使合营公司产品具有竞争性，经双方协商同意后，可适当大折扣，调整销售价格。<BR> 
　　第三条　出口包销产品的结算<BR> 
　　３．１　合营公司外销的＿＿＿＿＿钻头，按每次订货合同进行结算，结算按两种形式进行：<BR> 
　　１）当订货合同所需的钻头发运后，合营公司将发票、帐单和运输机构的货单等全部单据提交中国银行＿＿＿＿＿分行并办妥托收手续，委托中国银行向订货单位收取货款。<BR> 
　　２）发票、帐单及提货单提交买方后，由买方将货款寄到中国银行合营公司帐户内。<BR> 
　　第四条　原材料及半成品购买<BR> 
　　合营公司向乙方购买原材料，乙方保证以与卖给其分公司相同的价格卖给合营公司，并保证在收到订单后按附录一所确定的时间内交货。<BR> 
