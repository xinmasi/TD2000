　　前言<BR> 
　　<BR> 
　　＿＿＿＿＿与＿＿＿＿＿根据《中华人民共和国中外合资经营企业法》和中国的其他有关法规，本着平等互利的原则，通过友好协商，同意在中华人民共和国＿＿＿＿＿共同投资举办生产及销售＿＿＿＿＿钻头的合资经营企业，特订立本合同。　　<BR> 
　　第一条　合营双方<BR> 
　　本合同的当事人为：＿＿＿＿＿（以下简称甲方）<BR> 
　　法定地址：＿＿＿＿＿<BR> 
