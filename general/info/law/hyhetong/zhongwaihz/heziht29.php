　　第一条　总则<BR> 
　　１．１　本会计程序是合营公司合同的组成部分，是合营公司制定财会制度的原则依据。<BR> 
　　１．２　合营公司设财务部，财务部经理具体负责财务会计工作，制定公司财务、信贷计划；负责审查稽核公司及其所属机构的各项财务收支、会计凭证、会计帐簿、会计报表和有关资料，财务部经理负责拟定公司内部财会制度细则及其人员分工，报董事会审批及地方财政部门备案。<BR> 
　　１．３　合营公司的会计年度，采用公历年度，从公历每年一月一日起至十二月三十一日为一个会计年度。<BR> 
　　１．４　合营公司将在中国银行开设人民币和外汇帐户，合营公司的收入和支出在此帐户内存取。外汇业务按照《中华人民共和国外汇管理暂行条例》和《对外资企业、中外合资经营企业外汇管理施行细则》办理。<BR> 
　　１．５　合营公司采用借贷复式记帐法和根据权责发生制的原则记帐。凡是当月已经实现的收益和已经发生的费用，无论款项是否收付，都作为当月的收益与费用入帐；凡是不属于当月的收益与费用，即使款项已在当月收付，都不作为当月的收益与费用处理。<BR> 
　　１．６　合营公司会计核算工作使用中文为工作文字，月度、季度和年度会计报表应同时用中文和英文编制，一切自制的会计凭证以及帐册、报表的标题和栏头，应同时使用中文和英文。<BR> 
　　１．７　合营公司以人民币为记帐本位币。对于现金、银行存款、其他货币款项以及债权、债务、收益和费用等实际收付的货币，如与记帐本位币不一致时，还应按原币进行登记。<BR> 
　　１．８　公司的外币存款、外币借款和以外币结算的往来款项，除应登记实际收付的外币金额外，增加时，按记帐汇率折合为人民币记帐，记帐汇率采用当月一日中华人民共和国外汇管理局公布的外汇中间牌价折算：减少时，按帐面汇率折合为人民币，帐面汇率采用移动加权平衡法计算。因汇率不同而发生的折合为人民币的差额，作为外汇兑换损益（简称汇兑损益）处理，列入当期损益。<BR> 
　　１．９　合营公司应划清资本的支出与收益的支出分界线，凡是用于增加固定资产和无形资产而发生的各项支出，都是资本的支出，凡是为了取得本期收益而发生的各项支出，都是收益的支出。<BR> 
　　第二条　投入资本的核算<BR> 
　　２．１　合营公司应按合同规定的资本总额、出资比例、出资方式、在规定期限内投入资本，合营公司根据合营各方实际投入的资本进行核算。<BR> 
　　２．２　以现金投资的，以收到或存入开户的中国银行的金额和日期，作为投入资本的记帐依据。<BR> 
　　２．３　以实物投资的按合同附件四“实物出资作价协议”规定，经检验核实的实物清单中所列金额和收到实物的日期，作为记帐的依据。<BR> 
　　２．４　合营各方缴付的出资额，由中华人民共和国政府批准的注册会计师验证。出具验资报告后，由合营公司据以发给出资证明。<BR> 
　　第三条　物资管理<BR> 
　　３．１　合营公司的物资，是指库存的加工中的和在途的各种原材料、包装物、低值易耗品、在产品、自制半成品、产品等。<BR> 
　　３．２　合营公司的各种物资均按实际成本记帐，实际成本包括买价、运输、装卸、包装和保险、运输途中的合理损耗，入库前的挑选整理费，进口物资包括关税和产品税（或增值税）。<BR> 
　　自制的原材料、包装物、低值易耗品、半成品、产成品的实际成本，包括制造过程中所耗用的原材料、工资和有关费用。<BR> 
　　委托外单位加工完成的原材料、包装物、低值易耗品、半成品、产成品的实际成本、包括耗用的原材料或关成品的实际成本、加工费用和往返的运杂费。<BR> 
　　３．３　合营公司各种物资的收发领退，按实际数量及时办理会计手续，设置有数量有金额的明细帐，逐项逐笔登记。加强管理，各种物资的实际或进货原价，采用移动平均法计算。<BR> 
　　３．４　合营公司的各项存货实行“永续盘存制”每年至少进行实地盘点一次，发现盘盈、盘亏、毁损、变质等情况，应由有关部门查明原因、书面报告总经理。经严格审查，按董事会规定报经批准后，在年度决算前处理完毕。<BR> 
　　第四条　固定资产核算<BR> 
　　４．１　合营公司的固定资产，是指使用年限在一年以上，单项价值在＿＿＿＿＿元人民币以上的房屋、建筑物、机械、运输工具和其他生产设备等。单位价值在人民币＿＿＿＿＿以上，使用期限较短的物品，作为低值易耗品处理，按实际使用数列为费用，主要的生产设备，单位价值虽在人民币＿＿＿＿＿以下，仍作为固定资产核算。<BR> 
　　４．２　合营公司的固定资产分为：房屋及建筑物、机器设备、电子设备、运输工具和其他设备五大类，具体分类在固定资产目录中详细列出。<BR> 
　　４．３　合营公司的固定资产按到厂价登记入帐。作为投资的固定资产，以双方协商议定的价格作为原价。<BR> 
　　４．４　合营公司的固定资产的折旧，采用直线法计算。固定资产折旧率根据固定资产的原价，估计残值和使用年限计算确定。各类固定资产的使用年限和估计残值，按《中外合资经营企业所得税法施行细则》规定的固定资产计算折旧最短年限和估计残值执行。<BR> 
　　４．５　合营公司对固定资产的购入、销售、清理、报废和内部转移等，都要办理会计手续，并应设置固定资产明细帐进行核算，加强固定资产管理。<BR> 
　　４．６　合营公司的固定资产至少每年实地盘点一次，对盘盈、盘亏、毁损的固定资产，应由有关部门查明原因。经严格审查，按董事会规定报经批准后及时处理，一般应在年报决算前处理完毕。<BR> 
　　第五条　无形资产及其他资产的核算<BR> 
　　５．１　合营公司的无形资产包括专有技术、专利权、商标权、版权、场地使用权、其他特许权等，购入的无形资产，按实际支付的金额作为原价，从开始使用的年分起按规定的使用期限分月摊销。<BR> 
　　５．２　合营公司在筹办期间所发生的费用（不包括购建固定资产和无形资产的支出以及基建期内应计入工程成本的利息支出），按照合同规定并经合营双方协商同意，作为开办费记帐，在开始生产经营后分期摊销，每年摊销额不得超过百分之＿＿＿＿＿。<BR> 
　　第六条　成本和费用的核算<BR> 
　　６．１　合营公司要建立健全原始记录，实行定额管理，严格计量检验和物资收发领退制度，加强成本费用的管理和核算。<BR> 
　　６．２　一切与生产经营有关的支出，都应计入合营公司的成本和费用，包括下列各项：<BR> 
　　６．２．１　合营公司的生产成本包括：<BR> 
　　（１）直接材料的支出；<BR> 
　　（２）专用工具的支出；<BR> 
　　（３）燃料及动力的支出；<BR> 
　　（４）直接工资的支出；<BR> 
　　（５）外部加工费；<BR> 
　　（６）制造费用。<BR> 
　　６．２．２　制造费用是合营公司的车间为组织和管理生产所发生的各项费用，包括工资、折旧费、修理费、物料消耗、劳动保护费、水电费、加工费、差旅费、运输费、保险费等。<BR> 
　　６．２．３　合营公司的销售费和管理费用，应单独核算，不计入产品生产成本之内。<BR> 
　　（１）合营公司的销售费用包括产品在销售过程中所发生的，应由合营公司负担的运输费、装卸费、包装费、保险费、差旅费、佣金、广告费和销售机构人员工资、差旅费和其他经费。<BR> 
　　（２）合营公司的管理费用是指公司管理部门的经费和财务费用等开发，包括公司经费（人员工资、差旅费、办公费等）、工会经费、利息支出（减利息收入）、汇兑损失（减汇兑收益）、董事会费、顾问费、实际应酬费（此项费用不得超过纳税年度销售收入总额＿＿＿＿＿％，且不包括和与生产经营业务无关的交际费）、税金（产品税或增值税和企业所得税以外的其他税金）、场地使用费、技术转让费、无形资产摊销、职工培训费、研究发展费、其他管理费等。<BR> 
　　６．３　合营公司成本核算，采用品种法计算。<BR> 
　　６．４　合营公司应加强对成本费用的控制，建立责任成本制度，编制成本费用计划。随时按计划掌握支出，定期考核计划执行情况，分析成本费用升降原因，采取必要措施，努力降低成本费用，改善公司经营管理。<BR> 
　　第七条　销售和利润的核算<BR> 
　　７．１　合营公司的产品价格，原则上每年调整一次，在原材料、管理费用和其他费用波动较大的特殊情况下，可能调整价格，每当向客户出售产品或提供服务时，公司按当时的价格出具发票，送交客户。<BR> 
　　７．２　合营公司产品的销售应在产品已经发出，并已将发票、帐单和运输机构的提货单等全部货运单据，提交买方或通过银行办妥托收手续后，作为销售实现。<BR> 
　　７．３　产品销售和服务的收入，凡外币收入应由客户汇入公司开设的外汇帐户，凡人民币收入汇入公司开设的人民币帐户。<BR> 
　　７．４　合营公司的销售收入情况，由财务部在每季度结束后二十天内提出报告，并根据每半年销售的产品净销额提出应向乙方支付的提成费，由总经理审查后执行。<BR> 
　　７．５　合营公司利润总额的内容：包括产品销售利润，其他业务利润和营业外支出。<BR> 
　　营业外收入和营业外支出是指产品销售利润和其他业务利润以外的各项非营业损益，包括投资收益、投资损失、处理固定资产收益、处理固定资产损失、罚款收入、罚款支出、捐赠支出、坏帐损失、非常损失等。<BR> 
　　７．６　合营公司的利润净额应是每个会计年度实现的利润总额减去以下各项后的净数。<BR> 
　　７．６．１　按照《中华人民共和国中外合资经营企业所得税法》和《中华人民共和国中外合资经营企业所得税施行细则》缴纳的所得税。<BR> 
　　７．６．２　公司确定应提取的储备基金、职工奖励及福利基金、企业发展基金。<BR> 
　　７．７　储备基金可在公司发生亏损时，用于垫补亏损，职工奖励及福利基金只能用于支付职工奖金和职工集体福利。公司发展基金用于购买固定资产，增加流动资金，扩大公司的生产经营。<BR> 
　　７．８　合营公司以前年度如有亏损时，应先从本年利润中弥补，在年度的亏损未弥补以前，不得分配利润，合营公司以前年度如有未分配的利润，可以与本年可供分配的利润一并进行分配或抵补本年亏损后进行分配。<BR> 
　　７．９　合营公司在年终时，根据当年实现的利润或亏损，以及以前年度未分配的利润或未弥补的亏损，编制利润分配方案，提交董事会讨论决定后，据以记帐，列入年度决算。<BR> 
　　第八条　会计科目和会计报表<BR> 
　　８．１　合营公司的会计科目和会计报表，按中华人民共和国财政部发布的《中外合资经营工业企业会计科目和会计报表》执行。合营公司在不影响会计核算要求和会计报表指标汇总的前提下，可以根据具体情况增加或减少规定的会计科目和会计报表项目。<BR> 
　　合营公司的会计科目分为资产、负债、资本、成本和损益五类会计科目，按照科目的分类，分别编号。<BR> 
　　８．２　合营公司的会计报表包括：<BR> 
　　（１）资产负债表；<BR> 
　　（２）利润表；<BR> 
　　（３）财务状况变动表；<BR> 
　　（４）有关附表。<BR> 
　　８．３　合营公司在报送年度会计报表的同时，报送财务情况说明书，主要说明：<BR> 
　　（１）企业的生产经营情况；<BR> 
　　（２）利润的实现和分配情况；<BR> 
　　（３）资金的增减变动和周转情况；<BR> 
　　（４）外汇的收支和平衡情况；<BR> 
　　（５）工商统一税、所得税、土地使用费、提成费的交纳和支付情况；<BR> 
　　（６）各项财产物资的盘盈、盘亏和毁损、报废情况；<BR> 
　　（７）其他需要说明的问题。<BR> 
　　８．４　合营公司的季度和年度会计报表，分别报送合营各方和当地税务部门，合营公司主管部门和同级财政部门。合营公司的季度会计报表，于季度终后二十日内报出。年度会计报表于年度终了后四个月内，连同注册会计师的查帐报告一并报出。<BR> 
　　第九条　会计凭证和会计帐簿<BR> 
　　９．１　合营公司的会计凭证包括：原始凭证和记帐凭证。各种会计凭证必须内容真实，手续完备，数字准确；必须正确及时地填制凭证，审核凭证，合理地传递凭证，严格和完善地保管凭证。<BR> 
　　９．２　合营公司应设置的帐簿包括：日记帐、总帐和明细帐三种主要帐簿和各种必要的辅助性的查帐簿，各种帐簿应做到内容完整，数字准确，摘要清楚，登记及时。<BR> 
　　第十条　审计<BR> 
　　１０．１　合营公司按照《中外合资经营企业所得税法》的规定，聘请中华人民共和国政府批准的注册会计师，对公司的年度会计报表和全年帐目进行审查，并出具审计报告。<BR> 
　　１０．２　合营公司对审计人员提供所需要的凭证、帐簿和有关资料，查帐人员负责保密。<BR> 
　　第十一条　解散与清算<BR> 
　　１１．１　合营公司合同期满或提前终止合同宣布解散，进行清算时，由清算委员会对公司的财产、物资、债权、债务进行全面清查，财产以帐面为依据编制资产负债表和财产目录，制定清算方案，报经董事会讨论通过后，处理财产物资，收回债权，缴纳应交税金，清偿债务，妥善解决各种遗留问题。<BR> 
　　１１．２　合营公司办理解散清算所发生的清算费用和清算委员会成员的酬劳，从公司现存的财产中优先支付。<BR> 
　　１１．３　合营公司在清算过程中，所发生的清算收益，除去清算费用和各种清算损失后的清算净收益，视同利润处理。<BR> 
　　１１．４　合营公司的剩余财产，除合营公司的合同规定者外，按合营各方的出资比例进行分配。<BR> 
　　１１．５　合营公司解散、清算的会计报表，经中华人民共和国政府批准的注册会计师进行审查，并出具证明方为有效。<BR> 
　　１１．６　合营公司解散后，各项帐册及文件交由中方合营者保存。<BR> 
