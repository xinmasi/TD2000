　　２．２　培训方法应采用讲课与实际操作相结合，乙方应针对不同专业培训人员，各有侧重。<BR> 
　　２．３　乙方对各专业培训人员尽力确保其在培训期内，能掌握有关的工艺技术和技术决窍，并获得实际生产的经验。其中技术工人必须直接参加有关钻头制造的操作，使其经过培训后能独立进行生产工作。<BR> 
　　３．培训费用<BR> 
　　３．１　乙方派遣到合营公司进行＿＿＿＿＿次共＿＿＿＿＿人日的培训工作的人员的旅费及报酬，由乙方承担。<BR> 
　　３．２　合营公司所选派培训人员的往返机票、住宿、医疗以及生活费用，全部由合营公司负责。但乙方在培训期内应负责解决所在地的住宿，并免费提供培训人员在市内的交通。<BR> 
　　３．３　本协议所列各项培训项目的费用，除３．２款所规定的外，其余费用均已列入入门费之内，乙方不得另收费。<BR> 
