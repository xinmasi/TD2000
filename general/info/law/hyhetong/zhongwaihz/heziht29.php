　　乙方派遣的人员应是乙方厂内能胜任工作的技术人员，并在规定的期限内圆满完成支援任务，乙方派遣人员的旅途费用及报酬由乙方支付。<BR> 
　　在技术验收阶段，乙方在其所有的试验设备上，免费为合营公司进行原材料分析及产品试验，提供结果资料，提出改进意见，在合营公司正常生产后，乙方每年对合营公司产品免费进行一次上述项目的抽样试验。<BR> 
