　　第二条　成立合资经营公司<BR> 
　　甲乙双方根据《中华人民共和国中外合资经营企业法》和中国的其他有关法规，决定在中国境内建立合资经营＿＿＿＿＿钻头的有限责任公司（以下简称合营公司）。<BR> 
　　２．１　合营公司的中文名称为：＿＿＿＿＿<BR> 
　　英文名称为：＿＿＿＿＿<BR> 
　　２．２　合营公司的法定地址：＿＿＿＿＿<BR> 
　　２．３　合营公司的一切活动，必须遵守中华人民共和国的法律，法令和有关条例规定。<BR> 
　　２．４　合营公司的组织形式为有限责任公司，甲乙方以各自认缴的出资额对合营公司的债务承担责任，各方按出资额在注册资本中所占比例分享利润，分担风险及亏损。<BR> 
　　第三条　生产经营目的、范围和规模<BR> 
　　３．１　甲乙双方合资经营的目的是：采用＿＿＿＿＿先进制造技术，技术诀窃，以科学的经营管理方法，制造与销售具有国际先进水平的＿＿＿＿＿钻头。不断提高产品质量，使合营公司产品的质量和价格在国际市场上均具有竞争力，提高企业经济效益，使投资双方获得满意的经济利益。<BR> 
　　３．２　生产经营范围：合营公司生产和销售各种类型和尺寸的＿＿＿＿＿钻头及乙方在合营期间所发展并已投产的所有其他型号的＿＿＿＿＿钻头。<BR> 
　　合营公司还从事以下与生产及销售业务有关的活动；<BR> 
　　（１）对销售的产品提供必要的技术服务；<BR> 
　　（２）研究与发展＿＿＿＿＿钻头新产品，以便更好地为用户服务。<BR> 
　　３．３　生产规模：合营公司投产后第＿＿＿＿＿年，全面生产时，应具有生产＿＿＿＿＿只各种规模型号＿＿＿＿＿钻头的生产能力。<BR> 
　　第四条　投资总额与注册资本<BR> 
　　４．１　合营公司的投资总额与注册资本均为＿＿＿＿＿美元，甲乙双方各投资＿＿＿＿＿美元，均为总注册资本的＿＿＿＿＿％。<BR> 
　　４．２　合营公司正式投产后，甲乙双方以头＿＿＿＿＿年的部分利润作为再投资，用于发展新工艺，提高产量或增加流动资金。<BR> 
　　４．３　甲乙双方将按本合同附件四“作价协议”中１．１及１．２条所列内容作为出资。出资的实物部分，按作价协议规定进行作价。出资的实物所有权归合营公司所有，免于任何抵押、质押、留置权等。<BR> 
　　４．４　合营公司每年应按中国地方政府规定缴付土地使用费。<BR> 
　　４．５　甲乙双方的现金分两期交付。在合营公司取得营业执照之日起三个月内，缴付各自认缴现金出资额的＿＿＿＿＿％，其余＿＿＿＿＿％在六个月内交齐。出资的现金应存入中国银行＿＿＿＿＿分行合营公司的帐户内，并取得凭证。实物出资部分按双方签署的合同附件四作价协议中的规定交付合营公司，合营双方缴付出资额后，经中国注册的会计师验证，出具验资报告，由合营公司据以发给出资证明。<BR> 
　　４．６　合营期内，甲乙双方均不得以任何名义和方式回抽其注册资本。<BR> 
　　４．７　合营一方需转让为其所有的部分或全部出资额时，须经另一方同意，董事会一致通过并报请原审批机构批准；合营一方转让部分或全部出资额时，另一方有优先购买权；合营一方向第三者转让出资额时，条件不得比向合营另一方转让优惠，违反上述规定的转让无效。<BR> 
　　第五条　合营各方的责任<BR> 
　　５．１　甲方有责任完成下述各项事宜：<BR> 
　　５．１．１　办理为设立合营公司向中国有关主管部门申请批准，登记注册，领取营业执照等事宜。<BR> 
　　５．１．２　协助合营公司办理各项可能的减免税申请手续。<BR> 
　　５．１．３　协助合营公司向有关银行办理开户及获取贷款的手续。<BR> 
　　５．１．４　向土地主管部门办理申请取得土地使用权的手续。<BR> 
　　５．１．５　组织合营公司厂房及工程设施的土建设计、施工。<BR> 
　　５．１．６　按本合同附件四“出资协议”１．１条所列项目在规定期间内提供现金、机械设备等。<BR> 
　　５．１．７　协助办理乙方为出资而提供的机械设备、器具的进口报关手续。<BR> 
　　５．１．８　协助合营公司在中国境内购置设备、材料、原料及通讯设施。<BR> 
　　５．１．９　协助合营公司联系落实水、电、气和其他能源、交通等基础设施。<BR> 
　　５．１．１０　协助合营公司招聘中国国内的能胜任本职工作的经营管理人员、技术人员、工人和所需的其他人员。<BR> 
　　５．１．１１　协助外籍工作人员办理所需的入境签证、工作许可证和旅行手续等。<BR> 
　　５．１．１２　协助解决合营公司职工的食宿、医疗、交通等。<BR> 
　　５．１．１３　协助乙方办理乙方所得外汇汇出手续。<BR> 
　　５．１．１４　办理合营公司委托的其他事项。<BR> 
　　５．２　乙方有责任完成下述各项事项：<BR> 
　　５．２．１　按本合同附件四“出资协议”１．２条所列提供现金、机械设备、工具、原材料等。<BR> 
　　５．２．２　协助合营公司向国外银行获取贷款。<BR> 
　　５．２．３　提供必要的设备安装、调试及试生产技术人员。<BR> 
　　５．２．４　培训合营公司的技术人员、工人和管理人员。<BR> 
　　５．２．５　保证按合同规定返销合营公司产品，并采用多种方式与甲方密切配合，使合营公司达到外汇收支平衡。<BR> 
　　５．２．６　协助合营公司在国际市场上购置设备、原材料。<BR> 
　　５．２．７　协助中方人员办理所需的入境签证和必要的手续。<BR> 
　　５．２．８　协助解决中方人员在国外的住宿及交通等。<BR> 
　　５．２．９　办理合营公司委托的其他事宜。<BR> 
　　第六条　技术转让<BR> 
　　６．１　甲乙双方同意，将由甲方代表合营公司与乙方签订技术转让协议，以取得合营公司经营所需的先进技术，包括产品设计、制造工艺及生产管理技术和各种测试方法、材料配方、产品的质量标准、质量保证等。<BR> 
　　６．２　乙方对转让技术提供如下保证：<BR> 
　　６．２．１　向合营公司转让的全部技术是实用的、完整的、准确的、可靠的、符合合营公司要求的，按照转让技术生产的产品质量能达到乙方同类产品的质量标准，产量能达到规定的生产能力。<BR> 
　　６．２．２　技术转让协议中规定的全部资料，均应按时提供给合营公司。<BR> 
　　６．２．３　在合营公司整个合同的有效期间，乙方保证将其改进和发展的并已用于生产的所有新技术及时地、完整地提供给合营公司。<BR> 
　　６．２．４　按技术转让协议的规定对合营公司技术人员的工人进行技术培训，使他们能及时掌握所转让的技术。<BR> 
　　６．２．５　提供合营公司认为必要的技术支持。<BR> 
　　６．２．６　乙方将帮助中方试验、评价热稳定性＿＿＿＿＿产品。<BR> 
　　第七条　产品销售<BR> 
　　７．１　乙方负责包销合营公司年产量＿＿＿＿＿产品，但最多为＿＿＿＿＿只，其余产品将由合营公司销售到国内市场及按本合同附件五“产品销售协议”原则所确定的国际市场。<BR> 
　　７．２　如果乙方未能按７．１条的规定完成包销数量，则应按未完成包销产品的只数占应完成包销产品的总只数的比例，将分得的利润或等值的进口原材料，补偿给合营公司，超过＿＿＿＿＿年销售数量时，可将超额完成的只数作为下年度包销数不足的补偿。<BR> 
　　７．３　经董事会一致通过，报中国审批机构批准，合营公司可在中国境内外设立分支机构。<BR> 
　　７．４　合营公司产品使用乙方的产品商标，并注明中国制造。<BR> 
　　第八条　董事会<BR> 
　　８．１　合营公司的注册登记之日为合营公司董事会成立之日。<BR> 
　　８．２　董事会由＿＿＿＿＿名董事组成，其中甲方委派＿＿＿＿＿名，乙方委派＿＿＿＿＿名。董事长由甲方委派，副董事长由乙方委派，董事及董事长任期＿＿＿＿＿年，经委派方继续委派可以连任。甲乙方在任何时候均可经书面通知撤换由其委派的董事会成员。<BR> 
　　８．３　董事会是合营公司最高权力机构，决定合营公司一切重大事宜，下列重大事宜须经董事会一致通过，方可作出决议：<BR> 
　　８．３．１　合营公司章程的修改；<BR> 
　　８．３．２　合营公司的解散；或合营公司合营期的延长；<BR> 
　　８．３．３　合营公司注册资本的增加；<BR> 
　　８．３．４　合营公司与其他经济组织的合并；<BR> 
　　８．３．５　合营任一方出资额的转让；<BR> 
　　８．３．６　总经理及副总经理的任免；<BR> 
　　８．３．７　聘请在中国注册的审计师；<BR> 
　　８．３．８　决定在中国境内外设立分支机构事宜；<BR> 
　　８．３．９　其他合营双方均认为属重大事宜，需经董事会一致通过的事项；<BR> 
　　其他事宜可由多数通过作出决议。<BR> 
　　８．４　董事长是合营公司的法定代表，董事长因故不能履行职责时，可经书面授权副董事长或其他董事代其职权。<BR> 
　　董事会会议每年至少要召开一次，会议按如下规定进行：<BR> 
　　８．５．１　董事会会议由董事长或由其书面授权代表负责召集并主持；<BR> 
　　８．５．２　会议一般应在合营公司法定地址所在地召开，特别情况由董事会决定会议召开地点；<BR> 
　　８．５．３　经三分之一以上董事提议，董事长可召开临时董事会会议；<BR> 
　　８．５．４　董事会会议应在有三分之二以上董事出席时方可举行，董事因故不能出席会议，可出具书面委托，授权他人代表其出席会议及表决，全体出席董事会会议的合法成员构成会议的法定人数，每人持一票表决权；　　<BR> 
　　８．５．５　董事会会议应作好书面记录，决议部分应使用中英文两种文字，会议记录经与会成员签字后，连同会议期间收到的委托书，一并归档；<BR> 
　　８．５．６　会议休会期间，根据董事书面表决所作出的决议与董事会会议决定具有同等效力。<BR> 
　　８．６　董事除在公司另任职外，合营公司不支付董事会成员薪金。董事或董事书面委托的代表在董事会会议期间发生的与董事会议有关的旅行、交通、住宿补贴及会议本身费用，按董事会核准的数额，由全营公司支付。<BR> 
　　第九条　管理机构<BR> 
　　９．１　合营公司设管理机构，负责公司的日常经营管理工作。管理机构设总经理一人，副总经理一人，首届总经理由＿＿＿＿＿方推荐，首届副总经理由＿＿＿＿＿方推荐，经董事会任命，任期＿＿＿＿＿年。下届正、副总经理由董事会确定任命。<BR> 
　　９．２　总经理职责是执行董事会会议的各项决议，主持合营公司的日常经营管理工作，副总经理协助总经理工作。<BR> 
　　９．３　合营公司可设立若干部门，各部门负责总经理和副总经理交办的事项。<BR> 
　　９．４　总经理、副总经理有营私舞弊或严重失职行为，经董事会会议决定，可随时撤换。<BR> 
　　第十条　原材料及设备的采购<BR> 
　　１０．１　合营公司所需的生产用设备、原材料、运输工具等，在质量、价格及交货期等条件相同情况下，应优先在中国购买。<BR> 
　　１０．２　在合营公司需要时可委托乙方在国外或委托甲方在国内购买设备，受委托方可提取＿＿＿＿＿的手续费。在选购过程中，受委托方的对方在认为必要时，有权派人参与选购工作。<BR> 
