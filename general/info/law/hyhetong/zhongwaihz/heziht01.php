　　第四十四条　合营公司按照《中华人民共和国中外合资经营企业法》的规定提取储备基金、企业发展基金及职工福利奖励基金，每年提取的比例由董事会根据公司经营情况讨论决定。<BR> 
　　第四十五条　合营公司的会计年度从每年一月一日起至十二月三十一日止，一切记帐凭证、单据、报表、帐簿，用中文书写。（注：也可同时用甲乙双方同意的一种外文书写。）<BR> 
　　第四十六条　合营公司的财务审计聘请在中国注册的会计审查、稽核，并将结果报告董事会和总经理。<BR> 
　　如乙方认为需要聘请其他国家的审计师对年度财务进行审查，甲方应予以同意。其所需要一切费用由乙方负担。<BR> 
　　第四十七条　每营业年度的头三个月，由总经理组织编制上一年度的资产负债表、损益计算书和利润分配方案，提交董事会会议审查。<BR> 
　　合营企业的全部利润，在缴纳所得税、提取第四十四条所列基金后，应按合营各方出资比例，由董事会确定的利润分配方案进行分配。（若以前年度的亏损未弥补，不得分配利润；以前年度未分配的利润可并入本年度利润分配。另外，对分配形式应加以规定。）<BR> 
　　<BR> 
　　第十五章　合营期限、解散与清算<BR> 
　　<BR> 
　　第四十八条　本合营企业在下述情况下解散：<BR> 
　　１．合营期满；<BR> 
　　２．合营期满之前，出现下述任何一种情况或事件，经董事会决议，本合营企业也可解散；<BR> 
　　ａ．合营遭受重大损失，无法继续经营；<BR> 
　　ｂ．任何一方违反经营合同规定，使本企业无法继续经营；<BR> 
　　ｃ．合营企业达不到经营目的，投资无法回收；<BR> 
　　ｄ．不可抗力，等。<BR> 
