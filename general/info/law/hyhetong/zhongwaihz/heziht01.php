　　第十九章　违约责任<BR> 
　　<BR> 
　　第五十六条　甲、乙任何一方未按本合同第五章的规定依期按数提交完出资额时，从逾期第一个月算起，每逾期一个月，违约一方应缴付应交出资额的百分之＿＿的违约金给守约的一方。如逾期三个月仍未提交，除累计收取违约方应交出资额的百分之＿＿的违约金外，守约一方有权按本合同第五十三条规定终止合同，并要求违约方赔偿损失。<BR> 
