　　第二十章　不可抗力<BR> 
　　<BR> 
　　第五十九条　由于地震、台风、水灾、火灾、战争以及其它不能预见并且对其发生和后果不能防止或避免的不可抗力，致使直接影响合同的履行或者不能按约定的条件履行时，遇有上述不可抗力的一方，应立即电报通知对方，并应在十五天内，提供不可抗力详情及合同不能履行、或者部分不履行、或者需要延期履行的理由的有效证明文件，此项证明文件应由不可抗力发生地区的公证机构出具。按其对履行合同影响的程度，由双方协商决定是否解除合同，或者部分免除履行合同的责任，或者延期履行合同。<BR> 
