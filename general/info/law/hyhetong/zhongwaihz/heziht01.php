　　<BR> 
　　第五十二条　合营公司的各项保险均在中国人民保险公司投保，投保险别、保险价值、保期等按照中国人民保险公司的规定由合营公司董事会会议讨论决定。　　<BR> 
　　第十八章　合同的修改、变更与解除<BR> 
　　<BR> 
　　第五十三条　对本合同及其附件的修改必须经甲、乙双方签署书面协议，并报原审批机构批准，才能生效。<BR> 
　　第五十四条　由于不可抗力，致使合同无法履行，或是由于合营公司连年亏损、无力继续经营，经董事会一致通过，并报原审批机构批准，可以提前终止合营期限和解除合同。<BR> 
　　第五十五条　由于一方不履行合同、章程规定的义务，或严重违反合同、章程规定，造成合营公司无法经营或无法达到合同规定的经营目的，视作违约方单方终止合同，他方除有权向违约一方索赔外，并有权按合同规定报原审批机构批准终止合同。如甲、乙双方同意继续经营，违约方应赔偿合营公司的经济损失。<BR> 
