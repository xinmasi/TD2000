　　第五十条　合营公司的期限为＿＿年。合营公司的成立日期为合营公司营业执照签发之日。<BR> 
　　经一方提议，董事会会议一致通过，可以在合营期满六个月前向对外经济贸易部（或其委托的审批机构）申请延长合营期限。<BR> 
　　<BR> 
　　第十六章　合营期满财产处理<BR> 
　　<BR> 
　　第五十一条　合营期满或提前终止合营，合营公司应依法进行清算，清算后的财产，根据甲、乙各方投资比例进行分配。<BR> 
