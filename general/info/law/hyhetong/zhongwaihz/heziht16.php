　　第二十三章　附加条款<BR> 
　　<BR> 
　　第五十条　修改<BR> 
　　合同的任何修改须经董事会决定后，呈交审批机关批准，方为有效。<BR> 
　　第五十一条　前写全约及照会<BR> 
　　本合同经审批机构批准正式生效后，订约四方所有的前口头和书面的协议或照会等，如与合同相抵触时，以本合同为准。<BR> 
