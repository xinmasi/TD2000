　　第三章　成立合作经营公司<BR> 
　　<BR> 
　　第三条　甲方和乙方在平等互利条件下，同意相互合作，在中华人民共和国＿＿＿＿＿举办合作经营企业，企业名称为：＿＿＿＿＿。<BR> 
　　第四条　公司是按照“中华人民共和国中外合作经营企业法”及其他有关法规的规定，双方以各自的法人身份共同建立的经济实体。<BR> 
　　第五条　公司的一切经济、业务活动，必须遵守中国政府法律、法令及有关条例规定，并受其保护。<BR> 
　　第六条　甲乙双方对合作经营公司的债务、风险、亏损共同承担责任，其盈利共同分享。<BR> 
　　<BR> 
　　第四章　经营目的、经营范围与经营规模<BR> 
　　<BR> 
　　第七条　合作公司的经营目的：以发展中国的国民经济，实现四个现代化并取得合法利润为目的。其宗旨为，通过双方密切合作，使海洋开发事业取得突破性进展，满足国内外市场对对虾、鳗鱼等水产品日益增长的需求，双方在经济上获得实惠。<BR> 
　　第八条　合作公司的经营范围：生产国内外市场急需的对虾、鳗鱼等水产产品，争取在国际市场上有较强的竞争能力。<BR> 
　　第九条　合作公司的经营规模：年产对虾＿＿＿＿＿吨，成鳗＿＿＿＿＿吨，以及其他水产品。<BR> 
　　<BR> 
　　第五章　合作条件及其构成<BR> 
　　<BR> 
　　第十条　甲方提供土地＿＿＿＿＿亩使用；乙方出资金额＿＿＿＿＿美元。<BR> 
　　第十一条　甲方以土地使用，乙方以资金，构成合作条件。<BR> 
　　第十二条　合作方式<BR> 
　　甲方提供土地使用，乙方提供现金或实物、设备。<BR> 
　　第十三条　乙方投资的实物或设备，应经甲方主管部门审查同意，报审批机构批准。<BR> 
　　第十四条　由合作企业与乙方签订买卖合同经审查批准后，三个月内应由甲方派员实地考察并委托中国银行按其规定向乙方银行开具信用证。<BR> 
　　第十五条　乙方收到甲方银行信用证后，＿＿＿＿＿个月内应将所购全部设备、实物运至＿＿＿＿＿港。<BR> 
　　第十六条　甲方双方必须按商定的期限，如数划出土地供使用和付出资金。否则由违约方担负其由此而产生的一切经济损失。<BR> 
　　<BR> 
　　第六章　合作各方的责任<BR> 
　　<BR> 
　　第十七条　甲方有责任履行下列义务：<BR> 
　　１．向中国政府授权机关申请批准并向工商行政管理部门注册登记；<BR> 
　　２．向有关部门办理合作公司使用土地的有关手续；<BR> 
　　３．根据生产需要，合理安排合作公司的用房、公用设施、订购可在国内生产的机器、设备、工具等；<BR> 
　　４．协助采购国内供应的原材料、包装材料、其他消耗品等，办理燃料、水、电增加供应和电话、电传、电报挂号等申请手续；<BR> 
　　５．办理职工的招聘手续，推荐合作公司所需的管理技术人员，经考核后由董事会根据需要择优录用；<BR> 
　　６．办理合作公司外籍人员的邀请、居住手续，对其办公、交通、生活等方面进行安排；<BR> 
　　７．协助办理产品出口的有关运输、报关等事项；<BR> 
　　８．负责办理由乙方发运至＿＿＿＿＿港或＿＿＿＿＿港的全部设备运到合作公司所在地；<BR> 
　　９．上述各项之外另有双方协议规定的该由甲方分担的事项。<BR> 
　　第十八条　乙方有责任履行下列义务：<BR> 
　　１．提供对生产、办公等建筑物的要求；<BR> 
　　２．提供合作公司所需的进口生产设备、检测仪器清单和技术资料，并确认在国内订购的机器设备、工具清单和要求；<BR> 
　　３．提供产品的出口加工标准、操作规程等技术指导和先进的企业管理方法；<BR> 
　　４．提供与合作公司产品有关的国外技术情报及市场信息；<BR> 
　　５．对技术人员和职工进行技术培训；<BR> 
　　６．负责采购需由国外供应的原材料、易损件、零配件、消耗品等；<BR> 
　　７．从甲方委托中国银行的乙方银行开具信用证之日起，乙方应将双方研究确定的先进可靠的设备、检测仪器按商定的日期运到＿＿＿＿＿港。负责设备的安装调试并正常投产；<BR> 
　　８．努力提高产品在国际市场上的竞争能力，不断扩大外销市场，保证合作公司的外汇平衡和取得较高的经济效益；<BR> 
　　９．上述各条以外另有双方协议规定的须由乙方分担的事项。<BR> 
　　第十九条　任何一方因不履行各自的义务而给公司造成损失时，须负责赔偿损失。<BR> 
