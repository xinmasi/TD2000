　　第二章　合作各方<BR> 
　　<BR> 
　　第二条　合作各方<BR> 
　　甲　方：＿＿＿＿＿注册国家：＿＿＿＿＿国<BR> 
　　法定地址：＿＿＿＿＿<BR> 
　　法定代表：＿＿＿＿＿<BR> 
