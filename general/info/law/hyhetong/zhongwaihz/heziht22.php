　　第七章　董事会的组成<BR> 
　　<BR> 
　　第二十条　本公司为法人式的合作经营企业，董事会为公司的最高权力机构。<BR> 
　　第二十一条　董事会由＿＿＿＿＿名董事组成，甲方委派＿＿＿＿＿名，乙方委派＿＿＿＿＿名，董事会设董事长、副董事长各一名，董事＿＿＿＿＿名，任期均为＿＿＿＿＿年。董事长由乙方担任，可以连任。<BR> 
　　第二十二条　董事长是合作企业的法定代表人，董事长不能履行职责时，应授权副董事长或其他董事代表合作企业。<BR> 
　　第二十三条　董事会会议每年举行＿＿＿＿＿次例会，一般应在＿＿＿＿＿月在合作公司所在地召开，如有必要也可在其他地方举行。根据需要，董事长在征得副董事长同意后，也可临时召开董事会会议。董事会议由董事长负责召集主持，董事长不能召集时，可委托副董事长或其他董事召集主持。<BR> 
　　董事长应在三周前将召开董事会会议的日期、地点、议题通知董事会各成员。<BR> 
　　第二十四条　董事不能出席董事会会议时，可出具委托书委托代表出席，行使董事发言权和表决权，其一名代表不能同时担任两名或以上的名额（董事会会议应有包括出其委托书的董事代表在内的三分之二以上的董事出席，才能举行）。<BR> 
　　第二十五条　董事会会议应本着平等互利、友好协商的原则，研究讨论问题。<BR> 
　　下列事项由出席董事会会议的董事一致通过方可作出决议：<BR> 
　　１．合作企业合同和章程的修改；<BR> 
　　２．合作企业的终止、解散；<BR> 
　　３．合作企业注册资本的增加、转让；<BR> 
　　４．合作企业与其他经济组织的合并。<BR> 
　　其他事项，可根据合作企业的章程载明的议事规则作出决议。董事会决议以中文书写一式四份，经正、副董事长签署后，由合作公司，乙方各执一份，甲方执二份。<BR> 
　　第二十六条　董事会聘请总经理一名，副总经理一名，并决策任期年限。正副经理要执行董事会决议，负责合作公司的经营管理，并定期向董事会汇报生产、经营情况。<BR> 
　　<BR> 
　　第八章　经营管理机构<BR> 
　　<BR> 
　　第二十七条　合作企业设经营管理机构，负责企业的日常经营管理工作，经营管理机构设总经理一名，副总经理一名及其他高级管理人员，其聘用办法均由董事会任命，任期＿＿＿＿＿年。<BR> 
　　第二十八条　总经理的职责、权限：<BR> 
　　１．执行甲乙双方所订合同、章程及董事会决议；<BR> 
　　２．提名各职能部门负责人，审定招聘工作人员，并报董事会备案；<BR> 
　　３．制订全企业的经营管理制度，对各职能部门布置、指导、监督和检查工作；<BR> 
　　４．定期向董事会提出工作报告、财务报告和利润分配方案；<BR> 
　　５．对原材料、零配件的采购、成品销售及专项协作合同和流动资金的借贷作出决定；<BR> 
　　６．审定职责部门制定内外销产品价格，并对价格作适当幅度的调整作出决定；<BR> 
　　７．代表企业接待重要的业务联系单位人员、谈判和签署文件；<BR> 
　　８．主持企业行政会议，对行政会议的讨论事项及决议负责执行；<BR> 
　　９．解决各职能部门向总经理请示的其他重大问题；<BR> 
　　１０．在董事会授权范围内，代表企业或指派代理人出席涉及企业的审批或仲裁、调解会议；<BR> 
　　１１．对职工违反规章制度的处分作出行政方面的最后决定；<BR> 
　　１２．其他由总经理负责的事项。<BR> 
　　第二十九条　副总经理职责、权限：<BR> 
　　１．协助总经理负责本企业的经营管理；<BR> 
　　２．总经理外出时，代替总经理行使职权；<BR> 
　　３．代表企业进行业务谈判；<BR> 
　　４．处理其他工作矛盾和有关问题；<BR> 
　　５．其他应由副总经理负责处理的问题。<BR> 
　　<BR> 
　　第九章　筹备和建设<BR> 
　　<BR> 
　　第三十条　合作公司在筹建期间，在董事会下设立筹建处负责各项筹建工作。筹建处人员的组成由董事会讨论决定，筹建期间的各项费用分年摊入生产成本。　　<BR> 
　　第十章　劳动管理、工会<BR> 
　　<BR> 
　　第三十一条　合作公司职工的雇用、解雇、劳动工资、劳动纪律、劳保福利等事项。除按“中华人民共和国中外合资企业劳动管理规定”办理外，根据董事会决议实行。<BR> 
　　第三十二条　合作企业职工有权按照《中华人民共和国工会法》和《中国工会章程》建立工会组织，开展工会活动。合作企业积极支持本企业的工会工作。<BR> 
　　<BR> 
　　第十一章　生产与销售<BR> 
　　<BR> 
　　第三十三条　合作企业在每年度以前召开的董事会会议上制定下一年度生产进度及进口、出口计划，并报主管部门。<BR> 
　　计划执行中在保证合作公司一定的经济效益和外汇收支平衡的前提下，可根据国内外市场情况予以合理的调整。<BR> 
　　第三十四条　进口原材料采购对象，参考乙方情报，研究其质量、规格、价格后，由正、副总经理商定，在国内能提供满足需要的原材料情况下，应优先在国内购买。其支付办法，货币按照国内规定办理。<BR> 
　　第三十五条　合作企业生产的对虾、鳗鱼等水产品，通过中国出口商品检验局检后，根据年度出口计数由公司直接出口，也可以参加广交会对外成交出口销售产品。<BR> 
　　第三十六条　合作公司原则上规定，凡符合出口标准的产品全部出口，确保公司外汇收支平衡并积极创汇。<BR> 
　　第三十七条　本公司产品内销部分由甲方负责，外销部分由乙方负责，一切内外经销事项均以公司名义出面。<BR> 
　　第三十八条　出口产品的销售价格和数量应考虑合作公司的外汇收支平衡和成本核算。随着国际市场情况的变化而加以及时调整。<BR> 
　　第三十九条　内销产品按中国政府规定的物价政策执行，具体价格总经理决定，报主管部门和物价部门备案。外销产品价格根据国家市场根据国际市场随行就市或根据广交会成交价由公司总经理决定。<BR> 
　　<BR> 
　　第十二章　财务、会计、审计<BR> 
　　<BR> 
　　第四十条　合作公司的会计制度根据中华人民共和国财政部门有关规定，结合本公司具体情况经董事会通过制定的会计制度，付诸实行。<BR> 
　　第四十一条　公司各类报表于次月十日前向合作双方报告，年终报表在次月底前提出，由公司委托在中国注册的会计师审核。各类报表均报主管部门、统计部门及其有关部门备案。<BR> 
　　第四十二条　公司采用借贷记帐法记帐，用中文书写，会计报表以人民币为记帐本位币，其他货币属于合作投资的均以中国银行外汇牌价换算。属于贸易往来的按贸易汇价结算，外汇往来按《中华人民共和国外汇管理暂行条例》办理。<BR> 
　　第四十三条　合作公司的财务审计，聘请在中国注册的会计师审查，稽核，并将结果报告董事会和总经理。<BR> 
　　第四十四条　合作公司在中国银行＿＿＿＿＿分行开设人民币和外币帐户。<BR> 
　　<BR> 
　　第十三章　税收、利润和亏损<BR> 
　　<BR> 
　　第四十五条　合作公司按中华人民共和国税法条款规定交纳各种税金，并参照《关于中外合作经营项目进出口货物的监管和免税的规定》向税务机关提出减免税收申请。<BR> 
　　第四十六条　所得税甲乙双方分别缴纳。甲方按国家有关规定上交，乙方按国务院关于华侨投资优惠的暂行规定执行。<BR> 
　　第四十七条　合作公司在扣除由董事会确定提留的储备资金、职工福利资金、奖励资金、企业发展基金和纳税后，余下的净利润甲乙双方对等分成。<BR> 
　　第四十八条　乙方所得的净利润汇往国外时，按“中华人民共和国外汇管理条例”的有关规定办理。<BR> 
　　第四十九条　公司发生亏损时，经董事会会议讨论通过时，可用储备基金弥补，或按对等比例承担亏损责任。<BR> 
　　<BR> 
　　第十四章　合同的审批、生效、延长和终止<BR> 
　　<BR> 
　　第五十条　本公司合作期限定为＿＿＿＿＿年，本合同按照中外合作经营的有关规定申请批准。然后，合作公司持批准证书到工商行政管理机关登记，并领取营业执照，同时，乙方还要以自己的名义到工商行政机关登记。公司合作期自领取营业执照之日计算。本公司经上级批准机关批准之日起生效。<BR> 
　　第五十一条　合作期满前六个月，如双方愿意继续合作，可申请并经上报批准机关批准后延长合作期。<BR> 
　　第五十二条　在合作期内，出现下列情况可提前终止本合同，解散合作企业：<BR> 
　　１．公司发生严重亏损，无力继续经营时；<BR> 
　　２．合作一方不履行合作企业合同、章程规定的义务，致使企业无法继续经营；<BR> 
　　３．因自然灾害、战争等不可抗力遭受严重损失时；<BR> 
　　４．公司未达到其经营目的，同时又无发展前途，公司的合同、章程所规定的其他解散因素已经出现时。<BR> 
　　经双方作出最大努力不可挽回时，由董事会提出解散申请书，报审批机关批准后，提前终止合作企业。<BR> 
　　属于本条第二款情况解散合同企业时，不履行合作企业合同、章程规定的义务一方，应对合作企业所造成的损失负责赔偿责任。<BR> 
　　第五十三条　合作期未满，公司解散时，以其全部资产对其债务承担责任，偿还债务后的剩余资产支付职工一定的安置费，余下部分双方对等分配，对于剩余财产超过注册资本的增值部分视同利润，应依法交纳所得税后对等分成。<BR> 
　　第五十四条　合作期满本合同自失效，合作公司所有资产不附任何条件归甲方所有，不再另行清单。<BR> 
　　第五十五条　公司解散或终止后，各项帐册及文件应由甲方保存。<BR> 
