　　第一章　总则<BR> 
　　<BR> 
　　第一条　＿＿＿＿＿（以下简称甲方）和＿＿＿＿＿（以下简称乙方），根据，“中华人民共和国中外合作经营企业法”及其这有关法规的规定，在平等互利的原则基础上，同意以各自的法人身份签订本合作经营合同。<BR> 
