　　<BR> 
　　第十章　董事会<BR> 
　　<BR> 
　　第十九条　合作公司设董事会。公司注册登记之日，为董事会正式成立之日。<BR> 
　　第二十条　董事会是合作公司的最高权力机构，决定合作公司章程的制订和修改；决定公司转让、合并、停业和解散；决定公司经营决策、财务预算和决算；决定公司利润分配和亏损弥补办法；聘请总经理、副总经理和高级管理人员；决定公司职工工资和制定职工奖惩办法等一切重大事宜。<BR> 
　　第二十一条　董事会由董事＿＿名组成，其中甲方委派＿＿名，乙方委派＿＿名。董事长由甲方委派，副董事长＿＿名，由乙方委派。<BR> 
　　董事长、副董事长和董事任期四年，经各方继续委派可以连任。<BR> 
　　第二十二条　董事会会议每年至少召开一次，由董事长召集并主持。董事长因故不能召集董事会议时，可委托副董事长或其他董事召集并主持。经三分之一以上的董事提议，董事长可召开董事会临时会议。会议记录应归档保存。<BR> 
　　第二十三条　召开董事会须有三分之二以上的董事出席方有效。董事不能出席时，可以出具委托书委托他人代为出席和举行表决。<BR> 
　　第二十四条　董事长是合作公司的决定代表。董事长因故不能履行其职责时，可临时授权副董事长或其他董事代理。<BR> 
　　<BR> 
　　第十一章　经营管理机构<BR> 
　　<BR> 
　　第二十五条　合作公司设经理部，负责公司的日常经营管理。<BR> 
　　经理部设总经理一人，副总经理＿＿人。总经理由＿＿方推荐；副总经理由＿＿方推荐＿＿人，另一方推荐＿＿人，均由董事会聘请，任期＿＿＿＿年。<BR> 
　　第二十六条　总经理的职责是执行董事会会议的决议，组织领导合作公司的日常经营管理工作。副总经理协助总经理工作。<BR> 
　　第二十七条　总经理必须每季度向董事会报告公司的经营情况，半年向董事会作一次财务结算报告。<BR> 
　　第二十八条　总经理、副总经理及其他管理人员有营私舞弊或严重失职行为时；经董事会会议作出决议，给予应得的处分直至解聘，对公司造成的经济损失；应负赔偿责任。<BR> 
　　<BR> 
　　第十二章　劳动管理<BR> 
　　<BR> 
　　第二十九条　合作公司员工的招聘、解雇或辞职一律实行合同制。员工的聘请由公司做出计划，报当地劳动部门核准后，由公司自行招聘，经考核择优录用。<BR> 
　　第三十条　合作公司员工的劳动工资、劳动保险、生活福利和奖惩等事项，依照《中华人民共和国中外合作经营企业法》的有关规定，经董事会制订施行方案，由公司、公司工会与员工集体或个人订立劳动合同，按合同的规定执行。<BR> 
　　<BR> 
　　第十三章　财务会计和审计<BR> 
　　<BR> 
　　第三十一条　合作公司设总会计师和总出纳员各一人，负责公司总的会计工作；厂部、商场和维修服务部分别建立帐目，每个部门分别设会计师和出纳员各一人，负责各个部门的财务会计工作。<BR> 
　　前款所列会计和出纳员的人选，均由甲、乙方协商推荐，董事会聘请。<BR> 
　　第三十二条　合作公司的财务会计制度，根据有关规定，结合本合作公司的实际情况制定。并报当地财政部门和税务部门备案。<BR> 
　　第三十三条　合作公司设审计师一人，由甲方推荐，董事会聘请。<BR> 
　　审计师负责审查、稽核公司的财务收支和会计帐目，并向董事会报告。<BR> 
　　<BR> 
　　第十四章　纳税与保险<BR> 
　　<BR> 
　　第三十四条　合作公司应按中华人民共和国有关税法缴纳各种税款。<BR> 
　　第三十五条　合作公司的各项保险均应向设在××特区的保险公司投保。投保办法、投保险别、保险价值、保期等均按中国人民保险公司的规定由合作公司董事会决定。<BR> 
　　<BR> 
　　第十五章　合同的修改、补充、变更与解除<BR> 
　　<BR> 
　　第三十六条　本合同及其附件修改或补充，必须经甲、乙方协商一致，签署书面协议，并报经对外经济贸易部（或其委托的审批机构）批准方能生效。<BR> 
　　第三十七条　在合同有效期内由于本合同第四十二条规定的不可抗力，造成公司严重损失，或因公司连续亏损，致使合同不能继续履行，经合作公司董事会特别决议，并报原审批机关批准，可以提前终止合同或解除合同。<BR> 
　　<BR> 
　　第十六章　违约责任<BR> 
　　<BR> 
　　第三十八条　由于一方不履行合同、章程规定的义务，或严重违反合同、章程规定，造成合作公司无法经营或无法达到合同规定的经营目的，视作违约方片面终止合同。对方除有权向违约的一方索赔外，并有权按合同规定报原审批机关批准终止合同。如甲、乙方同意继续经营，违约方仍应赔偿履约一方的经济损失。<BR> 
　　第三十九条　甲、乙任何一方如未按本合同第十一条、第十二条以及第十三条的规定提供合作条件时，以逾期的第一个月算起，每逾期一个月，违约方应缴付＿＿＿＿＿＿元违约金给守约的一方。（注：或按出资额的百分比计算）如逾期＿＿＿个月仍未提供，除累计缴付违约金外，守约一方有权按照本合同第三十八条规定终止合同，并要求违约方赔偿损失。<BR> 
　　第四十条　由于一方的过失，造成本合同及其附件不能履行或不能完全履行时，由过失的一方承担违约责任；如属双方的过失，根据实际情况，由双方分别承担各自应负的违约责任。<BR> 
　　第四十一条　为保证本合同及其附件的履行，甲、乙方在合同生效后＿＿天内相互提供履约的银行担保书。<BR> 
　　<BR> 
　　第十七章　不可抗力<BR> 
　　<BR> 
　　第四十二条　在合作期间，由于地震、台风、水灾、火灾、战争或其他不能预见并且对其发生和后果不能防止和避免的不可抗力事故，致使直接影响合同的履行或者不能按约定的条件履行时，遇有上述不可抗力事故的一方，应立即将事故情况电报通知对方，并应在１５天内提供事故的详细情况及合同不能履行，或者部分不能履行，或者需要延期履行的理由的有效证明文件。此项证明文件应由事故发生地区的公证机构出具。按照事故对履行合同影响的程度，由双方协商决定是否解除合同，或者部分免除履行合同的责任，或者延期履行合同。<BR> 
　　<BR> 
　　第十八章　争议的解决<BR> 
　　<BR> 
　　第四十三条　凡因执行本合同所发生的或与本合同有关的一切争议，双方应首先通过友好协商解决，如果协商不能解决，应提请中国国际贸易促进委员会对外经济贸易仲裁委员会仲裁。仲裁的裁决是终局的，对双方都有约束力。<BR> 
　　第四十四条　本合同的订立、效力、解释、履行和争议的解决均受中华人民共和国法律的保护和管辖。<BR> 
　　<BR> 
　　第十九章　文字<BR> 
　　<BR> 
　　第四十五条　本合同用中文和＿＿＿文写成，两种文字具有同等效力。上述两种文本如解释有矛盾，以中文本为准。<BR> 
