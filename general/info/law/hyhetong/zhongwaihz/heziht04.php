　　第四十七条　本合同及其附属文件，均须经中国对外经济贸易部（或其委托的审批机关）批准，并自批准之日起生效。<BR> 
　　第四十八条　合作公司对甲、乙双方或甲、乙双方互送通知的方法，如果采用电报或电传时，凡涉及各方权利、义务的，应随之发出书面信件通知。合同中所列的甲、乙双方的法定地址，即为甲、乙方的收件地址。<BR> 
