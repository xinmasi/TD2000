　　１．本合同在签字后，报对外经济贸易部审批，自批准之日起生效。<BR> 
　　２．合同条款的修正、变更、补充，由合资各方协商，以书面形式一致同意后，报对外经济贸易部审批，经批准后与本合同具有同等效力。<BR> 
　　３．本合同未规定的事项，根据《合资企业法》及有关法律，由合资各方协商决定。<BR> 
　　第四十七条　向合资各方发送文件的地址，以本合同第一条所记载的各方的法定地址为准。<BR> 
　　第四十八条　本合同于＿＿＿年＿＿＿月＿＿＿日，由合资各方的授权代表，在中国＿＿＿＿＿签字。<BR> 
