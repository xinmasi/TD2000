　　３．甲方主管委员会以及乙方、丙方和丁方的主管领导应在签约后１个月内确认或不确认本合同。本合同在上述主管委员会和主管领导确认之后，方可报请经贸部审批。<BR> 
　　如果上述主管委员会和主管领导在规定的１个月期限内未确认本合同，或者经贸部在本合同报请审批后３个月内未批准本合同，则合同各方不再承担任何义务。<BR> 
　　４．本合同生效后，有效期为＿＿＿＿＿年。<BR> 
　　如果在本合同期满前至少１年合同所有各方均未通知合同其余各方，其打算在本合同期满时终止本合同的意图，则乙方、丙方和丁方应在本合同期满＿＿＿＿＿个月前，共同向经贸部提出申请，要求将本合同延长＿＿＿＿＿年，经经贸部批准后生效。合营公司应在批准后１个月内向工商局办理合营公司期限延长的登记手续。<BR> 
　　仅由中国的合同一方或两方通知甲方其打算退出本合同的意图，则不影响本合同在其余各方之间的有效性。<BR> 
　　对于进一步延长本合同期限，本款规定同样适用。<BR> 
　　５．合营公司董事会可决定解散合营公司，从而决定提前终止本合同。<BR> 
　　在下列任何一种情况下，董事会应解散合营公司：<BR> 
　　５．１合营公司连续＿＿＿＿＿年严重亏损，而且预测不可能合情合理地得出经济状况会改善到使合同所有各方满意的结论；<BR> 
　　５．２合同任何一方违反本合同的任何实质性条款，以致合营公司有无法继续经营的危险，除非上述违约能够或者已经在违约书面通知后＿＿＿＿＿天内予以纠正；<BR> 
　　５．３不可抗力所造成的延误持续＿＿＿＿＿个月以上，而且合同任何一方的全体董事要求董事会解散合营公司，除非在上述期限内能按该合同方所希望的方式合理地修改其义务使之适应新的情况，或者其余合同各方决定在要求解散合营合同的合同方不参加的情况下继续经营合营公司。<BR> 
　　５．４甲方和合营公司之间的技术转让协议因该协议的任何实质性条款遭到违反而终止；<BR> 
　　５．５在任何其他情况下，如果在董事长和副董事长共同向董事会提出解决方案后董事会仍不能对某一重大问题通过决议。<BR> 
　　６．董事会有关解散公司的决议中，还应规定清算程序和原则，确定清算委员会的人选及其报酬。<BR> 
　　７．本合同的提前终止不损害任何合同一方对另一方违反本合同提出索赔的权利。<BR> 
　　第二十二条　清算和分配<BR> 
　　１．本合同按本合同第二十一条第４款和第５款终止时，应由合营公司董事会所任命的清算委员会确定估价。<BR> 
　　２．估价应反映董事会决定解散合营公司之日或者董事会确定为合营公司清算开始之日的合营公司财务状况。此外，估价还应反映那一天的合营公司的价值。<BR> 
　　３．在确定估价时，清算委员会应采用编制经审计的合营公司年度资产负债表时所采用一贯原则。估价应以合营公司全部产权（即固定资产加流动资产，减负债，加或减重新估价调整差额）的价值为依据。这一价值还应加上一个有待合同各方谈判商定的，反映合营公司将来利润率的因素（继续经营价值）。在确定合营公司将来利润率时，应适当参照合营公司当时和以前的利润率。<BR> 
　　４．清算委员会应就估价作出一致决定，并在董事会决定解散合营公司后＿＿＿＿＿天内把该估价提交董事会审批，如果清算委员会在上述＿＿＿＿＿天期限内未能一致确定估价，则每个清算委员会成员都应向董事会提出自己的看法，提请董事会决定。<BR> 
　　５．如董事会在估价提请审批后＿＿＿＿＿天内不能就估价作出一致决定，则合同各方均可按本合同第二十六的规定提交仲裁。<BR> 
　　６．由估价确定的合营公司价值中甲方的份额应由乙方和丁方共同购买，用＿＿＿＿＿币现金支付。支付应不迟于董事会作出决定之日或仲裁裁决之日后＿＿＿＿＿天。最终确定的未付金额，应自董事会作出决定之日或仲裁裁决之日后第＿＿＿＿＿天起按本合同第八条第１款的规定计算利息。鉴于上述付款，甲方应按乙方和丁方所购买的合营公司价值中甲方份额的比例，向乙方向丁方转让甲方在合营公司中的权利，尤其是其在合营公司注册资本中投资比例方面的权利。<BR> 
　　第二十三条　部分失效<BR> 
　　如果本合同任何条款失效或不能执行，则其余条款应不受影响，继续有效。如为了达到本合同在商业上的目的而有必要更换任何上述条款，则合同各方应尽快会晤，按照签订本合同时所持的精神，为取得尽可能相同的经济效果，来商定新的条款，以取代失效的条款，并使新条款在法律上生效。上述新条款应追溯的原条款失效或不能执行之时起开始适用。<BR> 
　　第二十四条　不可抗力<BR> 
　　１．如果合同任何一方因不可抗力未能履行本合同，则该方对于在不可抗力持续期间不履行其义务不承担责任。在不可抗力而造成的中止履行合同，应限于不可抗力的影响存续的时间内。合同各方都将尽最大努力将不可抗力，特别是由此而引起的延误所造成的后果减轻到最低程度。<BR> 
　　２．合同各方在其他方面仍受本合同的约束，因不可抗力引起的问题应通过协商适当解决，使本合同能合理地继续履行，但是，如因不可抗力造成的延误持续＿＿＿＿＿个月以上，则合同任何一方应有权要求董事会终止本合同，除非在上述＿＿＿＿＿个月期限内能按该方所希望的方式合理地修改其义务使之适应新的情况。<BR> 
　　３．不可抗力事故是指提出遭受不可抗力的合同一方在签订本合同时不能预见到的，阻碍其实际履行义务的，不可避免的自然现象。就本合同而言，不可抗力事故包括（但不限于）地震、流行病、严重的火灾、水灾、台风、海上事故等自然现象以及战争和爆炸。<BR> 
　　４．遇不可抗力的合同一方应立即（不尽于获悉发生不可抗力后＿＿＿＿＿天），由邮寄、电传或电报通知合同其余各方。这＿＿＿＿＿天期限自该获悉发生不可抗力之日算起。如未按上述方式通知，则遇不可抗力的合同一方即失去其提出遭受不可抗力的权利。遭受不可抗力的合同一方同样有义务按照相同的期限通知合同其他各方不可抗力事故结束。<BR> 
　　５．遭受不可抗力的合同一方有义务证明所发生的，为本合同所规定的不可抗力事故，以及事故持续的时间。<BR> 
　　第二十五条　未行使权利<BR> 
　　合同任何一方未行使其按本合同所享有的任何权利，均不应视为放弃这一权利，也不应妨碍该方以后行使上述权利。<BR> 
