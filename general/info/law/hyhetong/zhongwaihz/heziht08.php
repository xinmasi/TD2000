　　３．高级职员（执行经理、部门经理、分部经理）的薪金、社会保险、福利、差旅费等的标准，由董事会决定。执行经理由董事会个别地与其签订书面雇用合同。部门和分部经理以及其余外籍职工由执行管理委员会个别地与其签订书面雇用合同。<BR> 
