　　２．８甲方应不断地在每次修改时更新有关＿＿＿＿＿自制件国产率发展计划所列零部件的工艺技术资料。<BR> 
　　２．９甲方应向合营公司提供有关＿＿＿＿＿和适用的选用件的一整套下列售后服务技术资料。<BR> 
　　－－经销部设施手册，包括厂房的工程设计和改造<BR> 
　　－－服务资料目录；<BR> 
　　－－修理手册；<BR> 
　　－－随车资料：使用说明书，维修时间表；<BR> 
　　－－维修资料：修理项目清单、专用工具目录（包括甲方有权自由处分的图纸）维修服务站设备目录；<BR> 
　　－－有关销售、服务和配件的培训资料，包括服务管理手册；<BR> 
　　－－保用办理制度；<BR> 
　　－－在服务资料目录中所列的，甲方在世界范围内向公司经销网提供的其他资料。<BR> 
　　２．１０甲方应不断地在每次作出在世界范围内适用于合同产品的修改时，更新售后服务技术资料。<BR> 
　　３．甲方确认，提供给合营公司的技术资料按照本条所规定的范围是完整的，所包含的技术性能与甲方或附属公司用于合同产品的技术性能是相同的。如发现技术资料短缺、错发、损坏或不清晰，甲方应尽快补发和更换。如发现甲方的技术资料中有错误，甲方应立即采取措施予以纠正。在此范围内所产生的费用应由＿＿＿＿＿负担。<BR> 
　　４．有关其他合同产品的技术资料的范围，将在开始制造这些合同产品前及时商定。<BR> 
　　第四条　技术修改和改进<BR> 
　　１．合营公司有权按本条第２至第９款的规定为修改和改进合同产品进行开发工作（不断的产品改进）。<BR> 
　　２．在本协议期限内，甲方和合营公司在计划对合同产品进行修改、改进和开发工作时，应及时以书面告诉对方。<BR> 
　　３．１合营公司及其协作厂（根据与合营公司签订的许可证再转让协议使用＿＿＿＿＿公司技术的协作厂），只要在根据本协议和（或）许可证再转让协议制造合同零部件，就可以使用甲方和（或）附属公司所作的修改，无需支付任何专门报酬。<BR> 
　　３．２同样，甲方及附属公司只要在制造以合同零部件的设计为基础的零部件，就可以使用合营公司和（或）其协作厂（根据与合营公司签订的许可证再转让协议使用甲方技术的协作厂）所引起的修改，无需支付任何专门报酬。<BR> 
　　４．在对散装车中的零部件的修改实施之前，甲方应及时通知合营公司。对上述修改，合营公司原则上应予以接受。但是，因散装车中零部件修改而会影响到合同零部件的，则随之而相应修改的程度和采纳上述修改的日期应由协议双方确定。因从甲方进口散装车中零部件的修改所引起的合营公司自制合同零部件的修改，其投资应由合营公司负担。<BR> 
　　５．为了出口＿＿＿＿＿发动机，合营公司应按甲方所要求的时间采纳技术修改和改进。<BR> 
　　６．对合同产品的产品责任在于甲方。<BR> 
　　合营公司对合同零部件所作的修改和改进，如涉及标有需进行技术工程鉴定的合同产品的安全性、基本设计和性能，则应经甲方书面批准后才可予以实施，除非协议双方视不同情况另有决定。<BR> 
　　７．但是，合营公司有权根据国内情况对合同零部件自行修改和改进，其条件是：<BR> 
　　（１）保持甲方的设计标准和质量标准；<BR> 
　　（２）不涉及标有需进行技术工程鉴定的合同产品的安全性、基本设计和性能；<BR> 
　　（３）不会引起对从甲方和（或）附属公司进口散装车中的零部件进行任何更改；<BR> 
　　（４）经修改的合同零部件可以与散装车中的原零部件互换。<BR> 
　　合营公司应把修改和改进交甲方审批，甲方不得无故不予批准。<BR> 
　　８．甲方在决定对于不包括在散装车内的甲方零件部进行修改之前，如会影响到合同零部件，应事先及时告诉合营公司。合营公司应按以下方式处理：<BR> 
　　（１）涉及合同产品安全性、基本设计和性能的修改（强制性修改），合营公司应在技术工程鉴定后采纳。如需较长时间作生产设备，则合营公司可决定推迟采纳时间，但应以可能继续交付散装车内相应零部件为限。<BR> 
　　（２）对于不涉及合同产品安全性、基本设计和性能的修改，合营公司有权根据实际情况自行酌情决定，并把其决定通知甲方。<BR> 
　　９．有关技术修改和改进的资料，第三条第２．３款的规定同样适用。<BR> 
　　１０．如果甲方将来的产品不能满足在中国的使用要求和增加的需求量，而且合营公司有能力筹措投资和设计开发其自己的汽车所需的资金（特别是通过利用其企业发展基金），市场状况也表明这一设计开发工作是合理的，则合营公司可以决定设计开发自己的汽车，以实现合营合同所规定的主要业务活动。<BR> 
　　第五条　技术资料的交付<BR> 
　　１．１第三条２．１、２．６、１、２．９各款所规定的技术资料应在合营公司建立后＿＿＿＿＿个月内开始交付，并在此后＿＿＿＿＿个月内交付完毕。<BR> 
　　１．２产品技术资料的交付应按照自制件国产率发展计划和外购件国产率发展计划（合营合同附件八）的顺序进行，急需的先行交付。具体安排由联合工作组确定，该工作组及其工作程序另行商定。<BR> 
　　１．３有关工程设计的工艺技术资料的交付应按照自制件国产率发展计划（合营合同附件八）的顺序进行，急需的先行交付，具体安排应在合营公司和甲方要签订的工程设计协议中予以规定。<BR> 
　　１．４售后服务技术资料的交付应按照由联合工作组确定的顺序进行。<BR> 
　　２．甲方应按甲方标准提供第三条和第四条所规定的技术资料＿＿＿＿＿文本各式一份，凡有英文的，则提供英文本。<BR> 
　　３．按第三条第２．１、２．６、１、２．９各款交付的技术资料，应采用能多次复制的材料交付。增加的技术资料和以后交付的技术资料的交付，应由协议双方根据上述技术资料最合适的传递方式随时加以决定。<BR> 
　　４．第三条第２．１、２．６、１、２．９各款所规定的技术资料应在＿＿＿＿＿机场或＿＿＿＿＿港免费交付。<BR> 
　　５．＿＿＿＿＿在技术资料提单上所盖的印戳日期，为技术资料的实际交付日期。合营公司应立即将带有到达日期邮戳的上述技术资料提单的复印本两份寄交甲方备查。<BR> 
　　６．甲方应在每批技术资料发运后＿＿＿＿＿个工作日内，将合同号、技术资料提单号、发运日期、技术资料项号、件数、重量、运输工具、班机号或班轮号以及预计抵达日期用电传或电报通知合营公司。同时用航空信件将下列单据寄交合营公司：<BR> 
　　（１）技术资料提单一式四份；<BR> 
　　（２）技术资料详细装箱单一式三份。<BR> 
　　７．如果技术资料在运输过程中全部或部分丢失或损坏，甲方在从运货代理收到技术资料丢失的书面通知或从合营公司收到技术资料损坏的书面通知后，应尽快（不超过甲方的＿＿＿＿＿个工作日）将上述丢失或损坏的技术资料免费补发合营公司。<BR> 
　　８．技术资料的包装应适合于长途运输。每个技术资料包装封面上和内部均应以＿＿＿＿＿文标明下述内容：<BR> 
　　（１）合同号；<BR> 
　　（２）收货人；<BR> 
　　（３）目的地；<BR> 
　　（４）重量（公斤）；<BR> 
　　（５）体积（立方米）；<BR> 
　　（６）箱号／件号；<BR> 
　　（７）唛头；<BR> 
　　（８）收货人代号。<BR> 
　　在每个技术资料包装箱内，应附有技术资料详细装箱清单一式三份，清单上需标明技术资料的序号、文件代号和名称。<BR> 
　　９．按第三条第２．３．３款规定交付的技术资料，费用应由合营公司负担，交付方式由项目手册确定。项目手册由联合工作组制定，以取代本条第３至第８款。<BR> 
　　１０．如果甲方未按本协议规定的时间进度交付技术资料，并且不说明理由，则甲方应采取适当措施加以解决。如因甲方未按时间进度交付技术资料而影响合营公司的经营，则甲方应按本协议第十六条的规定赔偿合营公司由此而受到的损失。<BR> 
　　第六条　培训<BR> 
　　１．甲方应协助合营公司培训人员。受训人数、受训期限和受训类别，以及其他细节（如受训人员和培训教师的姓名、年龄、专业和语言水平，以及培训目标、计划和资料）将由合营公司和甲方随时共同商定。商定时，双方要对合营公司在人员数量和素质上的需要予以适当的考虑。<BR> 
　　２．合营公司职员的培训应在中国以及甲方或附属公司进行。工人培训原则上应在中国由合营公司的外籍职工进行（在职培训）。如有必要，工人培训也可在甲方或附属公司进行。<BR> 
　　３．最初＿＿＿＿＿年期间在甲方或附属公司接受技术和管理方面培训的人数，在本协议附件二中具体规定。但是，协议双方认为有必要时可以共同决定更改本协议附件二规定的职工受训人数。<BR> 
　　４．合营公司应根据其需要和可能建立自己的培训设施。合营公司学徒人数暂定为每年＿＿＿＿＿人。<BR> 
　　５．本条第２款培训工作的培训教师，将根据双方按本条第１款共同规定的要求由甲方选择。合资公司应提供合格的技术人员协助培训教师工作。<BR> 
　　６．本条第２款培训工作的培训用语原则上为＿＿＿＿＿语。<BR> 
　　７．合营公司与其受训人员签订的劳动合同和雇用合同应规定，经培训的职工在受训后＿＿＿＿＿年内不得辞职，并应从事经培训的工种。<BR> 
　　８．甲方向合营公司派遣培训教师以及合营公司向甲方派遣其受训人员，均需协助办理正式手续。为了便利办理上述手续，作为上述人员，即培训教师和受训人员的东道主的协议一方，在必要时应尽力保证出入境签证、工作许可证和旅行许可证的及时签发，并为上述培训教师和受训人员提供合理的住房、医疗、交通和日常生活。<BR> 
　　９．甲方还准备为制造是合营公司外购件，但同时是甲方自制件的合同零部件的可能的中国协作厂培训技术人员。培训计划、受训人数、培训期限和培训费用应由甲方、合营公司和上述协作厂另行商定。<BR> 
　　第七条　咨询<BR> 
　　１．甲方应在本协议范围内向合营公司提供咨询。咨询应在中国开始制造后，由甲方或附属公司书面及口头形式提供给合营公司，以甲方或附属公司自己的经验为限，原则上在甲方或附属公司进行。<BR> 
　　应甲方要求并经合营公司同意（不得无故不予同意），咨询也可以在中国进行。<BR> 
　　２．甲方应就合营公司的质量保证、产品技术、制造、销售和售后服务、采购的材料管理、财务、人事、组织和法律事务各方面的工作提供咨询：咨询主要应包括：<BR> 
　　（１）通过转让以下各方面的管理专有技术，协助建立合营公司的组织机构；<BR> 
　　－－各部门的工作范围（岗位责任）；<BR> 
　　－－内部和外部的信息流动；<BR> 
　　－－公司和职能部门的标准、表格和程序，包括相应信息制度。<BR> 
　　（２）根据甲方标准和中国情况、协助选择＿＿＿＿＿国内或国外可能的协作厂，并尽力鼓励上述生产甲方生产材料和外购件的协作厂通过向可能的中国协作厂转让技术和提供服务的方式提供援助，以此协助发展＿＿＿＿＿合同零部件的中国配套工业。<BR> 
　　（３）通过以下工作为解决在制造和（或）质量方面可能出现的困难提出建议：<BR> 
　　－－应合营公司的要求，分析在制造和（或）质量方面的问题，并提出解决问题的建议；<BR> 
　　－－就节省材料和（或）工时，以及改善内部材料流动提出建议，使生产最佳化；<BR> 
　　－－把甲方的实际质量改进和质量保证措施告诉合营公司，并就如何在合营公司加以应用提出建议。<BR> 
　　（４）通过以下工作就改进合营公司的制造工艺和（或）合营公司产品的质量保证措施提出建议：<BR> 
　　－－转让甲方有权自由处分的，经济性生产所必需的甲方工艺技术和质量技术；<BR> 
　　－－转让在工厂及机器设备的维护保养和能源消耗方面的改进和革新；<BR> 
　　－－就日常生产的技术修改和适用于合同零部件的表面修改提出建议；<BR> 
　　－－因合营公司中断生产和（或）国内协作厂无法保证零部件持续供应时，就安排有关零部件应来源提出建立和帮助，以免合营公司中断制造汽车。<BR> 
　　（５）协助合营公司安排人员访问甲方或附属公司，并就合营公司的日常业务提出建议。<BR> 
　　第八条　特殊服务<BR> 
　　１．除按合营合同由外籍职工传授有技术和按第七条提供咨询外，甲方还准备在合营公司提出要求时，向其提供特殊服务，但应以甲方能够提供所需专家和具有提供上述服务的能力为限。<BR> 
　　甲方可能向合营公司提供的特殊服务如下：<BR> 
　　（１）协助合同产品投产，直至合营公司完全达到生产能力和实现自制件国产率发展计划（合营合同附件）；<BR> 
　　（２）协助达到合同产品的质量标准；<BR> 
　　（３）根据第四条和第十二条规定进行技术工程鉴定的样品试验；<BR> 
　　（４）根据第四条和第十二条规定进行生产样品鉴定；<BR> 
　　（５）按中国市场要求修改产品；<BR> 
　　（６）协助保养维修机器、调备和其他工厂设施；<BR> 
　　（７）甲方能提供的其他特殊服务。<BR> 
　　２．对于为派遣到合营公司进行上述服务的甲方专家办理出入境签证，工作许可证等事宜，本协议第六条第８款同样适用。<BR> 
　　第九条　商标<BR> 
　　１．甲方许可合营公司在本协议期限内，有权在制造和销售合同产品时使用契约商标。但上述契约商标须已在中国注册。合营公司也可以使用甲方和合营公司商定的其他商标和商标名称。<BR> 
　　２．契约商标在合同产品上的使用方式的位置应由甲方决定，使用契约商标必须与协议有关，并仅限于本协议规定的范围。合营公司有权经与甲方商定许可其协作厂仅在制造和向合营公司销售合同零部件时使用契约商标。但是，上述协作厂无权再转让契约商标的使用权。<BR> 
　　３．１除契约商标外，合营公司还应在合同汽车上装上一块标牌，说明是合营公司经甲方许可在中国制造的。<BR> 
　　３．２甲方将决定汽车型号、汽车编号和发动机代号及编号，并通知合营公司。<BR> 
　　３．３标牌上的文字说明及标牌的安放方法和位置由合营公司和甲方商定。<BR> 
　　４．合营公司自己或要求厂在国产合同零部件上标示契约商标时，应按＿＿＿＿＿公司通常采用的方式。<BR> 
　　５．在可能可实际发生任何第三者侵犯契约商标的行为时，合营公司应通知甲方，并协助甲方对上述侵权行为进行诉讼（如提出诉讼的话），费用由甲方负担。<BR> 
　　６．甲方和合营公司可以随时决定，应把甲方还是合营公司视为出口合同产品的制造者。<BR> 
　　第十条　工业产权和专有技术<BR> 
　　１．甲方同意合营公司在本协议期限内享有工业产权和专有技术的非独占性使用权，用于制造、销售和使用合同产品。但是，甲方给予合营公司在中国制造合同汽车的独占性权利。合营公司有权向国内有关协作厂再转让甲方的工业产权和专有技术，以用于制造和向合营公司供应合同零部件。<BR> 
　　２．＿＿＿＿＿公司确认，甲方是工业产权和专有技术的无可争辩的合法拥有者和（或）合法使用者，因而有权向合营公司转让使用权，不会构成对任何第三者的侵权。<BR> 
　　３．如果中国以外的第三者提起侵权诉讼，甲方应保证合营公司利益不受侵害，并负责与上述第三者谈判。如果合营公司也卷入上述侵权诉讼，只要合营公司不采取与甲方建议相抵触的行动，甲方应帮助合营公司在诉讼中进行辩护，并应赔偿合营公司由此而引起的费用。<BR> 
　　４．合营公司保证，对于甲方提供给合营公司的资料不提出或导致提出任何工业产权的登记申请。<BR> 
　　５．协议双方共同开发的技术，任何一方未经另一方事先书面同意，不得提出登记申请。<BR> 
　　６．协议任何一方都有权免费使用另一方对合同产品作出的修改和改进，但无权对上述修改和改进提出登记申请。提出上述登记申请的权利属于作出上述修改和改进的一方。<BR> 
　　７．如果合营公司因合营公司所开发的合同零部件，或者甲方开发的但经营公司或其国内协作厂修改的零部件而侵犯第三者的工业产权，甲对此不负责任。对于因并非来源于甲方的合营公司制造工艺或生产设备而侵犯第三者的工业产权，甲方也不负责任。<BR> 
　　第十一条　合同产品的制造<BR> 
　　１．合营公司制造的汽车应由合营公司董事会按合营公司随时决定，并经合营公司和甲方商定。开始时合营公司应制造＿＿＿＿＿车（附件一）。<BR> 
　　２．甲方应在与合营公司商定的期限内，提供合同汽车的散装车。但是，甲方如在上述商定的期限届满前停止批量生产合同汽车，则应在停产前以书面形式及时把其停产决定通知合营公司，以便合营公司能够作出适当决定，是按当时条件继续向甲方或附属公司购买散装车，还是适用本条第１款。<BR> 
　　３．合营公司应根据国产率发展计划（合营公司附件八）自己制造或由有关协作厂制造合同零部件或材料，但是，<BR> 
　　（１）这些零部件要符合技术资料，并已按本协议第十二条测试通过。<BR> 
　　（２）这些合同零部件的合营公司入库价要不高于从甲方进口的相同零部件的合营公司入库价。<BR> 
　　第十二条　产品产量<BR> 
　　１．合营公司应负责所制造的合同产品在质量上达到甲方标准。<BR> 
　　为此，合营公司应提供适当的工作条件，购买合同产品质量控制所需的计量和测试设备，并在甲方指导下选择具体设备。甲方愿意在选择相应设备方面提供咨询和援助。此外，甲方还应向合营公司提供必要的质量标准和其他有关质量控制的技术资料（见第三条），以及适用的甲方测试专有技术。<BR> 
　　２．合营公司不得销售未达到甲方质量标准的合同产品。<BR> 
　　３．合营公司与协作厂签订合同时，应责成协作厂承担与上述义务相应的义务。<BR> 
　　４．合营公司的质量保证部门是唯一有权按甲方质量标准就合同产品的质量签发首件样品报告的部门，报告内容包括试验结果以及批准或不批准合同产品的决定。<BR> 
　　如果制造工艺、材料或协作厂有了变更，则应对有关零部件和（或）材料重新进行生产样品鉴定和（或）技术工程鉴定。<BR> 
　　在样品批准后，合同产品才可进行批量生产。在批量生产时，应视生产工艺所达到的质量对合同产品进行逐件或抽样试验。合营公司应进行各种必要的常规试验，以确保达到所需的质量。但是，如在特殊情况下合营公司不能进行试验时，则应合营公司要求，也可以由甲方，或者具有适当的试验设备和仪器并已向合营公司证明其有能力从事这种工作的研究所或其他第三者进行试验。<BR> 
　　按甲方规定需进行技术工程鉴定的合同产品，应送交甲方进行上述鉴定试验。但是，在甲方和合营公司认为合营公司或第三者具备试验条件时，甲方可以把这项工作逐步转移给合营公司或第三者，由他们代表甲方进行技术工程鉴定。<BR> 
　　合营公司可以派其人员参加标有需进行技术工程鉴定的国产零部件的试验。上述访问的时间表和计划将由协作双方商定。<BR> 
　　５．甲方在其图纸和技术要求上标有“＿＿＿＿＿”记号的零部件，合营公司应将试验结果按甲方要求写成文件并存档。<BR> 
　　６．此外，甲方的代表有权到合营公司亲自了解：<BR> 
　　－－合营公司所制造的合同产品的质量状况；<BR> 
　　－－制造工艺能力。<BR> 
　　甲方的代表也可以与合营公司的代表一起，亲自了解协作厂所制造的合同零部件的质量状况和协作厂的工艺能力，以及合同产品在市场上的可靠性和使用状况。<BR> 
　　第十三条　支付<BR> 
　　１．鉴定根据第三条第２款通过技术资料转让专人技术，根据第十条转让工业产权，以及根据本协议第七条对于＿＿＿＿＿车提供咨询，合营公司每制造一辆汽车应向甲方支付许可证咨询＿＿＿＿＿元＿＿＿＿＿币，这一许可证咨询费反映＿＿＿＿＿年的费用基础，并将按照甲方人员费用的年增长率逐步增长。<BR> 
　　如果咨询在中国进行，则合营公司还应负担与派遣甲方人员有关的额外费用，如旅费、膳宿以及其他可能在中国发生的费用，包括税款、关税、社会保险和福利费等。<BR> 
　　２．制造＿＿＿＿＿车最初＿＿＿＿＿年的许可证咨询费总额，包括年通货膨胀率在内，为＿＿＿＿＿元＿＿＿＿＿币。合营公司每年支付的最低金额，应为合营公司每年制造＿＿＿＿＿车的最低产量（最低产量即合营合同第四条规定的产量）乘以本条第１款规定的每辆车的许可证咨询费，这一最低金额也包括按甲方人员费用的年增长率增长的金额。<BR> 
　　３．即使合营公司的＿＿＿＿＿车年产量超过每年最低产量，每辆车的许可证咨询费应保持不变。如果未达到年最低产量，则每年许可证咨询费最低金额与迄止当时已缴付的总额之间的差额，应按前一年第四季度的发票支付给甲方。但是，如因产量增加，＿＿＿＿＿年内已达到上述＿＿＿＿＿元＿＿＿＿＿币，则在第＿＿＿＿＿生产年年底之前不再支付费用。<BR> 
　　４．在最初＿＿＿＿＿年后，协议双方应考虑车型、产量、国产率、将来的专有技术转让范围以及将来甲方的咨询程度等，按当时实际情况重新谈判许可证咨询费的金额。每辆车的许可证咨询费仍将以持续方式支付，并按甲方人员费用的年增长率每年予以调整。<BR> 
　　５．许可证咨询费以及咨询在中国进行时发生的额外费用（本条第１款），将按日历季度根据这三个月内制造＿＿＿＿＿年的数量记帐。每季度结束后＿＿＿＿＿天内，合营公司应将该季度制造汽车的数量通知甲方。甲方将开出上季度许可证咨询费的发票，在该发票发出之日起第＿＿＿＿＿天到期支付。<BR> 
　　６．对于合营公司根据本协议第八条所要求的特殊服务，合营公司应支付提供上述服务时有效的特殊服务费用。<BR> 
　　如果合营公司所要求的特殊服务是由甲方研究开发部提供的，偿付的费用应按提供上述服务时有效的甲方“研究开发部服务价目表”计算。有效的“研究开发部服务价目表”每年尽早提供给合营公司。关于技术工程鉴定的样品试验以及可能的生产样品鉴定，将支付以下费用：<BR> 
　　－－合营公司应承担合营公司所生产的零部件的试验鉴定费用，以及要甲方试验鉴定的零部件的材料费、包装费和运费。<BR> 
　　合营公司按提供上述服务时有效的“研究开发部服务价目表”向甲方支付材料费和试验鉴定费。<BR> 
　　－－国产外购件的试验鉴定费，以及要甲方试验鉴定的零部件的材料费、包装费和运费应由中国协作厂支付。试验鉴定费应按提供上述服务时有效的“研究开发部服务价目表”支付，与材料费一起通过合营公司支付给甲方。<BR> 
　　试验样品在明确商定的情况下方归还合营公司。<BR> 
　　如果合营公司所要求的特殊服务是由甲方其他部门提供的，偿付的工时费用应按每小时＿＿＿＿＿元＿＿＿＿＿币的费率计算，这一费率反映＿＿＿＿＿年的费用基础，将按甲方人员费用的年增长率增长。<BR> 
　　如果合营公司要求的特殊服务在中国进行，合营公司还应负担与派遣甲方人员有关的额外费用，如旅费、膳宿以及其他可能在中国发生的费用，包括税款、关税、社会保险和福利费等。<BR> 
　　７．对合营公司要求甲方或附属公司提供的特殊服务所偿付的费用，甲方将开出发票，在该发票开出之日起第＿＿＿＿＿天到期，按甲方所开币种支付。<BR> 
　　８．甲方在合营公司建立后最初＿＿＿＿＿年中，承担在＿＿＿＿＿公司为合营公司人员进行总量不超过＿＿＿＿＿个月的培训所发生的培训教师及材料费用。此外，甲方将免费提供住宿，去甲方工厂时所需的交通，以及工厂工作日在甲方食堂或招待所的膳食，但不包括星期六、星期日以及＿＿＿＿＿国公共假日的膳食。<BR> 
　　对在中国境外甲方或附属公司进行的培训，合营公司受训人员的差旅费、工资、薪金、包括津贴，均由合营公司承担。<BR> 
　　培训时间超过＿＿＿＿＿个月时，在进行培训的国家逗留的有关费用以及培训工作本身，和培训教师及材料等费用，均应由合营公司承担。<BR> 
　　对甲方培训教师偿付的工时费用，应按每小时＿＿＿＿＿元＿＿＿＿＿币的费率计算，这一费率反映＿＿＿＿＿年的费用基础，将按甲方人员费用的年增长率增长。<BR> 
　　如果合营公司要求甲方派出培训教师在中国进行培训，对甲方培训偿付的工时费用应按每小时＿＿＿＿＿元＿＿＿＿＿币计算，这一费率反映＿＿＿＿＿年的费用基础，将按甲方人员费用年增长率增长。此外，合营公司还应承担与甲方向合营公司派遣培训教师有关的额外费用，如旅费、膳宿以及其他可能在中国发生的费用，包括税款、关税、社会保险和福利费等。<BR> 
　　９．对于甲方或附属公司提供的培训应偿付的费用，甲方将开出发票，在该发票开出之日起第＿＿＿＿＿天到期，按甲方所开币种支付。<BR> 
　　１０．款额应以＿＿＿＿＿币付到甲方所指定的帐户。每笔款额在甲方可从该帐户中提用时即视为付讫。如果甲方在发票开出之日起第＿＿＿＿＿天尚未收到，则将按当时有效的＿＿＿＿＿币＿＿＿＿＿个月贷款的＿＿＿＿＿银行同业拆放利率加＿＿＿＿＿％的利息率支付利息。<BR> 
　　１１．如果中国对许可证咨询费和在中国偿付的特殊服务费用征收税款或其他费用，则合营公司应承担甲方在中国境外的赋税中不能抵免的那一部分税款和费用。合营公司为甲方代付的，并记在甲方在中国的帐户的税款或其他费用，其金额如能从甲方在中国境外应缴税额中抵免，则合营公司应将有关纳税收据交给甲方。<BR> 
　　１２．关于支付许可证咨询费的其他细节，在本协议附件三规定。<BR> 
　　第十四条　不可抗力<BR> 
　　１．如果协议任何一方因不可抗力未能履行本协议，则该方对于在不可抗力持续期间不履行其义务不承担责任。因不可抗力而造成中止履行本协议，应限于不可抗力的影响存续的时间内，协议双方都将尽最大努力将不可抗力，特别是由此而引起的延误所造成的后果减轻到最低程度。<BR> 
　　２．协议双方在其他方面应仍受本协议的约束。因不可抗力所引起的问题应通过协商适当解决，使本协议能合理地继续履行。但是，如因不可抗力造成的延误持续＿＿＿＿＿个月以上，则协议任何一方应有权发出通知，在通知＿＿＿＿＿个月后终止本协议，除非在上述＿＿＿＿＿个月期限内能按该方希望的方式全面地修改其义务，使之适用新的情况。<BR> 
　　３．不可抗力事故是指提出遭受不可抗力的协议一方在签订本协议时不能预见到的，阻碍其实际履行义务的，不可避免的自然现象。就本协议而言，不可抗力事故包括（但不限于）地震、流行病、严重的火灾、水灾、台风、海上事故等自然现象，以及战争、爆炸和确实妨碍任何一方履行其本协议义务的罢工。<BR> 
　　４．遇不可抗力的协议一方应立即（不迟于获悉发生不可抗力后＿＿＿＿＿天）用邮寄、电传或电报通知协议另一方。这＿＿＿＿＿天期限自该方获悉发生不可抗力之日算起。如未按上述方式通知，则遇不可抗力的协议一方即失去其提出遭受不可抗力的权利，遭受不可抗力的协议一方同样有义务按照相同的期限通知协议另一方不可抗力事故的结束。<BR> 
　　５．遭受不可抗力的协议一方有义务证明所发生的，为本协议所规定的不可抗力事故，以及事故持续的时间。<BR> 
　　第十五条　保密<BR> 
　　１．合营公司不得向任何第三者透露从甲方或附属公司得到的任何技术资料和专有技术。保密要求在本协议终止后＿＿＿＿＿年内继续有效。<BR> 
　　２．如有必要把部分技术资料转让给合营公司的国内协作厂，合营公司应登记好上述技术资料的所在地点。<BR> 
　　３．合营公司应以书面形式要求其协作厂和本公司雇员承担相应义务。合营公司应经常检查上述人员和协作厂履行保密义务的情况。<BR> 
　　４．除甲方和合营公司另有商定外，合营公司不得向任何第三者透露有关与甲方共同对合同产品所作修改和改进的任何技术资料和专有技术，甲方也不得向任何第三者透露按本协议第四条由合营公司或与合营公司共同所作修改和改进的任何技术资料和专有技术。但是对于附属公司，第四条第３．２款同样适用。如果甲方向附属公司透露由合营公司或由甲方和合营公司共同作出的修改和改进，则甲方应要求附属公司承担相应的保密义务。<BR> 
　　第十六条　责任<BR> 
　　１．协议任何一方或其职工均仅对于在履行本协议时或在提供其他（特别是自愿的）帮助时的严重过失和（或）故意失职承担责任。<BR> 
　　２．协议任何一方对于另一方的责任，附属公司的责任，及其职工的责任，仅限于履行本协议，如不能继续履行协议，则仅限于赔偿另一方所遭受的直接损失。<BR> 
　　第十七条　协议的转让和修改<BR> 
　　１．除本协议另有规定外，协议任何一方未经另一方面同意，不得将其本协议的权利和义务转让给任何第三者。<BR> 
　　２．对本协议的任何修改和增减，都应书面作出，并经中国主管部门批准后生效，作为本协议的组成部分。<BR> 
　　３．本协议未涉及但已在合营合同中规定的事项，同样适用于本协议。<BR> 
　　第十八条　协议期限<BR> 
　　１．本协议已与＿＿＿＿＿年＿＿＿＿＿月＿＿＿＿＿日签订的合营合同同时经对外经济贸易部批准。因此，本协议一经协议双方正式授权代表签署即刻生效，协议期限与合营合同的期限相同。<BR> 
　　２．最迟在第一阶段（合营合同第四条第１款）届满前＿＿＿＿＿个月，协议双方应商定第二阶段的合同产品。如甲方或附属公司的其他汽车取代＿＿＿＿＿车作为合同汽车或作为增加的合同汽车，则协议双方应进行协商，对本协议作必要的修改。<BR> 
　　３．如本协议任何实质性条款遭到违反，协议任何一方有权发出通知，在通知＿＿＿＿＿个月后终止本协议，而且不妨碍其向另一方提出索赔。<BR> 
　　第十九条　部分失效<BR> 
　　如果本协议任何条款失效或不能执行，则其余条款应不受影响，继续有效。如为了达到本协议在商业上的目的而有必要更换任何上述条款，则协议双方应尽快会晤，按照签订本协议时所持的精神，为取得尽可能相同的经济效果，来商定新的条款，以取代失效的条款，并使新条款在法律上生效。上述新条款应追溯到原条款失效或不能执行之时起开始适用。<BR> 
　　第二十条　未行使权利<BR> 
　　协议任何一方未行使其按本协议所享有的任何权利，均不应视为放弃这一权利，也不应妨碍该方以后行使上述权利。<BR> 
　　第二十一条　协议终止的影响<BR> 
　　１．本协议按第十八条第１款终止后，合营公司有权以自己的商标继续制造合同产品，但不再使用契约商标。<BR> 
　　２．如本协议因归咎于甲方的原因而提前终止，合营公司或其继承者有权继续制造合同产品，但合营公司或其继承者要支付届时商定的许可证咨询费。如本协议因归咎于合营公司的原因而提前终止，合营公司或其继承者应停止制造合同产品，除非届时另有商定。如本协议因其他原因提前终止，协议双方应协商决定是否继续制造合同产品。<BR> 
　　第二十二条　争议的解决<BR> 
　　１．由本协议、违反本协议，本协议的期满终止和提前终止或失效所引起的，或与上述各项有关的任何争议、争论和索赔，均应通过谈判或调解解决。如果谈判或调解在＿＿＿＿＿个月内未能取得任何协议双方可以接受的结果，则上述争议、争论和索赔只应通过仲裁解决，而不诉诸有关法院。仲裁应按照当时有效的＿＿＿＿＿仲裁院的仲裁规则进行。仲裁裁决是终局的，对有关的协议方具有约束力。协议双方将在其国家内承认并执行仲裁裁决。<BR> 
　　２．仲裁应由＿＿＿＿＿仲裁院进行，仲裁地点为＿＿＿＿＿，仲裁使用的语言为＿＿＿＿＿文，仲裁庭由３（三）名仲裁员组成。<BR> 
　　３．仲裁庭应只适用在有关争议的原因发生之时详细成文并经正式公布的，一般能获得的中国法律。<BR> 
　　第二十三条　协议文字<BR> 
　　１．本协议用中文和英文书就，各签署原件４（四）份。两种文本均为正式文本，具有同等效力。中、英两种文本，协议双方各执２（两）份。<BR> 
　　２．工作文本用＿＿＿＿＿文。<BR> 
　　第二十四条　通知<BR> 
　　１．根据本协议需要或允许发生的所有通知均用＿＿＿＿＿文，应亲手递交或用挂号信、电传、电报发给协议另一方。也可通知协议另一方的其他地址。<BR> 
　　２．任何通知，凡是邮寄的，则在装有通知的信件寄出时应视为发出；要证明信件发出，只要证明装有该通知的信封上已写上正确的地址，贴上邮票，投入邮局或者投入各自的国家邮政管理部门所控制的任何信箱。<BR> 
