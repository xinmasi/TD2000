　　１．合同各方由本合同、违反本合同、本合同的期满终止和提前终止或失效所引起的，或与上述各项有关的任何争议、争议和索赔，均应通过谈判或调解解决。如果谈判或调解在＿＿＿＿＿个月内未能取得任何有关各方可以接受的结果，则上述争议、争论和索赔只应通过仲裁解决，而不诉诸有关法院。仲裁应按照当时有效的＿＿＿＿＿仲裁院的仲裁规则进行。仲裁裁决是终局的，对有关各方均具有约束力。合同各方将在其国家内承认并执行仲裁裁决。<BR> 
