　　第十九条　外籍雇员<BR> 
　　１．甲方应与合营公司商定，在甲方的可能范围内向合营公司派遣其专家，作为合营公司的临时雇员（即外籍职工）。甲方应按合营公司提出的具体职务要求选派专家。然而，合营公司有权辞退任何证明是不称职的外籍职工，拒绝接受或辞退任何证明其健康状况不足以胜任其职务要求的外籍职工。<BR> 
　　２．合营公司应与各外籍职工分别订立雇用合同，雇用合同应包括本合同附件十一所规定的主要条款。<BR> 
　　３．各外籍职工的报酬总额应按甲方标准，雇用合同中所规定的外籍职工所有人员费用（包括奖金），应由合营公司负担。<BR> 
　　４．合营公司应负责办理，或者必要时向中国有关部门申请以下（但不限于以下）各项：<BR> 
　　４．１在＿＿＿＿＿批准并可在＿＿＿＿＿续签的签证，包括有效期为＿＿＿＿＿个月的多次出入境签证，以及居住、工作和旅行许可证；<BR> 
　　４．２根据＿＿＿＿＿正规学校标准提供教育条件。<BR> 
　　５．外籍职工的住房在本合同附件中规定。<BR> 
　　第二十条　保险<BR> 
　　１．合营公司对其资产保火险、营业中断险和一般责任险。<BR> 
　　２．一切可能涉及合同各方利益的与保险有关事项，合营公司应尽快提请合同各方注意。<BR> 
