　　第二十一条　合同的生效和期限<BR> 
　　１．本合同已经合同各方正式授权代表签署，将报请甲方主管委员会以及乙方、丙方和丁方主管领导确认。在确认以后，本合同应报请经贸部审批，并在批准后生效。<BR> 
