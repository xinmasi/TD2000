　　第十一条　国产率<BR> 
　　１．合营公司制造的各种车型的国产率最终目标为１００％。合同各方应共同努力，促使本合同附件八中商定的外购件和自制件的＿＿＿＿＿车国产率发展计划得以实现。<BR> 
　　２．乙方、丙方和丁方保证国产率发展计划所需的先决条件得以完全实现。为此，除了要遵循经济和质量标准外，还要及时发展原材料和零部件的生产能力。因此，有必要实现以下（但不限于以下）先决条件：<BR> 
　　２．１合营公司应可以自由选择中国协作厂；<BR> 
　　２．２合营公司的中国协作厂应作出设备投资，和（或）购买按照甲方的质量标准制造＿＿＿＿＿零部件和在后阶段制造甲方许可的合营公司其他产品所需的技术许可证和（或）专有技术；<BR> 
　　２．３合营公司及其协作厂制造零部件所需的材料应在中国＿＿＿＿＿计划中予以考虑，并提供给合营公司及其协作厂。<BR> 
　　合营公司及其协作厂期望始终取得优惠进口关税税率，并能迅速办理结关手续。<BR> 
　　３．甲方应在技术转让协议范围内尽其最大努力向合营公司提供帮助和咨询，以期达到国产率目标。此外，甲方还应尽最大努力促使＿＿＿＿＿国内外可能的外购件协作厂以转让技术和提供服务的方式对可能的中国合作者予以援助。同样，乙方、丙方和丁方应促使其各自的上级组织和上述协作厂、将来可能的中国合作者在技术转让过程中对上述协作厂予以充分合作。<BR> 
　　４．如果合营公司及其协作厂的国产零部件未能保持甲方的质量标准，从而造成停产的危险，则有关零部件应由甲方提供。<BR> 
　　５．如果国产件的入库价高于甲方交付的零部件的合营公司入库价，则有关零部件应由甲方提供。<BR> 
　　６．有关国产率的具体技术问题应在技术转让协议中予以规定。<BR> 
　　第十二条　场地、基础设施和公用服务<BR> 
