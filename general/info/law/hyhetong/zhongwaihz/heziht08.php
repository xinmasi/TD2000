　　本合营合同在＿＿＿＿＿年＿＿＿＿＿月＿＿＿＿＿签订于中华人民共和国＿＿＿＿＿市，合同各方为：<BR> 
　　＿＿＿＿＿以下简称甲方），其法定地址在＿＿＿＿＿；<BR> 
　　＿＿＿＿＿以下简称乙方），其法定地址在＿＿＿＿＿；<BR> 
　　＿＿＿＿＿以下简称丙方），其法定地址在＿＿＿＿＿；<BR> 
　　＿＿＿＿＿以下简称丁方），其法定地址在＿＿＿＿＿；<BR> 
　　上述四方按照平等互利原则，经过友好谈判，决定根据一九七九年七月八日颁布的《中华人民共和国中外合资经营企业法》（以下简称《合资法》），在中国＿＿＿＿＿共同建立一个合资经营公司（以下简称合营公司）。建立这一合营公司的目的是：<BR> 
　　采用甲方公司的最新技术，制造汽车和发动机；<BR> 
　　采用现代化的管理方法经营合营公司，为中国汽车工业作出贡献。<BR> 
　　为此，甲、乙、丙、丁四方现达成协议如下：<BR> 
　　第一条　合同宗旨<BR> 
　　本合同宗旨为：<BR> 
　　１．规定合营公司的建立；<BR> 
　　２．规定合营公司的法律地位和性质；<BR> 
　　３．规定合营公司的经营范围；<BR> 
　　４．规定合同各方与合营公司有关的权利和义务。<BR> 
　　第二条　合营公司的成立、名称和法定地址<BR> 
　　１．合同各方同意按《合资法》建立合营公司，根据《合资法》第四条，合营公司的形式为有限责任公司。<BR> 
　　２．合营公司的名称为：<BR> 
　　中文：＿＿＿＿＿<BR> 
　　英文：＿＿＿＿＿<BR> 
　　缩写为：＿＿＿＿＿。<BR> 
　　３．合营公司的法定地址为＿＿＿＿＿。<BR> 
　　４．合营公司应在中国对外经济贸易部（以下简称“经贸部”）批准本合同后的一个月内，向工商行政管理局办理登记。合营公司自营业执照签发之日起即告成立。<BR> 
　　５．合营公司为中国法人，受中国法律的管辖和保护。<BR> 
　　６．商标“＿＿＿＿＿”已由甲方在国际上向瑞士日内瓦世界工业产权组织国际局注册，注册号为＿＿＿＿＿，并单独在＿＿＿＿＿国家注册，在北京商标注册号为＿＿＿＿＿。甲方许可合营公司在本合同期限内有权使用这一商标作为合营公司名称的一个组成部分，但是“＿＿＿＿＿”这一商标在中国要继续得到注册，并且甲方应能继续按本合同、章程和技术转让协议的规定在合营公司中施加其影响，特别是对合营公司所制造汽车的质量施加影响。<BR> 
　　在本合同终止时，合营公司应不经甲方提出要求立即改变公司名称、新的公司名称不得把“＿＿＿＿＿”这一商标或其任何缩写作为一个组成部分，也不得有任何其他类似于上述商标的组成部分。同样，如果甲方在合营公司资本中的份额不论由于何种原因而减少，致使甲方认为它将丧失按本合同、技术转让协议和合营公司章程的规定继续在合营公司充分施加影响的法律上的或实施上的可能性，特别是对于合营公司所制造汽车的质量充分施加影响，则经甲方提出书面要求，合营公司应立即以同样方式改变其公司名称。如合营公司未履行这一义务，则甲方根据本合同有权办理一切手续，以改变合营公司或其合法继承者的公司名称。<BR> 
　　第三条　合营公司的经营范围<BR> 
　　１．合营公司的主要业务活动如下：<BR> 
　　１．１制造汽车；<BR> 
　　１．２制造发动机；<BR> 
　　１．３制造零部件；<BR> 
　　１．４进口为制造、装配、测试、服务、培训以及辅助业务活动所需的各种货物；<BR> 
　　１．５有关法律和法规允许时进口整车；<BR> 
　　１．６在国内销售合营公司所制造的汽车。<BR> 
　　１．７在国内销售维修服务配件；<BR> 
　　１．８出口汽车、零部件、配件、附件和冲压模具；<BR> 
　　１．９售后服务。<BR> 
　　２．为了实现其主要业务，合营公司可以按合营公司章程的规定开展与主要业务有关的任何其他活动。<BR> 
　　第四条　车型范围、数量和生产能力<BR> 
　　１．合营公司在建立后最初＿＿＿＿＿年（以下称为“第一阶段”）内制造轿车。有关要制造的轿车及其制造的具体细节应在合营公司和甲方将要签订的技术转让协议中商定。以后，合营公司将制造由甲方或其附属公司所开发的其他车型。制造上述车型也应在技术转让协议中予以规定。<BR> 
　　２．在第一阶段，合营公司应具有以下装配制造能力；<BR> 
　　汽车厂　有冲压车间、拼装车间、油漆车间和总装车间，生产能力为＿＿＿＿＿班年产＿＿＿＿＿辆，包括配件；<BR> 
　　发动机厂　发动机制造是指生产＿＿＿＿＿发动机，其制造设备的生产能力年度＿＿＿＿＿台，其中每年至少应有＿＿＿＿＿台装配成＿＿＿＿＿发动机，以满足在中国销售的轿车对发动机的需要。<BR> 
　　３．汽车厂和发动机厂投产时间按时间进度表。<BR> 
　　４．乙方保证购买合营公司生产的＿＿＿＿＿轿车数量如下：（略）<BR> 
　　如果需求量高于上述数量，合营公司将要求中国有关部门对进口散装车所需的外汇予以支持，并从国内提供足够的原材料、零部件和能源。<BR> 
　　５．在本合同期限内，合营公司生产的汽车应逐步做到在价格上具有竞争力，但是，要有下列条件：<BR> 
　　５．１国产零部件要有货供应，并在价格和质量上具有竞争力；<BR> 
　　５．２产量要增加；<BR> 
　　５．３国内汽车工业的发展要得到合理保护。<BR> 
　　６．甲方保证在发动机投产＿＿＿＿＿年后购买由合营公司制造的＿＿＿＿＿发动机，但是＿＿＿＿＿发动机到甲方的入库价要不高于甲方制造的＿＿＿＿＿发动机的入库价，质量按甲方标准和交货条件方面均具有竞争力。价格和交货条件应在甲方和合营公司将要签订的购货协议中规定。<BR> 
　　第五条　资本、投资比例和资金筹措<BR> 
　　１．合营公司的注册资本应为人民币＿＿＿＿＿元。<BR> 
　　２．合同各方在合营公司注册资本中投资比例和认缴额应为：<BR> 
　　甲方＿＿＿＿＿％，计人民币＿＿＿＿＿元；<BR> 
　　乙方＿＿＿＿＿％，计人民币＿＿＿＿＿元；<BR> 
　　丙方＿＿＿＿＿％，计人民币＿＿＿＿＿元；<BR> 
　　丁方＿＿＿＿＿％，计人民币＿＿＿＿＿元；<BR> 
　　３．合同各方对合营公司注册资本的出资如下：<BR> 
　　３．１甲方<BR> 
　　－－实物，合人民币＿＿＿＿＿元，<BR> 
　　－－现金，相当于人民币＿＿＿＿＿元的＿＿＿＿＿币；<BR> 
　　３．２乙方<BR> 
　　－－实物，合人民币＿＿＿＿＿元；<BR> 
　　－－现金，计人民币＿＿＿＿＿元；<BR> 
　　３．３丙方<BR> 
　　－－现金，相当于人民币＿＿＿＿＿元；<BR> 
　　３．４丁方<BR> 
　　－－现金，计人民币＿＿＿＿＿元。<BR> 
　　４．合同各方应按上述规定的比例分期缴付其认缴额。各期出资缴付的时间、金额、出资方式由董事会根据合营公司的需要决定。合同各方第一次现金出资应在第一次董事会会议后３０天内付讫。合同各方对合营公司的各期出资应按以下方式记入合营公司帐册：<BR> 
　　４．１实物出资在房屋，机器设备等以及投资前费用作为资产入帐之时视为付讫；<BR> 
　　４．２现金出资在现金存入合营公司所指定的中国银行的帐户之时视为付讫。<BR> 
　　合同各方按章程规定付讫出资后，合营公司应向有关合同方出具出资证明书。<BR> 
　　５．合同任何一方未按本条第４款规定的日期付讫出资额时，如出资逾期不超过３０天，则该违约方应以其应当出资的货币，就其应缴付的出资额，根据中国银行当时的短期贷款利率，自逾期第一天起，按逾期天数支付利息，作为合资公司的财务收益。如出资逾期超过３０天，则违约方除应支付本款前述利息外，每逾期３０天，应向其他各方缴付其应缴出资额的＿＿＿＿＿％的违约金。违约金应以与出资相同的货币支付。如逾期长达９０天，则采用下列程序：<BR> 
　　５．１如违约方为甲方，则乙、丙、丁方公司有权根据本合同第二十一条第５款共同或单独要求终止本合同，并就此项违约提出索赔。<BR> 
　　５．２如违约方中有一方为乙方，则甲方有权根据本合同第二十一条第５款要求终止本合同，并就此项违约提出索赔。<BR> 
　　５．３如违约方为丙方和丁方两方或其中一方，则其余各方应继续履行本合同。并有权就此项违约提出索赔。丙方和（或）丁方已缴付的出资，应按其在合营公司注册资本中出资的面值，由乙方购买。<BR> 
　　５．４如果乙方和甲方在没有丙方和丁方两方或其中一方参加的情况下继续经营合营公司，任何一方在此后再逾期９０天不付讫其出资，则本数５．１、５．２、５．３各节同样适用。<BR> 
　　６．合营公司以其全部财产对其债务和义务承担责任，合同各方对合营公司的责任以各自认缴的注册资本为限。<BR> 
　　７．注册资本中现金和（或）实物的外汇出资，应按出资之日国家外汇管理局公布的官方汇率折算成人民币入帐。现金外汇出资应存入合营公司的银行帐户。<BR> 
　　８．在本合营合同签字之前，乙方和丙方已代表合营公司提出申请，要求在本合同批准后３０天内确定人民币贷款限额，以筹措投资和日常业务，诸如支付进口物品，许可证咨询费、特殊服务费、股利、外籍职工薪金等所需的资金。这一货款限额为人民币，但可以用于人民币付款，也可以按兑现之日国家外汇管理局公布的官方汇率兑换成外汇。外汇在经批准的外汇额度内提供。上述货款条件的优惠程度应不低于给于其他中外合营企业的贷款条件。<BR> 
　　９．在开始留存储备金之前，合营公司的注册资本应为总投资（固定资产和流动资产）的＿＿＿＿＿％，总投资的其余＿＿＿＿＿％应通过银行贷款解决。此后，合营公司资本结构中的＿＿＿＿＿；＿＿＿＿＿的产权一负债比率应视为一条长期的资金筹措准则。<BR> 
　　第六条　增资和资本转让<BR> 
　　１．董事会一致决议后，经合同各方书面同意，可以增加合营合同的注册资本。但是，合同各方在新增注册资本中的比例应与原注册资本中的比例相同。<BR> 
　　２．董事会一致决议后，经合同其他各方事先书面同意，合同一方方可将其在合营公司的全部或部分注册资本转让给本合同另一方或第三者，或将其本合同所规定的权利和义务转让给本合同另一方或第三者。如合同一方欲转让其全部或部分注册资本，合同其他各方享有优先购买权。合同一方向第三者转让其合营公司注册资本的条件，不得优惠于其原先向合同其他各方提供的转让条件。<BR> 
　　３．合同一方转让其在合营公司的全部或部分注册资本，或接纳新的合营者，应由合同各方签署书面文件。上述书面文件应视为合同的补充。<BR> 
　　４．增资和资本转让应在合营公司章程中更详细地予以规定。<BR> 
　　５．发生上述增资、资本转让和接纳新合营者时，合营公司应在经贸部批准后１个月内向工商局办理变更登记手续。<BR> 
　　６．尽管上述各款规定，合同各方同意，甲方可以将其在合营公司注册资本的份额不超过＿＿＿＿＿％的部分转让给＿＿＿＿＿投资公司或一家由甲方选择的＿＿＿＿＿国银行。在此情况下，＿＿＿＿＿公司还可将＿＿＿＿＿公司有权委派的五名合营公司董事中一名或几名董事的委派权转让给＿＿＿＿＿投资公司或上述银行。<BR> 
　　第七条　利润率<BR> 
　　１．合同各方应按投资比例，分享利润，分担亏损。<BR> 
　　２．根据本条第３款按注册资本的投资比例分配给合同各方的净利润，为利润总额减去根据中国税法规定缴纳的所得税以及提取的储备基金、职工奖励及福利基金和企业发展基金（以下简称“三项基金”）。三项基金提取的金额由董事会决定。<BR> 
　　３．合同各方同意，合营公司应在其建立后第四个全会计年度内实现金额为注册资本＿＿＿＿＿％（百分之＿＿＿＿＿￣百分之＿＿＿＿＿）的税后（汇出税除外）净利润分配，自其建立后第五个全会计年度起，实现每年金额为注册资本＿＿＿＿＿％（百分之＿＿＿＿＿￣百分之＿＿＿＿＿）的净利润分配。<BR> 
　　４．然而，初期亏损应在第二个全会计年度末予以平衡。<BR> 
　　５．最初和以后的销售价格应按本合同附件五确定。<BR> 
　　第八条　利润汇给和资本汇回<BR> 
