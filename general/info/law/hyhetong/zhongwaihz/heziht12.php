　　第四条　合营企业为根据中国法律成立的中国法人，其一切活动受中国法律的管辖，其正当权益受中国法律的保护。如公布新法律，则按《中华人民共和国合同法》规定执行。<BR> 
　　第五条　合营企业是有限责任公司。甲乙双方分别以各自认缴的出资额限度对合营企业承担责任，并按各自认缴的出资额的比例分配利润，承担风险和损失。　　<BR> 
　　第三章　合营企业的宗旨、经营范围和规模<BR> 
　　<BR> 
　　第六条　合营企业的宗旨是：本着友好合作精神，共同建造，经营具有现代化水平的＿＿＿＿＿俱乐部，为中外人士（新闻工作者、实业家、商界人士及其他各界人士）提供社交、会议、办公、通讯、康乐、食宿场所和服务。通过先进的经营管理手段和优质、高效率的服务，获得双方均满意的社会效益和经济效益。<BR> 
　　第七条　合营企业的经营范围是：社交和会议场所、康乐项目、旅馆、办公楼、餐馆、附属的通讯设备和商品部，以及其他有关的生活、工作服务设施。<BR> 
　　第八条　合营企业的建设和经营的规模如下：<BR> 
　　总占地面积＿＿＿＿＿平方米；<BR> 
　　新建建筑面积＿＿＿＿＿平方米；<BR> 
　　其中：旅馆部分约＿＿＿＿＿平方米（约＿＿＿＿＿间客房）办公楼分约＿＿＿＿＿平方米；<BR> 
　　原有建筑面积＿＿＿＿＿平方米。<BR> 
　　<BR> 
　　第四章　投资总额和注册资本<BR> 
　　<BR> 
　　第九条　合营企业的投资总额为＿＿＿＿＿美元，投资中包括下列费用：<BR> 
　　１．合营企业进行经营所需的土地处置费；<BR> 
　　２．市政工程设施费；<BR> 
　　３．甲方原有建筑物、构筑物及固定在建筑物上的设备移转合营企业的作价；<BR> 
　　４．设计费（包括勘测费）；<BR> 
　　５．建设费（包括新建筑的建设及Ｆ．Ｆ．Ｅ．庭际绿化和附属设施的建设）；<BR> 
　　６．筹建费；<BR> 
　　７．开业筹备费；<BR> 
　　８．新建筑建成开业前的流动资金；<BR> 
　　９．建设期间的贷款利息；<BR> 
　　１０．其他由董事会决定的不可预见的开支的费用。<BR> 
　　第十条　合营企业进行经营所需用地已由甲方进行了处置，其中处置费为＿＿＿＿＿美元。甲方原有建筑物、构筑物和固定在建筑物上的设备在合营企业成立后移交给合营企业，作价为＿＿＿＿＿美元。<BR> 
　　第十一条　合营企业的注册资本固定为＿＿＿＿＿美元。其中甲方出资额为＿＿＿＿＿美元，占＿＿＿＿＿％；乙方出资额为＿＿＿＿＿美元，占＿＿＿＿＿％。<BR> 
　　第十二条　甲乙双方分别按前条规定的出资金额以如下方式出资：<BR> 
　　１．甲方：甲方的土地处置费＿＿＿＿＿美元，原有建筑物，构筑物和固定在建筑物上的设备作价＿＿＿＿＿美元，合计＿＿＿＿＿美元，作出出资。土地处置费和原有建筑物、构筑物及固定在建筑物上的设备的详情，见本合同附件一《甲方出资一览表》。<BR> 
　　２．乙方：以现金＿＿＿＿＿美元作为出资，乙方三家投资者的投资比例分别为：＿＿＿＿＿％，＿＿＿＿＿％，＿＿＿＿＿％。<BR> 
　　第十三条　甲乙双方根据以下规定向合营企业缴足全部出资额。<BR> 
　　１．甲方土地处置费＿＿＿＿＿美元，现有建筑物、构筑物及固定在建筑物上的设备作价＿＿＿＿＿美元。甲方应在合营企业和中国政府土地管理部门签订用地合同后＿＿＿＿＿天内将全部土地和现有建筑物、构筑物及固定在建筑物上的设备交付合营企业验收。<BR> 
　　２．乙方应分两批将其应缴足的注册资本现金＿＿＿＿＿美元汇入合营企业开立的银行帐户。第一批应于合营企业和中国政府土地管理部门签订用地合同后十五（１５）天内交付＿＿＿＿＿％的注册资本，计＿＿＿＿＿美元；<BR> 
　　第二批应于＿＿＿年＿＿＿月＿＿＿日之前交付＿＿＿＿＿％的注册资本，计＿＿＿＿＿美元。<BR> 
　　第十四条　甲乙任何一方未能在前条规定的期间内全部或部分履行出资义务，即构成违约。违约方需根据延误的时间和金额，按利率＿＿＿＿＿％／日向非违约方支付延误赔偿金。如超过期限＿＿＿＿＿个月仍未履行出资义务，非违约方可解除本合同，并有权要求违约方赔偿因违约而对非违约方造成的经济损失。<BR> 
　　第十五条　甲乙双方缴足出资额后，须由在中国注册的会计师验证并出具验资报告，并由合营企业发给董事长和副董事长签署的出资说明书。<BR> 
　　第十六条　合营企业所需的投资总额中，除本章规定的注册资本＿＿＿＿＿美元外，不足部分＿＿＿＿＿美元由合营企业另行筹资。<BR> 
　　第十七条　为筹措第十六条所列投资总额中不足部分的资金＿＿＿＿＿美元，合营企业委托＿＿＿＿＿银行牵头，＿＿＿＿＿银行为副牵头组织的国际银团贷款。<BR> 
　　投资总额如超过＿＿＿＿＿美元，合营企业可向上述国际银团申请接受建设费，（《可行性分析报告》中所列＿＿＿＿＿美元）的＿＿＿＿＿％为限度的备用信贷。<BR> 
　　如仍不足，合营企业在得到中国银行书面同意的情况下，可向其他银行申请接受以投资总额中未完成投资（投资总额扣除第九条第一款和第三款所指费用后，即《可行性分析报告》中所列的＿＿＿＿＿美元）的＿＿＿＿＿％（扣除前款所述建筑费的＿＿＿＿＿％的金额）为限度的借款。<BR> 
　　第十八条　合营企业接受贷款，在中国国家外汇管理局监督下进行。按＿＿＿＿＿银行牵头、组织的国际银团的贷款数额提供担保。合营企业将其全部资产提供给＿＿＿＿＿以作为上述担保的反担保。＿＿＿＿＿收取担保费。<BR> 
　　第十九条　贷款协议、担保协议和反担保协议应在合营企业成立后尽快签署。<BR> 
　　第二十条　甲乙任何一方如向第三者转让其全部或部分出资额，必须事先经另一方书面同意。<BR> 
　　第二十一条　甲乙任何一方在转让其全部或部分出资额时，另一方有权优先购买。但是一方提出转让时，另一方须在接到书面通知＿＿＿＿＿天内书面签复是否接受转让，如逾期未作出接受转让的答复，即视为放弃优先购买权。<BR> 
　　任何一方向第三者转让其出资额的条件，不能比向本合同另一方提出的条件优惠。<BR> 
　　违反上述条款规定之一的，其转让无效。<BR> 
　　第二十二条　合营企业注册资本的转让，须经董事会会议的通过或确认，并报原审批机构批准，向登记管理机构办理变更登记手续。<BR> 
　　第二十三条　甲方同意乙方在合营企业成立后，成立以＿＿＿＿＿为首的由＿＿＿＿＿、＿＿＿＿＿、＿＿＿＿＿组成的投资公司，如未发生乙方不履行本合同义务的情况，乙方可向该投资公司转让乙方出资者的资格或全部出资额。但是，该投资公司必须具有履行本合同，承担其合同规定的各项义务的能力。<BR> 
　　乙方应于转让前＿＿＿＿＿天书面通知甲方，并由合营企业报原审批机构批准。甲方要为尽快取得该项批准进行积极协助。<BR> 
　　如乙方不按上述方式进行转让，则必须要继续履行本合同规定的乙方的各项义务。<BR> 
