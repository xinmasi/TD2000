　　第五章　合营双方的责任<BR> 
　　<BR> 
　　第二十四条　合营双方除必须履行本合同其他条款所规定的义务外，还应负责协助办理下述事项：<BR> 
　　甲方：１．辅助合营企业向中国有关当局办理合营企业成立的申请批准、注册登记和领取营业执照等手续；<BR> 
　　２．协助合营企业同中国政府土地管理部门签订用地合同，取得土地使用权；向中国有关部门办理原有建筑物、构筑物及固定在建筑物上的设备所有权移交给合营企业的手续；<BR> 
　　３．负责提供新建建筑物和改造原有建筑物所必要的有关法规、数据和资料；<BR> 
　　４．在合营企业的经营管理机构成立之前，协助乙方办理有关外籍业务人员的入境、居留等手续；<BR> 
　　５．协助合营企业办理合营企业建设工程和经营中的水、电、煤气、暖气、通讯、道路等有关基础设施的建设及正常使用的联系事宜；<BR> 
　　６．协助合营企业办理建设工程和经营所必需从中国境外采购进口的机具、材料、设备、交通工具及其他用品的报关手续，在中国境内的运输和申报减免税手续等事项；<BR> 
　　７．协助合营企业办理招聘中国籍经营管理人员、技术人员和营业人员的事宜；<BR> 
　　８．协助合营企业就改造原有建筑物、新建筑物的方案设计和扩大初步设计事宜，尽快取得中国有关审批部门的批准；<BR> 
　　９．尽最大努力协助合营企业，在原有建筑物改造完成之时，新建筑土建工程完成之时，使其通过中国有关验收部门的峻工检查；<BR> 
　　１０．尽最大努力辅助合营企业取得＿＿＿＿＿银行牵头组织的国际银团的贷款，向中国国家外汇管理局办理贷款许可手续；<BR> 
　　１１．协助办理合营企业委托的其他有关事项。<BR> 
　　乙方：１．根据董事会决定的方针和计划，尽最大努力协助合营企业在中国境外联系以最优惠的价格采购或租用建设工程和经营所必须从中国境外进口的机具、材料、设备、交通工具及其他用品，并安排运抵指定的中国港口；<BR> 
　　２．根据合营企业的利益和需要，推荐和派遣有能力胜任和有合作精神的人员参加合营企业筹建和经营管理工作；<BR> 
　　３．尽最大努力协助合营企业为其经营管理人员、技术人员和经营人员在中国境外培训提供场所和一切必要的条件，或其他有关安排；<BR> 
　　４．辅助办理合营企业委托的其他有关事项。<BR> 
