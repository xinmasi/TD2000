　　<BR> 
　　第一条　会计总则<BR> 
　　１．１　此会计程序是＿＿＿＿＿（以下简称乙方）和＿＿＿＿＿（以下简称甲方）合资经营的＿＿＿＿＿（以下简称“公司”）的合营合同的附件，此会计程序规定条款的有效期限与合营合同一致。<BR> 
　　１．２　公司的会计制度和会计程序是公司业务的组成部分，同时使制造加工、利润管理和工程体系的采用更加合理。公司将采用＿＿＿＿＿方及其分支机构的会计制度和程序，以便充分利用吸收＿＿＿＿＿方及分支机构的管理经验、管理方法及现代化整体业务体系。<BR> 
　　１．３　公司会计的记录、报告等将完全依照中华人民共和国财政部一九八五年三月四日颁布的《中华人民共和国中外合营企业的会计制度》中有关规则执行。<BR> 
　　１．４　会计记帐应以中文和英文同时记帐，公司的月报、季报和年报以及所有记帐凭证、帐簿、报表表头和这些文件报表的标题均应同时使用中文和英文。<BR> 
　　１．５　公司将采用人民币为簿记记帐的基本货币。<BR> 
　　１．６　公司经营所需的经营资本和消耗资金应反映在董事会批准的预算中来。总经理将有权根据批准的预算安排使用中国银行的贷款以及要求乙方和甲方共同分缴公司的注册资本。<BR> 
　　１．７　经费超出或经营预算以外的资金支出将由董事会批准采取有关策略和会计程序加以处理。<BR> 
　　第二条　资本支付的计算<BR> 
　　甲方转入合营企业公司的制造设备的价值将按合营公司收到财产日登记入册的单价值再加＿＿＿＿＿％来计算。<BR> 
　　第三条　现金和往来帐户的计算<BR> 
　　３．１　在记帐过程中，外汇转换成人民币时，应以国家外汇管理局公布的当月第一天的报价为准。<BR> 
　　３．２　帐面汇率将按先进先出法计算。<BR> 
　　第四条　财产盘存的计算<BR> 
　　４．１　财产盘存科目的计算将采用后进先出法。<BR> 
　　４．２　公司各种材料、设备和其他物资的收、发和退还应按手续办理。<BR> 
　　第五条　固定资产的计算<BR> 
　　５．１　公司的固定资产应是指设备器材、机床工具、厂房及各种建筑物等，其使用期限为一年以上，按中国财政部门的规定计算。<BR> 
　　折旧期应以中华人民共和国中外合资企业所得税法所允许的最小期限为准。<BR> 
　　５．２　合营企业制定适当的程序以批准公司拥有的或租用的固定资产的增加及转让，这一程序需经董事会批准。<BR> 
　　第六条　无形资产和其他资产的计算<BR> 
　　６．１　技术转让许可证费应于转让许可证协议初期较短的时间内或＿＿＿＿＿年期限内摊销完毕。<BR> 
　　６．２　筹建费用应在＿＿＿＿＿年期限内摊销完毕。<BR> 
　　第七条　成本和费用的计算<BR> 
　　公司将以契约的形式以确定成本分类核算制，这一制度应在合同的主件和附件中写明。销售费、一般管理费和非经营费或非经营收入将被视为日常费用或日常收入并将不予核查或分配入承包成本。<BR> 
　　第八条　销售和利润的核算<BR> 
　　８．１　合同规定销售记录和销售成本的计算应采用全部完工法。<BR> 
　　８．２　公司将根据税后净收入提取储备基金、发展基金、职员和工人的奖金以及福利基金。三种基金的总数一般应当超过税后净收入的＿＿＿＿＿％。若有特殊的提取比例应由董事长决定。<BR> 
　　８．３　总经理在财政年度结算后的２个月内准备出利润分配方案并将方案提交董事会审查，批准并贯彻执行。<BR> 
　　第九条　帐户分类和会计报表<BR> 
　　９．１　未经审查的合营企业的会计报表，应于次月１０日前发送管理者和股东手中。<BR> 
　　９．２　送交乙方的会计报表和会计报告应采用美元和人民币同时表示。公司将按＿＿＿＿＿标准报告提供给乙方。<BR> 
　　９．３　送交股东和管理者手中的会计报表应包括预算和实际变化的比较。重大的变化应由总经理在呈送的补充报告中加以说明。<BR> 
