　　第一条　定义<BR> 
　　除因特殊需要在本合同上下文另有明确含义外，下列各词词语在本合同中的定义如下：<BR> 
　　１．１　公司是指甲乙双方合资经营的＿＿＿＿＿公司。<BR> 
　　１．２　专有技术（Ｋｎｏｗ－Ｈｏｗ）是指＿＿＿＿＿方从获得的按技术转让和许可证合同转让给公司的，为设计、生产、制造和销售本公司产品，以及为改进本公司这些产品进行技术改造所需的一切专有技术、知识、经验的技能。它包括技术资料、图纸、试验方法、试验报告、制造工艺、设备说明书、质量控制、计算机程序与应用、安装与调试方法、企业管理、销售、技术服务和＿＿＿＿＿方通过其关联公司派遣的技术人员、管理人员与工人所掌握的各种经验、知识和技巧。<BR> 
　　１．３　专利（Ｐａｔｅｎｔ）是指＿＿＿＿＿方从其关联公司得到的，以＿＿＿＿＿方在＿＿＿＿＿国和其他国家已获取专利和将依据技术转让许可证合同转让给公司的发明。<BR> 
　　１．４　合同产品是指公司按本合同附件中所列的要求而设计、生产、制造、安装和调试的电站锅炉、工业锅炉和有关产品。<BR> 
　　１．５　工业锅炉是指压力小于＿＿＿＿＿公斤／平方厘米，容量小于＿＿＿＿＿吨／小时的蒸汽锅炉和不同容量等级的热水锅炉。<BR> 
　　１．６　电站锅炉是指容量大小或等于＿＿＿＿＿ＭＷ，用于发电的锅炉。<BR> 
　　１．７　签字日期是指合资经营双方正式签订本合同的日期。<BR> 
　　１．８　批准日期是指中华人民共和国对外经济贸易主管部门正式批准本合同的日期。<BR> 
　　１．９　成立日期是指在上述当局批准后，由工商行政管理局办理，注册登记，签发本公司营业执照的日期。<BR> 
