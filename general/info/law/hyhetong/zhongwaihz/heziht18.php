      <p> 　　<BR> 
　　（１）这是为拟设立的中外合资企业提供的章程参考格式。合资企业应根据合资企业的合同及合资企业经营管理的具体要求和条件填写，或增减或改写有关条款。<BR> 
　　（２）中外合资经营企业章程参考格式：<BR> 
　　<BR> 
　　中外合资经营企业章程（参考格式）<BR> 
　　<BR> 
　　第一章　总则<BR> 
　　<BR> 
　　第一条　根据《中华人民共和国中外合资经营企业法》，中国＿＿公司（以下简称甲方）与＿＿国＿＿公司（以下简称乙方）于＿＿年＿＿月＿＿日在中国＿＿＿＿签订的建立合资经营＿＿＿＿有限责任公司合同（以下简称合营公司），制订本公司章程。<BR> 
　　第二条　合营公司名称为＿＿＿＿有限责任公司。<BR> 
　　外文名称为：<BR> 
　　合营公司的法定地址为：＿＿省＿＿市＿＿路＿＿号。<BR> 
　　第三条　甲、乙双方的名称、法定的地址为：<BR> 
　　甲方：中国＿＿公司<BR> 
　　＿＿省＿＿市＿＿路＿＿号。<BR> 
　　乙方：＿＿国＿＿公司<BR> 
　　＿＿国＿＿。<BR> 
　　第四条　合营公司为有限责任公司。<BR> 
　　第五条　合营公司为中国法人，受中国法律管辖和保护。其一切活动必须遵守中国的法律、法令和有关条例规定。<BR> 
　　<BR> 
　　第二章　宗旨、经营范围<BR> 
　　<BR> 
　　第六条　合营公司宗旨为：使用先进技术，生产和销售＿＿产品，达到＿＿水平，获取甲乙双方满意的经济利益。（注：每个合营企业都可以根据自己的特点写。）<BR> 
　　第七条　合营公司经营范围为：设计、制造和销售＿＿产品以及对销售后的＿＿产品进行维修服务。<BR> 
　　第八条　合营公司生产规模为：<BR> 
　　＿＿年＿＿（表示量的单位）<BR> 
　　＿＿年＿＿<BR> 
　　＿＿年＿＿<BR> 
　　第九条　合营公司向国内、外市场销售其产品，其销售比例如下：<BR> 
　　＿＿年：出口占百分之＿＿；<BR> 
　　中国内销售占百分之＿＿。<BR> 
　　＿＿年：出口占百分之＿＿；<BR> 
　　中国内销售占百分之＿＿。<BR> 
　　（注：销售渠道、方法、责任可根据各自情况而定。）<BR> 
　　<BR> 
　　第三章　投资总额和注册资本<BR> 
　　<BR> 
　　第十条　合营公司的投资总额为人民币＿＿元。<BR> 
　　合营公司注册资本为人民币＿＿元。<BR> 
　　第十一条　甲、乙方出资如下：<BR> 
　　甲方：认缴出资额为人民币＿＿元，占注册资本百分之＿＿。<BR> 
　　其中：现金＿＿元；<BR> 
　　机械设备＿＿元；<BR> 
　　厂房＿＿元；<BR> 
　　土地使用权＿＿元；<BR> 
　　工业产权＿＿元；<BR> 
　　其它＿＿元。<BR> 
　　乙方：认缴出资额为人民币＿＿元，占注册资本百分之＿＿。<BR> 
　　其中：现金＿＿元；<BR> 
　　机械设备＿＿元；<BR> 
　　工业产权＿＿元；<BR> 
　　其它＿＿元。<BR> 
　　第十二条　甲、乙方应按合同规定的期限缴清各自出资额。<BR> 
　　第十三条　甲、乙方缴付出资额后，经合营公司聘请在中国注册的会计师验资，出具验资报告后，由合营公司据此发给出资证明书。出资证明书主要内容是：合营公司名称、成立日期、合营者名称及出资额、出资日期，发给出资证明书日期等。<BR> 
　　第十四条　合营期内，合营公司不得减少注册资本数额。<BR> 
　　第十五条　任何一方转让其出资额，不论全部或部分，都须经另一方同意。一方转让时，另一方有优先购买权。<BR> 
　　第十六条　合营合同注册资本的增加、转让，应由董事会一致通过后，并报原审批机构批准，向原登记机构办理变更登记手续。<BR> 
　　<BR> 
　　第四章　董事会<BR> 
　　<BR> 
　　第十七条　合营公司设董事会，董事会是合营公司的最高权力机构。<BR> 
　　第十八条　董事会决定合营公司的一切重大事宜，其职权主要如下：<BR> 
　　－－决定和批准总经理提出的重要报告；<BR> 
　　（如生产规划、年度营业报告、资金、借款等）<BR> 
　　－－批准年度财务报表、收支预算、年度利润分配方案；<BR> 
　　－－通过公司的重要规章制度；<BR> 
　　－－决定设立分支机构；<BR> 
　　－－修改公司规章；<BR> 
　　－－讨论决定合营公司停产、终止或与另一个经济组织合并；<BR> 
　　－－负责合营公司终止和期满时的清算工作；<BR> 
　　－－其它应由董事会决定的重大事宜。<BR> 
　　第十九条　董事会由＿＿名董事组成，其中甲方委派＿＿名，乙方委派＿＿名。董事任期为四年，可以连任。<BR> 
　　第二十条　董事会董事长由甲、乙双方协商确定或由董事会选举产生。<BR> 
　　第二十一条　甲、乙方在委派和更换董事人选时，应书面通知董事会。<BR> 
　　第二十二条　董事会例会每年召开＿＿次。经三分之一以上的董事提议，可以召开董事临时会议。<BR> 
　　第二十三条　董事会会议原则上在公司所在地举行。<BR> 
　　第二十四条　董事会会议由董事长召集并主持，董事长缺席时由副董事长召集并主持。<BR> 
　　第二十五条　董事长应在董事会开会前三十天书面通知各董事，写明会议内容、时间和地点。<BR> 
　　第二十六条　董事因故不能出席董事会会议，可以书面委托代理人出席董事会。<BR> 
　　第二十七条　出席董事会会议的法定人数为全体董事的三分之二，不够三分之二人数时，通过的决议无效。<BR> 
　　第二十八条　董事会每次会议，须作详细的书面记录，并由全体出席董事签字，代理人出席时，由代理人签字。<BR> 
　　第二十九条　下列事项须董事会一致通过。<BR> 
　　（注：每个合营企业可根据各自情况而定。）<BR> 
　　第三十条　下列事项须董事会三分之二以上董事或过半数董事通过。<BR> 
　　（注：每个合营企业可根据各自情况而定。）<BR> 
　　<BR> 
　　第五章　经营管理机构<BR> 
　　<BR> 
　　第三十一条　合营公司设经营管理机构，下设生产、技术、销售、财务、行政等部门。（注：根据具体情况写。）<BR> 
　　第三十二条　合营公司设总经理一人，副总经理＿＿人，正、副总经理由董事会聘请。<BR> 
　　第三十三条　总经理直接对董事会负责，执行董事会的各项决定，组织领导合营公司的日常生产、技术和经营管理工作。副总经理协助总经理工作，当总经理不在时，代理行使总经理的职责。<BR> 
　　第三十四条　合营公司日常工作中重要问题的决定，应由总经理和副总经理联合签署方能生效。需要联合签署的事项，由董事会具体规定。<BR> 
　　第三十五条　总经理、副总经理的任期为＿＿年。经董事会聘请，可以连任。<BR> 
　　第三十六条　董事长或副董事长、董事经董事会聘请，可兼任合营公司总经理、副总经理及其他高级职员。<BR> 
　　第三十七条　总经理、副总经理不得兼任其它经济组织的总经理或副总经理、不得参与其它经济组织对本合营公司的商业竞争行为。<BR> 
　　第三十八条　合营公司设总工程师、总会计师和审计师各一人，由董事会聘请。<BR> 
　　第三十九条　总工程师、总会计师、审计师由总经理领导。<BR> 
　　总会计师负责领导合营公司的财务会计工作，组织合营公司开展全面经济核算，实行经济责任制。<BR> 
　　审计师负责合营公司的财务审计工作，审查稽核合营公司的财务收支和会计帐目，向总经理并向董事会提出报告。<BR> 
　　第四十条　总经理、副总经理、总工程师、总会计师、审计师和其他高级职员请求辞职时，应提前向董事会提出书面报告。<BR> 
　　以上人员如有营私舞弊或严重失职行为的，经董事会决议，可随时解聘。如触犯刑法的，要依法追究刑事责任。<BR> 
　　<BR> 
　　第六章　财务会计<BR> 
　　<BR> 
　　第四十一条　合营公司的财务会计按照中华人民共和国财政部制定的中外合资经营企业财务会计制度规定办理。<BR> 
　　第四十二条　合营公司会计年度采用日历年制，自一月一日起至十二月三十一日止为一个会计年度。<BR> 
　　第四十三条　合营公司的一切凭证、帐簿、报表，用中文写。<BR> 
　　第四十四条　合营公司采用人民币为记帐本位币。人民币同其它货币折算，按实际发生之日中华人民共和国国家外汇管理局公布汇价计算。<BR> 
　　第四十五条　合营公司在中国银行或中国银行同意的的其它银行开立人民币及外币帐户。<BR> 
　　第四十六条　合营公司采用国际通用的权责发生制和借贷记帐法记帐。<BR> 
　　第四十七条　合营公司财务会计帐册上应记载如下内容：<BR> 
　　一、合营公司所有的现金收入、支出数量；<BR> 
　　二、合营公司所有的物资出售及购入情况；<BR> 
　　三、合营公司注册资本及负债情况；<BR> 
　　四、合营公司注册资本的缴纳时间、增加及转让情况。<BR> 
　　第四十八条　合营公司财务部门应在每一个会计年度头三个月编制上一个会计年度的资产负债表和损益计算书，经审计师审核签字后，提交董事会会议通过。<BR> 
　　第四十九条　合营各方有权自费聘请审计师查阅合营公司帐簿。查阅时，合营公司应提供方便。<BR> 
　　第五十条　合营公司按照《中华人民共和国中外合资经营企业税法施行细则》的规定，由董事会决定其固定资产的折旧年限。<BR> 
　　第五十一条　合营公司的一切外汇事宜，按照《中华人民共和国外汇管理暂行条例》和有关规定以及合营合同的规定办理。<BR> 
　　<BR> 
　　第七章　利润分配<BR> 
　　<BR> 
　　第五十二条　合营公司从缴纳所得税后的利润中提取储备基金、企业发展基金、职工奖励及福利基金。提取的比例由董事会确定。<BR> 
　　第五十三条　合营公司依法缴纳所得税和提取各项基金后的利润，按照甲、乙方在注册资本中的出资比例进行分配。<BR> 
　　第五十四条　合营公司每年分配利润一次。每个会计年度后三个月内颁布利润分配方案及各方应分的利润额。<BR> 
　　第五十五条　合营公司上一个会计年度亏损未弥补前不得分配利润。上一个会计年度未分配的利润，可并入本会计年度利润分配。<BR> 
　　<BR> 
　　第八章　职工<BR> 
　　<BR> 
　　第五十六条　合营公司职工的招收、招聘、辞退、辞职、工资、福利、劳动保险、劳动保护、劳动纪律等事宜，按照《中华人民共和国中外合资经营企业劳动管理规定》及其实施办法办理。<BR> 
　　第五十七条　合营公司所需要的职工，可以由当地劳动部门推荐，或者经劳动部门同意后，由合营公司公开招收，但一律通过考试，择优录用。<BR> 
　　第五十八条　合营公司有权对违犯合营公司的规章制度和劳动纪律的职工，给予警告、记过、降薪的处分，情节严重，可予以开除。开除职工须报当地劳动人事部门备案。<BR> 
　　第五十九条　职工的工资待遇，参照中国有关规定，根据合营公司具体情况，由董事会确定，并在劳动合同中具体规定。<BR> 
　　合营公司随着生产的发展，职工业务能力和技术水平的提高，适当提高职工的工资。<BR> 
　　第六十条　职工的福利、奖金、劳动保护和劳动保险等事宜，合营公司将分别在各项制度中加以规定，确保职工在正常条件下从事生产和工作。<BR> 
　　<BR> 
　　第九章　工会组织<BR> 
　　<BR> 
　　第六十一条　合营公司职工有权按照《中华人民共和国工会法》的规定，建立工会组织，开展工会活动。<BR> 
　　第六十二条　合营公司工会是职工利益的代表，它的任务是：依法维护职工的民主权力和物质利益；协助合营公司安排和合理使用福利、奖励基金；组织职工学习政治、业务、科学、技术知识，开展文艺、体育活动；教育职工遵守劳动纪律，努力完成合营公司的各项经济任务。<BR> 
　　第六十三条　合营公司工会代表职工和合营公司签订劳动合同，并监督合同的执行。<BR> 
　　第六十四条　合营公司工会负责人有权列席有关讨论合营公司的发展规划、生产经营活动等问题的董事会会议，反映职工的意见和要求。<BR> 
　　第六十五条　合营公司工会参加调解职工和合营公司之间发生的争议。<BR> 
　　第六十六条　合营公司每月按合营公司职工实际工资总额的百分之二拨交工会经费。合营公司工会按照中华全国总工会制定的《工会经费管理办法》使用工会经费。<BR> 
　　<BR> 
　　第十章　期限、终止、清算<BR> 
　　<BR> 
　　第六十七条　合营期限为＿＿年。自营业执照签发之日起计算。<BR> 
　　第六十八条　甲、乙方如一致同意延长合营期限，经董事会会议作出决议，应在合营期满前六个月内原审批机构提交书面申请，经批准后方能延长，并向原登记机构办理变更登记手续。<BR> 
　　第六十九条　甲、乙方如一致认为终止合营符合各方最大利益时，可提前终止合营。合营公司提前终止合营，需董事会召开全体会议作出决定并报原审批机构批准。<BR> 
　　第七十条　发生下列情况之一时，甲、乙任何一方有权依法终止合营。<BR> 
　　（注：每个合资企业可根据自己的情况而定。）<BR> 
　　第七十一条　合营期满或提前终止合营时，董事会应提出清算程序、原则和清算委员会人选，组成清算委员会，对合营公司财产进行清算。<BR> 
　　第七十二条　清算委员会任务是对合营公司的财产、债权、债务进行全面清查，编制资产负债表和财产目录，制定清算方案，提请董事会通过后执行。<BR> 
　　第七十三条　清算期间，清算委员会代表公司起诉或应诉。<BR> 
　　第七十四条　清算费用和清算委员会成员的酬劳应从合营公司现存财产中优先支付。<BR> 
　　第七十五条　清算委员会对合营公司的债务全部清偿后所剩余的财产按甲、乙方在注册资本中的出资比例进行分配。<BR> 
　　第七十六条　清算结束后，合营公司应向审批机构提出报告，并向原登记机构办理注销登记手续，缴回营业执照，同时对外公告。<BR> 
　　第七十七条　合营公司结业后，其各种帐册，由甲方保存。<BR> 
