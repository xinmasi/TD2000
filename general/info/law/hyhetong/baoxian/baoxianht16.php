      <p> 　　<BR> 
　　为了保障少年儿童健康成长，协助家长为其子女筹集教育、婚嫁资金，并在其子女遭受意外事故时，能得到一定的经济补偿，特举办本保险。<BR> 
　　<BR> 
　　第一章　投保条件<BR> 
　　<BR> 
　　第一条　凡二十一周岁至五十周岁的家长（投保人），均可为其一周岁至十五周岁、身体健康的子女（被保险人）向保险公司（保险人）投保儿童保险。但对投保时，身体不健康，不能正常工作和劳动的投保人，不适用本条款第七条的规定。<BR> 
　　投保人如发生变动时，应及时通知保险人，经保险人核准后，方可办理更改手续，否则，保险人不承担保险责任。<BR> 
　　<BR> 
　　第二章　保险期限和保险责任<BR> 
　　<BR> 
　　第二条　保险期限从被保险人起保时起至二十二周岁期满时止，分别为七至二十一年。<BR> 
　　第三条　保险人对被保险人负有以下保险责任：<BR> 
　　１．被保险人在保险有效期内因意外伤害事故而残废，保险人按意外伤害保险金额拨付全部或部分保险金（见附表一），但年度给付金额不得超过意外伤害保险金额。<BR> 
　　２．被保险人在保险有效期内因意外伤害事故而死亡，保险人按意外伤害保险金额给付全部保险金，同时给付死亡退保金（见附表二），保险责任即告终止。<BR> 
　　３．被保险人在保险有效期内因疾病死亡，保险人给付死亡退保金，保险责任即告终止。<BR> 
　　４．被保险人在保险有效期内考取全日制高等院校的本科生和大专生时，保险人每年按注册证明给付约定的教育金，给付期限以被保险人年满二十二周岁为限。<BR> 
　　５．被保险人生存至保险期满，保险人给付婚嫁保险金（见附表三），保险责任即告终止。<BR> 
　　<BR> 
　　第三章　除外责任<BR> 
　　<BR> 
　　第四条　保险人对下列情况不负给付保险金的责任。<BR> 
　　１．投保人对投保条件有隐瞒、欺骗或违约行为。<BR> 
　　２．由于投保人、被保险人的故意行为或犯罪行为造成被保险人死亡或残废。<BR> 
　　３．由于被保险人打架、斗殴、酗酒自杀造成死亡或残废。<BR> 
　　４．由于战争或敌对行为造成被保险人死亡或残废。<BR> 
　　５．被保险人在中华人民共和国境外发生的死亡或残废。<BR> 
　　６．因各种原因造成医疗费支出。<BR> 
　　７．其他不属于保险责任范围内的事情。<BR> 
　　<BR> 
　　第四章　保险费与保险金额<BR> 
　　<BR> 
　　第五条　保险费每月分五元、十元、十五元、二十元四档，由投保人在投保时选定，按月缴付保险费，缴费期限与保险期限同样。<BR> 
　　第六条　月交保险费五元、十元、十五元、二十元者，年度意外伤害保险金额分别为人民币一千元、二千元、三千元、四千元，年度教育金分别为一百元、二百元、三百元、四百元，被保险人参加本保险时的年龄超过十足岁（含十足岁者），教育金减半。<BR> 
　　第七条　在保险期内投保人夫妇均因意外伤害事故身亡或自保险生效之日起二周年后因疾病死亡，可由被保险人或其监护人持保险证及必要证件向保险人申请，经保险人调查核实后，从次月起，可免交保险费全数，如果投保人夫妇之一发生上述事故时，可免交保险费半数。<BR> 
　　<BR> 
　　第五章　生效　失效　复效　退保<BR> 
　　<BR> 
　　第八条　保险单从起保当月的一日起期，但须在投保人交付第一期保险费后，保险单才开始生效。<BR> 
　　第九条　投保人如未按规定交付保险费，并逾期一月未办补交手续的，保险单便自动失效，保险人不负给付保险金的责任，但可退还生存退保金（见附表四）。<BR> 
　　第十条　在保险单失效后两年内，投保人可以提出复效申请，经保险人审核同意并由投保人补交失效期间的保险费及其利息后，保险单恢复效力。<BR> 
　　第十一条　申请退保时，保险单必须期满二年且按规定交足保费，凡符合申请条件而要求退保者，保险人按规定退给其生存退保金。<BR> 
　　<BR> 
　　第六章　保险金的申请和给付<BR> 
　　<BR> 
　　第十二条　被保险人在保险有效期间发生保险责任范围内的死亡或残废时，投保人应持保险单及时向保险人提出保险金给付申请，并提供下列证明：<BR> 
　　１．被保险人死亡时应提供死亡证明书。<BR> 
　　２．被保险人因意外伤害者造成残废时，应提供县级以上医院出具的残废程度证明。<BR> 
　　保险人在接到申请后，经过调查核实，按规定给付保险金，如果从伤亡事故发生日起经过二周年不提出申请，即作为自动放弃权益。<BR> 
　　第十三条　被保险人生存至保险期满，投保人或被投保人可持保险单及本人身份证明向保险人申请领取婚嫁金，保险责任自期满之日起终止。<BR> 
