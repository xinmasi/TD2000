      <p> 　　<BR> 
　　一、保险财产<BR> 
　　保险财产指在本保险单明细表中列明的财产及费用。<BR> 
　　经被保险人特别申请，并经本公司书面同意，下列物品及费用经专业人员或公估部门鉴定并确定价值后，亦可作为保险财产。<BR> 
　　（一）金银、珠宝、钻石、玉器；<BR> 
　　（二）古玩、古币、古书、古画；<BR> 
　　（三）艺术作品、邮票；<BR> 
　　（四）建筑物上的广告、天线、霓虹灯、太阳能装置等；<BR> 
　　（五）计算机资料及其制作、复制费用。<BR> 
　　下列物品一律不得作为保险财产：<BR> 
　　（一）枪支弹药、爆炸物品；<BR> 
　　（二）现钞、有价证券、票据、文件、档案、帐册、图纸；<BR> 
　　（三）动物、植物、农作物；<BR> 
　　（四）便携式通讯装置、电脑设备、照相摄像器材及其他贵重物品；<BR> 
　　（五）用于公共交通的车辆。<BR> 
　　二、责任范围<BR> 
　　在本保险期限内，若本保险单明细表中列明的保险财产因以下列明的风险造成的直接物质损坏或灭失（以下简称“损失”），本公司同意按照本保险单的规定负责赔偿。<BR> 
　　（一）火灾；<BR> 
　　（二）爆炸，但不包括锅炉爆炸；<BR> 
　　（三）雷电；<BR> 
　　（四）飓风、台风、龙卷风；<BR> 
　　（五）风暴、暴雨、洪水<BR> 
　　但不包括正常水位变化、海水倒灌及水库、运河、堤坝在正常水位线以下的排水和渗漏，亦不包括由于风暴、暴雨或洪水造成存放在露天或使用芦席、蓬布、茅草、油毛毡、塑料膜或尼龙等作罩棚或覆盖的保险财产的损失；<BR> 
　　（六）冰雹；<BR> 
　　（七）地崩、山崩、雪崩；<BR> 
　　（八）火山爆发；<BR> 
　　（九）地面下陷下沉，但不包括由于打桩、地下作业及挖掘作业引起的地面下陷下沉；<BR> 
　　（十）飞机坠毁、飞机部件或飞行物体坠落；<BR> 
　　（十一）水箱、水管爆裂，但不包括由于锈蚀引起水箱、水管爆裂。<BR> 
　　三、除外责任<BR> 
　　本公司对下列各项不负责赔偿：<BR> 
　　（一）被保险人及其代表的故意行为或重大过失引起的任何损失和费用；<BR> 
　　（二）地震、海啸引起的损失和费用；<BR> 
　　（三）贬值、丧失市场或使用价值等其他后果损失；<BR> 
　　（四）战争、类似战争行为、敌对行为、武装冲突、恐怖活动、谋反、政变、罢工、暴动、民众骚乱引起的损失和费用；<BR> 
　　（五）政府命令或任何公共当局的没收、征用、销毁或毁坏；<BR> 
　　（六）核裂变、核聚变、核武器、核材料、核幅射以及放射性污染引起的任何损失和费用；<BR> 
　　（七）大气、土地、水污染及其他各种污染引起的任何损失和费用，但不包括由于本保险单第二条责任范围列明的风险造成的污染引起的损失；<BR> 
　　（八）本保险单明细表或有关条款中规定的应由被保险人自行负担的免赔额；<BR> 
　　（九）其他不属于本保险单第二条责任范围列明的风险引起的损失。<BR> 
　　四、赔偿处理<BR> 
　　（一）如果发生本保险责任范围内的损失，本公司选择下列方式赔偿：<BR> 
　　１．按受损财产的价值赔偿；<BR> 
　　２．赔付受损财产基本恢复原状的修理、修复费用；<BR> 
　　３．修理、恢复受损财产，使之达到与同类财产基本一致的状况。<BR> 
　　（二）受损财产的赔偿按损失当时的市价计算。市价低于保险金额时，赔偿按市价计算；市价高于保险金额时，赔偿按保险金额与市价的比例计算。如本保险所载项目不止一项时，赔款按本规定逐项计算。<BR> 
　　（三）保险项目发生损失后，如本公司全部损失赔付，其残值应在赔款中扣除，本公司有权不接受被保险人对受损财产的委付。<BR> 
　　（四）任何属于成对或成套的项目，若发生损失，本公司的赔偿责任不超过该受损项目在所属整对或整套项目的保险金额中所占的比例。<BR> 
　　（五）发生损失后，被保险人为减少损失而采取必要措施所产生的合理费用，本公司可予以赔偿，但本项费用以保险财产的保险金额为限。<BR> 
　　（六）本公司赔偿损失后，由本公司出具批单将保险金额从损失发生之日起相应减少，并且不退还保险金额减少部分的保险费。如被保险人要求恢复至原保险金额，应按约定的保险费率加缴恢复部分从损失发生之日起至保险期限终止之日止按日比例计算的保险费。<BR> 
　　（七）被保险人的索赔期限，从损失发生之日起，不得超过两年。<BR> 
　　五、被保险人的义务<BR> 
　　被保险人及其代表应严格履行下列义务：<BR> 
　　（一）投保时，被保险人及其代表应对投保申请书列明的事项以及本公司提出的其他事项作真实、详尽的说明或描述；<BR> 
　　（二）被保险人及其代表应根据本保险单明细表和批单中的规定按期缴付保险费；<BR> 
　　（三）在保险期限内，被保险人应采取一切合理预防措施，包括认真考虑并付诸实施本公司代表提出的合理的防损建议，由此产生的一切费用，均由被保险人承担；<BR> 
　　（四）在发生引起或可能引起本保险单项下索赔的损失时，被保险人及其代表应：<BR> 
　　１．立即通知本公司，并在七天或经本公司书面同意延长的期限内以书面提供事故发生的经过，原因和损失程序；<BR> 
　　２．采取一切必要措施防止损失的进一步扩大并将损失减少到最低程序；<BR> 
　　３．在本公司的代表或检验师进行勘查之前，保留事故现场及有关实物证据；<BR> 
　　４．根据本公司的要求提供作为索赔依据的所有证明文件、资料和单据。<BR> 
　　六、总则<BR> 
　　（一）保单效力<BR> 
　　被保险人严格地遵守和履行本保险单的各项规定，是本公司在本保险单项下承担赔偿责任的先决条件。<BR> 
　　（二）保单无效<BR> 
　　如果被保险人或其代表漏报、错报、虚报或隐瞒有关本保险的实质性内容，则本保险单无效。<BR> 
　　（三）保单终止<BR> 
　　除非经本公司书面同意，本保险单将在下列情况下自动终止：<BR> 
　　１．被保险人丧失保险利益；<BR> 
　　２．承保风险扩大。<BR> 
　　本保险单终止后，本公司将按日比例退还被保险人本保险单项下未到期部分的保险费。<BR> 
　　（四）保单注销<BR> 
　　被保险人可随时书面申请注销本保险单，本公司亦可提前十五天通知被保险人注销本保险单。对本保险单已生效期间的保险费，前者本公司按短期费率计收，后者按日比例计收。<BR> 
　　（五）权益丧失<BR> 
　　如果任何索赔含有虚假成分，或被保险人或其代表在索赔时采取欺诈手段企图在本保险单项下获取利益，或任何损失是由被保险人或其代表的故意行为或纵容所致，被保险人将丧失其在本保险单项下的所有权益。对由此产生的包括本公司已支付的赔偿在内的一切损失，应由被保险人负责赔偿。<BR> 
　　（六）合理查验<BR> 
　　本公司的代表有权在任何适当的时候对保险财产的风险情况进行现场查验。被保险人应提供一切便利及本公司要求的用以评估有关风险的详情和资料。但上述查验并不构成本公司对被保险人的任何承诺。<BR> 
　　（七）重复保险<BR> 
　　本保险单负责赔偿损失、费用或责任时，若另有其他保障相同的保险存在，不论是否由被保险人或他人以其名义投保，也不论该保险赔偿与否，本公司仅负责按比例分摊赔偿的责任。<BR> 
　　（八）权益转让<BR> 
　　若本保险单项下负责的损失涉及其他责任方时，不论本公司是否已赔偿被保险人，被保险人应立即采取一切必要的措施行使或保留向该责任方索赔的权利。在本公司支付赔偿后，被保险人应将向该责任方追偿的权利转让给本公司，移交一切必要的单证，并协助本公司向责任方追偿。<BR> 
　　（九）争议处理<BR> 
　　被保险人与本公司之间的一切有关本保险的争议应通过友好协商解决。如果协商不成，可申请仲裁可向法院提出诉讼。除事先另有协议外，仲裁或诉讼应在被告方所在地进行。<BR> 
