　　２．说明<BR> 
　　<BR> 
　　申请公证要按照法定标准向公证机关缴纳费用。《公证暂行条例》第２２条规定：“公证处办理公证事务，应当按规定收费。”公证费的计费方式主要实行两种，一是按件收费，如一般的民事公证每件收费１０元；二是按标的收费，如经济合同法公证。根据规定，公证费属于国家财政收入的组成部分，公证机关不能随意乱收或减、免，但对于特殊情况，当事人交费确有困难的，可以向公证机关申请减、免。凡是申请减、免公证费的，当事人应当提出书面申请，讲明申请减免的理由和申请减、免的金额；公证处收到申请后，由公证处主任或副主任决定是否减收或免收公证费。申请减、免公证费应注意的问题有：①减收公证费和免收公证费是两种不同的行为，一个当事人只能提出其中一种请求。②减、免公证费要有充分的理由，并提交足够的证明。下列公证事项当事人可以申请减免公证费用：①办理抚恤金、劳动保险证明的；②办理养老金、子女助学金证明的；③当事人所在的工作单位、城市街道办事处或者乡、镇人民政府证明当事人确实经济困难无力负担的；④其他特殊情况。<BR> 
