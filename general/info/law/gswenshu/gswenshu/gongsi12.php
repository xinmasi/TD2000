          </span></u>股份有限公司筹委会（以下简称股份公司）的委托，担任贵公司的特聘法律顾问，就发起设立<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股份有限公司事宜进行审查并出具法律意见书。</p>
        <p >本所律师根据《中华人民共和国公司法》（以下简称《公司法》）以及其它有关法律、法规及行政规章的规定，就发起设立<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股份有限公司的合法性发表法律意见。</p>
        <p >本所律师是根据对事实的了解和对有关法律、法规的理解出具体法律意见书，而且依赖于以下前提：</p>
        <p >1．股份公司已向本所律师提供了一切应予提供的文件资料；</p>
        <p >2．股份公司向本所律师承诺，提供的资料文件为真实、准确，且不存在重大遗漏或重大误导之处。</p>
        <p >本法律意见书，仅供以发起设立方式设立<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股份有限公司申报批准时使用，非经本所同意，不得用于其他目的。</p>
        <p class=MsoBodyTextIndent>本所律师根据中国有关法律、法规，按照律师行业公认的业务标准、道德规范就有关问题发表法律意见如下：</p>
        <p >股份公司各发起人的主体资格</p>
        <p ><u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年<u><span>&nbsp;&nbsp;&nbsp; </span></u>月<u><span>&nbsp;&nbsp;&nbsp; </span></u>日，<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>（集团）有限责任公司（以下简称集团公司）与<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司等五家发起人签订了发起人协议，就共同出资以发起方式设立股份公司的有关事宜达成一致。</p>
        <p >股份公司各发起人具体情况如下：</p>
        <p >1．股份公司的主发起人是集团公司，集团公司是<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市的国有独资有限责任公司，其经营范围为：<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>。现持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>省工商行政管理局颁发的注册号为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>的《企业法人营业执照》。</p>
        <p >2．股份公司第二发起人是<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂，是隶属于<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>市<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span></u>乡的集体企业，其经营范围为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>。现持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县工商行政管理局颁发的注册号为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>的《企业法人营业执照》。</p>
        <p >3．股份公司第三发起人是<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂，是隶属于<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>市<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span></u>乡的集体企业，其经营范围为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>。现持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县工商行政管理局颁发的注册号为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>的《企业法人营业执照》。</p>
        <p >4．股份公司第四发起人是<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司，是一家隶属于<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县的国有有限责任公司，其经营范围为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>。现持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>肥工商行政管理局颁发的注册号为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>的《企业法人营业执照》。</p>
        <p >5．股份公司第五发起人是<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司，是一家隶属于<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县商业局的国有企业，其经营范围为<u><span>&nbsp;&nbsp;&nbsp; </span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</u>。现持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>县工商行政管理局颁发的注册号为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>的《企业法人营业执照》。</p>
        <p ><u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年<u><span>&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>月<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span></u>日，股份公司进行了名称预先核准登记，获得<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>省工商行政管理局核发的（<span>&nbsp;&nbsp;&nbsp; </span>）名称预核企字[<u>&nbsp;&nbsp;&nbsp; 
          </u>]第<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span></u>号《企业名称预先核准通知书》。</p>
        <p >根据《发起人协议》五家发起人共同委派代表组成筹委会，目前已按法定程序向有关部门申报股份公司设立。</p>
        <p >本所律师认为，集团公司、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</u>有限责任公司和<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司具备发起设立股份公司的主体资格，适合《公司法》关于股份公司发起人的有关规定，因此各发起人发起设立股份公司的主体资格合法、有效。</p>
        <p >二、股份公司的设立方式</p>
        <p >股份公司拟由集团公司、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>厂、<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司和<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司等五家发起人以发起方式设立。</p>
        <p >股份公司的设立方式符合《公司法》第74条：“股份公司的设立，可以采取发起设立或者募集设立的方式”和第75条：“设立股份公司，应当有3人以上为发起人、其中须有过半数的发起人在中国境内有住所”的要求。</p>
        <p >三、股份公司的发起方式</p>
        <p >集团公司拟采取将部分剥离后的优良经营性资产以发起设立的方式联合其他四家发起人发起设立股份公司。上述资产经评估后再折股投入股份公司，其他四家发起人均以现金入股。</p>
        <p >根据<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>会计师事务所对集团公司拟投入股份公司的资产进行评估并出具的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>会计师评报字[<u>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </u>]第7号《资产评估报告》（此评估结果已得到集团公司上级主管部门<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>市<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>局<u><span>&nbsp;&nbsp;&nbsp;&nbsp; </span></u>字[<u>&nbsp;&nbsp;&nbsp; </u>]第<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>号文的确认）。集团公司将其经评估确认的资产人民币<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>元以<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></u>%的折股比率折为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股，由集团公司持有并行使股权。其他四家发起人以<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>%的比率以现金认购股份公司<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股的股份，并行使其相应的股权。</p>
        <p >根据<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>会计师事务所出具的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>会验字[<u>&nbsp;&nbsp;&nbsp; </u>]第<u><span>&nbsp;&nbsp;&nbsp; 
          </span></u>号，集团公司等五家发起人所认缴的出资在<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年<u><span>&nbsp;&nbsp;&nbsp; </span></u>月<u><span>&nbsp;&nbsp;&nbsp; </span></u>日之前均已到位。</p>
        <p >四、发起人与股份公司的关系</p>
        <p >股份公司成立之后，股本总额为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股，主发起人集团公司持有<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>股的股份，占股份公司总股本的<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>%，为股份公司的绝对控股股东。股份公司主发起人集团公司与股份公司的股权关系业经有关主管部门批准，股份公司其他各发起人与股份公司的持股关系也符合我国现行法律、法规的规定。</p>
        <p >由于集团公司为股份公司的绝对控股股东，因此集团公司及其下属企业为股份公司的关联企业。修配团公司及其下属企业将向股份公司提供职工就餐、供暖、宿舍等生活服务。为此，集团公司已与股份公司按照市场原则，在公平、合理的基础上签订了《综合服务协议》，确定了双方之间的关联交易。根据本所律师对股份公司提供的材料的审查，股份公司其他发起人与股份公司之间不存在关联交易。</p>
        <p >经本所律师对集团公司与股份公司之间存在的关联交易项目及有关交易协议的内容的审查，没有发现有违反我国现行法律、法规规定之处。</p>
        <p >五、关于股份公司章程（草案）</p>
        <p >股份公司筹委会已决定了股份公司章程（草案），并已经过股份公司发起人会议讨论通过。本所律师在对该章程（草案）审查后认为，该章程（草案）适用《公司法》的规定，其内容完整、合法。但该章程（草案）尚须股份公司创立大会决议通过并经<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>省工商行政管理局注册登记后方能生效。</p>
        <p >六、股份公司的组织机构</p>
        <p >根据股份公司提供的《重组方案》及股份公司章程（草案），股份公司将建立以下组织机构：</p>
        <p >1．股东大会。股东大会为股份公司的最高权力机构。股东大会的召开、职权及议事规则等均已由股份公司章程予以规定。</p>
        <p >2．董中会。董事会为股份公司的常设权力机构，向股东大会负责并报告工作。董事会的组成、产生、职权及议事规则等均由股份公司章程予以规定。</p>
