        <p >(一)为减少公司。资本而注销股份；</p>
        <p >(二)与持有本公司股票的其他公司合并；</p>
        <p >(三)法律、行政法规许可的其他情况。</p>
        <p >第十六条<span>&nbsp; </span>公司增加或者减少注册资本，应当依法向公司登记机关办理变更登记。</p>
