        <p >（一）向社会公众发行股份；</p>
        <p >(二)向现有股东配售新股；</p>
        <p >(三)向现有股东派送新股；</p>
        <p >(四)法律、行政法规许可的其他方式发行新股。</p>
        <p >公司增资发行新股，经公司股东大会通过后，根据国家有关法律、行政法规规定的程序呈报审批。</p>
        <p >第十四条<span>&nbsp; </span>公司需要减少注册资本时，必须编制资产负债表及财产清单。</p>
