      <p>一、现将本文书的制作要点介绍如下：</p>
      <div style='layout-grid:15.6pt'>
<p >1．公司的名称和住所。</p>
        <p >2．公司的经营范围。</p>
        <p >3．公司的注册资本。</p>
        <p >4．股东的姓名或者名称。</p>
        <p >5．股东的权利和义务。</p>
        <p >6．股东的出资方式和出资额。</p>
        <p >7．股东转让出资的条件。</p>
        <p >8．公司的机构及其产生的办法、职权、议事规则。</p>
        <p >9．公司的法定代表人。</p>
        <p >10．公司的解散事由与清算办法。</p>
        <p >11．股东认为需要规定的其他事项。</p>
        <p >二、格式：</p>
        <p align=center style='
text-align:center;line-height:22.0pt;
'><span style='font-size:14.0pt;font-family:华文楷体;'>××有限责任公司章程</span><span
lang=EN-US style='font-size:14.0pt;font-family:华文楷体;
"Times New Roman"'></span></p>
        <p >依据《中华人民共和国公司法》（以下简称《公司法》及有关法律、法规的规定，由<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>等<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>&nbsp;&nbsp;</u>方（人）共同出资，设立<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司，并制定本章程。</p>
        <p align=center >第一章<span>&nbsp; </span>公司名称和住所</p>
        <p >第一条<span>&nbsp; </span>公司名称：<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>有限责任公司（以下简称公司）</p>
        <p >第二条<span>&nbsp; </span>住所：<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u></p>
        <p align=center >第二章<span>&nbsp; </span>公司经营范围</p>
        <p >第三条<span>&nbsp; </span>经公司登记机关核准，公司经营范围：（略）</p>
        <p align=center >第三章<span>&nbsp; </span>公司注册资本</p>
        <p >第四条<span>&nbsp; </span>公司注册资本：<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>万元人民币</p>
        <p >公司增加或减少注册资本，必须召开股东会并由持有2/3以上表决权的股东通过并作出决议。公司减少注册资本，还应当自作出决议之日起10日内通知债权人，并于30日内在报纸上至少公告3次。公司变更注册资本应依法向登记机关办理变更登记手续。</p>
        <p align=center >第四章<span>&nbsp; </span>股东的姓名、出资方式、出资额</p>
        <p >第五条<span>&nbsp; </span>股东的姓名、出资方式及出资额如下：</p>
        <p >（股东姓名、出费方式、出资额）</p>
        <p >第六条<span>&nbsp; </span>公司成立后，应向股东签发出资证明书。</p>
        <p align=center >第五章<span>&nbsp; </span>股东的权利和义务</p>
        <p >第七条<span>&nbsp; </span>股东享有如下权利：</p>
        <p >（一）参加或推选代表参加股东会并根据其出资份额享有表决权；</p>
        <p >（二）了解公司经营状况和财务状况；</p>
        <p >（三）选举和被选举为董事会或监事会成员；</p>
        <p >（四）依照法律、法规和公司章程的规定获取股利并转让；</p>
        <p >（五）优先购买其他股东转让的出资；</p>
        <p >（六）优先购买公司新增的注册资本；</p>
        <p >（七）公司终止后，依法分得公司的剩余财产；</p>
        <p >（八）其他权利。</p>
        <p >第八条<span>&nbsp; </span>股东承担以下义务：</p>
        <p >（一）遵守公司章程；</p>
        <p >（二）按期缴纳所认缴的出资；</p>
        <p >（三）依其所认缴的出资额承担公司的债务；</p>
        <p >（四）在公司办理登记注册手续后，股东不得抽回投资；</p>
        <p >（五）法律、行政法规规定的其他义务。</p>
        <p align=center >第六章<span>&nbsp; </span>股东转让出资的条件</p>
        <p >第九条<span>&nbsp; </span>股东之间可以相互转让其全部或者部分出资。</p>
        <p >第十条<span>&nbsp; </span>股东转让出资由股东讨论通过。股东向股东以外的人转让其出资时，必须经全体股东过半数同意；不同意转让的股东应当购买该转让的出资，如果不购买该转让的出资，视为同意转让。</p>
        <p >第十一条<span>&nbsp; </span>股东依法转让其出资后，由公司将受让人的姓名、住所以及受让的出资额记载于股东名册。</p>
        <p align=center >第七章<span>&nbsp; </span>公司的机构及其产生办法、职权、议事规则</p>
        <p >第十二条<span>&nbsp; </span>股东会由全体股东组成，是公司的最高权力机构，行使下列职权：</p>
        <p >（一）决定公司的经营方针和投资计划；</p>
        <p >（二）选举和更换董事，决定有关董事的报酬事项；</p>
        <p >（三）选举和更换由股东代表出任的监事，决定有关监事的报酬事项；</p>
        <p >（四）审议批准董事会、（或执行董事）的报告；</p>
        <p >（五）审议批准监事会或者监事的报告；</p>
        <p >（六）审议批准公司的年度财务预算方案、捷算方案；</p>
        <p >（七）审议批准公司的利润分配方案和弥补亏损的方秉；</p>
        <p >（八）对公司增加或者减少注册资本作出决议；</p>
        <p >（九）对发行公司债券作出决议；</p>
        <p >（十）对股东向股东以外的人转让出资作出决议；</p>
        <p >（十一）对公司合并、分立、变更公司形式、解散和清算等事项作出决议；</p>
        <p >（十二）修改公司章程。</p>
        <p >第十三条<span>&nbsp; </span>股东会的首次会议由出资最多的股东召集和主持。</p>
        <p >第十四条<span>&nbsp; </span>股东会会议由股东按照出资比例行使表决权。</p>
        <p >第十五条<span>&nbsp; </span>股东会会议分为定期会议和临时会议，并应当于会议召开15日以前通知全体股东。定期会议应每年召开2次，临时会议由代表1／4以上表决权的股东，1眉的董事，或者1／3以上的监事提议方可召开。股东出席股东会议也可书面委托他人参加股东会议，但委托书中应载明被委托人的权限。</p>
        <p >第十六条<span>&nbsp; </span>股东会会议由董事会召集，董事长主持。董事长因特殊原因不能履行职务时，由董事长指定的副董事长或者其他董事主持。</p>
        <p >（注：如果公司不设立董事会的，股东会会议由执行董事召集并主持。）</p>
        <p >第十七条<span>&nbsp; </span>股东全会议应对所议事项作出决议，决议应当代表1/2以上表决权的股东表决通过，但股东会对公司增加或者减少注册资本、分立、合并、解散或者变更公司形式、修改公司章程所作出的决议，应当代表2/3以上表决权的股东表决通过。股东会应当对所议事项的决定作出会议纪录，出席会议的股东应当在会议记录上签名。</p>
        <p >第十八条<span>&nbsp; </span>公司设董事会，成员为<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>人，由股东会选举（委派）。董事任期<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>年，任期后满，可连选连任，董事在任期届满前，股东会不得无故解除其职务。董事会设董事长1人，副董事长<u><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
          </span></u>人。董事长、副董事长由董事会选举和罢免。</p>
        <p >第十九条<span>&nbsp; </span>董事会行使下列职权：</p>
        <p >（一）负责召集股东会，并向股东会报告工作；</p>
        <p >（二）执行股东会决议；</p>
        <p >（三）决定公司的经营计划和投资方案；</p>
        <p >（四）制订公司的年度财务预算方案、决算方案；</p>
        <p >（五）制订公司的利润分配方案和弥补亏损方案；</p>
        <p >（六）制订公司增加或者减少注册资本方案；</p>
        <p >（七）拟订公司合并、分立、变更公司形式、解散的方案；</p>
        <p >（八）决定公司内部管理机构的设置；</p>
        <p >（九）聘任或者解聘公司经理（总经理）（以下简称为经理），根据经理的提名，聘任或者解聘公司副经理，财务负责人，决定其报酬事项；</p>
        <p >（十）制定公司的基本管理制度。</p>
        <p >（注：有限责任公司不设董事会的，董事会有关条款可不要。）</p>
        <p >第二十条<span>&nbsp; </span>董事会由董事长召集并主持。董事长因特殊原因不能履行职务时，由董事长指定副董事长或者其他董事召集和主持，1／3以上董事可以提议召开临时董事会会议，并应于会议召开10日前通知全体董事。</p>
        <p >第二十一条<span>&nbsp; </span>董事会对所议事项作出的决定应由1／2以上的董事表决通过方为有效，并应作成会议记录，出席会议的董事应当在会议记录上签名。</p>
        <p >第二十二条<span>&nbsp; </span>公司设经理1名，由董事会聘任或者解聘，经理对董事会负责，行使下列职权：</p>
        <p >（一）主持公司的生产经营管理工作，组织实施董事会决议；</p>
        <p >（二）组织实施公司年度经营计划和投资方案；</p>
        <p >（三）拟订公司内部管理机构设置方案；</p>
        <p >（四）拟订公司的基本管理制度；</p>
        <p >（五）制定公司的具体规章；</p>
        <p >（六）提请聘任或者解聘公司副经理，财务负责人；</p>
        <p >（七）聘任或者解除应当由董事会聘任或者解聘以外的负责管理人员；</p>
        <p >（八）公司章程和董事会授予的其他职权。</p>
        <p >经理列席董事会会议。</p>
        <p >第二十三条<span>&nbsp; </span>公司监事会，成员3人，并在其组成人员中推选1名召集人，监事会中股东代表监事与职工代表监事的比例为2:1。监事会中职工代表由公司职工民主选举产生。监事任期每届3年，任期届满，可连选连任。</p>
        <p >（注：股东人数较少，规模较小的公司可设1―2名监事。）</p>
        <p >第二十四条<span>&nbsp; </span>监事会（或监事）行使下列职权：</p>
        <p >（一）检查公司财务；</p>
        <p >（二）对董事、经理执行公司职务时违反法律、法规或者公司章程的行为进行监督；</p>
        <p >（三）当董事和经理的行为损害公司利益时，要求董事和经理予以纠正；</p>
        <p >（四）提议召开临时股东会；</p>
        <p >（五）公司章程及有关法律、行政法规规定的其他职权。</p>
        <p >监事列席董事会会议。</p>
        <p align=center >第八章<span>&nbsp; </span>公司的法定代表人</p>
        <p >第二十五条<span>&nbsp; </span>董事长为公司的法定代表人，任期为<span>&nbsp;&nbsp;&nbsp;&nbsp; </span>年，由董事会选举和罢免，任期后满，可连选连任。</p>
        <p >第二十六条<span>&nbsp; </span>董事长行使下列职权：</p>
        <p >（一）召集和主持股东会议和董事会议；</p>
        <p >（二）检查股东会议和董事会议的落实情况，并向董事会报告；</p>
        <p >（三）代表公司签署有关条件；</p>
        <p >（四）在发生战争、特大自然灾害等紧急情况下，对公司事务行使特别裁决权和处置权，但这类裁决权和处置权须符合公司利益，并在事后向董事会和股东会报告。</p>
        <p >（五）提名公司经理人选，由董事会任免；</p>
        <p >（六）其他职权。</p>
        <p >（注：公司设立执行董事而不设董事会的，执行董事为公司法定代表人，执行董事职权参照本条款及董事会职权。）</p>
        <p align=center >第九章<span>&nbsp; </span>财务、会计、利润分配及劳动用工制度</p>
        <p >第二十七条<span>&nbsp; </span>公司应当依照法律、行政法规和国务院财政主管部门的规定建立本公司的财务、会计制度，并应在第一会计年度终了时制作财务会计报告，并应于该会计年度终了后60日内送交各股东。</p>
        <p >第二十八条<span>&nbsp; </span>公司利润分配按照下列顺序执行：提取10%的法定公积金；5%的法定公益金；弥补亏损；向股东按出资比例分配利润。</p>
        <p >第二十九条<span>&nbsp; </span>劳动用工制度按国家法律、法规及国务院劳动部门的有关规定执行。</p>
        <p align=center >第十章<span>&nbsp; </span>公司的解散事由与清算办法</p>
        <p >第三十条<span>&nbsp; </span>公司为永久存续公司。</p>
        <p >第三十一条<span>&nbsp; </span>公司有下列情形之一的，可以解散：</p>
        <p >（一）公司章程规定的解散事由出现时；</p>
        <p >（二）股东会决议解散；</p>
        <p >（三）因公司合并或者分立需要解散的；</p>
        <p >（四）公司违反法律、行政法规被依法责令关闭的。</p>
        <p >第三十二条<span>&nbsp; </span>公司解散时，应依据《公司法》的规定成立清算组对公司进行清算。清算结束后，清算组应当制作清算报告，报股东会或者有关主管部门确认，并报送公司登记机关，申请注销公司登记，并公告公司终止。</p>
        <p align=center >第十一章<span>&nbsp; </span>股东认为需要规定的其他事项</p>
        <p >第三十三条<span>&nbsp; </span>公司根据需要或涉及公司登记事项变更的，可修改公司章程，修改后的公司章程不得与法律、法规相抵触。修改公司章程由股东会代表2／3以上表决权的股东表决通过。修改后的公司章程应送原公司登记机关备案，涉及变更登记事项的，同时应向公司登记机关做变更登记。</p>
        <p >第三十四条<span>&nbsp; </span>公司章程的解释权属于董事会。</p>
        <p >第三十五条<span>&nbsp; </span>公司登记事项以公司登记机关核定为准。</p>
        <p >第三十六条<span>&nbsp; </span>本章程经各方出资人共同订立；自公司设立之日起生效。</p>
        <p >第三十七条<span>&nbsp; </span>本章程应报公司登记机关备案1份。</p>
        <p >全体股东亲笔签字：</p>
        <p align=right >年<span>&nbsp;&nbsp;&nbsp; </span>月<span>&nbsp;&nbsp;&nbsp; </span>日</p>
